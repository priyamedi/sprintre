					<h2 class="test-h2 mt-3">Why is the Streptococcus Group B Antigen test in CSF done?</h2>
<p>The test is performed to identify a Group B Streptococcus infection in the central nervous system, typically when
    meningitis is suspected.</p><div class="mt-3"></div>
<h2 class="test-h2 mt-3">Who should get this test?</h2>
<p>This test is recommended for individuals displaying severe symptoms of infection, such as high fever, severe
    headache, neck/joint pain and stiffness, vomiting, high sensitivity to light, and sudden altered mental states.</p>
	



					
