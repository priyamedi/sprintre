<p>Cultivated wheat, known scientifically as Triticum aestivum, is one of the most widely cultivated cereal
grains worldwide. It's a staple food used in the production of flour for bread, pasta, and various other
food products. However, during the pollination season, wheat plants release pollen which can be a
source of allergens for certain individuals. Wheat pollen allergy is less common than wheat protein
allergy but can cause symptoms in sensitized individuals.
 </p>
