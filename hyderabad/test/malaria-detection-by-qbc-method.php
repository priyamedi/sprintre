<h2 class="test-h2 mt-3">Why is the Malaria Detection by QBC Method performed?</h2>
<p>The test is conducted when there's a suspicion of malaria, based on symptoms like high fever, chills,
headaches, and body aches. It helps in early detection and management of the disease.</p>

<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>A small sample of your blood will be drawn from your arm. This sample will then be centrifuged in a
special tube coated with acridine orange and observed under ultraviolet light to detect any malaria
parasites.</p>
