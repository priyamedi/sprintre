<h1 class="test-h1">BOH (Bad Obstetric History) Panel - Comprehensive </h1>
<div class="post-text">
<p>The BOH (Bad Obstetric History) Panel is a comprehensive set of tests that are conducted to evaluate various factors that might have contributed to recurrent pregnancy losses in a woman. The term “Bad Obstetric History” refers to the history of two or more consecutive spontaneous abortions. The panel aims to identify underlying conditions or issues that may increase the risk of pregnancy loss, and its findings can help in managing future pregnancies more effectively.
 </p>
<p>This panel combines various tests including ANA (Antinuclear Antibodies), APA (Antiphospholipid Antibodies) IgG/IgM, Anti Cardiolipin - IgG/IgM/IgA, Lupus Anticoagulant, TSH (Thyroid-Stimulating Hormone), TORCH-10 (a series of tests for infectious diseases) and Karyotyping of the Couple. This extensive panel investigates autoimmune diseases, thyroid function, infections, and chromosomal abnormalities, which are factors known to affect pregnancy outcomes.
 </p>
