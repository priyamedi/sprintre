<h1 class="test-h1">FISH for IgH-FGFR3 Fusion / t(4;14)</h1>
<div class="post-text">
<p>FISH, or Fluorescence In Situ Hybridization, for IgH-FGFR3 Fusion/t(4;14) is a diagnostic test that is primarily utilized in the evaluation of multiple myeloma, a type of blood cancer affecting plasma cells. The t(4;14) translocation is a chromosomal abnormality that involves the exchange of genetic material between chromosome 4 and 14. This results in the fusion of two genes, IgH (Immunoglobulin Heavy Chain) and FGFR3 (Fibroblast Growth Factor Receptor 3). The abnormal fusion of these genes plays a significant role in the pathogenesis of multiple myeloma.</p>
<p>FISH uses fluorescent probes that specifically bind to areas of DNA that are of interest, in this case, the IgH and FGFR3 genes. This allows the visualization of these genes under a fluorescence microscope to determine if the IgH-FGFR3 fusion is present.</p>
