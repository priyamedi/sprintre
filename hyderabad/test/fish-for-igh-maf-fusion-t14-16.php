<h1 class="test-h1">FISH for IgH-MAF Fusion / t(14;16)</h1>
<div class="post-text">
<p>Fluorescence In Situ Hybridization, commonly known as FISH, for IgH-MAF fusion/t(14;16) is a diagnostic test that is used in the evaluation and management of multiple myeloma. Multiple myeloma is a type of cancer that affects plasma cells, which are a type of white blood cell responsible for producing antibodies. In multiple myeloma, abnormal plasma cells multiply uncontrollably and produce abnormal antibodies. The t(14;16) translocation is a specific chromosomal abnormality in which there is an exchange of genetic material between chromosomes 14 and 16. This leads to the fusion of the Immunoglobulin Heavy Chain (IgH) gene on chromosome 14 with the MAF (c-MAF) gene on chromosome 16.</p>
<p>The FISH test uses fluorescent probes that bind to specific areas on chromosomes, and in this case, the areas of interest are the IgH and MAF genes. Under a fluorescence microscope, it is possible to see if the IgH-MAF fusion is present.</p>
