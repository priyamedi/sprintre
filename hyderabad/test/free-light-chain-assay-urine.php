<h1 class="test-h1">Free Light Chain Assay (Kappa & Lambda) - Urine </h1>
<div class="post-text">
<p>Free light chain assay for urine, commonly known as urine free light chain (UFLC) test, measures the free (unbound) kappa and lambda light chains in the urine. Light chains are small proteins produced by plasma cells, a type of white blood cell. Normally, they combine with heavy chains to form immunoglobulins, or antibodies, which play a vital role in the immune response. When there's an overproduction of light chains, they can be released into the blood and eventually appear in the urine as free light chains. </p>
<p>The measurement of free light chains in the urine and the ratio of kappa to lambda light chains (kappa:lambda ratio) can provide valuable insights into conditions that affect plasma cells, such as multiple myeloma, a type of bone marrow cancer. This test is particularly useful for monitoring the response to treatment and tracking disease progression in these conditions. </p>
