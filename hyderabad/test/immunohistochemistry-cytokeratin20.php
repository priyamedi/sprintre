<h2 class="test-h2 mt-3">What does the IHC - CK20 test measure?</h2>
<p>The IHC - CK20 test detects the presence of CK20, a protein expressed in certain types of epithelial cells. Increased
    expression of CK20 can be indicative of certain types of cancer.</p>
<h2 class="test-h2 mt-3">Why is the IHC - CK20 test important?</h2>
<p>The IHC - CK20 test helps identify the origin of certain types of cancer, assisting in the diagnosis and influencing
    the treatment plan.</p>
