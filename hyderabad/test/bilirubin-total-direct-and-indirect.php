<h1 class="test-h1">Bilirubin (Total, Direct and Indirect)</h1>
<div class="post-text">
<p>Bilirubin is a yellow pigment produced during the breakdown of red blood cells. The liver processes bilirubin, which is then eliminated from the body through the stool. The Bilirubin (Total, Direct, and Indirect) test measures the levels of different forms of bilirubin in the blood. Total Bilirubin measures the sum of direct and indirect bilirubin. Direct Bilirubin measures the conjugated form which is processed by the liver, and Indirect Bilirubin measures the unconjugated form circulating in the blood.</p>
<p>Abnormal levels of bilirubin may indicate issues with the liver or bile ducts, or hemolytic anemia, which is the breakdown of red blood cells. This test is commonly used to diagnose and monitor liver diseases such as hepatitis, cirrhosis, or jaundice.</p>
