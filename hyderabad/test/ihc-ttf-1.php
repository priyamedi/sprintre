<p>The IHC - TTF-1 test uses a technique known as immunohistochemistry (IHC) to detect the presence of
specific proteins like TTF-1 in tissue samples. It is used primarily in a pathological context to help identify
the origin of a cancer, particularly when the primary site is unclear.</p>
