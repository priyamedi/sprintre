<h2 class="test-h2 mt-3">What does the Allergen, Individual - Food Chick Pea test entail?</h2>
<p>The Allergen, Individual - Food Chick Pea test requires a blood sample, taken from a vein in the arm. The sample is then sent to a laboratory where it is analyzed for specific IgE antibodies reacting to chickpea proteins.</p>
<h2 class="test-h2 mt-3">Who should get this test done?</h2>
<p>This test is generally advised for individuals who experience symptoms suggestive of an allergic reaction after consuming chickpeas. The symptoms could range from skin irritations, such as hives or eczema, to more severe reactions like shortness of breath or anaphylaxis.</p>
