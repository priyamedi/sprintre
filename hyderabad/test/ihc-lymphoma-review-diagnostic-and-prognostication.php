particularly large cell lymphomas. Lymphomas are a group of cancers that begin in the lymphocytes,
which are white blood cells and an essential part of the immune system. Large cell lymphomas are so
named because the lymphocytes appear larger than normal when viewed under a microscope.</p>
<p> In the Lymphoma Review Diagnostic and Prognostication - Suspect Large Cell Lymphoma test, 10 to 12
markers are used. This panel of markers helps to determine the type of lymphoma, its growth rate, and
potential response to treatment, all of which contribute to the disease's prognostication or predicted
course and outcome.</p>
