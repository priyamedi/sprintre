								<h2 class="test-h2 mt-3">What does the test measure?</h2>
								<p>This test measures specific Immunoglobulin E (IgE) antibodies in the blood that your
									body may produce in response to certain foods. The presence and level of these
									antibodies can indicate an allergic reaction or intolerance to these foods.</p>

								<h2 class="test-h2 mt-3">How often should I get this test?</h2>
								<p>The frequency of the test depends on your individual health condition and your
									doctor's advice. If you have recurrent symptoms of food allergies or intolerances,
									your doctor may recommend you to have this test more regularly.</p>

								<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
								<p>The normal values can vary depending on the lab conducting the test. Generally, a low
									level or absence of specific IgE antibodies against the tested foods is considered
									normal.</p>

								<h2 class="test-h2 mt-3">What do abnormal test results mean?</h2>
								<p>Abnormal results can mean that you have an allergy or intolerance to one or more of
									the foods tested. High levels of specific IgE antibodies can indicate an allergic
									reaction or intolerance to the respective food.</p>

								<h2 class="test-h2 mt-3">What factors can affect the results of this test?</h2>
								<p>Factors such as recent exposure to the food items tested, the consumption of certain
									medications, and the presence of any other illnesses can potentially influence the
									results.</p>

								<h2 class="test-h2 mt-3">What precautions should I take before the test?</h2>
								<p>There are no specific precautions to be taken before this test. Just ensure you are
									well-rested and calm when you go for the blood sample collection.</p>

								<h2 class="test-h2 mt-3">What are the modifiable factors affecting the results of this
									test?</h2>
								<p>The primary modifiable factor is your diet. If you have been exposed to the tested
									food items shortly before the test, your body might produce specific IgE antibodies
									against them, potentially affecting the results.</p>

								<h2 class="test-h2 mt-3"> What are the non-modifiable factors affecting the results of
									this test?</h2>
								<p>Your genetic predisposition to allergies is a non-modifiable factor that can affect
									the results of this test. Some people may have a higher risk of developing allergies
									due to their genetic makeup.</p>

								<h2 class="test-h2 mt-3"> Which doctor should I consult if my test results are abnormal?
								</h2>
								<p>You should consult an allergist or a gastroenterologist if your test results are
