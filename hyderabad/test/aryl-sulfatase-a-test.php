	<h2 class="test-h2 mt-3">What is Metachromatic Leukodystrophy?
 </h2>
	<p>Metachromatic leukodystrophy is a genetic disorder caused by mutations in the ARSA gene, which is responsible for the production of the Aryl Sulfatase A enzyme. This enzyme is essential for the breakdown of sulfatides, which are fatty substances present in the myelin sheath surrounding nerve fibers. In MLD, sulfatides accumulate in various tissues, including the nervous system. This accumulation is believed to contribute to the progressive destruction of the white matter (myelin) of the brain and peripheral nerves.
 </p>
	<h2 class="test-h2 mt-3">What are the Symptoms of MLD?
 </h2>
	<p>The symptoms of MLD vary depending on the age of onset and progression of the disease. Common symptoms include muscle wasting and weakness, loss of sensation, difficulty walking, behavior changes, slurred speech, and seizures.
 </p>
