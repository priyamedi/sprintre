<h2 class="test-h2 mt-3">What symptoms might indicate a soy allergy?</h2>
<p>Symptoms of a soy allergy can include itching, hives, eczema, tingling in the mouth, wheezing, abdominal pain,
    diarrhea, nausea or vomiting, and in severe cases, anaphylaxis.</p>
<h2 class="test-h2 mt-3">Why is it important to get tested for soy allergy?</h2>
<p>Getting tested for soy allergy is important because it helps in the diagnosis and management of the allergy. Knowing
    that you are allergic to soy can help you make informed choices about your diet and avoid potential allergic
    reactions.</p>
