<h2 class="test-h2 mt-3">Why might I need an NPM1 mutation detection test?</h2>
<p>If you have been diagnosed with acute myeloid leukemia (AML) or have symptoms indicative of AML, your healthcare
    provider may recommend this test. The test is also useful for tracking the progress of AML treatment.</p>
<h2 class="test-h2 mt-3">How is the NPM1 mutation detection test performed?</h2>
<p>The test is conducted using a sample of your peripheral blood or bone marrow. The DNA is extracted and analyzed for
    the presence of mutations in the NPM1 gene.</p>
