<h2 class="test-h2 mt-3">Why is the Flowcytometry - CD33 test done?</h2>
<p>The Flowcytometry - CD33 test is performed to help diagnose conditions that result in abnormal levels of
    CD33-expressing cells, like acute myeloid leukemia. It's also used to monitor the effectiveness of treatments
    targeting CD33-expressing cells.</p>
<h2 class="test-h2 mt-3">Is there any special preparation required for this test?</h2>
<p>No, there are no specific preparations needed for the Flowcytometry - CD33 test. You can continue with your regular
    diet and routine unless instructed otherwise by your doctor.</p>
