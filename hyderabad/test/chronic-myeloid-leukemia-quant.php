<h1 class="test-h1">CML Quant: Chronic Myeloid Leukemia with Karyotyping and BCR/ABL - IS by PCR Quantitative </h1>
<div class="post-text">
<p> Chronic Myeloid Leukemia (CML) is a form of leukemia characterized by the abnormal growth of myeloid cells in the bone marrow and their accumulation in the blood. CML is a genetic disease and is most often associated with a specific chromosomal abnormality known as the Philadelphia chromosome. The Philadelphia chromosome arises from a reciprocal translocation between chromosomes 9 and 22, which results in the BCR-ABL fusion gene. The protein produced by this gene is a tyrosine kinase that promotes cell proliferation, contributing to the development of leukemia. <br><br>
CML Quant is a testing approach that includes Karyotyping and BCR/ABL International Scale (IS) by Polymerase Chain Reaction (PCR) Quantitative. Karyotyping is used to visualize the chromosomes in the cells, which can confirm the presence of the Philadelphia chromosome. BCR/ABL - IS by PCR Quantitative is a highly sensitive method that measures the levels of the BCR-ABL fusion gene and is used to monitor the patient’s response to therapy over time.</p>
