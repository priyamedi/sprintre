<h1 class="test-h1">TPMT Enzyme Activity (Thiopurine Methyl Transferase) Plasma </h1>
<div class="post-text">
<p>The TPMT Enzyme Activity test measures the activity level of the Thiopurine Methyl Transferase (TPMT) enzyme in the plasma. This enzyme is crucial for metabolizing thiopurine medications, such as azathioprine, mercaptopurine, and thioguanine, commonly used to treat conditions like autoimmune disorders and leukemia.</p>
<p>Some people have variations in the TPMT gene that cause them to produce too little or too much of this enzyme, which can impact how they metabolize these medications. Low TPMT activity can cause toxic levels of medication to build up in the body, leading to side effects like bone marrow suppression. Conversely, high TPMT activity might result in treatment inefficiency.</p>
<p>The TPMT Enzyme Activity test is typically ordered before initiating therapy with thiopurine medications to help guide dosage and minimize the risk of adverse effects.</p>
