<h2 class="test-h2 mt-3">What is BRCA 2 Mutation Carrier Testing?</h2>
<p>BRCA 2 Mutation Carrier Testing is a genetic test that identifies mutations in the BRCA2 gene. Carriers of a BRCA2 mutation have an increased risk of developing breast, ovarian, and other cancers.</p>
<h2 class="test-h2 mt-3">Who should consider undergoing this test?</h2>
<p>Individuals with a family history of breast or ovarian cancer, or who belong to populations with a high prevalence of BRCA2 mutations, such as those of Ashkenazi Jewish descent, should consider this test. Additionally, people diagnosed with breast or ovarian cancer at a young age may also benefit from testing.</p>
