<h2 class="test-h2 mt-3">What is the importance of getting the Celiac Profile Test done?</h2>
<p>This test is critical for diagnosing celiac disease. If you have symptoms like diarrhea, abdominal pain, weight loss,
    or signs of malnutrition, your doctor may recommend this test. Timely diagnosis can prevent complications like
    malnutrition, osteoporosis, infertility, and certain types of lymphoma.</p>
<h2 class="test-h2 mt-3">Is fasting required for the Celiac Profile Test?</h2>
<p>No, fasting is not necessary for the Celiac Profile Test.</p>
