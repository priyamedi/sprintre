	<h2 class="test-h2 mt-3">What is the MPN-3 test? </h2>
	<p>The MPN-3 test combines three genetic analyses – Karyotyping, BCR-ABL mutation analysis through FISH, and JAK2V617F mutation analysis via PCR. It is utilized for the diagnosis and management of Myeloproliferative Neoplasms. </p>
	<h2 class="test-h2 mt-3">Why is the MPN-3 test important? </h2>
	<p>The MPN-3 test is essential for diagnosing MPNs, especially Chronic Myeloid Leukemia, which is often associated with the BCR-ABL fusion gene. The JAK2V617F mutation is also a characteristic feature in other MPNs, and Karyotyping helps identify chromosomal abnormalities that may be associated with these disorders. </p>
