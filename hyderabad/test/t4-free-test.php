<h2 class="test-h2 mt-3"> Why is the Free T4 test done?</h2>
<p>The Free T4 test is done to evaluate thyroid function and to help diagnose thyroid diseases. It is usually ordered
    when a person has symptoms of hyperthyroidism or hypothyroidism, or to monitor the effectiveness of treatment for
    these conditions.</p>
<h2 class="test-h2 mt-3">What preparation is needed for the Free T4 test?</h2>
<p>No special preparation is needed for the Free T4 test. However, some medications may affect the results of the test,
    so it's important to discuss with your healthcare provider about any medications you are taking.</p>
