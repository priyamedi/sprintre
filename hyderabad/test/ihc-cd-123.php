	<h2 class="test-h2 mt-3"> How is the test performed? </h2>
<p>The test is performed on a tissue sample, typically obtained through a biopsy. The sample is then processed in a laboratory where it is stained with a dye that binds to the CD123 protein if present.</p>

	<h2 class="test-h2 mt-3">Are there any risks associated with the test?</h2> 
<p>The risks associated with this test are primarily related to the biopsy procedure used to collect the tissue sample. These can include pain, bleeding, and <a   href="https://www.sprintdiagnostics.in/symptoms/frequent-infections"><b style="color:#2C53A5">infection</b></a> at the biopsy site.</p>

	<h2 class="test-h2 mt-3">What does a positive result mean? </h2>
<p>A positive result indicates that the CD123 protein is present in the tissue sample. This could suggest a certain type of blood cancer, such as hairy cell leukemia, acute myeloid leukemia, or blastic plasmacytoid dendritic cell neoplasm.</p>

	<h2 class="test-h2 mt-3">What does a negative result mean? </h2>
<p>A negative result means that the CD123 protein was not detected in the tissue sample. This can help rule out certain types of blood cancers that overexpress the CD123 protein.</p>

	<h2 class="test-h2 mt-3">How long will it take to get the results? </h2>
