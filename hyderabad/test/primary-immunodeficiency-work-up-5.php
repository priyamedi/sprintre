<h2 class="test-h2 mt-3">When should I get this test?</h2>
<p>You should get this test if you experience recurrent, severe, or unusual infections, indicative of a possible
    immunodeficiency disorder.</p>
<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>The frequency of this test depends on your personal health condition and your healthcare provider's advice.</p>
