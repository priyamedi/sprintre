<h2 class="test-h2 mt-3">What is the purpose of the Cytomegalovirus Antibodies (CMV) Panel - IgG & IgM test?</h2>
<p>The test is used to determine if you have been infected with CMV either recently or in the past. It's particularly
    important for pregnant women, as CMV can be dangerous for the unborn child, and in individuals with weakened immune
    systems.</p>
<h2 class="test-h2 mt-3">How is the CMV antibodies test performed?</h2>
<p>A healthcare provider will take a blood sample from a vein in your arm. This sample is then sent to a laboratory
    where it will be tested for the presence of CMV antibodies - IgG and IgM.</p>

