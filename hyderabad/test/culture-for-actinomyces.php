<h2 class="test-h2 mt-3">What is Culture for Actinomyces?</h2>
<p>Culture for Actinomyces is a laboratory test used to detect the presence of Actinomyces bacteria in a
sample of tissue, fluid, or pus from the body. It helps in the diagnosis of actinomycosis, a rare bacterial
infection.</p>
<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>The test is performed when a doctor suspects that a patient may have an infection caused by
Actinomyces bacteria, particularly when there are lumps on the face or neck, or signs of infection in
other parts of the body.</p>
