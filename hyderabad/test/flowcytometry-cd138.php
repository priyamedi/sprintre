<h2 class="test-h2 mt-3">When should I get this test?</h2>
<p>This test is typically ordered when a doctor suspects a plasma cell disorder or wants to monitor the progression of diseases like multiple myeloma.</p>
<h2 class="test-h2 mt-3">What does the Flowcytometry - CD138 test measure?</h2>
<p>This test measures the number and percentage of cells that express CD138, a marker for plasma cells.</p>
<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>The frequency of this test largely depends on your doctor's evaluation of your health status and whether you are currently being treated for a plasma cell disorder.</p>
<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
<p>The normal values can vary. However, a significant increase in CD138-positive cells could be indicative of a plasma cell disorder.</p>
<h2 class="test-h2 mt-3">What precautions should be taken before undergoing this test?</h2>
<p>There are no specific precautions required for this test. However, it's always important to inform your doctor of any medications or supplements you're taking.</p>
<h2 class="test-h2 mt-3">What factors can affect the levels of CD138?</h2>
<p>Certain conditions, particularly plasma cell disorders like multiple myeloma, can lead to an increased level of CD138. Certain treatments, like chemotherapy, can also impact the levels of CD138.</p>
<h2 class="test-h2 mt-3">Which doctor should I consult in case of abnormal results?</h2>
<p>In case of abnormal results, you should consult a hematologist or oncologist who specializes in the treatment of blood and bone marrow diseases.</p>
<h2 class="test-h2 mt-3"> How is the Flowcytometry - CD138 test performed?</h2>
<p>This test is performed on a blood sample. The blood is treated with a specific antibody that binds to CD138, which is then detected and quantified by a flow cytometer.</p>
<p>The Flowcytometry - CD138 test provides valuable insights into the health of your plasma cells. An increase in CD138 expression might indicate a plasma cell disorder. If you have been diagnosed with a condition like multiple myeloma, this test can be used to monitor disease progression and the effectiveness of treatment. Always consult with your doctor for any concerns or questions regarding this test and its results.</p>

