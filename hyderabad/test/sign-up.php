	function toastShow() {
		var x = document.getElementById("snackbar");
		x.className = "show";
		setTimeout(function () { x.className = x.className.replace("show", ""); }, 1000);
	}
	$("#genderTitle").change(function () {


		var genderTitle = document.getElementById("genderTitle").value;

		if (genderTitle == "Mrs" || genderTitle == "Miss" || genderTitle == "Baby" || genderTitle == "BabyGirl") {

			// $('#genderTitle').append('<option value="Female" selected >Female</option>');
			document.getElementById("gender").value = "Female"
		} else {
			document.getElementById("gender").value = "Male"

		}

	});

	$("#addNewMenmber").submit(function (e) {
		e.preventDefault();
		var titile = document.getElementById("genderTitle").value;
		var patientName = document.getElementById("patientName").value;
		var patientAge = document.getElementById("patientAge").value;
		var patientGender = document.getElementById("gender").value;
		var mobileNUmber = document.getElementById("mobileNUmber").value;
		var bloodGroup = document.getElementById("Blood").value;



		var settings = {
			"url": "web-api/pateint-submit.php",
			"method": "POST",
			"timeout": 0,
			"headers": {
				"Content-Type": "application/json"
			},
			"data": JSON.stringify({
				"title": titile,
				"patient_name": patientName,
				"patient_age": patientAge,
				"patient_gender": patientGender,
				"mobile_no": mobileNUmber,
				"blood_group": bloodGroup
			}),
		};

		$.ajax(settings).done(function (response) {
			if (response.error === false) {
				$("#addNewMenmber")[0].reset()
				document.getElementById("snackbar").innerHTML = response.message;
				$("#snackbar").css("background", "#01a9ac");
				toastShow();
				$("#mymodel").model("show");
			} else {
				reloaUpdate();
				document.getElementById("snackbar").innerHTML = response.message;
				$("#snackbar").css("background", "#e35d4b");
				toastShow();
			}

			// console.log(response);
			// alert(response.message)
		});
	})


</script>

