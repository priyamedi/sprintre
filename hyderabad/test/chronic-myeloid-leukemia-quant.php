	<h2 class="test-h2 mt-3">What is Chronic Myeloid Leukemia (CML)? </h2>
	<p>Chronic Myeloid Leukemia is a type of cancer that starts in the blood-forming cells in the bone marrow. It is characterized by the uncontrolled growth of myeloid cells in the bone marrow and their accumulation in the blood. CML is associated with a specific genetic abnormality called the Philadelphia chromosome. </p>
	<h2 class="test-h2 mt-3">What is the role of Karyotyping in CML diagnosis? </h2>
	<p>Karyotyping is a laboratory procedure that allows for the visualization of an individual's chromosomes. In CML, karyotyping is important for the identification of the Philadelphia chromosome, which is a hallmark of this disease.
