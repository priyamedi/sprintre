<h2 class="test-h2 mt-3"> What is the IHC-Prolactin test?</h2>
	<p>
The IHC-Prolactin test is a pathology test that uses specific antibodies to detect the presence of prolactin
in tissue samples. It is typically used to diagnose prolactinomas, a type of benign tumor of the pituitary
gland that overproduces prolactin.</p>
<h2 class="test-h2 mt-3">How is the IHC-Prolactin test performed?</h2>
<p>
The test is performed on a tissue sample, often obtained through a biopsy. The tissue is then treated
with antibodies specific to prolactin. If prolactin is present, it will bind with the antibodies and can be
detected under a microscope.</p>
