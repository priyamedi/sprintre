<p>Various factors can affect the levels of desipramine in the blood, including age, liver function, genetic factors,
    interactions with other medications, and adherence to the medication regimen.</p>
<h2 class="test-h2 mt-3">Which doctor should be consulted in case of abnormal Desipramine levels?</h2>
<p>In case of abnormal desipramine levels, it’s important to consult the healthcare provider who prescribed the
    medication. This might be a primary care physician, psychiatrist, or another specialist.</p>
