<h2 class="test-h2 mt-3">Why is the Bilirubin Total test done?</h2>
<p>The test is performed to assess liver function and help diagnose conditions such as liver disease, anemia, and jaundice. It’s also used to monitor the progress of treatment for these conditions.</p>

<h2 class="test-h2 mt-3">What do high levels of Total Bilirubin indicate?</h2>
<p>High levels of Total Bilirubin can indicate liver disorders such as hepatitis, cirrhosis, or liver cancer. It can also suggest hemolytic anemia or problems with the bile ducts.</p>

