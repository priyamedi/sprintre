<h2 class="test-h2 mt-3">What is the Legionella Antigen Detection test used for?</h2>
<p>This test is used to detect the presence of Legionella bacteria, which causes Legionnaires' disease - a severe form
	of pneumonia. Early detection helps in timely treatment and can be crucial for recovery.</p>
<h2 class="test-h2 mt-3">How is the Legionella Antigen Detection test performed?</h2>
<p>A urine sample is collected in a sterile container, which is then sent to the laboratory for analysis. The test
	detects antigens, which are substances found on the Legionella bacteria.</p>
