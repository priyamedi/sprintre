<h2 class="test-h2 mt-3"> What is the purpose of the Neuroblastoma Profile test?</h2>
	<p>
The test helps in the diagnosis and monitoring of neuroblastoma, a common type of cancer in children.
By measuring the levels of VMA, 5-HIAA, and HVA in the urine, this test can aid in the detection of
neuroblastoma and other related conditions.</p>
<h2 class="test-h2 mt-3"> How is the test performed?</h2>
<p>
The test requires a 24-hour urine collection. You'll be given a special container to collect your child's
urine over a 24-hour period.</p>
