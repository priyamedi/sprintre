<h2 class="test-h2 mt-3">What is Trisomy 12?</h2>
	<p>Trisomy 12 is a chromosomal abnormality where there are three copies of chromosome 12 instead of
the normal two. This can be associated with certain types of cancer, particularly chronic lymphocytic
leukemia (CLL).</p>

<h2 class="test-h2 mt-3">Why is the FISH for CEP12 / Trisomy 12 test done?</h2>
<p>The FISH for CEP12 / Trisomy 12 test is primarily done to detect the presence of an extra chromosome
12 in cells. This information can be critical in diagnosing and managing diseases such as CLL.</p>
