<h1 class="test-h1">IHC - Breast hormone receptor panel (ER, PR, HER2Neu) </h1>
<div class="post-text">
<p>Immunohistochemistry - Breast Hormone Receptor Panel is a diagnostic test performed on a tissue sample, typically from a breast biopsy or surgery. The panel examines the presence and amount of three important receptors in breast cancer cells: Estrogen Receptor (ER), Progesterone Receptor (PR), and Human Epidermal Growth Factor Receptor 2 (HER2Neu).
 </p>
<p>These receptors are proteins found on the surface of many breast cancer cells. Their presence can influence the growth of the cancer and how it responds to treatment. If a tumor is found to have one or more of these receptors, it is often more responsive to hormone therapies or targeted therapies.
 </p>
