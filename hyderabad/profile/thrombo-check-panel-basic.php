<h2 class="test-h2 mt-3">What does the Thrombo Check Panel Basic measure?</h2>
<p>The Thrombo Check Panel Basic measures the activity of Protein C, the levels of Free Protein S antigen, and the
    amount of AT III in the blood. These three factors play a critical role in preventing excessive blood clotting.</p>
<h2 class="test-h2 mt-3">Why do I need this test?</h2>
<p>If you have a history of unexplained blood clots, recurrent miscarriages, or a family history of clotting disorders,
    your healthcare provider may recommend this test to identify potential clotting problems. It can also guide
    treatment strategies for preventing clot formation.</p>
