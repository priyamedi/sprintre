<h1 class="test-h1">FISH for del(20q) </h1>
<div class="post-text">
<p>Fluorescence In Situ Hybridization, known as FISH, for del(20q) is a specialized diagnostic test that looks
for the deletion of a segment of the long arm of chromosome 20. This deletion is associated with various
hematological disorders, such as myelodysplastic syndromes (MDS), acute myeloid leukemia (AML), and
chronic myelomonocytic leukemia (CMML). Understanding the presence or absence of this deletion is
crucial for doctors to diagnose, predict the course of the disease, and formulate a treatment plan.</p>
<p>Chromosome 20q deletion is recognized as a recurrent chromosomal abnormality in hematological
malignancies. Detection of the deletion can provide additional information regarding the genomic
background of the disease, which can be vital for the prognostication and therapeutic decisions. </p>
<p>FISH uses fluorescent probes that bind to specific DNA sequences on chromosomes. Under a
fluorescence microscope, a technician can detect if there is a deletion in the long arm of chromosome
20.</p>
