<h1 class="test-h1">G6PD Glucose 6 Phosphate Dehydrogenase - Neonatal Screen</h1>
<div class="post-text">
<p>The Glucose-6-Phosphate Dehydrogenase (G6PD) - Neonatal Screen is a diagnostic test conducted on newborns to identify the presence of Glucose-6-Phosphate Dehydrogenase deficiency (G6PD deficiency), a genetic disorder. G6PD is an enzyme found in red blood cells, and its deficiency can lead to a type of anemia called hemolytic anemia.</p>
<p>In G6PD deficiency, red blood cells break down upon exposure to certain infections, medications, or substances found in fava beans. Newborn screening for G6PD deficiency allows for early detection, enabling proper management and prevention of triggers that can cause hemolytic anemia.</p>
