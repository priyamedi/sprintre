					<h2 class="test-h2 mt-3">What is the purpose of the C-Peptide 24 Hr Urine Test?</h2>
<p>The test measures C-Peptide levels in the urine over 24 hours to assess the pancreas’s insulin production. It's
    used for the diagnosis and management of diabetes and other disorders that affect insulin production.</p>
	<div class="mt-3"></div>
<h2 class="test-h2 mt-3">How do I collect my urine for this test?</h2>
<p>You will be given a special container to collect your urine. It is important to collect all urine produced in a
    24-hour period. Typically, you will start in the morning, empty your bladder, and discard this sample. Then, you
    collect all subsequent urine in the container until the next morning.</p>

	



					
