<h2 class="test-h2 mt-3">Why is the Glycine - Quantitative CSF Test important?</h2>
<p>This test is important in diagnosing and monitoring certain metabolic disorders, such as nonketotic hyperglycinemia
    (NKH), a rare genetic disorder that affects glycine metabolism. High levels of glycine in the CSF can cause
    seizures, breathing and feeding difficulties, and developmental delay.</p>
<h2 class="test-h2 mt-3">Do I need to fast for this test?</h2>
<p>No, fasting is not required for this test.</p>
