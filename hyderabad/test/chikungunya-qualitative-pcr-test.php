<h2 class="test-h2 mt-3">What is the Chikungunya Qualitative PCR Test?</h2>
<p>The Chikungunya Qualitative PCR Test is a diagnostic tool used to detect the presence of the Chikungunya virus in the
	blood. It uses a technique called polymerase chain reaction (PCR) to amplify and then detect the virus's genetic
	material.</p>
<h2 class="test-h2 mt-3">How is the Chikungunya Qualitative PCR Test performed?</h2>
<p>The test is performed using a blood sample, which is typically drawn from a vein in the arm. This sample is then
	tested in a laboratory using PCR technology to detect the presence of the Chikungunya virus.</p>
