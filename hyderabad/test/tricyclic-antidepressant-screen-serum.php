<h2 class="test-h2 mt-3">What is the Tricyclic Antidepressant Screen test?</h2>
<p>The Tricyclic Antidepressant Screen is a blood test used to measure the levels of tricyclic antidepressants in the
    body.</p>
<h2 class="test-h2 mt-3">Why might my doctor order a Tricyclic Antidepressant Screen test?</h2>
<p>Your doctor may order this test if you are on a tricyclic antidepressant to ensure that your levels are within the
    therapeutic range. The test can also be ordered if a TCA overdose is suspected.</p>
