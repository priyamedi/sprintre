<h2 class="test-h2 mt-3">Why is the Mercury - Urine Spot test done?</h2>
	<p>
The Mercury - Urine Spot test is used to determine if you have been exposed to excessive amounts of
mercury. It is typically performed when a person shows symptoms of mercury poisoning or has been in
an environment with high levels of mercury. </p>
<h2 class="test-h2 mt-3">Is fasting required for this test?</h2>
<p>
No, fasting is not necessary for this test. You can continue your regular diet and daily activities unless
instructed otherwise by your healthcare provider. </p>
