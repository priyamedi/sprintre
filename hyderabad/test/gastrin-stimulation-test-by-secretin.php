<h2 class="test-h2 mt-3">What is the Gastrin Stimulation Test by Secretin?</h2>
<p>This is a blood test that measures the level of the hormone gastrin before and after an injection of secretin. It
    helps diagnose conditions that cause excessive stomach acid production.</p>
<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>This test is performed when a patient has symptoms suggestive of gastrinomas or Zollinger-Ellison syndrome. Symptoms
    may include recurrent peptic ulcers, diarrhea, and gastroesophageal reflux disease (GERD).</p>
