<h2 class="test-h2 mt-3">What is the Erythropoietin (EPO) test used for?</h2>
<p>The EPO test is used to evaluate the production levels of erythropoietin in the blood, helping to
understand the role it may be playing in a person's red blood cell count. It's often used in cases of
anemia or polycythemia (high red blood cell count).</p>

<h2 class="test-h2 mt-3">How is the EPO test performed?</h2>
<p>The EPO test is performed by taking a blood sample from a vein in your arm. This sample is then sent to
a laboratory, where the level of erythropoietin is measured.</p>
