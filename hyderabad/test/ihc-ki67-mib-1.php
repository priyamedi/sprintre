<h2 class="test-h2 mt-3"> Why is the IHC - KI67 (MIB-1) test done?</h2>
	<p>
The test is often used in research and diagnostic settings to understand how rapidly cells are dividing in
a tissue sample. This can provide valuable information for diagnosing and grading cancers, as tumors
with higher Ki67 levels are often more aggressive. </p>
<h2 class="test-h2 mt-3"> How is the IHC - KI67 (MIB-1) test performed?</h2>
<p>
The test is performed on a tissue sample, which is fixed on a slide and treated with the Ki67 antibody.
The antibody binds to the Ki67 protein in the cell nuclei, and the bound antibody is then visualized using
a microscope. </p>
