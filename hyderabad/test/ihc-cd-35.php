<div class="post-text">
<p><strong>Immunohistochemistry (IHC)</strong> is a type of microscopy that uses the principle of antibodies binding specifically to antigens in biological tissues to visualize the distribution of specific cellular components within a sample. The <strong>IHC - CD 35</strong> test specifically identifies the presence of the CD35 protein in a tissue sample. CD35 is a membrane protein that is found on mature B cells and most cells of the innate immune system. It is also known as complement receptor 1 (CR1) and has a role in the complement system, which is part of the immune system that enhances the ability to clear microbes and damaged cells from an organism, promote inflammation, and attack the pathogen's plasma membrane.
