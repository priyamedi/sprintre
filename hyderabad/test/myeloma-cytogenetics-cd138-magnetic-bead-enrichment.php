	<p>CD138 is a protein found on the surface of plasma cells. Magnetic bead enrichment involves using magnetic beads that bind to CD138, allowing for the isolation of plasma cells from a bone marrow sample. This step is essential for increasing the accuracy of chromosomal analysis in multiple myeloma. </p>
	<h2 class="test-h2 mt-3">What is the role of karyotyping in Myeloma Cytogenetics? </h2>
	<p>Karyotyping involves visualizing the chromosomes in a cell to detect any structural abnormalities. In the context of multiple myeloma, it helps in identifying changes in the number or structure of chromosomes in plasma cells. </p>
