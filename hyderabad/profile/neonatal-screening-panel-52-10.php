<h2 class="test-h2 mt-3"> How is the sample collected?</h2>
<p>
A small prick is made on the heel of the newborn to obtain a few drops of blood. The blood is collected
on special filter paper, dried, and then sent to the lab for testing.</p>
<h2 class="test-h2 mt-3"> What does a positive result mean?</h2>
<p>A positive result indicates that the newborn may have one or more of the conditions included in the
panel. Further confirmatory tests will be necessary to establish a definitive diagnosis.</p>
<h2 class="test-h2 mt-3">How accurate is this test?</h2>
<p>
While the test is highly accurate, it's important to remember that no test is 100% accurate. There may
be false negatives or positives. Any positive result should be confirmed with further testing.</p>
<h2 class="test-h2 mt-3"> What happens if my baby tests positive?</h2>
<p>
If your baby tests positive, it does not necessarily mean they have the condition. It means that further
tests are needed. Early detection allows for early intervention, which can significantly improve your
child’s long-term health outcomes.</p>
<h2 class="test-h2 mt-3">How long will it take to get the results?</h2>
