							<h2 class="test-h2 mt-3"> What is this test panel for?</h2>
								<p>This panel of tests is used to evaluate a specific type of anemia known as macrocytic
									anemia. This condition is characterized by larger-than-normal red blood cells
									(RBCs), also known as macrocytes, and is often due to deficiencies in Vitamin B12 or
									Folate.</p>

								<h2 class="test-h2 mt-3"> What do these tests measure?</h2>
								<ul>
									<li>The <b>Complete Blood Count (CBC)</b> measures the count and size of different
										cells in your blood, including red blood cells, white blood cells, and
										platelets.</li>
									<li>The <b>Reticulocyte Count</b> measures the count of young red blood cells,
										indicating the rate of production and release by the bone marrow.</li>
									<li><b>Ferritin</b> is a protein that stores iron. Its level in blood indicates the
										total iron storage in the body.</li>
									<li><b>Folate (Folic Acid)</b> test measures the amount of this type of Vitamin B in
										your blood, which is crucial for red blood cell development.</li>
									<li><b>Lactate Dehydrogenase (LDH)</b> is an enzyme found throughout the body. Its
										levels can increase due to cell damage or destruction, which could be a
										consequence of anemia.</li>
									<li><b>Liver Function Tests (LFT)</b> evaluate how well your liver is working. Some
										liver conditions can affect the size of your red blood cells.</li>
									<li>The <b>Vitamin B12</b> test measures the amount of this vitamin in your blood.
										Like folate, B12 is important for red blood cell development and a deficiency
										can lead to macrocytic anemia.</li>
								</ul>


