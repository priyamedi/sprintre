<h2 class="test-h2 mt-3">What are the symptoms of Leptospirosis?</h2>
<p>Leptospirosis symptoms may include high fever, chills, headache, muscle aches, vomiting, jaundice, red eyes,
    abdominal pain, diarrhea, and rash. Severe cases can lead to kidney or liver failure and meningitis.</p>
<h2 class="test-h2 mt-3">How is the Leptospira detection using Dark Field Microscopy performed?</h2>
<p>A sample of blood or urine is collected from the patient. The sample is then examined under a dark field microscope,
    where the Leptospira bacteria, if present, will appear as bright, thin, coiled shapes against a dark background.</p>
