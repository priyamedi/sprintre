<h2 class="test-h2 mt-3"> What is the VDRL test for CSF?</h2>
	<p>
The VDRL test for CSF is a diagnostic test used to detect the presence of syphilis infection in the
brain or spinal cord.</p>
<h2 class="test-h2 mt-3">Why is the test performed on CSF?</h2>
<p>
The test is performed on CSF to detect neurosyphilis, a condition where syphilis infects the
central nervous system.</p>
