<p>The Cortisol Test is a diagnostic test that measures the levels of cortisol in the blood to evaluate the function
    of the adrenal glands and assess how the body is responding to stress.</p>
<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>For a blood test, a healthcare professional will draw a small sample of blood from your arm. These samples are
    then sent to a laboratory for analysis.</p>
