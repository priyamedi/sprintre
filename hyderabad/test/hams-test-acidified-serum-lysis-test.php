<h1 class="test-h1">Ham's Test (For PNH Screen) - Acidified Serum Lysis Test </h1>
<div class="post-text">
<p>Ham's test, also known as the Acidified Serum Lysis Test, is a diagnostic procedure used primarily to identify Paroxysmal Nocturnal Hemoglobinuria (PNH), a rare and potentially life-threatening blood disorder. PNH is characterized by the destruction of red blood cells, known as hemolysis, leading to the release of hemoglobin into the bloodstream and urine. This occurs because the red blood cells in PNH patients are highly sensitive to the complement system, a part of the immune system, resulting in their destruction.
 </p>
<p>In Ham's test, a blood sample is collected from the patient and is mixed with acidified serum. If the patient has PNH, their red blood cells will break down, leading to a positive Ham's test. This test is crucial as it aids in the diagnosis of PNH, guiding the treating doctor in creating a suitable treatment plan.
 </p>
