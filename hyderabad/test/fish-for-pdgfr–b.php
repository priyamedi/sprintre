	<h2 class="test-h2 mt-3">What is the FISH for PDGFR-B test used for?</h2>
<p>A sample of blood or bone marrow is taken from the patient. The sample is then processed in a laboratory where
    special probes are used to visualize the PDGFR-B gene under a microscope.</p>
<h2 class="test-h2 mt-3">Who should consider taking this test?</h2>
<p>No special preparation is needed for the FISH for PDGFR-B test.</p>
<h2 class="test-h2 mt-3">What does a positive result mean?</h2>
<p>A negative result suggests that there is no detectable rearrangement involving the PDGFR-B gene in the analyzed
    cells.</p>
<h2 class="test-h2 mt-3">Can medication affect the result of the test?</h2>
<p>The test itself has very few risks and is similar to having blood drawn for routine testing. However, bone marrow
    biopsy, if required, can cause discomfort and has slightly more risks.</p>
<h2 class="test-h2 mt-3">Is the test conclusive for diagnosing a disease?</h2>
<p>Lifestyle choices such as diet, exercise, and exposure to environmental toxins can potentially affect the expression
    of PDGFR-B. Medication can also modify its activity.</p>
<h2 class="test-h2 mt-3">What are the non-modifiable factors that affect PDGFR-B?</h2>
<p>The frequency of testing varies depending on the underlying condition and the doctor’s recommendation.</p>
<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
<p>If you have abnormal results, you should consult a hematologist or oncologist as they specialize in blood disorders
