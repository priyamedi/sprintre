<h2 class="test-h2 mt-3">Why is the Fungus Culture [STOOL] test performed?</h2>
<p>This test is performed when a healthcare provider suspects a fungal infection in the gastrointestinal tract. Symptoms may include persistent diarrhea, abdominal pain, unexplained weight loss, or blood in the stool.</p>

<h2 class="test-h2 mt-3">How is the stool sample collected for the test?</h2>
<p>The stool sample should be collected in a clean, dry container. It's important to avoid contaminating the stool sample with urine or toilet paper.</p>

