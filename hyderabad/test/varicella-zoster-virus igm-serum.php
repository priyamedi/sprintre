<h2 class="test-h2 mt-3">What is a Varicella Zoster Virus IgM test?</h2>
	<p>
The Varicella Zoster Virus IgM test is a blood test that measures the levels of IgM antibodies specific to
the varicella-zoster virus. High levels of these antibodies suggest a recent or ongoing infection.</p>
<h2 class="test-h2 mt-3">Who should get a Varicella Zoster Virus IgM test?</h2>
<p>
This test is often recommended to individuals who exhibit signs of a varicella-zoster virus infection, such
as a rash of itchy blisters. It can also be useful for those in close contact with infected individuals or for
healthcare workers in relevant settings.</p>
