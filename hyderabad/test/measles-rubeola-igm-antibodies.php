<p>This test is performed to determine if a person has a current or recent measles infection. It is particularly
useful in cases where the symptoms suggest measles.</p>

<h2 class="test-h2 mt-3">How is the sample collected?</h2>
<p>A healthcare professional will collect a blood sample from a vein, typically in your arm.</p>
