<h2 class="test-h2 mt-3">Why would I need a TBG test?</h2>
<p>A TBG test may be ordered if you have symptoms of a thyroid disorder or if your healthcare provider wants to evaluate
    the amount of TBG in your blood due to certain conditions like liver disease or pregnancy.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>A small blood sample is required for this test, typically drawn from a vein in your arm.</p>
