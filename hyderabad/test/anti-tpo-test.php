<h2 class="test-h2 mt-3">What is the Anti-TPO test? </h2>
<p>The Anti-TPO test is a blood test that measures the levels of anti-thyroid peroxidase antibodies in the blood. Elevated levels of Anti-TPO indicate that the body's immune system is attacking the thyroid gland.</p>
<h2 class="test-h2 mt-3">Why is the Anti-TPO test important? </h2>
<p>This test is important for diagnosing autoimmune thyroid diseases such as Hashimoto's thyroiditis and Graves' disease. It is also used to help understand the cause of an underactive or overactive thyroid.</p>
