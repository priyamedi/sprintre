	<h2 class="test-h2 mt-3">What is the Allergen, Individual - Food Celery test?
 </h2>
	<p>The test is designed to determine whether your body has an allergic response to celery. This is established by examining the level of specific IgE antibodies in your blood that react to celery.
 </p>
	<h2 class="test-h2 mt-3">Why is this test important?
 </h2>
	<p>This test is crucial if you experience symptoms suggesting an allergic reaction after eating celery. Symptoms can include itchy or tingly mouth, hives, <a href="https://www.sprintdiagnostics.in/symptoms/nausea-and-vomiting"><b style="color:#2C53A5">nausea</b></a>, stomach cramps, or in severe cases, anaphylaxis.
 </p>
