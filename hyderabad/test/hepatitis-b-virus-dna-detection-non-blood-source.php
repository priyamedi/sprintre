<h2 class="test-h2 mt-3">What is the Hepatitis B Virus DNA Detection test using non-blood sources?</h2>
	<p>
This test uses qualitative Polymerase Chain Reaction (PCR) technology to detect the presence of
Hepatitis B virus DNA in non-blood samples such as saliva, urine, or tissue.</p>
<h2 class="test-h2 mt-3"> Why is testing on non-blood sources important?</h2>
<p>
Testing on non-blood sources is important when blood collection is not feasible or preferred, and it
provides an alternative method for the detection of Hepatitis B virus DNA.</p>
