<h2 class="test-h2 mt-3">How is the sample collected?</h2>
<p>The sample is collected through chorionic villus sampling, a procedure where a small sample of cells from the
    placenta is removed using a thin needle or tube.</p>
<h2 class="test-h2 mt-3">What are the risks associated with chorionic villus sampling?</h2>
<p>Chorionic villus sampling is generally safe but carries minor risks, such as infection, bleeding, or miscarriage.
    It’s important to discuss the risks with your healthcare provider.</p>
<h2 class="test-h2 mt-3">Q5: What does a positive result indicate?</h2>
<p>A positive result indicates that the fetus has a mutation in the DMD gene and is likely to have Duchenne Muscular
    Dystrophy.</p>
<h2 class="test-h2 mt-3">Are there options if the test is positive for DMD?</h2>
<p>If the test is positive for DMD, it is essential to have genetic counseling to discuss options, which may include
    continuation of the pregnancy with management plans or considering termination.</p>
<h2 class="test-h2 mt-3">What is the benefit of early diagnosis?</h2>
<p>Early diagnosis allows families to make informed decisions regarding the pregnancy and to prepare for the management
    of DMD if they choose to continue the pregnancy.</p>
<h2 class="test-h2 mt-3">Is there a cure for Duchenne Muscular Dystrophy?</h2>
<p>Currently, there is no cure for DMD, but there are treatments that can help manage symptoms and improve quality of
    life.</p>
<h2 class="test-h2 mt-3">What treatment options are available for DMD?</h2>
<p>Treatments for DMD include medications, physical therapy, respiratory support, and surgical interventions to manage
    symptoms and slow disease progression.</p>
<h2 class="test-h2 mt-3">Q10: How is DMD inherited?</h2>
<p>DMD is inherited in an X-linked recessive pattern. Males inherit the mutated gene from their mothers, who are usually
    carriers of the gene.</p>
<h2 class="test-h2 mt-3">Can females be affected by DMD?</h2>
<p>Females are typically carriers of DMD and may have mild symptoms. In rare cases, females may have a more severe form
    of the disorder.</p>
<h2 class="test-h2 mt-3">Can DMD be prevented?</h2>
<p>DMD cannot be prevented, but with prenatal testing like the DMD Test for CVS, families can make informed decisions
    regarding pregnancies at risk for DMD.</p>
<h2 class="test-h2 mt-3">Are there support groups for families affected by DMD?</h2>
<p>Yes, there are support groups and organizations that provide information, resources, and support to families affected
    by DMD.</p>
<h2 class="test-h2 mt-3">Is there ongoing research for DMD?</h2>
<p>There is ongoing research aimed at finding a cure and better treatments for DMD. This includes gene therapy,
    medication trials, and studies on muscle function.</p>
<h2 class="test-h2 mt-3">What is the long-term outlook for individuals with DMD?</h2>
<p>The long-term outlook for individuals with DMD varies. The disorder is progressive, and individuals may experience a
    decline in muscle function over time. Early intervention and management can improve the quality of life.</p>
 
