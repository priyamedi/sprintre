<h2 class="test-h2 mt-3">What is the STD Profile?</h2>
<p>The STD Profile is a series of tests used to identify various STDs. It includes tests for HIV antibodies, HSV IgM
	antibodies, VDRL and TPHA (for syphilis), and Chlamydia trachomatis IgG.</p>
<h2 class="test-h2 mt-3">Why is the STD Profile done?</h2>
<p>The STD Profile is done to identify or rule out sexually transmitted diseases in individuals who exhibit symptoms of
	such diseases or those who have a high risk of contracting them.</p>
