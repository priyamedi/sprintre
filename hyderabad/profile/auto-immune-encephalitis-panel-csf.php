<h2 class="test-h2 mt-3"> What is the purpose of the Auto Immune Encephalitis Panel-CSF?</h2>
	<p>
The Auto Immune Encephalitis Panel-CSF aims to detect specific autoantibodies associated with
different types of autoimmune encephalitis in the cerebrospinal fluid. It assists in diagnosing the exact
type of encephalitis, which is key to implementing effective treatment. </p>
<h2 class="test-h2 mt-3"> How is the cerebrospinal fluid (CSF) sample obtained for the test?</h2>
<p>
The CSF sample is usually obtained through a lumbar puncture (spinal tap). This procedure involves
inserting a needle into the lower back to withdraw cerebrospinal fluid. </p>
