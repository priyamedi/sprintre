<h2 class="test-h2 mt-3">What is the TSH Receptor Antibodies test?</h2>
<p>The TSH Receptor Antibodies (TRAb) test is a blood test that measures the level of TRAb in the blood. These
    antibodies can interfere with the function of the thyroid gland and lead to thyroid diseases.</p>
<h2 class="test-h2 mt-3">Why would I need a TRAb test?</h2>
<p>Your healthcare provider might order a TRAb test if you have symptoms of an autoimmune thyroid disorder, such as
    Graves' disease or Hashimoto's thyroiditis, especially if other thyroid tests are inconclusive.</p>
