<h2 class="test-h2 mt-3">Why is the Fungus Culture [URETHRAL SWAB] test performed?</h2>
<p>The Fungus Culture [URETHRAL SWAB] test is performed when a healthcare provider suspects a fungal infection in the urethra. Symptoms that might warrant this test include pain during urination, discharge, or itching around the urethral opening.</p>

<h2 class="test-h2 mt-3">How is the urethral swab sample collected?</h2>
<p>During this test, a healthcare professional will carefully insert a sterile swab into the urethra to collect a sample. While this procedure can cause some discomfort, it is generally quick and not painful.</p>

