<h1 class="test-h1">Bilirubin Total</h1>
<div class="post-text">
<p>Bilirubin is a yellow pigment that occurs in the blood and is a product of the breakdown of old red blood cells. The liver processes bilirubin so it can be eliminated from the body through the stool. A Bilirubin Total test measures the total amount of bilirubin in the blood, including both conjugated (direct) and unconjugated (indirect) forms. This test is vital in evaluating liver function, diagnosing liver diseases such as hepatitis or cirrhosis, and identifying conditions like jaundice.</p>
<p>The Bilirubin Total test is usually part of a liver panel, which is a group of tests that provide a comprehensive picture of liver function. It’s especially important for newborns, as high levels of bilirubin can be an indicator of jaundice, which can be harmful if left untreated.</p>
