<h2 class="test-h2 mt-3">What is the purpose of the Hepatitis Panel - Atypical Virus Evaluation?</h2>
<p>The purpose of this panel is to identify the presence of specific atypical viral infections that may cause liver
    inflammation or hepatitis. The presence of these viruses may suggest that the patient's symptoms are not due to the
    traditional hepatitis viruses but due to an atypical virus.</p>
<h2 class="test-h2 mt-3">What does a positive result mean?</h2>
<p>A positive result indicates the presence of the specific antibody in your bloodstream, suggesting a recent or current
    infection with the corresponding atypical virus.</p>
