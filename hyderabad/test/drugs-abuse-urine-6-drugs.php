<h2 class="test-h2 mt-3">Why is the Drugs of Abuse urine test performed?</h2>
<p>This test is performed to determine if an individual has used any of the six drugs it screens for. It can be part of
    employment screenings, legal requirements, or monitoring for drug rehabilitation programs.</p>
<h2 class="test-h2 mt-3">How is the Drugs of Abuse urine test performed?</h2>
<p>A urine sample is collected in a clean container. The sample is then sent to a laboratory where it is analyzed for
    the presence of the metabolites of the six drugs.</p>
