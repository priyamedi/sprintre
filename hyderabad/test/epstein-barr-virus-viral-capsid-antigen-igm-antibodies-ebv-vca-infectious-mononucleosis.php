	<h2 class="test-h2 mt-3">Why is the EBV VCA IgM test ordered? </h2>
	<p>This test is often ordered when a person, especially a young adult or teenager, has symptoms suggestive of mononucleosis such as fatigue, fever, sore throat, swollen lymph nodes, and sometimes a swollen spleen.
 </p>
