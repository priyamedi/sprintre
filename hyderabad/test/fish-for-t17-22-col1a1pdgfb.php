	<h2 class="test-h2 mt-3"> Who should undergo the FISH for t(17;22) COL1A1/PDGFB test?</h2>
	<p> Individuals who have a skin lesion suspected to be DFSP, based on clinical examination and initial histopathological evaluation, should undergo this test.</p>
	<h2 class="test-h2 mt-3"> What does a positive result for t(17;22) COL1A1/PDGFB indicate?</h2>
