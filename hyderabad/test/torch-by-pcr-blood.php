<h2 class="test-h2 mt-3">Why is the TORCH by PCR test performed?</h2>
<p>The test is used to detect active infection with Toxoplasma, CMV, Rubella, or Herpes viruses, particularly in
    pregnant women. These infections can lead to severe complications for the fetus and newborn. Early detection allows
    for prompt treatment and management to reduce these risks.</p>
<h2 class="test-h2 mt-3">How is the TORCH by PCR test performed?</h2>
<p>The test requires a blood sample, typically drawn from a vein in your arm. The sample is then sent to a laboratory
    where PCR technology is used to identify the genetic material of the pathogens.</p>
