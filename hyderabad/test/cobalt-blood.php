<h2 class="test-h2 mt-3">What is the purpose of the Cobalt, Blood test?</h2>
<p>The purpose of this test is to measure the amount of cobalt in your blood. This test is often used to check for
    cobalt exposure in those who work in industries that use cobalt or for patients who have cobalt-containing implants,
    such as some types of hip replacements.</p>
<h2 class="test-h2 mt-3">Do I need to fast before this test?</h2>
<p>No, there is no need to fast before this test. You can continue with your usual eating and drinking routines.</p>
