<h1 class="test-h1">Hepatitis Panel - Acute Screen-2</h1>
<div class="post-text">
<p>Hepatitis, a medical condition defined by the inflammation of the liver, is typically caused by a viral infection. However, other possible causes of hepatitis include autoimmune hepatitis and hepatitis that occurs as a secondary result of medications, drugs, toxins, and alcohol. Hepatitis viruses are the most common cause of hepatitis in the world but other infections, toxic substances (e.g., alcohol, certain drugs), and autoimmune diseases can also cause hepatitis.</p>
<p>Hepatitis can be temporary (acute) or long term (chronic) depending on whether it lasts for less than or more than six months. The Hepatitis Panel - Acute Screen-II is a blood test that checks for different types of hepatitis viruses, including Hepatitis A (HAV), Hepatitis B (HBV), Hepatitis C (HCV), and Hepatitis E (HEV). This panel consists of different individual tests that detect specific antibodies and antigens related to these viruses.</p>

