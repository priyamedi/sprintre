<h2 class="test-h2 mt-3">What does a negative result mean?</h2>
<p>A negative result suggests the patient's symptoms are not due to the bacteria included in the panel. Other tests may
    be needed to identify the cause of the symptoms.</p>
<h2 class="test-h2 mt-3">Is there any risk associated with this test?</h2>
<p>The test involves taking a sample from the throat or respiratory tract which may cause mild discomfort. However, it
