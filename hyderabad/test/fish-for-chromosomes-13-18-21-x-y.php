<h2 class="test-h2 mt-3">What does FISH for Chromosomes 13, 18, 21, X & Y entail?</h2>
<p>FISH for these chromosomes is a diagnostic test that uses fluorescent probes to visualize and map the
genetic material in an individual's cells. It is often used to detect abnormalities in chromosomes 13, 18,
21, X, and Y, which can be associated with various genetic disorders.</p>

<h2 class="test-h2 mt-3">Why is this test important?</h2>
<p>This test is important because it helps in detecting chromosomal abnormalities early, often during
pregnancy. Early diagnosis can assist in making informed decisions regarding the management of the
pregnancy and preparations for any special care that might be needed after birth.</p>
