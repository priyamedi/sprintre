<p>No, this test specifically looks for mutations in the FAH gene, which is associated with Tyrosinemia Type I.</p>
<h2 class="test-h2 mt-3">Does a positive result mean I will develop symptoms?</h2>
<p>Not necessarily. While a positive result indicates that you have a mutation that can cause Tyrosinemia, it does not
