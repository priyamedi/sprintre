<h1 class="test-h1">Karyotyping (CVS/Amniotic Fluid)</h1>
<div class="post-text">
<p>Karyotyping involving Chorionic Villus Sampling (CVS) or Amniotic Fluid is a genetic test that analyzes the chromosomes of a fetus. Chromosomes are the structures in cells that carry genes. Normally, each human cell contains 46 chromosomes. However, if there is an abnormal number or structure of chromosomes, it can lead to genetic disorders and developmental abnormalities.</p>

<p>Chorionic villi are tiny finger-like structures found in the placenta, whereas amniotic fluid is the fluid surrounding the fetus inside the womb. CVS involves collecting a sample of these chorionic villi, while amniocentesis involves taking a sample of the amniotic fluid. Both of these samples contain the fetal cells needed for karyotyping.</p>

<p>This procedure is generally performed during pregnancy to check for genetic disorders in the fetus. It is often recommended for individuals who have a family history of genetic disorders, are of advanced maternal age, or have had abnormal results from other prenatal tests.</p>
