<h1 class="test-h1">IHC - Microsatellite Instability for Colorectal Carcinoma PMS-2</h1>
<div class="post-text">
<p><strong>Microsatellite instability (MSI)</strong> is a condition that leads to changes in the lengths of short sequences of DNA. This instability is caused due to the loss of DNA mismatch repair (MMR) function, which can result in the development of cancer. Among the MMR proteins, PMS2 protein is one of them. An IHC - PMS2 test is performed to check for the presence or absence of this protein in the cells of a tissue sample. The absence of PMS2 can indicate a high likelihood of MSI.</p>
<p>This test plays a crucial role in the diagnosis and treatment planning of colorectal carcinoma. Colorectal carcinoma is the third most common cancer and is also a leading cause of cancer-related deaths. The results of this test help in identifying individuals with a genetic condition known as Lynch syndrome, also known as hereditary nonpolyposis colorectal cancer (HNPCC), which increases the risk of colorectal cancer and certain other types of cancer.</p>

