<h2 class="test-h2 mt-3">What does the NIPT test involve? </h2>
<p>This test involves drawing a sample of blood from the mother. This sample is then sent to a laboratory where cell-free DNA from the fetus is isolated and analyzed.</p>

<h2 class="test-h2 mt-3">How is NIPT different from traditional prenatal screening tests? </h2>
<p>NIPT is a screening test that offers more accurate information, a lower false-positive rate, and can be performed earlier in pregnancy than traditional screening tests. Additionally, because it is a non-invasive test, it does not carry the risk of miscarriage that invasive diagnostic tests such as amniocentesis and chorionic villus sampling (CVS) do.</p>

