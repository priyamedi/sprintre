<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>The frequency of this test depends on your doctor's recommendation and your symptoms. It's usually not done routinely.</p>

<h2 class="test-h2 mt-3">What are the normal values for the Smith (Sm) IgG Antibodies test?</h2>
