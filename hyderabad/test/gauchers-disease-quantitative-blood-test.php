<div class="post-text">

<p>Gaucher's disease is an inherited, genetic disorder resulting from a deficiency of an enzyme called glucocerebrosidase. This enzyme deficiency leads to the accumulation of certain specific fatty substances in organs, primarily the spleen and liver, causing these organs to enlarge and not function normally. Gaucher's disease is the most common of the lysosomal storage diseases. It is a form of sphingolipidosis, as it involves dysfunctional metabolism of sphingolipids.</p>

<p>The quantitative blood test for Gaucher's disease is used to measure the amount of glucocerebrosidase in the blood, which aids in diagnosing this disorder. Genetic testing can also be used to confirm a diagnosis of Gaucher's disease.</p>

