<h2 class="test-h2 mt-3">What is APOE mutation screening?</h2>
<p> APOE mutation screening is a genetic test that examines variations in the APOE gene, which is involved in fat metabolism. The test is mainly used to assess the risk of Alzheimer's disease and cardiovascular conditions.</p>
<h2 class="test-h2 mt-3">Why is APOE mutation screening important?</h2>
