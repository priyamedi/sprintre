<h2 class="test-h2 mt-3">What is the Quantitative Rubella Virus IgG Antibody Test?</h2>
<p>This test measures the level of IgG antibodies against the Rubella virus in your blood. It helps to determine whether
    you have been recently infected, are currently infected, or have immunity against rubella.</p>
<h2 class="test-h2 mt-3">Do I need to fast before this test?</h2>
<p>No, fasting is not required for this test.</p>
