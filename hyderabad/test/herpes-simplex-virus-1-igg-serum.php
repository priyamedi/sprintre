<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is primarily done to confirm a past infection with HSV-1. It may also be used to diagnose atypical cases of
    HSV infection, such as herpes simplex encephalitis or disseminated herpes infection, especially in people with
    compromised immune systems.</p>
<h2 class="test-h2 mt-3">What does a positive HSV-1 IgG test mean?</h2>
<p>A positive HSV-1 IgG test indicates that the individual has been infected with the HSV-1 virus at some point in the
    past. This person is now likely to have some immunity against the virus.</p>
