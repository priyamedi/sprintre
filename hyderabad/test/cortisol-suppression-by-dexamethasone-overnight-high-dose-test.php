    syndrome.</p>
<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>The test requires taking a high dose of dexamethasone, usually at 11 p.m., and having blood drawn the next morning
    at 8 a.m. for cortisol measurement.</p>
