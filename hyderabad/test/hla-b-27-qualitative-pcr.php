<h2 class="test-h2 mt-3">What does the HLA B-27 Qualitative PCR test detect?</h2>
<p> This test detects the presence or absence of the <strong>HLA B-27</strong> gene variant. The presence of this gene variant is associated with a higher risk of developing certain autoimmune conditions, most notably ankylosing spondylitis.</p>
<h2 class="test-h2 mt-3">Who should consider this test?</h2>
<p> This test is often recommended for individuals experiencing <a href="https://www.sprintdiagnostics.in/symptoms"><b style="color:#2C53A5">symptoms</b></a> of ankylosing spondylitis, such as chronic lower back pain, or who have a family history of autoimmune disorders associated with HLA B-27.</p>
