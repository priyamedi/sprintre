<h2 class="test-h2 mt-3">What is the Respiratory Tract Infection Profile - IgM diagnostic test?</h2>
<p>It is a diagnostic test that detects the presence of IgM antibodies against a panel of pathogens that commonly cause
    respiratory infections. These include Mycoplasma pneumoniae, Coxiella burnetti, Chlamydia pneumonia, RSV, Legionella
    pneumophila, Influenza virus, Adenovirus, and Parainfluenza virus.</p>
<h2 class="test-h2 mt-3">Who should take this test?</h2>
<p>This test is generally recommended for individuals presenting with symptoms of a respiratory tract infection, such as
    cough, fever, shortness of breath, and fatigue, particularly if initial treatments have not improved the symptoms.
