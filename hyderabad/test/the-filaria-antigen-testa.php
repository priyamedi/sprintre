<h2 class="test-h2 mt-3">What is the Filaria Antigen Test used for?</h2>
<p>The Filaria Antigen Test is used to detect antigens produced by filarial worms in the blood, which is
indicative of a filarial infection. It is used for early diagnosis and monitoring of filariasis.</p>

<h2 class="test-h2 mt-3">How is the Filaria Antigen Test performed?</h2>
<p>The test is performed by taking a sample of blood, usually from a vein in the arm. The blood is then
tested in a laboratory for the presence of filarial antigens.</p>
