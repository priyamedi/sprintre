<h2 class="test-h2 mt-3">What is the Sodium - Serum test?</h2>
<p>The Sodium - Serum test is a blood test that measures the amount of sodium, an important electrolyte, in your blood.</p>

<h2 class="test-h2 mt-3">Why is the Sodium - Serum test done?</h2>
<p>This test is usually ordered as a part of a routine metabolic panel or if a doctor suspects an electrolyte imbalance, kidney disease, or a condition affecting the levels of water in your body.</p>

