	<h2 class="test-h2 mt-3">What is the MPN-1 test? </h2>
	<p>The MPN-1 test is a diagnostic procedure used to detect chromosomal abnormalities (through karyotyping) and the JAK2V617F mutation (through PCR) in patients suspected of having Myeloproliferative Neoplasms (MPNs). </p>
	<h2 class="test-h2 mt-3"> Why is the MPN-1 test important? </h2>
	<p>The MPN-1 test is crucial in the diagnosis and management of MPNs. Detecting the JAK2V617F mutation or chromosomal abnormalities can help confirm a diagnosis, provide information about the disease’s progression, and guide treatment decisions. </p>
