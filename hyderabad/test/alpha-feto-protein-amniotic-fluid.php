<h2 class="test-h2 mt-3">What is Alpha Feto Protein?</h2>
<p>Alpha Feto Protein (AFP) is a protein produced by the yolk sac and the liver of the developing fetus. Levels of AFP can be measured in both the blood of the pregnant woman and in the amniotic fluid.</p>
<h2 class="test-h2 mt-3">What is the purpose of measuring Alpha Feto Protein in amniotic fluid?</h2>
<p>Measuring AFP levels in amniotic fluid is primarily used for the detection of certain birth defects, such as neural tube defects (e.g., spina bifida or anencephaly) and abdominal wall defects. It may also be elevated in other conditions such as fetal distress or death.</p>
