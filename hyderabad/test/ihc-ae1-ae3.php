	<h2 class="test-h2 mt-3">What should I do if my results are abnormal?</h2>
<p>If your results are abnormal, it's important to discuss them with your doctor. They can help interpret the results and guide you on the next steps, which may include further testing or treatment options.</p>

	<h2 class="test-h2 mt-3">Can this test differentiate between different types of cancer?</h2>
