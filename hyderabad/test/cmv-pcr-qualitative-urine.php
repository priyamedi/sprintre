<h2 class="test-h2 mt-3">What is the CMV - PCR Qualitative test in urine?</h2>
<p>The CMV - PCR Qualitative test in urine is a diagnostic test that detects the presence of CMV DNA in the
urine. It helps identify an active CMV infection.</p>

<h2 class="test-h2 mt-3">Who might need this test?</h2>
<p>This test is primarily used in immunocompromised individuals such as HIV patients, organ transplant
recipients, or people undergoing chemotherapy. It may also be used in newborns to diagnose congenital
CMV infection.</p>
