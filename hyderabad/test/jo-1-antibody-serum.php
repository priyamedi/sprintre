<h2 class="test-h2 mt-3">Can the test be performed during pregnancy?</h2>
<p>Yes, the test can be performed during pregnancy if it's necessary. However, you should always discuss the need and
    the timing of the test with your healthcare provider.</p>
<h2 class="test-h2 mt-3">What do abnormal test results indicate?</h2>
<p>Abnormal results typically indicate the presence of an autoimmune disorder such as polymyositis or dermatomyositis.
    Higher levels of Jo-1 antibodies are suggestive of an ongoing autoimmune response in the body.</p>
<h2 class="test-h2 mt-3">Can this test differentiate between different autoimmune disorders?</h2>
<p>This test primarily helps in identifying conditions like polymyositis and dermatomyositis. However, it may not
