<h1 class="test-h1">Lung Cancer Mutation Panel- EGFR PCR with ALK-D5F3 IHC </h1>
<div class="post-text">
<p>The EGFR gene makes a protein that helps cells grow and divide. Mutations in this gene can cause the protein to be overactive, leading to the development of cancer. Identifying such mutations can help doctors determine the most effective treatment plan, often involving targeted therapies.
 </p>
<p>ALK rearrangements, on the other hand, involve changes in the ALK gene that can lead to abnormal cell growth. They are less common but can be present in certain types of lung cancer. Identifying ALK rearrangements is important because they can be specifically targeted by certain therapies.
 </p>
 <p>The D5F3 antibody used in this test is specific for the ALK protein and is used to detect the presence of the protein in tissue samples. The presence of the ALK protein suggests an ALK rearrangement.
</p>
