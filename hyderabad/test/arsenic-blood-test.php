	<h2 class="test-h2 mt-3">Why is this test important? </h2>
	<p>This test is crucial for those who might have been exposed to arsenic through occupational hazards or are living in an area where water or soil is contaminated with arsenic. It is used to detect acute or recent exposure to arsenic. </p>
	<h2 class="test-h2 mt-3">What information does the test provide?  </h2>
	<p>The test measures the amount of arsenic present in the blood. This information can be used to assess if an individual has been exposed to hazardous levels of arsenic and help in making decisions regarding treatment or further evaluation. </p>
