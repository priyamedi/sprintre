	<h2 class="test-h2 mt-3">What information does the AML Cytogenetics 2 panel provide? </h2>
	<p>This panel provides detailed information regarding chromosomal structure and specific abnormalities such as AML-ETO, Inv(16), and MLL. This information is critical for diagnosing AML, identifying its subtype, and understanding its progression. </p>
	<h2 class="test-h2 mt-3">Is fasting required for this test? </h2>
	<p>No, fasting is not required for the AML Cytogenetics 2 panel. </p>
	<h2 class="test-h2 mt-3">When should I get this test done? </h2>
	<p>This test is generally recommended if you have signs or symptoms suggestive of leukemia or if abnormal blood counts are detected during routine blood tests. It's also used for monitoring the disease during treatment. </p>
	<h2 class="test-h2 mt-3">What are the normal values for this test? </h2>
	<p>In a healthy individual, there should be no chromosomal abnormalities. The presence of the specific abnormalities tested for in this panel is indicative of AML. </p>
	<h2 class="test-h2 mt-3">What factors can affect the test results? </h2>
	<p>Genetic mutations, the presence of leukemia cells, and certain medications can affect the results of this test. </p>
	<h2 class="test-h2 mt-3">How will this test affect my treatment options? </h2>
	<p>The results of this test will help your doctor to tailor the treatment based on the specific subtype of AML, which can be more effective in managing the disease. </p>
	<h2 class="test-h2 mt-3">What doctor should I consult if I have abnormal test results? </h2>
	<p>If the test results are abnormal, it is important to consult a hematologist or oncologist who specializes in blood cancers. </p>
	<h2 class="test-h2 mt-3">Can this test be done at home? </h2>
	<p>No, this test requires a sample of blood or bone marrow, which must be collected in a medical facility. </p>
	<h2 class="test-h2 mt-3">Are there any risks associated with this test? </h2>
	<p>The blood draw is relatively safe but may cause minor pain. When a bone marrow sample is needed, there may be pain at the site of the biopsy. </p>
	<h2 class="test-h2 mt-3">What is FISH and how does it help in the diagnosis of AML? </h2>
	<p>FISH or Fluorescence In Situ Hybridization is a test that maps the genetic material in human cells, including specific genes or portions of genes. In diagnosing AML, it is used to find specific changes in chromosomes and genes that might not be visible under a microscope. </p>
	<h2 class="test-h2 mt-3">What is Inv(16) and why is it significant? </h2>
	<p> Inv(16) is a chromosomal abnormality where part of chromosome 16 is inverted. It is one of the common abnormalities in AML and is associated with a particular subtype of the disease. It has specific implications for prognosis and treatment.</p>
