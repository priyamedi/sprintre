<h2 class="test-h2 mt-3">What is the MRD-T-ALL Panel?</h2>
<p> The MRD-T-ALL Panel is a diagnostic test that employs Flow Cytometry to detect minimal residual disease (MRD) in patients with T-cell Acute Lymphoblastic Leukemia (T-ALL).</p>

<h2 class="test-h2 mt-3">Do I need to fast or prepare for this test? </h2>
<p>There is no need for special preparation or fasting before this test. However, make sure to inform your doctor about any ongoing medications or health conditions.</p>

