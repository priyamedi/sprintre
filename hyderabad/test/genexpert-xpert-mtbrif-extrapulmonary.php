<h2 class="test-h2 mt-3">What is the GeneXpert MTB/RIF test?</h2>
<p>The GeneXpert MTB/RIF test is a rapid molecular test that detects the presence of Mycobacterium tuberculosis complex,
    the bacteria that causes tuberculosis, and resistance to the antibiotic rifampicin.</p>
<h2 class="test-h2 mt-3">Why is this test important?</h2>
<p>This test is crucial because it rapidly diagnoses tuberculosis and identifies rifampicin resistance, which is a
    marker for multidrug-resistant tuberculosis. Early detection allows for timely initiation of appropriate treatment,
    reducing the risk of disease spread and severe complications.</p>
