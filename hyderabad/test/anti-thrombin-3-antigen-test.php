<h2 class="test-h2 mt-3">What is the Anti-Thrombin III Antigen test?</h2>
<p> The Anti-Thrombin III Antigen test measures the amount of anti-thrombin protein in the blood. This protein is crucial in preventing excessive blood clotting.</p>
<h2 class="test-h2 mt-3">How is the Anti-Thrombin III Antigen test performed?</h2>
<p> A healthcare professional will take a blood sample from a vein in your arm. The sample is then sent to a laboratory where the amount of anti-thrombin protein is measured.</p>
