<h1 class="test-h1">HE4 (Human Epididymis Protein-4) Test </h1>
<div class="post-text">
<p>Human Epididymis Protein 4 (HE4) is a protein that is found in low levels in the blood of most individuals. However, increased levels of HE4 have been associated with certain types of cancer, most notably ovarian cancer. The HE4 test is a blood test that measures the amount of HE4 in the blood and can be used as a tumor marker to monitor the progression of ovarian cancer or to detect the recurrence of the disease in women who have already been diagnosed and treated for ovarian cancer.
 </p>
<p>It is important to note that the HE4 test is not used for the initial diagnosis of ovarian cancer, as levels can be elevated for other reasons. The test is most commonly used in conjunction with other tests and diagnostic procedures to evaluate the presence and progression of ovarian cancer in patients who are known to have the disease or have a high risk of developing it.
 </p>
