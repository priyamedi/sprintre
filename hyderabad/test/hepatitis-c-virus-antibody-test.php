<h2 class="test-h2 mt-3"> What is the Hepatitis C Virus (HCV) Antibody Test?</h2>
	<p>
The HCV Antibody Test is a blood test that checks for antibodies to the Hepatitis C virus. It is used to
screen individuals for a past or current Hepatitis C infection.</p>
<h2 class="test-h2 mt-3"> Who should undergo this test?</h2>
<p>
Anyone who has been exposed to the Hepatitis C virus, such as healthcare workers, people who have
used injected drugs, received a blood transfusion or organ transplant before 1992, or have engaged in
high-risk sexual behavior, should consider taking this test.</p>
