<h2 class="test-h2 mt-3">Why is the Amino Acid CSF Qualitative Screen performed?</h2>
<p>This test is performed to evaluate the levels of amino acids in the cerebrospinal fluid, which can help diagnose and monitor metabolic and neurological disorders, especially in cases where symptoms suggest an imbalance of amino acids affecting the nervous system.</p>

<h2 class="test-h2 mt-3">How is the CSF sample collected?</h2>
<p>The CSF sample is typically collected through a lumbar puncture, or spinal tap, which involves inserting a needle into the lower back to draw fluid from the spinal canal. This procedure is performed by a trained medical professional.</p>

