<div class="post-text">
<p>Fragile X Syndrome (FXS) is a genetic condition associated with intellectual disability, behavioral and learning challenges, as well as specific physical characteristics. It is the most common known single-gene cause of autism and inherited intellectual disability. The FMR1 Mutation Screen is a diagnostic test that identifies the mutations in the FMR1 gene that cause FXS.
 </p>
<p>The FMR1 gene, located on the X chromosome, typically contains a certain number of CGG repeats. However, in individuals with Fragile X Syndrome, this number is significantly higher. The FMR1 mutation screen helps detect the increased number of CGG repeats in the FMR1 gene.
 </p>
