	<h2 class="test-h2 mt-3">What is pleural fluid?</h2>
	<p> Pleural fluid is a thin layer of fluid that lubricates the pleural surfaces, allowing the lungs to move
smoothly within the chest cavity during breathing. When excess fluid accumulates in the pleural space, it
is referred to as pleural effusion.</p>
	<h2 class="test-h2 mt-3">How is pleural fluid collected for Culture and Sensitivity testing?
 </h2>
	<p> Pleural fluid is collected via a procedure known as thoracentesis. During this procedure, a needle is
inserted through the chest wall into the pleural space. Ultrasound may be used to guide the needle to
the appropriate area for fluid collection. </p>
