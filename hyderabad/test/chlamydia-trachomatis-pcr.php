	<h2 class="test-h2 mt-3">What is the Chlamydia Trachomatis PCR test? </h2>
	<p>The Chlamydia Trachomatis PCR test is a type of diagnostic tool that detects the DNA of the Chlamydia trachomatis bacteria in a sample from the patient, usually a swab from the genital area or urine. </p>
	<h2 class="test-h2 mt-3"> Why is this test done? </h2>
	<p>This test is performed when a patient presents with symptoms suggestive of Chlamydia trachomatis infection, such as unusual genital discharge or discomfort during urination. It can also be conducted as part of routine STI screening for individuals with multiple sexual partners or a new sexual partner.
 </p>
