<h2 class="test-h2 mt-3"> What is the Factor V Activity test?</h2>
	<p>The Factor V Activity test measures the activity and levels of Factor V, an essential protein that aids in
blood clotting. This test helps in the evaluation of clotting function and in the diagnosis of disorders
related to Factor V.</p>

<h2 class="test-h2 mt-3"> Why is this test done?</h2>
<p>This test is done to investigate the cause of abnormal bleeding or clotting. It is also used to diagnose
Factor V Leiden mutation and Factor V deficiency, which are genetic disorders affecting blood clotting.</p>
