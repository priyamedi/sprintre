<h2 class="test-h2 mt-3">What is the Alcohol (Ethanol), Quantitative - Urine test?</h2>
<p>The <strong>Alcohol (Ethanol), Quantitative - Urine test</strong> is a urine test that measures the amount of ethanol, a type of alcohol, in your urine.</p>
<h2 class="test-h2 mt-3">Why is the Alcohol (Ethanol), Quantitative - Urine test done?</h2>
