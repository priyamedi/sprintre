<h2 class="test-h2 mt-3"> What is the Ante Natal Profile IV?</h2>
	<p>
Ante Natal Profile IV is an extensive set of tests performed during pregnancy. It includes all the tests of
Ante Natal Profile II and additional tests for Antibody Screening, and Rh & Kell typing.</p>
<h2 class="test-h2 mt-3"> Why are Antibody Screening and Rh & Kell typing included in this profile?</h2>
<p>
Antibody Screening is included to detect atypical antibodies in the blood that may impact the
pregnancy. Rh & Kell typing is done to identify any blood group incompatibility between the mother and
the unborn child, which could potentially lead to hemolytic disease of the newborn, a serious condition.</p>
