<h2 class="test-h2 mt-3">What is Alpha Feto Protein?</h2>
<p>Alpha Feto Protein (AFP) is a protein that is produced in large amounts by the developing fetus. In adults, its levels are typically very low, but can be elevated in certain liver diseases and cancers.</p>
<h2 class="test-h2 mt-3">Why is AFP measured in ascitic fluid?</h2>
<p>Ascites is the buildup of fluid in the space between the lining of the abdomen and the organs. Measuring AFP in ascitic fluid can be used to help determine the cause of the ascites, particularly in the context of liver disease and liver cancer.</p>
