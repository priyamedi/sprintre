<h2 class="test-h2 mt-3">Why is the Real Time PCR for Zika Virus test ordered?</h2>
<p>The test may be ordered if you have symptoms of Zika virus infection and you live in or have recently traveled to an
    area where Zika is known to be present.</p>
<h2 class="test-h2 mt-3">How is the sample collected for the test?</h2>
<p>The test requires a blood sample, which is drawn from a vein in your arm.</p>
