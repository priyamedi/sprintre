<h2 class="test-h2 mt-3"> Why would my doctor order a Neuronal (Paraneoplastic) Antibody Profile test?</h2>
	<p>
Your doctor may order this test if you have unexplained neurological symptoms and they suspect a
paraneoplastic syndrome. The test can help identify antibodies associated with these syndromes,
providing clues about the presence of an underlying cancer.</p>
<h2 class="test-h2 mt-3"> What does a positive result mean?</h2>
<p>
A positive result means that one or more of the antibodies the test looks for was detected in your blood.
This suggests the presence of a paraneoplastic syndrome, indicating that there may be a hidden cancer
in your body.</p>
