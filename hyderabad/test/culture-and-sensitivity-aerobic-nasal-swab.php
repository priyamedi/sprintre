<h2 class="test-h2 mt-3"> What is the Culture and Sensitivity - Aerobic [Nasal Swab] test used for?</h2>
<p> This test is primarily used to identify the presence of bacteria in the nasal passages and to determine
the most effective antibiotics for treatment. It can be used to diagnose upper respiratory tract
infections, or to screen for bacteria that are commonly carried in the nose and can cause infections. </p>
	
<h2 class="test-h2 mt-3"> How is the nasal swab sample collected? </h2>
<p> A healthcare professional will use a sterile swab to collect a sample from the inside of your nasal
passages. The swab is inserted into the nostril and rotated gently before being withdrawn. The process
is quick and generally causes minimal discomfort. </p>
