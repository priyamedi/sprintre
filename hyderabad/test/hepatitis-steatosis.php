<h1 class="test-h1">Hepatitis - Steatosis </h1>
<div class="post-text">
<p>Hepatitis refers to inflammation of the liver, which can be caused by various factors including viruses, toxins, and lifestyle choices. Steatosis is a condition where fat accumulates in the liver cells. Alcoholic Steato Hepatitis (ASH) is a type of liver disease characterized by inflammation and fat accumulation in the liver due to excessive alcohol consumption. Non-Alcoholic Steato Hepatitis (NASH) is similar to ASH but occurs in individuals who do not consume alcohol excessively. Both ASH and NASH can progress to more severe liver diseases such as cirrhosis or liver cancer if left untreated.</p>

<p>ASH and NASH are part of a spectrum of liver diseases known as Alcoholic Liver Disease (ALD) and Non-Alcoholic Fatty Liver Disease (NAFLD) respectively. The initial stage, which involves fat accumulation without significant inflammation or liver damage, is known as simple steatosis or fatty liver.</p>
