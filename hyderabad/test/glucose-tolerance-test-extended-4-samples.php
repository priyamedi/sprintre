<div class="post-text">
<p>Glucose Tolerance Test (GTT), specifically the extended version with 4 samples, is a medical test that determines how quickly glucose is cleared from the blood. It is primarily used to diagnose diabetes, a condition that impairs the body's ability to process blood sugar. The test may also be used to diagnose gestational diabetes, a type of diabetes that develops during pregnancy.</p>
