<h2 class="test-h2 mt-3"> What is Culture and Sensitivity - Aerobic [ET] test?</h2>
<p> This is a diagnostic test in which a sample is collected from the lower respiratory tract via an
endotracheal tube. The sample is cultured in the laboratory to identify any bacteria that might be
present. Additionally, sensitivity testing is performed to determine which antibiotics are effective
against the identified bacteria. </p>

<h2 class="test-h2 mt-3"> Why is the test important?</h2>
<p> This test is vital for the diagnosis and management of respiratory infections in patients on mechanical
ventilation or those at risk. It allows for the identification of the bacteria causing the infection and helps
in the selection of the most effective antibiotic for treatment. </p>
