<h2 class="test-h2 mt-3">Why might I need an NRAS mutation analysis test?</h2>
<p>Your doctor might recommend an NRAS mutation analysis if you have been diagnosed with a type of cancer where NRAS
    mutations are common, such as melanoma, colorectal cancer, or certain types of leukemia. The test can guide
    treatment decisions and provide information on your prognosis.</p>
<h2 class="test-h2 mt-3">How is the NRAS mutation analysis test performed?</h2>
<p>The test is performed on a sample of your tissue or blood. The DNA in the sample is extracted and analyzed for the
    presence of mutations in the NRAS gene.</p>
