<h2 class="test-h2 mt-3">Why is the Flowcytometry - CD2 test performed?</h2>
<p>The Flowcytometry - CD2 test is often used in diagnosing and monitoring certain conditions that affect the immune
    system, like lymphomas, leukemias, and autoimmune disorders. It can help determine whether the number of T cells is
    increased or decreased, which can indicate an immune response or an underlying immune disorder.</p>
<h2 class="test-h2 mt-3">Is fasting required for this test?</h2>
<p>No, fasting is not required for the Flowcytometry - CD2 test.</p>
