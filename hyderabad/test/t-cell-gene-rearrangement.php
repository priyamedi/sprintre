<h2 class="test-h2 mt-3">Why is the T-Cell Gene Rearrangement test performed?</h2>
<p>The T-Cell Gene Rearrangement test is performed to diagnose or monitor diseases and conditions that cause the clonal
	expansion of T cells. These conditions can include various types of lymphomas and leukemias, and other
	lymphoproliferative disorders.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test requires a sample of blood, bone marrow, or tissue, depending on the type and location of the suspected
	disorder. The sample is then sent to a laboratory where the DNA is extracted, and PCR is performed to amplify the
	TCR genes. The resulting DNA fragments are then analyzed to detect any clonal gene rearrangements.</p>
