<p>Gilbert's Syndrome is a common and mild liver disorder where the liver does not properly process bilirubin. Bilirubin is a yellow byproduct of the breakdown of old red blood cells. People with Gilbert's Syndrome often have elevated levels of bilirubin in the blood, which can cause the skin and eyes to occasionally turn yellow (jaundice). This syndrome is typically harmless and does not require treatment.</p>
