<h2 class="test-h2 mt-3">What is the purpose of the IHC - CD23 test?</h2>
<p> The <strong>IHC - CD23</strong> test is used to detect the presence of CD23 protein in tissue samples, which can help diagnose certain types of leukemia and lymphoma and monitor allergic conditions.</p>
<h2 class="test-h2 mt-3">What does a positive result mean?</h2>
<p> A positive result indicates the presence of CD23 protein in the tissue sample. This can be a sign of certain types of leukemia or lymphoma or could indicate an allergic condition, depending on the context and other test results.</p>
