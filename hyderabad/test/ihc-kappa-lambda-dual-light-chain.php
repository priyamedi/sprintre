<div class="post-text">
<p>Immunohistochemistry (IHC) is a laboratory process that uses antibodies to detect specific proteins, or
antigens, in cells within a tissue section. The Kappa/Lambda (Dual) Light Chain test is an IHC test that
detects these specific proteins – the Kappa and Lambda light chains. These are part of the structure of
antibodies produced by plasma cells.

 </p>
<p> The primary function of antibodies, also known as immunoglobulins, is to bind to specific foreign
particles, such as bacteria and viruses, to help protect the body from infections. The light chains,
including Kappa and Lambda, are part of the structure of these antibodies. In a healthy person,
approximately 60% of light chains are Kappa and 40% are Lambda.</p>
<p>However, certain diseases, such as lymphoma or multiple myeloma, can cause an overproduction of one
type of light chain over the other, resulting in an abnormal Kappa/Lambda ratio. The Kappa/Lambda
(Dual) Light Chain IHC test is useful in diagnosing these conditions.</p>
