<h2 class="test-h2 mt-3">Why is the Threonine Quantitative - Urine 24H test performed?</h2>
<p>The Threonine Quantitative - Urine 24H test is performed to evaluate the threonine levels in the body. It helps
	detect possible metabolic disorders, evaluate nutritional status, or monitor the effectiveness of nutritional
	therapy.</p>
<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>This test requires a 24-hour urine collection. You will be given a special container to collect all the urine you
	pass over 24 hours. The collected sample is then sent to the laboratory for analysis.</p>
