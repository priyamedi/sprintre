<h2 class="test-h2 mt-3">Why is a PTPN11 genetic test done?</h2>
<p>The PTPN11 genetic test is done to confirm a diagnosis of a disorder related to the gene, most commonly Noonan
    syndrome, or to identify carriers among relatives of affected individuals.</p>
<h2 class="test-h2 mt-3">What are the implications of a positive result?</h2>
<p>A positive result indicates that a mutation in the PTPN11 gene is present. It is advised to consult with a genetic
