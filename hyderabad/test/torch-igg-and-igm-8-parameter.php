<h2 class="test-h2 mt-3">What are IgG and IgM antibodies?</h2>
<p>IgM antibodies are the first type of antibody produced by the immune system in response to an infection. They
    indicate a recent or ongoing infection. IgG antibodies are produced later and last for a long time in the blood,
    indicating a past infection or immunity.</p>
<h2 class="test-h2 mt-3">How is the sample collected for this test?</h2>
<p>A healthcare provider will draw a small amount of blood from a vein in your arm. The procedure is usually quick and
    causes minimal discomfort.</p>
