<p>Haptoglobin is a protein produced by the liver, and its primary role is to bind free hemoglobin released during the breakdown of red blood cells. This binding prevents kidney damage that could be caused by hemoglobin. There are different forms of haptoglobin, and these are determined by the Haptoglobin gene. The Haptoglobin - Genotype test is used to determine the genetic makeup of the haptoglobin protein in an individual.
 </p>
