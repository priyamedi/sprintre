	<h2 class="test-h2 mt-3">How is the tissue sample obtained?</h2>
<p>The tissue sample is obtained via a biopsy, a procedure in which a small amount of tissue is removed for testing.</p>

	<h2 class="test-h2 mt-3">What do the markers in this panel signify? </h2>
<p>Each marker in the panel - S100, HMB45, and Melan A - is a protein commonly found in melanoma cells. Their presence in the tissue sample can indicate the presence of melanoma.</p>

	<h2 class="test-h2 mt-3">When should I receive the results? </h2>
<p>Typically, results are available within 7 to 10 days.</p>

	<h2 class="test-h2 mt-3">Do I need any special preparation for this test? </h2>
<p>No specific preparation is required. Follow the instructions given by your healthcare provider regarding the biopsy procedure.</p>

	<h2 class="test-h2 mt-3">When should this test be conducted? </h2>
<p>This test is usually performed when there are suspicions of melanoma, often following a physical examination of the skin.</p>

	<h2 class="test-h2 mt-3">What does the test measure? </h2>
<p>The test measures the presence of the specific proteins S100, HMB45, and Melan A in the tissue sample, which could indicate melanoma.</p>

	<h2 class="test-h2 mt-3">How often should this test be conducted? </h2>
<p>This test is generally conducted once for diagnosis but may be repeated in case of suspicion of recurrence or progression.</p>

	<h2 class="test-h2 mt-3">What are the normal values for this test? </h2>
<p>In a healthy tissue sample, these markers would typically not be present. Their presence may suggest melanoma.</p>

	<h2 class="test-h2 mt-3">What precautions should be taken for this test? </h2>
<p>The main precautions relate to the biopsy procedure. Discuss with your healthcare provider about any risks and potential complications.</p>

	<h2 class="test-h2 mt-3">What factors can affect these markers? </h2>
<p>These markers are primarily affected by the presence of melanoma. Factors that increase the risk of melanoma, like excessive sun exposure and genetic predisposition, can influence these markers.</p>

	<h2 class="test-h2 mt-3">Which healthcare professional should I consult if the test results are abnormal?</h2>
<p>If the test results are abnormal, you should consult a dermatologist or an oncologist who specializes in skin cancers.</p>

	<h2 class="test-h2 mt-3">Are there additional tests that may be conducted alongside this test? </h2>
<p>Yes, other tests like genetic testing or additional imaging scans may be conducted based on your healthcare provider's advice.</p>

	<h2 class="test-h2 mt-3">Is this test accurate? </h2>
<p>The IHC test is highly accurate, but results should be interpreted in conjunction with other diagnostic information and clinical symptoms.</p>

