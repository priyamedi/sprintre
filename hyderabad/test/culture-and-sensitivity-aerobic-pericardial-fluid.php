<h2 class="test-h2 mt-3">What is pericardial fluid?</h2>

<p> Pericardial fluid is a small amount of fluid that is present between the layers of the pericardium, which is the sac that surrounds the heart. This fluid acts as a lubricant, reducing friction between the heart and the pericardial sac during the heart's movements. </p>

<h2 class="test-h2 mt-3"> How is the pericardial fluid sample collected?</h2>
<p> A sample of pericardial fluid is generally collected through a procedure called pericardiocentesis. This procedure involves inserting a needle through the chest wall into the pericardial space to withdraw fluid. This is done under sterile conditions, often using imaging guidance to ensure accuracy. </p>

