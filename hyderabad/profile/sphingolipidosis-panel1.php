<h2 class="test-h2 mt-3">When should one get this test done?</h2>
<p>This test is usually done when there is a suspicion of a sphingolipidosis, based on symptoms or family history. It might also be recommended for couples planning to start a family, particularly if there's a known risk of sphingolipidoses.</p>

