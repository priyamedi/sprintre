<h2 class="test-h2 mt-3">What is the Breast Cancer Monitor Panel?</h2>
<p>The Breast Cancer Monitor Panel is a blood test that measures the levels of two markers, CA 15-3 and CEA, which can be elevated in individuals with breast cancer.</p>
<h2 class="test-h2 mt-3">Who should get the Breast Cancer Monitor Panel?</h2>
<p>Individuals diagnosed with breast cancer, especially those undergoing treatment, should get this test. It can be used to monitor the effectiveness of treatment and check for cancer recurrence.</p>
