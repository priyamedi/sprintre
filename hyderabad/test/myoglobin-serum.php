<h2 class="test-h2 mt-3">Why is the Myoglobin - Serum test done?</h2>
<p>The test is performed to determine if there has been damage to muscle tissue, including heart muscle. It is commonly
    ordered when a doctor suspects a heart attack.</p>
<h2 class="test-h2 mt-3">What does a high level of myoglobin indicate?</h2>
<p>A high level of myoglobin in your blood is usually a sign of muscle damage, like after a heart attack or a severe
    injury.</p>
