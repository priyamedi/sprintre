<h2 class="test-h2 mt-3"> What is LAP Score?</h2>
	<p>LAP Score is a measure of the activity of the enzyme alkaline phosphatase within
white blood cells. It can provide valuable information about the functioning and
health of your bone marrow and is often used in the evaluation of blood disorders.</p>

<h2 class="test-h2 mt-3">Why is the LAP Score test done?</h2>
<p>The LAP Score test is primarily done to help diagnose chronic myelogenous leukemia
(CML) and distinguish it from other conditions that cause a high white blood cell
count. It can also be used to monitor the effectiveness of treatment in patients with
CML.</p>
