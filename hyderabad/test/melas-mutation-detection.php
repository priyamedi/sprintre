<h2 class="test-h2 mt-3">What does MELAS-Mutation Detection test do?</h2>
<p>The MELAS-Mutation Detection test identifies specific genetic mutations in the mitochondrial DNA
associated with MELAS syndrome. The most common mutation is m.3243A>G in the MT-TL1 gene.</p>

<h2 class="test-h2 mt-3">Who should get tested for MELAS mutations?</h2>
<p>Individuals who show signs and symptoms suggestive of MELAS or have a family history of the syndrome
should consider getting tested.</p>
