<h1 class="test-h1">NARP (Neurogenic Ataxia Retinitis Pigmentosa) - Blood </h1>
<div class="post-text">
<p>Neurogenic muscle weakness, Ataxia, and Retinitis Pigmentosa (NARP) is a rare genetic disorder characterized by a variety of symptoms primarily affecting the nervous system. This condition is caused by mutations in the MT-ATP6 gene, which provides instructions for making a protein essential for normal mitochondrial function. The NARP blood test is designed to identify mutations in the MT-ATP6 gene, helping in the diagnosis of this condition.</p>

<p>The severity and range of symptoms associated with NARP can vary widely among affected individuals. Common symptoms include muscle weakness, sensory neuropathy, ataxia (a lack of muscle coordination affecting gait), and retinitis pigmentosa, a condition that causes progressive vision loss. Since NARP is an inherited condition, it often runs in families, and genetic testing may be recommended for family members of an individual diagnosed with NARP.</p>
