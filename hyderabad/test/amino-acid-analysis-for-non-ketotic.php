<h2 class="test-h2 mt-3"> What is <strong>Non-Ketotic Hyperglycinemia (NKH)</strong>?</h2>
	<p>
NKH is a metabolic disorder where the body cannot effectively break down the amino acid glycine,
leading to an excess of glycine in the brain and spinal fluid, which can cause severe neurological issues.</p>
<h2 class="test-h2 mt-3"> What are the symptoms of Non-Ketotic Hyperglycinemia?</h2>
<p>
<a href="https://www.sprintdiagnostics.in/symptoms/"><b style="color:#2C53A5">Symptoms</b></a> usually manifest in early infancy and may include lethargy, seizures, muscle stiffness,
developmental delay, breathing problems, and feeding difficulties.</p>
