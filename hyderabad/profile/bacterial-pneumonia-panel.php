	<h2 class="test-h2 mt-3">Why is the Bacterial Pneumonia Panel ordered?
 </h2>
	<p>The panel is ordered when a patient presents symptoms consistent with pneumonia, such as cough, fever, shortness of breath, and chest pain. It can help determine whether the cause of the pneumonia is bacterial and, if so, identify the specific bacteria responsible.
 </p>
	<h2 class="test-h2 mt-3">What does a positive result mean?
 </h2>
	<p>A positive result indicates the presence of one or more of the bacteria (Mycoplasma pneumoniae, S.Pneumoniae, or Chlamydia Genus) in the respiratory sample, suggesting a bacterial pneumonia infection.
 </p>
