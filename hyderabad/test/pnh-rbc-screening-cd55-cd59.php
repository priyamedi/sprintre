<h2 class="test-h2 mt-3">Are there any specific preparations for this test?</h2>
<p>There are no specific preparations needed for this test. However, it's recommended for patients to inform their
    healthcare provider about any medications or supplements they are taking as some of these might interfere with the
    test results.</p>
<h2 class="test-h2 mt-3">When should one get this test?</h2>
<p>This test should be conducted when a patient exhibits symptoms suggestive of PNH, including but not limited to,
    dark-colored urine, particularly upon waking, fatigue, shortness of breath, rapid heart rate, and easy bruising or
    bleeding.</p>
<h2 class="test-h2 mt-3">What information does this test provide?</h2>
<p>The test detects the absence or deficiency of CD55 and CD59 proteins on red blood cells, which are characteristic
    features of PNH.</p>
<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>This test is generally conducted when PNH is suspected based on clinical symptoms and other lab findings. The
    frequency of follow-up testing depends on the patient's condition and the treating physician's judgment.</p>
<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
<p>In a healthy individual, the CD55 and CD59 proteins should be present on the surface of red blood cells. Their
    absence or deficiency may suggest PNH.</p>
<h2 class="test-h2 mt-3">What precautions need to be taken for this test?</h2>
<p>The test involves a routine blood draw, which is a relatively safe procedure. However, some people might experience
    minor bruising or discomfort at the site of the needle insertion.</p>
<h2 class="test-h2 mt-3">What factors can affect the levels of these proteins in the blood?</h2>
<p>The primary factor affecting these protein levels is the presence of the PNH-causing mutation in blood cell-producing
    stem cells. Certain medications may also impact the immune system and influence the results of this test.</p>
<h2 class="test-h2 mt-3">Who should be consulted in case of abnormal results?</h2>
<p>In the event of abnormal results, the patient should consult a hematologist, a medical doctor specializing in blood
    disorders.</p>
<h2 class="test-h2 mt-3">Can other medications affect the test results?</h2>
<p>Certain medications, particularly those impacting the immune system, could potentially affect the test results.</p>
<h2 class="test-h2 mt-3">What are the treatment options if PNH is detected?</h2>
<p>Treatment for PNH aims to ease symptoms and prevent complications, including reducing hemolysis, preventing blood
    clots, and managing symptoms of bone marrow dysfunction. Therapies may include blood transfusions, medications, and
    in severe cases, stem cell transplantation.</p>
<p>This test, PNH RBC Screening - CD55 & CD59, plays a crucial role in diagnosing PNH and
    monitoring its progression. It's crucial for patients to understand the importance of this test and its implications
    on the diagnosis, treatment, and prognosis of PNH. As always, early detection and appropriate treatment
    significantly impact the overall health outcome in patients with PNH. A thorough discussion with your healthcare
    provider is always recommended to understand the test results and next steps for management better.</p>


