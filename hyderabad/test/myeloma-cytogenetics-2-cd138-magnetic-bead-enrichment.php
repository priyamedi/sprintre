	<h2 class="test-h2 mt-3">What is the importance of detecting del17p in multiple myeloma? </h2>
	<p>Del17p refers to the deletion of a part of chromosome 17. This deletion is associated with an aggressive course of multiple myeloma and often indicates resistance to certain therapies. It is considered a high-risk cytogenetic abnormality. </p>
	<h2 class="test-h2 mt-3">What does IGH breakapart indicate? </h2>
	<p>IGH breakapart refers to rearrangements involving the immunoglobulin heavy chain gene located on chromosome 14. These rearrangements are significant because they are often associated with a more aggressive form of multiple myeloma and can influence treatment decisions. </p>
	<h2 class="test-h2 mt-3">Why are del13q, t(4;14), and t(14;16) important to detect? </h2>
	<p>These genetic abnormalities are associated with changes in the behavior of multiple myeloma. For example, t(4;14) and t(14;16) are translocations that are associated with a poorer prognosis, while del13q is a common deletion in multiple myeloma. </p>
