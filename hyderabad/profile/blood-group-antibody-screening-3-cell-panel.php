	<h2 class="test-h2 mt-3">What is Blood Group Antibody Screening (3 Cell Panel)?
 </h2>
	<p>Blood Group Antibody Screening (3 Cell Panel) is a test that detects antibodies in the blood that are directed against antigens on red blood cells. This is important for ensuring the safety and compatibility of blood transfusions.
 </p>
	<h2 class="test-h2 mt-3">Why is this test important?
 </h2>
	<p>This test is crucial for identifying individuals who have antibodies that may react with antigens on transfused red blood cells. This helps in selecting compatible blood for transfusion and preventing transfusion reactions.
 </p>
