<h2 class="test-h2 mt-3"> What is the importance of the IHC - PR Diagnostic Test?</h2>
	<p>
The IHC-PR test is critical for determining whether a breast cancer is hormone receptor-positive. This
information helps doctors decide if hormone therapies such as Tamoxifen or Aromatase inhibitors could
be effective treatment options.</p>
<h2 class="test-h2 mt-3"> Is fasting required for this test?</h2>
<p>
No, fasting is not required for the IHC - PR test as it uses tissue samples, not blood.</p>
