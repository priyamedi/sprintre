<h2 class="test-h2 mt-3">What is the CMV (Cytomegalovirus) IgG Antibodies - Serum test?</h2>
<p>This test is a blood test that measures the levels of specific IgG antibodies against the cytomegalovirus
(CMV) in your blood. These antibodies are produced by your immune system in response to a CMV
infection.</p>

<h2 class="test-h2 mt-3">Who might need this test?</h2>
<p>This test is usually recommended for people with symptoms of a CMV infection or for people who are at
high risk of infection, such as healthcare workers, childcare workers, and organ transplant recipients. It's
also recommended for pregnant women, as CMV infection during pregnancy can potentially cause harm
to the unborn child.</p>
