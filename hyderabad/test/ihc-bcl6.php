<h2 class="test-h2 mt-3">Why is the IHC - BCL6 test done?</h2>
<p>The <strong>IHC - BCL6 test</strong> is typically performed when a lymphoid tumor is suspected. It helps in diagnosing specific types of non-Hodgkin's lymphoma and can guide further treatment decisions.</p>
<h2 class="test-h2 mt-3">Is there any specific preparation required for this test?</h2>
<p>No special preparation is needed for the IHC - BCL6 test. The test is performed on a tissue sample, typically obtained through a biopsy or surgical procedure.</p>
