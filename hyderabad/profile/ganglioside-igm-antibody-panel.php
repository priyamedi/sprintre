<h2 class="test-h2 mt-3">What is a Ganglioside - IgM Antibody Panel by Immunoblot?</h2>
<p>This is a blood test designed to detect the presence of IgM antibodies against gangliosides in your blood. The
    presence of these antibodies can be indicative of certain autoimmune neurological disorders.</p>
<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>This test is ordered when a patient presents signs of a neurological disorder and the doctor suspects an autoimmune
    cause. The detection of these antibodies can assist in the diagnosis of conditions like multifocal motor neuropathy.
</p>
