<h2 class="test-h2 mt-3">What is the IHC-SALL4 test?</h2>
	<p>
The IHC-SALL4 test is an immunohistochemistry test that detects the presence of the SALL4 protein in
tissue samples. This protein is often associated with certain types of cancer, including germ cell tumors
and acute myeloid leukemia.</p>
<h2 class="test-h2 mt-3">How is the IHC-SALL4 test performed?</h2>
<p>
The test is performed on a tissue sample, usually obtained through a biopsy. The sample is treated with
antibodies that bind to the SALL4 protein. If the protein is present, it can be detected under a
microscope.</p>
