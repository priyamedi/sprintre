					<h2 class="test-h2 mt-3">What is the Creatine Kinase (CPK) - Serum Test?</h2>
<p>It is a blood test that measures the total levels of the enzyme Creatine Kinase in the blood, which helps in
    determining if there has been muscle damage.</p>
	<div class="mt-3"></div>
<h2 class="test-h2 mt-3">Why is the CPK - Serum Test performed?</h2>
<p>The test is performed to assess muscle damage which may be due to conditions such as heart attack, muscle injury,
    or muscle diseases. It is also used to evaluate unexplained muscle weakness or pain.</p>

	



					
