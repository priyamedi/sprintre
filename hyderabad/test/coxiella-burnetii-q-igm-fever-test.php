<h2 class="test-h2 mt-3">What is the Coxiella Burnetii - Q Fever (IgM Antibody) Test?</h2>
<p>This is a blood test that detects the presence of IgM antibodies to Coxiella burnetii. These antibodies usually
    indicate a recent or current infection with the bacterium that causes Q fever.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>A healthcare professional will draw a blood sample from a vein in your arm using a needle. The sample is then sent
    to a laboratory for analysis.</p>
