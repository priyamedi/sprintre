<h2 class="test-h2 mt-3"> What is the importance of the IBD Screening Panel I?</h2>
	<p>
The <strong>IBD Screening Panel I</strong> is used to help differentiate between Crohn's disease and ulcerative colitis
when a person has signs and symptoms of IBD. The antibodies measured in this panel are often present
in one condition more than the other. </p>
<h2 class="test-h2 mt-3"> Is fasting required for this test?</h2>
<p>
No, fasting is not necessary before this test. </p>
