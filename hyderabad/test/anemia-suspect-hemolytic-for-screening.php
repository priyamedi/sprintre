                                    <li> <b>LDH-Serum:</b> LDH is an enzyme found in many body tissues, including the
                                        liver.
                                        Elevated levels can indicate cell damage.</li>
                                    <li> <b>Liver Function Tests (LFT):</b> These tests measure various proteins and
                                        enzymes in
