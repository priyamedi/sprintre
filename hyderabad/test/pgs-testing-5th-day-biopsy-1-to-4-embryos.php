<h2 class="test-h2 mt-3">What is PGS testing?</h2>
<p>PGS testing is a highly specialized technique used during IVF that checks the embryos for chromosomal abnormalities.
    The process involves extracting a few cells from each embryo on the fifth day after fertilization, and then
    analyzing these cells to determine whether the right number of chromosomes are present.</p>
<h2 class="test-h2 mt-3">How is the biopsy performed during PGS testing?</h2>
<p>The biopsy is performed by an experienced embryologist in a lab. Using a high-powered microscope and precise tools, a
    few cells are gently extracted from each embryo. The procedure is done carefully to ensure that the embryo isn't
    harmed and can continue to grow if it's chromosomally normal.</p>
