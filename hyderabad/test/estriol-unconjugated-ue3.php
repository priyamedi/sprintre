<h2 class="test-h2 mt-3">Why is the Estriol - Unconjugated test done?</h2>
<p>The test is often done as part of a series of prenatal screening tests to assess the risk of certain
chromosomal abnormalities and congenital anomalies in the fetus, such as Down's syndrome and
Edwards syndrome.</p>

<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>A blood sample is drawn from a vein in the arm, and the levels of uE3 are measured in a laboratory. The
test is usually performed between the 15th and 20th weeks of pregnancy.</p>
