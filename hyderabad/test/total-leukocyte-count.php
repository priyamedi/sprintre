<h2 class="test-h2 mt-3">What is the purpose of the Total Leukocyte Count (TLC)?</h2>
<p>The Total Leukocyte Count is used to measure the total number of white blood cells in the blood. It helps evaluate a
    person's overall health and immune system, diagnose different types of diseases, including infections, leukemia, and
    immune disorders, and monitor response to treatment.</p>
<h2 class="test-h2 mt-3">Who should get a Total Leukocyte Count (TLC)?</h2>
<p>Anyone experiencing symptoms such as fatigue, fever, infection, inflammation, bruising or bleeding easily may be
    advised to take a TLC. It may also be ordered as part of a routine health check-up or to monitor treatment for
    certain conditions.</p>
