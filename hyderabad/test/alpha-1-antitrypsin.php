<h2 class="test-h2 mt-3">What is the Alpha-1-Antitrypsin test?</h2>
<p>The Alpha-1-Antitrypsin test is a blood test that measures the level of AAT protein in the blood. It is used
to diagnose Alpha-1-Antitrypsin Deficiency and assess the risk of developing lung and liver diseases.</p>

<h2 class="test-h2 mt-3">Why is the Alpha-1-Antitrypsin test important?</h2>
<p>This test is crucial for diagnosing AATD, a genetic disorder that can lead to lung diseases such as
emphysema and COPD, and liver diseases such as cirrhosis. Early detection helps in managing symptoms
and preventing complications.</p>
