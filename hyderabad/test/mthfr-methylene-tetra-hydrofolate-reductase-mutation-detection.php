<h2 class="test-h2 mt-3">What is the purpose of the MTHFR mutation test?</h2>
<p>The MTHFR mutation test is used to detect mutations in the MTHFR gene that can lead to high levels of homocysteine in
    the blood, increasing the risk of various health problems.</p>
