<h2 class="test-h2 mt-3">Why might my doctor order a Ri antibody test?</h2>
	<p>
Your doctor might order a <strong>Ri antibody test</strong> if you show neurological symptoms that cannot be explained
by other tests or examinations. These <a href="https://www.sprintdiagnostics.in/symptoms/"><b style="color:#2C53A5">symptoms</b></a> could include difficulty moving or coordinating certain body parts, vision problems, memory problems, and more.</p>
<h2 class="test-h2 mt-3">What does a positive Ri antibody test result mean?</h2>
<p>
A positive <strong>Ri antibody test</strong> result means that Ri antibodies were detected in your blood. This can indicate
a paraneoplastic syndrome related to certain types of cancers, including breast cancer and small cell
lung cancer.</p>
