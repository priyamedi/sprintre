<h2 class="test-h2 mt-3">What is the TSH test?</h2>
<p>The TSH test is a blood test that measures the level of thyroid stimulating hormone (TSH) in your blood. TSH is a
    hormone that controls the production of thyroid hormones by the thyroid gland.</p>
<h2 class="test-h2 mt-3">Why would I need a TSH test?</h2>
<p>Your healthcare provider might order a TSH test if you have symptoms of a thyroid disorder, such as weight changes,
    fatigue, hair loss, mood changes, irregular periods in women, and changes in heart rate. It's also used to monitor
    treatment for diagnosed thyroid disorders.</p>
