<h2 class="test-h2 mt-3">Why is the Estrone test done?</h2>
<p>The Estrone test is performed to evaluate ovarian function, detect estrogen-producing tumors, and
assess fertility or menstrual irregularities. In men, it is used to evaluate estrogen levels if there is
gynecomastia (enlargement of breast tissue).</p>

<h2 class="test-h2 mt-3">How is the Estrone test performed?</h2>
<p>The Estrone test is performed by drawing blood from a vein in your arm. The sample is then sent to a
laboratory for analysis to measure the level of estrone in the blood.</p>
