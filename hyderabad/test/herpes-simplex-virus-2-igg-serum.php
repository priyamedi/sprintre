<h2 class="test-h2 mt-3">What is the Herpes Simplex Virus (HSV) Type 2 IgG test?</h2>
<p>The HSV-2 IgG test is a blood test that detects the presence of antibodies (IgG) against the herpes simplex virus
    type 2. This test is typically used to diagnose previous exposure to this virus.</p>
<h2 class="test-h2 mt-3">Why is this test important?</h2>
<p>This test is crucial for diagnosing HSV-2 infections, especially in individuals who have had sexual contact with an
    infected partner. The test can help determine if a person has been previously exposed to the virus and developed
    antibodies.</p>
