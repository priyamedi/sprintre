<h2 class="test-h2 mt-3">What is a Myoglobin Urine test used for?</h2>
<p>This test is used to detect muscle damage. Elevated levels of myoglobin in urine may be a sign of conditions such as
    heart attack, severe injury, or chronic muscle diseases.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>A urine sample is collected in a clean, sterile container. The sample is then sent to a laboratory, where it is
    analyzed for myoglobin levels.</p>
