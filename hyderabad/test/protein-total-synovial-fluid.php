<h2 class="test-h2 mt-3"> Why is the Protein Total, Synovial Fluid test done?</h2>
	<p>
This test is often ordered to help diagnose the cause of joint inflammation and to distinguish between
different types of arthritis and other conditions. It can also be used to help determine if a joint infection
is present.</p>
<h2 class="test-h2 mt-3"> What do the results mean?</h2>
<p>
A higher than normal level of protein in your synovial fluid may indicate a joint infection, gout, or a type
of arthritis. Lower than normal levels can be seen in healthy joints but could also suggest a disorder that
lowers protein production.</p>
