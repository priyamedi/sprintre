<h2 class="test-h2 mt-3"> What precautions should I take after the test?</h2>
<p>Follow your doctor's instructions after the test. If your results are abnormal, you may need to make lifestyle changes or take medication to manage the underlying condition.</p>
<h2 class="test-h2 mt-3"> Are there any side effects of this test?</h2>
<p>There are no known side effects of this test.</p>
