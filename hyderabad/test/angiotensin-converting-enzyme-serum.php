<div class="post-text">
<p>The Angiotensin Converting Enzyme (ACE) test is a blood test that measures the amount of ACE in your
blood. ACE is an enzyme that helps regulate blood pressure by converting a hormone called angiotensin
I to a potent substance called angiotensin II. This test is typically used to help diagnose and monitor
certain diseases, most notably sarcoidosis, a disease characterized by inflammation in various parts of
the body, often affecting the lungs and lymph nodes.
</p>
<p> Besides sarcoidosis, elevated levels of ACE can be seen in a number of other conditions, including
Gaucher's disease, hyperthyroidism, leprosy, amyloidosis, and lung cancer. In some cases, ACE levels can
be low in conditions such as chronic liver disease, emphysema, or hypothyroidism. However, the
primary use of the ACE test in clinical practice remains the diagnosis and monitoring of sarcoidosis. </p>
