<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is used to measure the levels of manganese in the urine. It can be used to identify recent
exposure to excess manganese and can assist in monitoring the body's response to treatment for
manganese toxicity.</p>

<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>This test requires a urine sample. You will be asked to urinate into a clean container, and the sample will
be sent to the lab for analysis.</p>
