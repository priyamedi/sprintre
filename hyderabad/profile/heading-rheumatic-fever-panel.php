<p><strong>Rheumatic fever</strong> is a systemic inflammatory disease that can develop as a delayed reaction to a throat
infection caused by Streptococcus pyogenes, also known as group A streptococcus (GAS). If untreated, it
can lead to serious conditions such as rheumatic heart disease. The <strong>Rheumatic Fever Panel</strong> includes
several tests such as <a href="https://www.sprintdiagnostics.in/our-services/laboratory/hematology/"><b style="color:#2C53A5">Complete Blood Count (CBC)</b></a>
, Erythrocyte Sedimentation Rate (ESR),
</p>
<p> Antistreptolysin O (ASO), C-reactive protein (CRP), and a throat swab for culture and sensitivity. These
tests together help in the diagnosis and management of rheumatic fever.
The <a href="https://www.sprintdiagnostics.in/our-services/laboratory/hematology/"><b style="color:#2C53A5">CBC</b></a>
 evaluates the different components of blood, including red blood cells, white blood cells, and
platelets. ESR and CRP are markers of inflammation in the body. An ASO test measures antibodies
against streptolysin O, a toxin produced by group A strep. If you've had a recent strep <a href="https://www.sprintdiagnostics.in/symptoms/frequent-infections"><b style="color:#2C53A5">infection</b></a>, levels of
ASO in your blood will be higher. The throat swab culture is performed to detect the presence of GAS,
which could indicate a recent or current infection. </p>
