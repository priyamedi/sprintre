<h2 class="test-h2 mt-3">Why would I need this test?</h2>
<p>This test is recommended if there's a suspicion of neurological complications arising from a measles
infection, such as measles encephalitis.</p>

<h2 class="test-h2 mt-3">How is the sample collected?</h2>
<p>A sample of CSF is collected by a procedure known as lumbar puncture or spinal tap. It involves inserting
a thin needle between two vertebrae in your lower back to withdraw a small amount of CSF.</p>
