<h2 class="test-h2 mt-3">What is a Chimerism study?</h2>
<p>A Chimerism study is a test that tracks the proportion of donor and recipient cells in a patient's blood or bone
	marrow following a bone marrow transplant.</p>
<h2 class="test-h2 mt-3">Why is a Chimerism study conducted after a bone marrow transplant?</h2>
<p>A Chimerism study is used to monitor the success of the transplant. It can indicate whether the donor's cells have
	been accepted by the patient's body and are producing new blood cells. The test can also help detect early signs of
	relapse or graft failure.</p>
