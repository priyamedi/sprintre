<h2 class="test-h2 mt-3">Why is Cytology - Urine for Malignant Cells done?</h2>
<p>This test is done to detect the presence of abnormal or cancerous cells in the urine, which can be indicative of
    tumors in the urinary tract. It is particularly useful for diagnosing and monitoring bladder cancer.</p>
<h2 class="test-h2 mt-3">How is the urine sample collected?</h2>
<p>The urine sample is usually collected in a sterile container provided by the healthcare provider. It’s important to
    collect a mid-stream sample to avoid contamination by cells from the external genitalia.</p>
