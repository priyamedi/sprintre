<h2 class="test-h2 mt-3">Why is the Fungus Culture [WOUND SWAB] test done?</h2>
<p>The test is performed when a wound is not healing as expected, appears infected, or the patient experiences worsening symptoms around the wound area. These symptoms may include increased pain, redness, swelling, pus drainage, or fever.</p>

<h2 class="test-h2 mt-3">How is the wound swab collected?</h2>
<p>A healthcare provider will clean the wound area and then use a sterile swab to gently collect a sample from the wound. The sample will be sent to the laboratory for analysis.</p>

