<h2 class="test-h2 mt-3">What are hematologic malignancies?</h2>
<p>Hematologic malignancies are cancers that originate in the blood cells, bone marrow, or lymph nodes.
Common examples include leukemia, lymphoma, and multiple myeloma.</p>
<h2 class="test-h2 mt-3">Why is CSF or body fluids analysis necessary for hematologic malignancies?</h2>
<p>The analysis is essential to diagnose and monitor the spread of hematologic malignancies to the
central nervous system or other body cavities. It helps doctors to develop a treatment plan.</p>
