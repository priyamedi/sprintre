<h2 class="test-h2 mt-3">What does the hypercalcemia panel measure?</h2>
	<p>
The <strong>hypercalcemia panel</strong> measures the levels of albumin, calcium (corrected and serum), parathyroid
hormone (PTH), inorganic phosphorus, and vitamin D in the blood. These tests can help determine if you
have hypercalcemia and, if so, identify its cause.</p>
<h2 class="test-h2 mt-3"> Why do I need a hypercalcemia panel?</h2>
<p>
If you have <a href="https://www.sprintdiagnostics.in/symptoms"><b style="color:#2C53A5">symptoms</b></a> of hypercalcemia, such as <a href="https://www.sprintdiagnostics.in/symptoms/fatigue"><b style="color:#2C53A5">fatigue</b></a>, kidney stones, bone pain, or <a href="https://www.sprintdiagnostics.in/symptoms/depression"><b style="color:#2C53A5">depression</b></a>, your
doctor may order this panel. It helps to identify the underlying cause of the high calcium levels and
guides treatment.</p>
