<h2 class="test-h2 mt-3">What is the Flow Cytometry ZAP-70 (CLL Prognostication) with CD19 test?</h2>
<p>The Flow Cytometry ZAP-70 (CLL Prognostication) with CD19 is a diagnostic procedure that measures the expression of ZAP-70 in CD19 positive cells (B-cells) to predict disease progression and survival outcomes in patients with Chronic Lymphocytic Leukemia (CLL).</p>

<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>This test is performed to evaluate the prognosis of patients with CLL. A higher expression of ZAP-70 in B-cells is associated with a more aggressive disease course and a poorer prognosis.</p>

