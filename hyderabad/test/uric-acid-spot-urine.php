<h2 class="test-h2 mt-3">What is a Uric Acid Spot Urine Test?</h2>
<p>A Uric Acid Spot Urine Test is a diagnostic test that measures the level of uric acid in a single 'spot' urine
	sample.</p>
<h2 class="test-h2 mt-3">Why would I need a Uric Acid Spot Urine Test?</h2>
<p>You might need this test if your healthcare provider suspects you have high uric acid levels which could indicate a
	condition such as gout, or kidney disorders. It could also be part of a general health screening.</p>
