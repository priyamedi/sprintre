<h2 class="test-h2 mt-3">Why would my doctor recommend a MPS Type-I blood test?</h2>
<p>If your child is showing symptoms that may suggest MPS I, such as developmental delay, coarse facial features, vision
    and hearing problems, and abnormal bone size or shape, your healthcare provider may recommend a MPS Type-I blood
    test. The test helps to confirm or rule out a diagnosis of MPS I.</p>
<h2 class="test-h2 mt-3">How is the MPS Type-I blood test performed?</h2>
<p>The MPS Type-I blood test is performed using a sample of the patient's blood. The blood sample is drawn from a vein
    in the arm, which is then sent to a laboratory for analysis.</p>
