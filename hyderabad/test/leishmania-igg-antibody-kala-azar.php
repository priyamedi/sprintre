<h1 class="test-h1">Leishmania IgG Antibody (KALA-AZAR) </h1>
<div class="post-text">
<p>Leishmaniasis, also known as Kala-azar, is a disease caused by the parasite Leishmania, which is transmitted through the bite of a sandfly. This parasite primarily affects the immune system and can cause a variety of symptoms, ranging from skin sores to systemic disease affecting the spleen, liver, and bone marrow.</p>
<p>The Leishmania IgG Antibody (Kala-azar) test is a blood test that identifies antibodies produced by your immune system in response to the Leishmania parasite. If you've been exposed to Leishmania, your body will produce IgG antibodies as part of the immune response to fight off the parasite. By detecting the presence of these antibodies, this test can help diagnose leishmaniasis.</p>
