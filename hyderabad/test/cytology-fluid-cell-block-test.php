<h2 class="test-h2 mt-3">What is the purpose of the Cytology - Fluid/Lavage, Cell Block Test?</h2>
<p>This test is used to analyze cells in body fluids or washings from body cavities to detect abnormalities, infections,
    inflammations, or cancers.</p>
<h2 class="test-h2 mt-3">How is the sample collected?</h2>
<p>The sample is collected by aspirating fluid from body cavities such as the pleura or peritoneum, or by washing
    (lavage) a body cavity and collecting the fluid. The method of collection depends on the clinical question and the
    body site involved.</p>

