<div class="post-text">
<p>Hemogram is a comprehensive blood test that provides vital information about the components of blood including red blood cells, white blood cells, and platelets. It includes a Complete Blood Count (CBC) and Peripheral Smear (PS) along with an automated reticulocyte count. This test is often used as a broad screening panel to evaluate an individual's general health status, as well as to detect a variety of disorders, such as anemia, infection, and many other diseases.</p>
<p>A CBC provides quantitative information about the blood cells including Hemoglobin (Hb), Hematocrit (Hct), Red Blood Cell Count (RBC), White Blood Cell Count (WBC), Platelet Count, and RBC indices (MCV, MCH, MCHC). Peripheral Smear involves preparing a blood film on a slide and then examining it under a microscope to assess the physical characteristics of individual blood cells. Automated reticulocyte count gives an estimate of the bone marrow's production of new red blood cells.</p>
