<h1 class="test-h1">Routine Examination with Cytology - Ascitic Fluid </h1>
<div class="post-text">
<p>Ascites refers to the abnormal buildup of fluid in the abdomen. This condition is commonly associated
with severe liver diseases like cirrhosis. The fluid, also known as ascitic fluid, can be examined to
understand the underlying cause of the fluid buildup. <strong>Routine examination of ascitic fluid</strong> involves
physical, chemical, and microscopic examination, and cytology refers to the study of cells in the fluid.
 </p>
<p> The purpose of the <strong>routine examination of ascitic fluid</strong> is to determine the cause of the fluid buildup and
to distinguish between ascites due to liver disease and ascites due to other causes, such as cancer,
tuberculosis, or heart failure.</p>
