<h1 class="test-h1">IHC - diagnostic panel for Lung cancer  </h1>
<div class="post-text">
<p>Lung cancer, the leading cause of cancer deaths worldwide, begins when cells in the lung grow uncontrollably and form tumors. Accurate diagnosis of the type and subtype of lung cancer is paramount for selecting the best treatment options. Immunohistochemistry (IHC) plays an essential role in this process.</p>

<p>Immunohistochemistry (IHC) is a method that uses antibodies to detect the presence of specific proteins in tissue samples. This aids in identifying the type of cells involved in disease processes such as cancer. In the context of lung cancer, a panel of IHC markers including CK7, CK20, Napsin A, TTF1, P63, CK 5/6, CD56, Synapto, and Chromogranin are reflexively chosen to provide a comprehensive analysis of the cancer type and subtype.</p>
