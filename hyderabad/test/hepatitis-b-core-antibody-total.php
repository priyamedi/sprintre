<h2 class="test-h2 mt-3">What is the Hepatitis B Core Antibody Total test?</h2>
<p>The Hepatitis B Core Antibody Total test is used to detect antibodies to the core antigen of the hepatitis B virus. This test can show whether a person has been exposed to the virus in the past or is currently infected.</p>
<h2 class="test-h2 mt-3">Why is the HBcAb-Total test done?</h2>
<p>The HBcAb-Total test is used to screen for hepatitis B infections, monitor the condition of patients with hepatitis B, and to check if someone has recovered from a hepatitis B infection. It can also be used to determine eligibility for vaccination.</p>
