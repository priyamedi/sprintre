	<h2 class="test-h2 mt-3">What is the purpose of the Culture and Sensitivity test for pus?</h2>
	<p>The Culture and Sensitivity test is used to identify the bacteria present in pus and to determine the
most effective antibiotics to treat the infection. This helps the doctor in prescribing the most
appropriate treatment plan for the patient.</p>
	<h2 class="test-h2 mt-3">How is the pus sample collected?</h2>
	<p>A healthcare professional collects the pus sample by using a sterile swab or syringe. In cases where
the pus is inside an abscess, a needle may be used to draw out the fluid. The sample is then sent to the
laboratory for analysis.</p>
