<p>Neonatal screening helps detect certain metabolic and genetic disorders that may not be apparent at birth. Early
    detection and treatment of these disorders can prevent severe health problems, including developmental issues,
    growth problems, and even death.</p>
<h2 class="test-h2 mt-3">What disorders does this screening test for?</h2>
<p>This screening tests for 11 conditions: Congenital Hypothyroidism, Congenital Adrenal Hyperplasia, G6PD deficiency,
    Cystic Fibrosis, Phenylketonuria, Galactosemia, Biotinidase deficiency, Sickle Cell Disease, Sickle Cell Trait,
    Hemoglobin variants, and Beta Thalassemia.</p>
