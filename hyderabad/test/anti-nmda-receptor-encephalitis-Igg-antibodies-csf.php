<p>The test requires a CSF sample, which is obtained through a lumbar puncture (spinal tap). You should discuss any
    medications you're taking with your doctor, as some may need to be stopped before the procedure.</p>
<h2 class="test-h2 mt-3">When should I get the Anti NMDA Receptor Encephalitis IgG Antibodies - CSF test?</h2>
<p>This test may be ordered if you present with symptoms of Anti-NMDA Receptor Encephalitis, which may include memory
    loss, psychiatric symptoms, speech dysfunction, seizures, or involuntary movements.</p>
<h2 class="test-h2 mt-3">What does the Anti NMDA Receptor Encephalitis IgG Antibodies - CSF test measure?</h2>
<p>The test measures the presence of antibodies against NMDA receptors in the cerebrospinal fluid. A positive result
    suggests an autoimmune response against these receptors, indicating Anti-NMDA Receptor Encephalitis.</p>
<h2 class="test-h2 mt-3">How frequently should I get tested?</h2>
<p>This is not a routine test and is typically only performed when Anti-NMDA Receptor Encephalitis is suspected based on
    clinical symptoms.</p>
<h2 class="test-h2 mt-3">What are the normal values for the Anti NMDA Receptor Encephalitis IgG Antibodies - CSF
    test?</h2>
<p>Normally, antibodies against NMDA receptors should not be present in the CSF. A positive result may indicate
    Anti-NMDA Receptor Encephalitis.</p>
<h2 class="test-h2 mt-3">Are there any precautions I need to take?</h2>
<p>No specific precautions are necessary for this test. If the test result is positive, follow your healthcare
