<h2 class="test-h2 mt-3">What is Lyme disease?</h2>
<p>Lyme disease is an infectious disease caused by the bacterium Borrelia burgdorferi, transmitted
through the bite of infected black-legged ticks.</p>
<h2 class="test-h2 mt-3">What are the symptoms of Lyme disease?</h2>
<p>Symptoms may include fever, fatigue, headache, muscle and joint aches, swollen lymph nodes,
and a characteristic skin rash called erythema migrans.</p>
