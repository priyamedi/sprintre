<h2 class="test-h2 mt-3">Is the IHC - CK5/6 test painful?</h2>
<p>As the test involves the analysis of a tissue sample, any pain or discomfort would typically be associated with the
    biopsy or surgical procedure used to collect the sample, not the IHC - CK5/6 test itself.</p>
<h2 class="test-h2 mt-3">How accurate is the IHC - CK5/6 test?</h2>
