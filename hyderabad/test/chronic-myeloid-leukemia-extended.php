<div class="post-text">
<p>Chronic Myeloid Leukemia (CML) is a type of cancer that starts in the blood-forming cells of the bone marrow and invades the blood. It is characterized by the uncontrolled growth of myeloid cells in the bone marrow and the accumulation of these cells in the blood. CML is a genetic disease caused by the reciprocal translocation of chromosomes 9 and 22. This translocation leads to the formation of the Philadelphia chromosome, which produces an abnormal tyrosine kinase called BCR-ABL that drives the proliferation of leukemia cells. <br><br>
CML Extended is a combination of tests that are critical in the diagnosis and monitoring of CML. This includes Karyotyping, BCR/ABL by Fluorescence In Situ Hybridization (FISH), and BCR/ABL International Scale (IS) by Polymerase Chain Reaction (PCR) Quantitative. These tests provide a comprehensive analysis of the genetic alterations in CML and are important for prognosis and monitoring the response to therapy.<br><br>
Karyotyping is a test that involves the analysis of chromosomes in a cell. This test can detect the Philadelphia chromosome, which is present in most cases of CML. BCR/ABL by FISH is a test that can detect the BCR-ABL gene fusion at the DNA level, which is a hallmark of CML. BCR/ABL- IS by PCR Quantitative is a highly sensitive test that measures the amount of BCR-ABL mRNA, and it's used to monitor the response to therapy over time </p>
