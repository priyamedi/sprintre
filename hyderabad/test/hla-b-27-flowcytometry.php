	<h2 class="test-h2 mt-3">What does a positive result mean? </h2>
	<p>A positive result indicates the presence of the HLA-B27 protein in your blood cells. While this protein is found in a small percentage of the general population, it is significantly more common in individuals with certain autoimmune conditions like ankylosing spondylitis.
