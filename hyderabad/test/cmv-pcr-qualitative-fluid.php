<h2 class="test-h2 mt-3">What is the CMV-PCR Qualitative test on fluid?</h2>
<p>The CMV-PCR Qualitative test on fluid is a laboratory test that detects the presence of CMV DNA in
different fluid samples. It helps identify CMV infections in various parts of the body.</p>

<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is done when a person shows symptoms of a CMV infection in a specific part of the body, such
as the eyes, lungs, or gastrointestinal tract, especially if the person has a weakened immune system. It
can also be used to diagnose congenital CMV infection in newborns.</p>
