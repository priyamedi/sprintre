<h2 class="test-h2 mt-3">What is the ALL Leukemia Translocation Panel?</h2>
<p>The ALL Leukemia Translocation Panel is a diagnostic test used to detect specific chromosomal translocations in the
    genes of patients with Acute Lymphoblastic Leukemia. These translocations are genetic changes that may affect how
    the disease progresses and responds to treatment.</p>
<h2 class="test-h2 mt-3">Why is this test important?</h2>
<p>This test is important as it can provide valuable information regarding the patient's prognosis and guide
    personalized treatment strategies. It can also identify patients who may be eligible for specific targeted
    therapies.</p>
