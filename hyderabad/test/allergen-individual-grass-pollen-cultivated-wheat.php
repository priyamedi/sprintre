<h2 class="test-h2 mt-3">What are the symptoms of an allergy to Cultivated Wheat pollen?</h2>
	<p>
Symptoms can include sneezing, nasal congestion, itchy or watery eyes, skin rashes, shortness of breath,
and in some cases, asthma.</p>
<h2 class="test-h2 mt-3"> What time of the year is wheat pollen most prevalent?</h2>
<p>
Wheat typically pollinates in the late spring to early summer. However, this can vary based on
geographic location and environmental conditions.</p>
