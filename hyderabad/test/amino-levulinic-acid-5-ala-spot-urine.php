<h2 class="test-h2 mt-3">What is the Amino Levulinic Acid 5 - ALA - Spot Urine test?</h2>
<p>This test measures the levels of delta-aminolevulinic acid (ALA) in a single urine sample. ALA is an important compound in the body involved in the synthesis of heme, a vital component of hemoglobin in red blood cells.</p>

<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>This test is used to help diagnose disorders related to heme synthesis, such as porphyria. It's also used to monitor exposure to lead, as lead can disrupt heme synthesis and lead to elevated levels of ALA in the urine.</p>

