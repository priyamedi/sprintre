	<h2 class="test-h2 mt-3">When should I get this test done?</h2>
<p>The test is done when a patient doesn't respond to first-line TB treatment, or when the patient is suspected to have been infected by drug-resistant TB strain.</p>

	<h2 class="test-h2 mt-3">What does this test measure?</h2>
<p>This test measures the susceptibility or resistance of Mycobacterium tuberculosis to a panel of 10 first and second-line anti-TB drugs.</p>

	<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>The frequency of the test is determined by the doctor, depending on the patient's treatment response and disease progression.</p>

	<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
<p>The normal value would be 'sensitive' for all drugs, which indicates the drugs can effectively kill or inhibit the growth of the bacteria.</p>

