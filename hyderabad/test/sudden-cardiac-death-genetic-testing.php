<h2 class="test-h2 mt-3">What is Sudden Cardiac Death (SCD)?</h2>
<p>Sudden Cardiac Death (SCD) is an unexpected death due to loss of heart function. It is usually associated with a
    rapid and unexpected loss of pulse and consciousness, occurring within an hour of the onset of acute symptoms, or
    without any symptoms at all.</p>
<h2 class="test-h2 mt-3">What is the purpose of testing for SCD risk?</h2>
<p>The test is typically used to identify individuals who may be at high risk of SCD due to inherited genetic mutations.
    It can help in early detection and prevention.</p>
