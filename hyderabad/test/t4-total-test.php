<div class="post-text">
<p>The Total T4 or total thyroxine test is one of several tests that might be used to evaluate thyroid function and help diagnose thyroid diseases, including hypothyroidism and hyperthyroidism, usually after discovering that the thyroid stimulating hormone (TSH) level is abnormal.</p>

<p>Total T4 includes both free T4 and T4 that is bound to proteins in the bloodstream. Most of the T4 in the body is bound to proteins; only a small percentage is free and biologically active. The body's cells use T4, converting it into the active form of the hormone, T3. The Total T4 test, therefore, reflects the total amount of thyroxine in the blood, which includes the amount of T4 available to the tissues (free T4) and the T4 attached to proteins (bound T4).</p>
