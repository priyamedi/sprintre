<p>You should follow the sample collection instructions properly to avoid contamination, leading to inaccurate results.</p>

	<h2 class="test-h2 mt-3">What factors can affect the levels?</h2>
<p>The method of sample collection and handling, as well as the biological characteristics of the bacteria, can affect the test results.</p>

	<h2 class="test-h2 mt-3">What are the modifiable and non-modifiable factors?</h2>
<p>Modifiable factors include proper sample collection and handling, while non-modifiable factors involve the inherent properties of the TB bacteria.</p>

