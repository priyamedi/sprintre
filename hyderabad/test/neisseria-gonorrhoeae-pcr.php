<h2 class="test-h2 mt-3">Who should take the test?</h2>
<p>This test is recommended for anyone who has symptoms of gonorrhea or has been exposed to a sexual partner with the
    disease. It is also often performed as part of a broader sexually transmitted disease screening in individuals with
    high-risk sexual behaviors.</p>
<h2 class="test-h2 mt-3">What are the symptoms of gonorrhea?</h2>
<p>Symptoms in men can include a burning sensation when urinating, discharge from the penis, or painful or swollen
    testicles. In women, symptoms can include a painful or burning sensation when urinating, increased vaginal
    discharge, or vaginal bleeding between periods. However, many people with gonorrhea don't have any symptoms at all.
</p>
<h2 class="test-h2 mt-3">How accurate is the PCR test for Neisseria Gonorrhoeae?</h2>
<p>The PCR test is highly accurate at identifying the presence of Neisseria Gonorrhoeae, with few false positives or
    negatives. It is also able to detect the bacterium even in the early stages of infection, making it an effective
    tool for early diagnosis.</p>
<h2 class="test-h2 mt-3">What is the treatment for gonorrhea?</h2>
<p>Gonorrhea can usually be treated with antibiotics. It's crucial to complete the full course of medication, even if
    symptoms improve before the medication is finished. Additionally, it's important to notify all recent sexual
    partners so they can be tested and treated as well, if necessary.</p>
<h2 class="test-h2 mt-3">How can gonorrhea be prevented?</h2>
<p>The most reliable way to avoid infection is to abstain from sexual contact or to be in a long-term mutually
    monogamous relationship with a partner who has been tested and is known to be uninfected. The use of condoms
    correctly and consistently every time you have sex can also greatly reduce the risk.</p>
<h2 class="test-h2 mt-3">What complications can arise if gonorrhea is not treated?</h2>
<p>If left untreated, gonorrhea can lead to serious health problems. In women, it can cause pelvic inflammatory disease
    (PID), which can lead to chronic pelvic pain, ectopic pregnancy, and infertility. In men, it can lead to
    epididymitis, an inflammation of the tube at the back of the testicle, which can also lead to infertility. Gonorrhea
    can also increase the risk of getting or giving HIV, the virus that causes AIDS.</p>
<h2 class="test-h2 mt-3">Can I take the test at home?</h2>
<p>No, the test requires a sample to be collected by a healthcare professional. However, the sample collection procedure
    is quick and straightforward.</p>
<h2 class="test-h2 mt-3">Can gonorrhea be cured?</h2>
<p>Yes, gonorrhea can be cured with the right treatment, usually antibiotics. However, it's important to note that while
    treatment can clear the infection, it will not repair any permanent damage done by the disease. This is why early
    detection and treatment are crucial.</p>
<h2 class="test-h2 mt-3">Can I get gonorrhea more than once?</h2>
<p>Yes, having gonorrhea once does not protect you from getting it again. Even after successful treatment, you can still
    be reinfected if you have unprotected sex with an infected person.</p>
<h2 class="test-h2 mt-3">Is the test painful?</h2>
<p>The test may cause mild discomfort but should not be painful. If you experience severe pain during the sample
    collection, let your healthcare provider know immediately.</p>
<h2 class="test-h2 mt-3">How long will it take to get the results?</h2>
<p>The turn-around-time is typically 1-2 days. However, this can vary depending on the laboratory.</p>
<h2 class="test-h2 mt-3">What happens if my test result is positive?</h2>
<p>If your test result is positive, it means you have a gonorrhea infection. Your healthcare provider will discuss
    treatment options with you, which will usually involve antibiotics.</p>
<h2 class="test-h2 mt-3">Can pregnant women take the test?</h2>
<p>Yes, pregnant women can take the test. In fact, it's essential for pregnant women to get tested as gonorrhea can
    cause complications in pregnancy, including preterm birth and low birth weight. It can also be passed on to the baby
    during delivery, causing serious health problems.</p>
<h2 class="test-h2 mt-3">Can gonorrhea go away on its own?</h2>
<p>No, gonorrhea will not go away on its own. It requires treatment with antibiotics. Without treatment, the infection
    can persist and lead to serious health complications.</p>
<h2 class="test-h2 mt-3"> How does the PCR method work for this test?</h2>
<p>PCR (Polymerase Chain Reaction) is a technique used in the lab to make millions of copies of a particular section of
    DNA. In the case of this test, the DNA of the Neisseria gonorrhoeae bacterium is targeted. The mass amplification of
    the target DNA allows for easy detection and identification, making PCR a highly sensitive and accurate testing
    method.</p>
<h2 class="test-h2 mt-3">Is this test done as part of routine STI screening?</h2>
<p>Yes, this test is often included in routine sexually transmitted infection (STI) screenings, particularly for
    individuals who have multiple sexual partners, new sexual partners, or are in other high-risk groups.</p>
<h2 class="test-h2 mt-3">Can the test give a false positive or negative result?</h2>
<p>While the PCR test is highly accurate, no test is 100% perfect. There can be rare instances of false-positive or
    false-negative results. For example, a false positive might occur if the sample gets contaminated, while a
    false-negative might happen if the bacterial count in the sample is too low to detect.</p>
<h2 class="test-h2 mt-3">Are there any side effects or risks associated with the test?</h2>
<p>The test itself is generally safe with minimal risks. Some people might experience slight discomfort or light
    bleeding during or after the sample collection. If the bleeding continues or if you feel severe pain, contact your
    healthcare provider immediately.</p>
<h2 class="test-h2 mt-3">What does it mean if I have symptoms but the test result is negative?</h2>
<p>If you have symptoms of gonorrhea but your test result is negative, it could mean that you have another type of
    infection. It's important to discuss your symptoms and test results with your healthcare provider, who can guide you
    on the next steps.</p>
<h2 class="test-h2 mt-3">Do I need to abstain from sexual activity before the test?</h2>
<p>No, there are no specific sexual activity restrictions before the test. However, if you are experiencing symptoms of
    an STI, it is recommended to avoid sexual activity to prevent potentially passing the infection to others.</p>
<h2 class="test-h2 mt-3">Can the test detect other STIs?</h2>
<p>No, the Neisseria Gonorrhoeae PCR test is specific to gonorrhea. However, you can be tested for other STIs at the
    same time, if required. Your healthcare provider can guide you on which tests are appropriate based on your symptoms
    and risk factors.</p>
<h2 class="test-h2 mt-3">How can I protect myself from gonorrhea?</h2>
<p>The most effective way to prevent gonorrhea and other STIs is by using a condom every time you have sexual
    intercourse. It's also essential to have regular check-ups and screenings if you are sexually active, especially if
    you have multiple partners or new partners.</p>
<h2 class="test-h2 mt-3">Is there a vaccine for gonorrhea?</h2>
<p>As of now, there is no vaccine available to protect against gonorrhea. The best prevention methods are practicing
    safe sex and getting regular screenings.</p>
 
