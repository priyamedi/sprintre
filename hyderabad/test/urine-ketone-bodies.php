<h2 class="test-h2 mt-3">What is the Urine Ketone Bodies Test?</h2>
<p>The Urine Ketone Bodies Test is a diagnostic test that checks for the presence and level of ketones in the urine.</p>
<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>The test is performed to detect high levels of ketones in the body, which could be a sign of diabetes, starvation,
	alcoholism, or a ketogenic diet.</p>
