<h2 class="test-h2 mt-3">What is the purpose of the Multiple Sclerosis Profile - Screening test?</h2>
<p>The purpose of this test is to assist in diagnosing multiple sclerosis by identifying immune system activity and
    inflammation in the CNS, which are typical of MS.</p>
<h2 class="test-h2 mt-3">Is this test painful?</h2>
<p>The blood draw may cause minor discomfort. The lumbar puncture, which is used to collect the CSF sample, may cause
    discomfort or pain. Your doctor will use a local anesthetic to minimize the discomfort.</p>
