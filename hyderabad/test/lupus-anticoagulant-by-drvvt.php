	<h2 class="test-h2 mt-3">What is Lupus Anticoagulant?  </h2>
	<p>Lupus anticoagulant is an antibody produced by the immune system that targets the body's own cells, interfering with the blood's ability to clot properly. This can increase the risk of developing blood clots in veins or arteries.
 </p>
	<h2 class="test-h2 mt-3">What is the dRVVT test? 
 </h2>
	<p>The dRVVT test uses venom from the Russell's viper to initiate blood clotting. The venom specifically targets clotting factors in the blood, and the time taken for clotting to occur gives an indication of whether lupus anticoagulant is present.
 </p>
