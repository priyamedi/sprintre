<h2 class="test-h2 mt-3">What causes ASH and NASH?</h2>
<p>ASH is primarily caused by excessive and chronic consumption of alcohol. NASH can be due to various factors including
    obesity, type 2 diabetes, high cholesterol, or metabolic syndrome.</p>
<h2 class="test-h2 mt-3">What are the symptoms of ASH and NASH?</h2>
<p>In the early stages, ASH and NASH may not present any symptoms. As the condition progresses, symptoms such as
    fatigue, weakness, loss of appetite, jaundice (yellowing of the skin and eyes), and abdominal pain may develop.</p>
