<h2 class="test-h2 mt-3">What does this panel test for?</h2>
<p>This focused Hereditary Cancer Gene Panel screens for mutations in a broad range of genes known to be associated with
    an increased risk of various types of cancer. A mutation in one of these genes doesn't necessarily mean cancer is
    inevitable, but it does mean that the patient has a higher risk than the general population and should consider more
    regular or more specific screening procedures.</p>
<h2 class="test-h2 mt-3">When should I consider taking this test?</h2>
<p>This test is usually recommended for individuals who have a strong family history of certain cancers or who have a
    known mutation in their family. It's also suitable for individuals diagnosed with cancer at a younger age than
    typically expected or those with multiple primary cancers.</p>
