<h2 class="test-h2 mt-3">What is the purpose of the Total Testosterone test?</h2>
<p>The Total Testosterone test is generally done when an individual shows signs of abnormal testosterone levels, whether
	it be high or low. The results help doctors diagnose a variety of conditions such as hypogonadism, polycystic ovary
	syndrome (PCOS), and adrenal tumors.</p>
<h2 class="test-h2 mt-3">How is the Total Testosterone test performed?</h2>
<p>This test is performed using a standard blood sample. The healthcare provider will use a needle to draw blood from a
	vein in your arm or hand.</p>
