<h2 class="test-h2 mt-3">What is the purpose of the Myositis Profile-2 test?</h2>
<p>This test is designed to identify the presence of specific antibodies in the blood that are associated with different
    types of myositis, which is an inflammation of the muscles.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test is performed using a blood sample, which is obtained by drawing blood from a vein in your arm.</p>
