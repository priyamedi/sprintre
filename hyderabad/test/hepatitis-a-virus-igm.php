<h1 class="test-h1">Hepatitis A Virus (HAV) IgM </h1>
<div class="post-text">
<p>Hepatitis A is a liver infection that is caused by the Hepatitis A virus (HAV). The virus primarily spreads when an uninfected (and unvaccinated) person ingests food or water that is contaminated with the feces of an infected person. The infection is highly contagious, but not typically serious, and can be prevented through vaccination. The presence of HAV IgM antibodies in the blood indicates a recent infection with Hepatitis A. </p>
<p>HAV IgM antibodies are the first antibodies that are produced by the body in response to a Hepatitis A infection. They can usually be detected in the blood about 2-3 weeks after you catch the virus and can last about 2-6 months. The presence of these antibodies can be used to detect and diagnose an active or recent Hepatitis A infection. </p>
