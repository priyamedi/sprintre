	<h2 class="test-h2 mt-3">Why is the Fungus Culture [Pleural Fluid] test performed? </h2>
	<p>This test is performed to diagnose a fungal infection in the pleural space. This might be suspected if a patient presents symptoms such as chest pain, persistent cough, difficulty breathing, and fever.
 </p>
	<h2 class="test-h2 mt-3">How is the pleural fluid sample collected? </h2>
	<p> A procedure called thoracentesis is performed by a doctor to collect pleural fluid. The skin is first numbed with a local anesthetic, then a needle is carefully inserted into the pleural space to withdraw fluid.
 </p>
