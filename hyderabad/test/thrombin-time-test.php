<h1 class="test-h1">Thrombin Time Test </h1>
<div class="post-text">
<p>The Thrombin Time (TT) test is a blood test that measures the time it takes for a clot to form in the plasma of a blood sample after thrombin has been added. Thrombin is a protein involved in blood clotting, and it converts fibrinogen into fibrin, the substance that forms the mesh of a blood clot.</p>

<p>The Thrombin Time test is often used when a doctor suspects that a person has a bleeding disorder. It can help diagnose conditions that can cause excessive bleeding or clotting. The test is often used as part of a series of tests, which might include a complete blood count, prothrombin time, and partial thromboplastin time.</p>
