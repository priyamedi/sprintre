particularly if you have consumed undercooked poultry or unpasteurized milk, your doctor may order
this test. </p>
	<h2 class="test-h2 mt-3"> What are the normal values for this test?</h2>
	<p>A normal result for this test is negative, which means that no Campylobacter antigens were detected in
the stool sample. </p>
	<h2 class="test-h2 mt-3">What do abnormal results mean? </h2>
	<p> Abnormal results, i.e., a positive result, means that Campylobacter antigens were detected, suggesting
