count specific DNA sequences on chromosomes. By examining nine different chromosomes, this test can
detect a variety of chromosomal abnormalities. For example, abnormalities in chromosome 21 are
associated with Down syndrome, and anomalies in chromosomes X and Y can lead to sex-linked
disorders.</p>
