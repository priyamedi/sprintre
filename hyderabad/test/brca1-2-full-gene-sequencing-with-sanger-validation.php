<h2 class="test-h2 mt-3">What is the purpose of BRCA 1 & 2 Full Gene Sequencing?</h2>
<p>This test is performed to determine whether an individual carries mutations in the BRCA1 and/or BRCA2 genes, which are associated with a higher risk of breast and ovarian cancer, among others.</p>
<h2 class="test-h2 mt-3">What does Sanger validation mean?</h2>
<p>Sanger validation is an additional step in the testing process that uses Sanger sequencing, a highly reliable method, to confirm the results of the initial gene sequencing. This ensures accuracy and reliability in the results.</p>
