	<h2 class="test-h2 mt-3">What is the Anti-TG test in Cerebrospinal Fluid? </h2>
	<p>The Anti-TG test in Cerebrospinal Fluid measures the levels of antibodies against thyroglobulin, a protein produced by the thyroid gland, in the cerebrospinal fluid. It is used to investigate certain neurological conditions associated with autoimmune disorders.
 </p>
	<h2 class="test-h2 mt-3">Why is this test important? </h2>
