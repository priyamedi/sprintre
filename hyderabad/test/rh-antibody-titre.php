<h1 class="test-h1">Rh Antibody Titre (Anti D/Coomb's titre- Automated) </h1>
<div class="post-text">
<p> The <strong>Rh (Rhesus) Antibody Titre</strong> is a blood test that is primarily used during pregnancy to determine the
mother's Rh blood type (Rh positive or Rh negative), and to check whether she has developed antibodies
against the Rh antigen. The Rh antigen is a protein found on the red <a href="https://www.sprintdiagnostics.in/our-services/laboratory/hematology/"><b style="color:#2C53A5">blood</b></a> cells (RBCs) of about 85% of
people, and they are termed Rh positive. Those who lack the Rh antigen are referred to as Rh negative.
This is particularly important in cases where an Rh-negative woman is pregnant with an Rh-positive
fetus. The immune system of the Rh-negative mother may recognize the Rh antigen on the fetal blood
cells as foreign and produce antibodies against it, a process known as Rh sensitization. The <strong>Rh Antibody
Titre</strong> test measures the levels of these antibodies in the mother's blood.
</p>
<p> A person can also become sensitized following an Rh-incompatible <a href="https://www.sprintdiagnostics.in/our-services/laboratory/hematology/"><b style="color:#2C53A5">blood</b></a> transfusion or organ
transplant. The development of antibodies can lead to complications, such as hemolytic disease in a
newborn or hemolytic transfusion reactions. Hence, this test is useful to avoid such conditions by
monitoring the antibody levels.</p>
