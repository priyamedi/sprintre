<h1 class="test-h1">Troponin-I Quantitative </h1>
<div class="post-text">
<p>The Troponin-I Quantitative test is a blood test primarily used to identify heart damage indicative of a heart attack. Troponins are a group of proteins found in cardiac (heart) muscle fibers that regulate muscle contraction. Troponin-I is a subtype of this protein that is found predominantly in the heart. When there is damage to the heart, as occurs during a heart attack, troponin-I is released into the bloodstream.</p>
<p>Measuring levels of Troponin-I is therefore a way to determine whether the heart muscle has been injured. The Troponin-I test is usually ordered when a patient has symptoms such as chest pain, shortness of breath, dizziness, or other signs of a heart attack. It can also be used to monitor the progress of treatment for heart disease.</p>
