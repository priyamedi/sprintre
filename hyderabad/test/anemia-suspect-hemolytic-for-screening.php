                                <h2 class="test-h2 mt-3"> What do these tests measure?</h2>
                                <ul>
                                    <li> <b>Coombs Test - Direct & Indirect:</b> These tests detect antibodies that can
                                        cause
                                        your immune system to destroy red blood cells.</li>
                                    <li> <b>G6PD Quantitative:</b> Measures the activity of G6PD, an enzyme that
                                        protects red
                                        blood cells from damage.</li>
                                    <li> <b>Haptoglobin:</b> This protein binds free hemoglobin released from red blood
                                        cells
                                        and is low in hemolytic anemia.</li>
                                    <li> <b>Hemogram (CBC + Retic + PS):</b> This comprehensive blood test measures the
                                        count,
