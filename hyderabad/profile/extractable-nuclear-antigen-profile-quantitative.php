<h1 class="test-h1">ENA (Extractable Nuclear Antigen) Profile - Quantitative </h1>
<div class="post-text">
<p>Extractable Nuclear Antigen (ENA) profile is an important blood test used to help in the diagnosis and monitoring of autoimmune disorders. Autoimmune disorders are diseases in which the body’s immune system attacks its own tissues. The ENA profile specifically targets antibodies produced against various nuclear antigens found within the cell nucleus. These antibodies are known as anti-ENA antibodies and can be associated with different autoimmune disorders. </p>
<p>The ENA profile typically includes a panel of tests for specific antibodies including, but not limited to, anti-Smith, anti-RNP, anti-SSA/Ro, anti-SSB/La, anti-Scl-70, and anti-Jo-1. These antibodies are associated with diseases like systemic lupus erythematosus (SLE), Sjögren’s syndrome, scleroderma, polymyositis, and mixed connective tissue disease among others. The quantitative ENA profile measures the amount of these antibodies present in the blood, which can be indicative of the activity of the disease. </p>
