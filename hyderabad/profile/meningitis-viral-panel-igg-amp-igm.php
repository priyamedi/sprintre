spinal cord, specifically the meninges. It is typically less severe than bacterial meningitis but can still lead
to serious complications, especially in those with compromised immune systems. The Viral Meningitis
Panel tests for IgG and IgM antibodies for several viruses, including Coxsackie, Herpes Simplex Virus
