<h2 class="test-h2 mt-3">Why is the Streptococcus Group B Antigen test done?</h2>
<p>This test is conducted to identify the presence of Group B Streptococcus bacteria in the blood, often when a serious
	infection is suspected. For pregnant women, it is a routine part of prenatal care to prevent transmission to the
	newborn during birth.</p>
<h2 class="test-h2 mt-3">Who needs to take this test?</h2>
<p>Anyone exhibiting symptoms of a severe infection like fever, chills, and difficulty breathing may be advised to take
	this test. Pregnant women are also routinely tested for GBS between weeks 35 and 37 of their pregnancy.</p>
