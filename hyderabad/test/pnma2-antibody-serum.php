<p>The test is crucial for the early detection of a paraneoplastic syndrome and the possible underlying cancer. The
    earlier a cancer is detected, the more likely it can be successfully treated or managed.</p>
<h2 class="test-h2 mt-3">Is fasting required for this test?</h2>
