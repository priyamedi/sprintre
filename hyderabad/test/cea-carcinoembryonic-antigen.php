<h2 class="test-h2 mt-3">What is the importance of getting the CEA test done?</h2>
<p>The CEA test helps diagnose and manage certain types of cancers, especially colorectal cancer. It can be used to
    monitor the treatment response, detect recurrence, and in some cases, used for early detection of cancer.</p>
<h2 class="test-h2 mt-3">Is fasting required for the CEA test?</h2>
<p>No, fasting is not necessary for the CEA test.</p>
