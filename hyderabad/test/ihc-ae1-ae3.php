<h1 class="test-h1">IHC - AE1/AE3 </h1>
<div class="post-text">
<p>The <strong>IHC - AE1/AE3 test</strong> is a diagnostic procedure that uses specific antibodies, AE1 and AE3, to identify cytokeratins in tissue samples. Cytokeratins are proteins found in the cytoskeleton of epithelial cells, and they play a key role in maintaining the structural integrity of these cells.
 </p>
<p>Immunohistochemistry (IHC) is a method used in pathology to identify certain molecules (antigens) in tissue sections by exploiting the principle of antibodies binding specifically to antigens in biological tissues. In the AE1/AE3 test, the antibodies AE1 and AE3 are used to target and bind with cytokeratins in the tissue sample, providing valuable information about the presence and distribution of these proteins. This information can be crucial in diagnosing and characterizing various diseases, particularly cancers of epithelial origin such as carcinomas.
 </p>
