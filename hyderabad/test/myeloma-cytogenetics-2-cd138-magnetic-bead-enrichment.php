	<p>CD138 is a protein that is highly expressed on the surface of plasma cells. Magnetic Bead Enrichment uses magnetic particles bound to antibodies that recognize CD138, allowing for the separation and concentration of plasma cells from a bone marrow sample. This increases the accuracy and sensitivity of the FISH analysis. </p>
