<h1 class="test-h1">IHC - CEA</h1>
<div class="post-text">
<p>The IHC - CEA test is a powerful tool in the armory of modern diagnostic procedures, particularly in the field of oncology. This immunohistochemical test targets the presence of carcinoembryonic antigen (CEA), a type of glycoprotein that can be found in higher concentrations in cancerous tissues, particularly in colorectal cancer.</p>
<p>CEA was first identified in the 1960s and it was recognized as a marker produced by certain types of cancer, hence its name - Carcinoembryonic Antigen. It's an intriguing molecule because it's also produced in the developing fetus, but production typically ceases before birth. In adults, levels of CEA are usually very low, but these can rise in response to certain types of cancer or some non-cancerous conditions.</p>
