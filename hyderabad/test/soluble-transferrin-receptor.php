<h2 class="test-h2 mt-3">What is the Soluble Transferrin Receptor (sTFR) test?</h2>
<p>The sTFR test is a blood test that measures the amount of soluble transferrin receptors, a marker of iron status, in your blood.</p>

<h2 class="test-h2 mt-3">Why is the Soluble Transferrin Receptor (sTFR) test done?</h2>
<p>This test is done to evaluate iron status and to help differentiate between iron deficiency anemia and anemia of chronic disease. It can also be useful in the diagnosis and monitoring of iron overload conditions.</p>

