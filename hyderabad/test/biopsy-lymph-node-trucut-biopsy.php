	<h2 class="test-h2 mt-3">What is a Trucut biopsy? </h2>
	<p>A Trucut biopsy is a diagnostic procedure where a needle is used to extract a small core of tissue from an organ or lymph node. This sample is then analyzed microscopically to check for abnormalities such as cancer. </p>
	<h2 class="test-h2 mt-3">Why is a Trucut biopsy of a lymph node performed? </h2>
	<p>This biopsy is performed when there is suspicion of disease in the lymph node, such as cancer, infection, or an immune system disorder. It helps in the accurate diagnosis and planning of appropriate treatment. </p>
