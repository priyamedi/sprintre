<p>Immunohistochemistry (IHC) is a powerful diagnostic tool used in pathology to visualize the presence of specific proteins in tissues. The <strong>IHC - CD 123</strong> test specifically detects the expression of the CD123 protein in cells. CD123, also known as the interleukin-3 receptor alpha (IL-3Rα), is a protein that often overexpresses in certain types of blood cancers, such as hairy cell leukemia, acute myeloid leukemia, and blastic plasmacytoid dendritic cell neoplasm. It helps in distinguishing these conditions from other similar hematological malignancies, aiding in accurate diagnosis and guiding appropriate treatment strategies.
