<h2 class="test-h2 mt-3">What is a Urea - Body Fluid test?</h2>
<p>The Urea - Body Fluid test measures the amount of urea nitrogen, a waste product of protein metabolism, in body fluid
	samples. It helps diagnose or monitor conditions affecting the kidneys or liver.</p>
<h2 class="test-h2 mt-3">Why would I need a Urea - Body Fluid test?</h2>
<p>This test may be necessary if your healthcare provider suspects a problem with your kidneys or liver. It can also be
	used to monitor the effectiveness of treatments for these conditions.</p>
