<h2 class="test-h2 mt-3">Why is the Fungus Culture for ICD Fluid important?</h2>
<p> The test is important to diagnose fungal infections in individuals with an implanted intra-cardiac device. These infections are serious and may lead to complications or device failure if not properly managed.</p>
<h2 class="test-h2 mt-3">What are the symptoms of a fungal infection around an intra-cardiac device?</h2>
<p> Symptoms can include fever, chills, fatigue, chest pain, or warmth, redness, and discharge at the site where the device was implanted. Some individuals may not exhibit noticeable symptoms.</p>
