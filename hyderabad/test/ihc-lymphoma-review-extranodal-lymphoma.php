<h2 class="test-h2 mt-3">Why is the IHC - Lymphoma Review - Extranodal Lymphoma test performed?</h2>
	<p>
The test is performed to diagnose extranodal lymphomas and to determine the exact type of lymphoma
involved, which is crucial for devising an appropriate treatment plan.</p>
<h2 class="test-h2 mt-3"> How is the sample for this test collected?</h2>
<p>
The sample is typically collected through a biopsy procedure, where a small piece of tissue is removed
from the affected area. The specifics of the biopsy procedure will vary depending on the location and
nature of the suspected lymphoma.</p>
