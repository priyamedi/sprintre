<h2 class="test-h2 mt-3">What is a Chimerism Study?</h2>
<p>A Chimerism Study is a test used to assess the ratio of donor to recipient cells following a bone marrow transplant.
	It helps determine if the transplanted cells have engrafted and are producing new blood cells.</p>
<h2 class="test-h2 mt-3">Why is a Chimerism Study done after a bone marrow transplant?</h2>
<p>A Chimerism Study is done to monitor the success of the transplant. It helps identify if the transplant is successful
	and whether the new cells are growing. It also helps detect early signs of graft failure or disease relapse.</p>
