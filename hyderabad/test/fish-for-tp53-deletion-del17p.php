<h1 class="test-h1">FISH for TP53 Deletion / del17p </h1>
<div class="post-text">
<p>FISH for TP53 deletion, also known as del17p, is a diagnostic test that employs Fluorescence In Situ Hybridization (FISH) to detect deletions in the TP53 gene, which is located on the short arm of chromosome 17. TP53 is commonly known as a tumor suppressor gene and is crucial in preventing the development of tumors. The protein encoded by TP53 plays a significant role in DNA repair, cell cycle arrest, and apoptosis. Deletion or mutations in the TP53 gene are linked to numerous cancers, and a deletion specifically in the short arm of chromosome 17 where TP53 resides (17p) is associated with poor prognosis in cancers such as chronic lymphocytic leukemia (CLL) and multiple myeloma.</p>

<p>FISH uses fluorescent probes that bind to the parts of the chromosome of interest, in this case, chromosome 17. When viewed under a special microscope, the fluorescence helps in determining whether there is a deletion in the TP53 gene. The presence or absence of fluorescence indicates the status of the TP53 gene.</p>
