<h1 class="test-h1">Oculocutaneous Albinism Type 1 (TYR Gene)</h1>
<div class="post-text">
<p>Oculocutaneous albinism type 1 (OCA1) is a genetic condition characterized by a reduced or complete lack of melanin, the pigment that gives color to our skin, hair, and eyes. It is the most severe form of albinism. Individuals with OCA1 usually have milky white skin, white hair, and blue eyes at birth. The OCA1 test analyzes the TYR gene, which is responsible for producing an enzyme called tyrosinase, essential for melanin production.</p>
<p>Mutation in the TYR gene results in a faulty tyrosinase enzyme, leading to reduced or absent melanin production. By identifying any mutations in the TYR gene, this test can confirm a diagnosis of OCA1, allowing for early intervention and proper management of the condition.</p>


