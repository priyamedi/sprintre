<h2 class="test-h2 mt-3">Why is a fungus culture from the endotracheal tube performed? </h2>
<p>This test is performed to identify the presence of fungal infections in the respiratory tract of patients who are on mechanical ventilation through an endotracheal tube. Early detection of such infections is critical to initiate appropriate treatment and avoid complications.</p>
<h2 class="test-h2 mt-3">Who should undergo this test?</h2>
<p> Patients who are on mechanical ventilation via an endotracheal tube and show symptoms such as fever, difficulty in breathing, or have secretions from the respiratory tract that may suggest a fungal infection should undergo this test.</p>
