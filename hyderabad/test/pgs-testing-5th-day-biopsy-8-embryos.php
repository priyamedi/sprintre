	<h2 class="test-h2 mt-3">Who should consider PGS testing?</h2>
<p>Couples undergoing IVF, especially those with a history of recurrent miscarriages, multiple failed IVF cycles, genetic disorders, or advanced maternal age should consider PGS testing. It is recommended to discuss the option with a fertility specialist to make an informed decision.</p>

	<h2 class="test-h2 mt-3">What is a chromosomally normal embryo?</h2>
<p>A chromosomally normal, or euploid, embryo has the correct number of chromosomes, which is 46 in total, 23 from the egg and 23 from the sperm. These embryos have a higher likelihood of developing into a successful pregnancy and a healthy baby.</p>

	<h2 class="test-h2 mt-3">What happens if a chromosomal abnormality is detected in an embryo?</h2>
<p>If a chromosomal abnormality is detected in an embryo, it will not be selected for implantation. The decision to proceed with the transfer of any remaining normal embryos will be made in consultation with the fertility specialist.</p>

	<h2 class="test-h2 mt-3">Can PGS testing be used to select the sex of the baby?</h2>
<p>While PGS testing can identify the sex of the embryos as it involves the examination of the chromosomes, its primary goal is to identify chromosomally normal embryos and not to select the sex of the baby.</p>

	<h2 class="test-h2 mt-3">How long does it take to receive the results of PGS testing?</h2>
<p>The results of PGS testing are typically available within 7-10 days. The embryos are safely cryopreserved (frozen) during the waiting period until the results are ready.</p>

	<h2 class="test-h2 mt-3">How reliable is PGS testing?</h2>
<p>PGS testing is a highly reliable method for identifying chromosomal abnormalities in embryos. However, as with all tests, it's not 100% accurate and there is a small chance of false-positive or false-negative results.</p>

	<h2 class="test-h2 mt-3">Can the biopsy for PGS testing harm the embryos?</h2>
<p>The biopsy procedure, which involves removing a few cells from each embryo, is generally safe and does not harm the embryos when performed by a skilled embryologist. However, there is a minimal risk of damage to the embryo.</p>

	<h2 class="test-h2 mt-3">What are the risks associated with PGS testing?</h2>
<p>The main risk associated with PGS testing is the potential for damage to the embryo during the biopsy procedure. However, this risk is minimal and the procedure is generally safe when performed by a skilled embryologist. Other risks involve the potential for false-positive or false-negative results.</p>

	<h2 class="test-h2 mt-3">How many embryos can be tested at once?</h2>
<p>The number of embryos tested at once can vary. In this case, the test involves the assessment of eight embryos. However, the number can be more or less depending on the individual circumstances of the patients.</p>

	<h2 class="test-h2 mt-3">Can PGS testing detect all chromosomal abnormalities?</h2>
<p>While PGS testing is a powerful tool for detecting a wide range of chromosomal abnormalities, it may not detect all types of chromosomal abnormalities. It's important to discuss the limitations of the test with your healthcare provider.</p>

	<h2 class="test-h2 mt-3">Can PGS testing increase the chance of pregnancy?</h2>
<p>Yes, PGS testing can significantly increase the chance of a successful pregnancy. By identifying and selecting chromosomally normal embryos for transfer, the chances of implantation and development into a healthy baby are greatly improved.</p>

	<h2 class="test-h2 mt-3">Can PGS testing be used for gender selection?</h2>
<p>While PGS testing can determine the sex of the embryos, its primary purpose is to screen for chromosomal abnormalities. Any considerations regarding gender selection should be discussed with your healthcare provider.</p>

	<h2 class="test-h2 mt-3">How does age affect the results of PGS testing?</h2>
<p>As women age, the likelihood of chromosomal abnormalities in the embryos increases, which can be detected through PGS testing. Therefore, PGS testing is often recommended for women of advanced maternal age.</p>

