<h2 class="test-h2 mt-3"> How is the sample collected?</h2>
<p> A healthcare professional will gently swab the eye with a sterile cotton swab to collect the sample. It’s
a quick procedure but might be a bit uncomfortable.</p>

<h2 class="test-h2 mt-3"> What kind of infections can be detected through this test?</h2>
<p> This test can detect bacterial infections such as conjunctivitis and blepharitis. It is not designed to
detect viral or fungal infections.</p>

<h2 class="test-h2 mt-3"> What should I do before the test?</h2>
<p> Avoid touching or rubbing your eye, and don't use eye drops or medications before the test unless
instructed by a doctor. If you wear contact lenses, you might need to remove them before the test.</p>

<h2 class="test-h2 mt-3"> How long does it take to get the results?</h2>
<p> Preliminary results are often available within 24-48 hours. However, final identification and sensitivity
results may take up to 3-7 days.</p>

<h2 class="test-h2 mt-3"> How are the results interpreted?</h2>
<p> If bacteria are present, the lab report will list the type of bacteria and which antibiotics it is sensitive
to. Your doctor will use this information to prescribe the best treatment for your infection.</p>

<h2 class="test-h2 mt-3"> What if the bacteria are resistant to antibiotics?</h2>
<p> If the bacteria are resistant to common antibiotics, your doctor might prescribe a combination of
drugs or use alternative treatments.</p>

<h2 class="test-h2 mt-3"> What should I do if I have an eye infection?</h2>
<p> If you suspect you have an eye infection, it’s important to see a doctor as soon as possible. Eye
infections can worsen quickly, so prompt treatment is crucial.</p>

<h2 class="test-h2 mt-3"> Can I wear contact lenses if I have an eye infection?</h2>
<p> It is generally not recommended to wear contact lenses if you have an eye infection as it can worsen
the infection. Consult your doctor for specific instructions.</p>

<h2 class="test-h2 mt-3"> What are the symptoms of a bacterial eye infection?</h2>
<p> Symptoms may include redness, pain, itching, discharge, sensitivity to light, and a gritty feeling in the
eye.</p>

<h2 class="test-h2 mt-3"> Can eye infections be prevented?</h2>
<p> Maintaining good hygiene, such as washing your hands frequently and avoiding touching your eyes,
can help prevent eye infections. Also, follow proper care for contact lenses if you wear them.</p>

<h2 class="test-h2 mt-3"> Can bacterial eye infections spread to other people?</h2>
<p> Yes, some bacterial eye infections are highly contagious. It’s important to maintain good hygiene and
avoid sharing personal items like towels or eye makeup.</p>

<h2 class="test-h2 mt-3"> What are the possible complications of untreated bacterial eye infections?</h2>
<p> Untreated bacterial eye infections can lead to serious complications such as corneal ulcers, vision
loss, or the spread of infection to other parts of the eye and surrounding tissues. </p>
