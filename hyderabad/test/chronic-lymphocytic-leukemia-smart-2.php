	<h2 class="test-h2 mt-3">How is the CLL Smart 2 test performed? </h2>
	<p>A blood sample is drawn from the patient. This sample is then processed using Flow Cytometry to analyze the physical and chemical properties of lymphocytes and Fluorescence In Situ Hybridization (FISH) to analyze chromosomal abnormalities. </p>
	<h2 class="test-h2 mt-3">What is the significance of the genetic markers that CLL Smart 2 tests for? </h2>
	<p>17p deletion is associated with an aggressive form of CLL and resistance to chemotherapy. ATM deletion is associated with an intermediate prognosis. Trisomy 12 can indicate a variable course. 13q deletion is typically associated with a more favorable prognosis. These markers help in predicting the course of CLL and guiding treatment strategies. </p>
	<h2 class="test-h2 mt-3">What does it mean if any of these genetic markers are present? </h2>
	<p> The presence of these genetic markers can indicate the likely course of the disease and response to treatment. For example, 17p deletion often indicates an aggressive form and resistance to standard chemotherapy. This information is essential in guiding the choice of treatment.</p>
	<h2 class="test-h2 mt-3">What are the treatment options if any of these genetic markers are present? </h2>
