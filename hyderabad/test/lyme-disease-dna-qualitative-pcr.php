<h2 class="test-h2 mt-3">What is Lyme disease? </h2>
<p>Lyme disease is a bacterial infection primarily transmitted by ticks found in wooded and grassy areas. It's caused by the bacterium Borrelia burgdorferi. Common symptoms include fever, headache, fatigue, and a characteristic skin rash.</p>
<h2 class="test-h2 mt-3">Why is this test performed? </h2>
<p>This test is performed when Lyme disease is suspected based on exposure to ticks or symptoms. It is particularly helpful in the early stages of the disease when antibody tests may not yet be positive.</p>
