<h2 class="test-h2 mt-3">What is a CMV (Cytomegalovirus) IgM Antibodies test?</h2>
<p>A CMV IgM Antibodies test is a blood test that detects the presence of IgM antibodies to the
cytomegalovirus. The presence of these antibodies typically indicates a current or recent infection with
CMV.</p>

<h2 class="test-h2 mt-3">Why might I need this test?</h2>
<p>This test may be recommended if you have symptoms of a CMV infection, such as fever, fatigue, and
muscle aches. It can also be used in prenatal testing to determine if a pregnant woman has a recent
CMV infection, as the virus can cause complications in newborns.</p>
