<h2 class="test-h2 mt-3">What is the DMD Test for Amniotic Fluid?</h2>
<p>This is a prenatal diagnostic test that analyzes a sample of amniotic fluid to detect mutations in the DMD gene,
    which causes Duchenne Muscular Dystrophy.</p>
<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is performed to determine if a fetus has Duchenne Muscular Dystrophy, especially in cases where there is a
    known family history of DMD.</p>
