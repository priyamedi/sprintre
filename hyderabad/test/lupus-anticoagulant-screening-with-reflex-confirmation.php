<h2 class="test-h2 mt-3">Why is this test done? </h2>
<p>This test is done to detect the presence of lupus anticoagulants in the blood, which can cause abnormal blood clotting. It is often ordered when a person has symptoms suggestive of a blood clotting disorder or a systemic autoimmune disease like lupus.</p>
<h2 class="test-h2 mt-3">How is this test performed? </h2>
<p>This test is performed on a blood sample, which is drawn from a vein in your arm using a needle.</p>
