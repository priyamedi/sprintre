<h2 class="test-h2 mt-3">Why is this test ordered?</h2>
<p>The Thrombo Check or Thrombophilia Panel I is ordered if you have a history of unexplained blood clots, recurrent
    miscarriage, or a family history of blood clotting disorders. It is also used to monitor the effectiveness of
    treatment in patients diagnosed with a clotting disorder.</p>
<h2 class="test-h2 mt-3">What are Protein C, Protein S, and Antithrombin III?</h2>
<p>Protein C, Protein S, and Antithrombin III are natural anticoagulants—proteins in your blood that help regulate clot
    formation. A deficiency in any of these proteins can lead to an increased risk of forming abnormal blood clots.</p>
