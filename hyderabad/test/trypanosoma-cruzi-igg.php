<h2 class="test-h2 mt-3">What is the purpose of the Trypanosoma Cruzi IgG test?</h2>
<p>The test is primarily used to diagnose Chagas disease. It detects the presence of IgG antibodies that the body
    produces in response to an infection with Trypanosoma Cruzi.</p>
<h2 class="test-h2 mt-3">How is the Trypanosoma Cruzi IgG test performed?</h2>
<p>The test is conducted by drawing a small amount of blood from a vein, typically in your arm. The blood sample is then
    sent to a laboratory for analysis.</p>
