<div class="post-text">
<p>Alkaline Phosphatase - Bone sub-Fraction (Ostase) is a specific test designed to measure the level of
bone-specific alkaline phosphatase (BAP), a form of alkaline phosphatase produced in the cells that form
bone tissue. Alkaline phosphatase is an enzyme that is found throughout the body, but the highest
concentrations are in the liver, bones, kidneys, and digestive system. The Ostase test provides a direct
measure of the bone-specific form of this enzyme.
 </p>
<p>Bone diseases such as osteoporosis, Paget's disease, and others may cause an increase in the level of
BAP in the blood. Measuring the level of BAP can provide valuable information about the rate of bone
turnover, which can help diagnose specific bone conditions, monitor the progress of bone diseases, and
evaluate the effectiveness of their treatment. </p>
