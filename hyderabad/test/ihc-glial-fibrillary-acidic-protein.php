<h2 class="test-h2 mt-3">Why is the IHC - GFAP test performed?</h2>
<p> The IHC - GFAP test is performed to identify astrocytes in tissue samples and diagnose brain tumors originating from astrocytes, such as gliomas. It is also used to study various neurological diseases and conditions involving astrocytes.</p>
<h2 class="test-h2 mt-3">What types of samples are used for this test? </h2>
<p>The IHC - GFAP test is performed on tissue samples, typically obtained from the brain or spinal cord via biopsy or during surgery.</p>
