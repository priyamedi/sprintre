<h2 class="test-h2 mt-3">What is amniotic fluid karyotyping?</h2>
<p>Amniotic fluid karyotyping is a procedure where a sample of amniotic fluid is collected from the womb during pregnancy, and the chromosomes in the cells found in the fluid are analyzed to detect any genetic or chromosomal abnormalities in the fetus.</p>

<h2 class="test-h2 mt-3">Why is karyotyping for twins performed?</h2>
<p>This test is performed for twins to detect if there are any genetic disorders or chromosomal abnormalities in either or both of the fetuses. It helps in identifying conditions such as Down syndrome, Edwards syndrome, or other genetic abnormalities that may affect the development of the babies.</p>

