	<h2 class="test-h2 mt-3">What is a sputum culture and sensitivity test?</h2>
	<p>A sputum culture and sensitivity test is a laboratory test that analyzes sputum (mucus coughed up
from the lower airways) to identify any bacteria present and determine which antibiotics are effective
against them. This helps doctors diagnose and treat respiratory infections more effectively.</p>
	<h2 class="test-h2 mt-3">How do I collect a sputum sample?</h2>
	<p>To collect a sputum sample, you should not eat, drink, or brush your teeth for 1-2 hours before
collection. Rinse your mouth with water, take a deep breath, and cough forcefully to bring up sputum
from the lungs. Spit the sputum into a sterile container provided by the laboratory.</p>
