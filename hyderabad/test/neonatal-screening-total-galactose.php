<h2 class="test-h2 mt-3">Why is neonatal screening for total galactose important?</h2>
<p>This screening is vital because it can detect galactosemia, a potentially life-threatening condition, at an early
    stage. Early diagnosis allows for immediate treatment, significantly improving the health outcomes for the child.
</p>
<h2 class="test-h2 mt-3">What happens if galactosemia is not detected early?</h2>
<p>Untreated galactosemia can lead to severe complications such as liver damage, kidney failure, cataracts, and severe
    intellectual disability. These symptoms can begin to occur just a few days after a baby ingests galactose, usually
    through breast milk or formula.</p>
