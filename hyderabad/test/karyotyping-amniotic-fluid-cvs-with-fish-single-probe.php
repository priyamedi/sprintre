<h1 class="test-h1">Karyotyping (Amniotic Fluid/CVS) with FISH - Single Probe</h1>
<div class="post-text">
<p>Karyotyping with FISH using Amniotic Fluid or Chorionic Villus Sampling (CVS) is a diagnostic procedure that involves analyzing the chromosomes in the cells obtained from the amniotic fluid or the placenta. The amniotic fluid is the liquid surrounding the fetus, while chorionic villi are small finger-like projections of the placenta. Chromosomes carry genetic information and normally, each person has 46 chromosomes. However, certain genetic conditions are associated with an abnormal number or structure of chromosomes.</p>

<p>The test combines traditional karyotyping with Fluorescence In Situ Hybridization (FISH), which employs fluorescent probes that bind to specific parts of the chromosome. This enables the detection of specific genetic abnormalities that may not be visible through conventional karyotyping.</p>

<p>This procedure is mainly performed during pregnancy to screen for genetic disorders in the fetus. It is often recommended in cases of family history of genetic disorders, advanced maternal age, or if other screening tests have indicated a possible risk for chromosomal abnormalities.</p>

