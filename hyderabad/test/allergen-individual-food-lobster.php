<h2 class="test-h2 mt-3"> What are the symptoms of a lobster allergy?</h2>
<p>Symptoms of a lobster allergy can vary from person to person and may include itching or tingling in the mouth, hives, eczema, swelling of the lips, face, tongue, or throat, wheezing, nasal congestion, trouble breathing, stomach pain, diarrhea, nausea, or vomiting. In severe cases, it can cause anaphylaxis, which is a life-threatening emergency.</p>
<h2 class="test-h2 mt-3"> How is the Allergen, Individual - Food Lobster test performed?</h2>
<p>A healthcare professional will draw a blood sample from your arm. This sample will then be sent to a laboratory where it will be analyzed for the presence of specific IgE antibodies that react with proteins in lobster.</p>
