<h2 class="test-h2 mt-3">What is the Urea - 24 Hour Urine Test?</h2>
<p>It's a diagnostic test that measures the amount of urea nitrogen in your urine, which is collected over a 24-hour
    period.</p>
<h2 class="test-h2 mt-3">What does the test measure?</h2>
<p>This test measures the amount of urea nitrogen in your urine. Urea nitrogen is a waste product that your body
    produces after breaking down protein.</p>
