<h2 class="test-h2 mt-3">What is the purpose of the Glucose Challenge Test?</h2>
<p>The GCT is used to screen for gestational diabetes, which is a type of diabetes that only occurs during pregnancy.
    The test measures how effectively your body processes sugar, which is crucial to ensure the health of both you and
    your baby.</p>
<h2 class="test-h2 mt-3">How is the Glucose Challenge Test performed?</h2>
<p>During the GCT, you will drink a sugary solution. After one hour, a blood sample will be taken to measure your
    blood sugar levels.</p>
