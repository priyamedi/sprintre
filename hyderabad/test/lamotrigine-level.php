proper dosage of the drug: too low, and your symptoms may not be adequately
controlled; too high, and you may experience adverse side effects. The test is often
ordered after the start of a lamotrigine regimen, after a change in dosage, or if side
effects or a recurrence of symptoms occur.</p>
