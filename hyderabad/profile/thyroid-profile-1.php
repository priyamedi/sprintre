<h1 class="test-h1">Thyroid Profile - I (T3, T4, & TSH) </h1>
<div class="post-text">
<p>The Thyroid Profile - I, consisting of the triiodothyronine (T3), thyroxine (T4), and thyroid-stimulating hormone (TSH) tests, is a set of blood tests designed to evaluate the functioning of your thyroid gland. The thyroid is a small, butterfly-shaped gland located in your neck that plays a significant role in your metabolism, growth, and development.</p>

<p>T3 and T4 are hormones produced by the thyroid gland and are critical for maintaining the body's metabolism. TSH, produced by the pituitary gland in the brain, regulates the production of T3 and T4. The levels of these three hormones in your blood can indicate if your thyroid is overactive (hyperthyroidism), underactive (hypothyroidism), or functioning normally.</p>
