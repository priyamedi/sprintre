<h2 class="test-h2 mt-3">When should I take the Flowcytometry - CD24 test?</h2>
<p>Your doctor will typically order this test if you have symptoms suggesting a hematological disease, or to monitor the
    progression of a diagnosed B-cell malignancy.</p>
<h2 class="test-h2 mt-3">What information does the Flowcytometry - CD24 test provide?</h2>
<p>This test determines the presence and amount of CD24 protein on the surface of cells in a blood sample. It provides
    useful information about the potential presence and severity of diseases characterized by abnormal CD24 expression.
</p>
