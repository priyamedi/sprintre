	<h2 class="test-h2 mt-3">What information does the AML Cytogenetics 3 panel provide? </h2>
	<p>The panel provides information about the number and structure of chromosomes and identifies specific genetic mutations, namely AML-ETO, Inv(16), MLL, and BCR/ABL, which are associated with AML. This information is critical in establishing a diagnosis, predicting outcomes, and making treatment decisions. </p>
	<h2 class="test-h2 mt-3">Is fasting required for this test? </h2>
	<p>No, fasting is not required for this test. </p>
	<h2 class="test-h2 mt-3">When should this test be done? </h2>
	<p>This test is generally ordered when someone has symptoms suggestive of leukemia or if a healthcare provider detects abnormal cells in a complete blood count (CBC). It’s also used to monitor the effectiveness of treatment. </p>
	<h2 class="test-h2 mt-3">What are normal values for this test? </h2>
	<p>Normal values would show no chromosomal abnormalities or mutations. The presence of abnormalities such as AML-ETO, Inv(16), MLL, or BCR/ABL is indicative of AML. </p>
	<h2 class="test-h2 mt-3">How does this test impact my treatment options? </h2>
	<p>Knowing the specific chromosomal abnormalities present in your leukemia cells helps your doctor choose targeted therapies that are likely to be more effective in treating your specific subtype of AML. </p>
	<h2 class="test-h2 mt-3">Which doctor should I consult if I have abnormal test results? </h2>
	<p>If the test results are abnormal, it is important to consult a hematologist or oncologist who specializes in blood cancers. </p>
	<h2 class="test-h2 mt-3">What is the significance of the BCR/ABL mutation in AML? </h2>
	<p>BCR/ABL is a fusion gene that is often associated with Chronic Myeloid Leukemia (CML) but can also be seen in some cases of AML. The presence of BCR/ABL in AML can have implications for prognosis and treatment. </p>
	<h2 class="test-h2 mt-3">What is FISH and how does it help in the diagnosis of AML? </h2>
	<p>FISH, or Fluorescence In Situ Hybridization, is a test that maps the genetic material in human cells, including specific genes or portions of genes. In diagnosing AML, it is used to find specific changes in chromosomes and genes that might not be visible under a microscope. </p>
	<h2 class="test-h2 mt-3">What is Inv(16) and why is it significant? </h2>
	<p>Inv(16) is a chromosomal abnormality where part of chromosome 16 is inverted. It is one of the common abnormalities in AML and is associated with a particular subtype of the disease. It has specific implications for prognosis and treatment. </p>
	<h2 class="test-h2 mt-3">What is MLL and why is it significant? </h2>
	<p> MLL stands for Mixed Lineage Leukemia, which is a gene that can be involved in chromosomal translocations in leukemia. The rearrangements involving the MLL gene are associated with various types of leukemia, including AML.</p>
	<h2 class="test-h2 mt-3">What is AML-ETO and why is it significant? </h2>
	<p>AML-ETO is a fusion gene resulting from a translocation between chromosomes 8 and 21. It is one of the most common chromosomal abnormalities in AML and is typically associated with a better prognosis. </p>
	<h2 class="test-h2 mt-3">Are there any risks associated with this test? </h2>
	<p>The blood draw is relatively safe but may cause minor pain. When a bone marrow sample is needed, there may be pain at the site of the biopsy. <br><br>
