<h2 class="test-h2 mt-3">Why is the IHC - CD30 test done?</h2>
<p> The <strong>IHC - CD30</strong> test is primarily used to help diagnose and classify certain types of lymphomas, particularly Hodgkin lymphoma and anaplastic large cell lymphoma.</p>
<h2 class="test-h2 mt-3">How is the IHC - CD30 test performed?</h2>
<p> The IHC - CD30 test is performed on a tissue sample obtained through a biopsy. The sample is then examined under a microscope using special dyes that can detect the CD30 protein.</p>
