<h2 class="test-h2 mt-3">What is the IHC - CEA test used for?</h2>
<p>The IHC - CEA test is primarily used to determine the presence and extent of certain cancers. It's particularly used
    in diagnosing and managing colorectal cancer, but can also be elevated in cancers of the pancreas, stomach, breast,
    and lungs.</p>
<h2 class="test-h2 mt-3">Do I need to fast before taking the IHC - CEA test?</h2>
<p>No, fasting is not necessary for this test as it requires a tissue sample, usually obtained through biopsy or
    surgery.</p>
