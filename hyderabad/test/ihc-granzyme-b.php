<h2 class="test-h2 mt-3">What is the IHC - Granzyme B test?</h2>
<p>The IHC - Granzyme B test is a diagnostic procedure that identifies the presence of Granzyme B in a tissue sample. This protein is typically present in cytotoxic T cells and natural killer (NK) cells that participate in immune responses.</p>
<h2 class="test-h2 mt-3">Why is the IHC - Granzyme B test important?</h2>
<p>This test is critical as it helps identify cells involved in the immune response against infected or abnormal cells. It's especially useful in diagnosing certain types of lymphomas and other conditions where these immune cells are active.</p>
