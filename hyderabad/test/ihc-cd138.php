<div class="post-text">
<p>The <strong>Immunohistochemistry (IHC) - CD138 test</strong> is a laboratory analysis used to detect the presence of the CD138 antigen, also known as syndecan-1, in tissue samples. CD138 is a protein that is often found on the surface of certain cells, including some types of cancer cells, particularly multiple myeloma cells, where it is commonly overexpressed.
 </p>
<p>Immunohistochemistry involves using antibodies that bind to specific antigens in the tissue sample, allowing these antigens to be visualized under a microscope. The IHC - CD138 test uses antibodies designed to bind specifically to the CD138 antigen, if present. This test helps in the diagnosis, prognosis, and therapeutic planning of multiple myeloma and other related diseases.
 </p>
