<h1 class="test-h1">Multiple Sclerosis Profile - Screening </h1>
<div class="post-text">
<p>Multiple sclerosis (MS) is a chronic disease that affects the central nervous system (CNS), which includes the brain and spinal cord. In MS, the immune system mistakenly attacks the protective covering of nerve fibers, called myelin, resulting in communication issues between your brain and the rest of your body.</p>

<p>The Multiple Sclerosis Profile - Screening is a set of laboratory tests that aid in the diagnosis of MS. This profile includes Albumin and IgG measurement in both cerebrospinal fluid (CSF) and serum, testing for Oligoclonal bands, and the CSF Index. These tests help identify immune system activity and inflammation in the CNS, which are characteristic of MS.</p>

<p>The presence of Oligoclonal bands, which are bands of immunoglobulins, in the CSF but not in the blood is a strong indicator of MS. The Albumin CSF/Serum ratio and IgG CSF/Serum ratio, also known as the CSF Index, help assess whether the immune response is occurring within the CNS or if proteins from the blood have simply leaked into the CSF.</p>
