<h2 class="test-h2 mt-3">What is the GCV Resistance by Sequencing test?</h2>
<p>This test uses DNA sequencing techniques to detect mutations in the CMV virus that are associated with resistance to
    the antiviral drug ganciclovir.</p>
<h2 class="test-h2 mt-3">Why is this test important?</h2>
<p>The test is crucial in managing CMV infections, especially in immunocompromised patients. It allows healthcare
    providers to identify resistance to ganciclovir and adjust treatment strategies accordingly.</p>
