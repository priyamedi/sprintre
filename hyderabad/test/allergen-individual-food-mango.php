<h2 class="test-h2 mt-3">What are the symptoms of mango allergy?</h2>
<p>Symptoms of mango allergy can range from mild to severe and can include itching or tingling in the mouth, hives, eczema, swelling of the lips, face, tongue, or throat, nasal congestion, wheezing, stomach pain, nausea, vomiting, and in severe cases, anaphylaxis.</p>
<h2 class="test-h2 mt-3">How is the Allergen, Individual - Food Mango test performed?</h2>
<p>The test is conducted using a blood sample. A healthcare professional will draw blood from your arm. The sample is then sent to a laboratory where it is analyzed for the presence of specific IgE antibodies that react to mango proteins.</p>
