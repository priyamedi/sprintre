<h2 class="test-h2 mt-3">What does PGS testing involve?</h2>
<p>PGS testing involves the removal of a few cells from each embryo on the third day of development, at a stage when the
    embryo consists of about 6-8 cells. These cells are then analyzed for chromosomal abnormalities, while the embryo is
    preserved and, if healthy, can later be transferred into the uterus.</p>
<h2 class="test-h2 mt-3">How does PGS testing help in improving pregnancy outcomes?</h2>
<p>PGS testing improves pregnancy outcomes by ensuring that only chromosomally normal embryos are transferred into the
    uterus. This reduces the risk of miscarriages and increases the chances of a successful pregnancy and a healthy
    baby.</p>
