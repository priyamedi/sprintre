<h2 class="test-h2 mt-3">What is the Hepatitis B Envelope Antibody test?</h2>
<p>The Hepatitis B Envelope Antibody test is used to detect antibodies produced by the immune system in response to the hepatitis B envelope antigen. It helps in evaluating the stage of Hepatitis B infection and the patient’s infectivity.</p>
<h2 class="test-h2 mt-3">Why is the Anti-HBe test done?</h2>
<p>This test is performed to help monitor the course of a Hepatitis B infection. It is often done along with other Hepatitis B tests, such as HBsAg, HBeAg, and liver function tests, to understand the stage of the infection and guide treatment decisions.</p>
