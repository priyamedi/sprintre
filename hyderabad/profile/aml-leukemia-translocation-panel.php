<h2 class="test-h2 mt-3">Why is the AML Leukemia Translocation Panel performed?</h2>
<p>This panel is performed to identify specific chromosomal alterations in the leukemia cells, which can provide
    information about the subtype of AML, prognosis, and potential treatment strategies.</p>
<h2 class="test-h2 mt-3">What do the results of this panel indicate?</h2>
<p>The results of the panel will indicate whether any of the chromosomal alterations tested for are present in the
    leukemia cells. The presence of specific alterations can be associated with a more aggressive or more indolent
    course of the disease, and some may be targets for specific therapies.</p>
