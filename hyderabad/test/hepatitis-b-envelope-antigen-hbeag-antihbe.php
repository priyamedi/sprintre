									high-risk populations.</p>

								<h2 class="test-h2 mt-3">Can Hepatitis B be prevented?</h2>
								<p>Yes, Hepatitis B can be prevented through vaccination and by taking precautions such
									as practicing safe sex, not sharing needles, and avoiding contact with blood and
									bodily fluids.</p>

