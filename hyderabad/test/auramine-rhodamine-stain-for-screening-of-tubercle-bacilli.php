<h2 class="test-h2 mt-3"> What is the purpose of the Auramine Rhodamine Stain Test?</h2>
	<p>
The test is primarily used to detect Mycobacterium tuberculosis in a sputum sample. It helps diagnose
TB, especially in high-risk populations.</p>
