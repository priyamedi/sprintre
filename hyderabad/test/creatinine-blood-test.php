					<h2 class="test-h2 mt-3">What is the Creatinine Blood Test?</h2>
<p>The Creatinine Blood Test measures the level of creatinine in the blood. Creatinine is a waste product formed by
    the natural breakdown of muscles and is filtered out of the blood by the kidneys.</p>
