<h1 class="test-h1">Niemann Pick Disease - Quantitative - Blood (Sphingomyelinase deficiency)</h1>
<div class="post-text">
<p>Niemann-Pick disease is a group of rare, inherited disorders characterized by the accumulation of sphingomyelin, a type of lipid, within cells. These lipid accumulations can cause a variety of symptoms and complications, depending on the specific type of Niemann-Pick disease an individual has. There are currently three recognized types of Niemann-Pick disease - Types A, B, and C - each associated with its unique clinical presentation.</p>
<p>The Quantitative Blood Test for Niemann-Pick disease specifically screens for Types A and B, both of which are associated with a deficiency in the enzyme sphingomyelinase. This enzyme is responsible for breaking down sphingomyelin into other substances that the body can use. If sphingomyelinase is deficient or absent, sphingomyelin accumulates within cells, leading to the various manifestations of Niemann-Pick disease.</p>
