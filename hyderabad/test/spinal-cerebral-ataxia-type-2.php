<div class="post-text">
<p><strong>Spinocerebellar ataxia type 2 (SCA2)</strong> is a rare genetic disorder, characterized by progressive problems with movement. It is part of a group of genetic disorders known as hereditary ataxias, which involve a degeneration of the cerebellum, spinal cord, and other parts of the brain, impairing their function over time. This condition is marked by slow, uncoordinated movements (ataxia), including a lack of control over eye movements, speech difficulties, and difficulty swallowing.</p>
<p>SCA2 is caused by a mutation in the ATXN2 gene, which leads to an abnormal accumulation of a protein known as Ataxin-2, disrupting the regular function of nerve cells, and eventually leading to their death. A genetic test, which typically requires a blood sample, is the most accurate way to diagnose SCA2 and is particularly useful for confirming a diagnosis in people with a known family history of the disease.</p>

