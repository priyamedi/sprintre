	<h2 class="test-h2 mt-3">What does the presence of haemoglobin in urine indicate?</h2>
<p>The presence of haemoglobin in urine, known as hemoglobinuria, can indicate a number of conditions including infections, kidney disease, or conditions that cause the premature destruction of red blood cells.</p>

	<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>The test is performed on a urine sample that is collected in a sterile container. In most cases, a random urine sample is collected, but in some cases, your doctor may ask for a first morning sample or a 24-hour urine sample.</p>

	<h2 class="test-h2 mt-3">What do the test results mean?</h2>
<p>Elevated levels of haemoglobin in the urine can indicate a condition that is causing the premature destruction of red blood cells, kidney disease, or a urinary tract infection.</p>

