<h1 class="test-h1">Erythrocyte Sedimentation Rate (ESR) - Westergren's Method </h1>
<div class="post-text">
<p>Erythrocyte Sedimentation Rate (ESR), also known as sed rate, is a blood test that helps detect
inflammation in the body. The test measures the rate at which red blood cells (erythrocytes) settle at
the bottom of a test tube in one hour. The Westergren's method is one of the most common methods
used for measuring ESR and is considered the standard method. This test can be a valuable tool for
diagnosing and monitoring various conditions such as infections, autoimmune diseases, and cancers. </p>

