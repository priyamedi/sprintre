<h2 class="test-h2 mt-3">Why is the Filaria Antibody test done?</h2>
<p>The Filaria Antibody test is done to detect the presence of antibodies against filarial parasites in the
blood. It helps in diagnosing filariasis, a parasitic infection that affects the lymphatic system and can
cause severe swelling in limbs or skin and eye problems.</p>

<h2 class="test-h2 mt-3">What does a positive Filaria Antibody test indicate?</h2>
<p>A positive result indicates the presence of antibodies against filarial parasites, suggesting a current or
past infection with filariasis.</p>
