<h2 class="test-h2 mt-3"> Why might my doctor suggest an MPS typing test?</h2>
<p>If you or your child are experiencing symptoms suggestive of MPS, such as abnormal growth, bone deformities, vision
    and hearing problems, heart complications, and cognitive issues, your healthcare provider might suggest an MPS
    typing test. This test can help identify the specific type of MPS disorder and guide treatment decisions.</p>
<h2 class="test-h2 mt-3">How is the MPS typing test performed?</h2>
<p>The test requires a 24-hour urine sample. The patient will be given a container to collect all urine produced over a
    24-hour period. This sample is then sent to a laboratory, where it will be analyzed for the presence and levels of
    different GAGs, which can indicate a specific type of MPS.</p>
