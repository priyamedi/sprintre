	<h2 class="test-h2 mt-3">Can I eat and drink before the test? </h2>
<p>Yes, this test doesn't require any dietary restrictions. However, if the biopsy procedure requires anesthesia, you may need to fast before the procedure. Your doctor will provide specific instructions based on your circumstances.</p>

	<h2 class="test-h2 mt-3">Are there any risks associated with this test? </h2>
<p>The risks associated with this test are primarily related to the biopsy procedure used to collect the tissue sample. These may include infection, bleeding, or complications from anesthesia. The test itself, however, does not pose any risks.</p>

	<h2 class="test-h2 mt-3">How long will it take to get the results? </h2>
<p>The turnaround time for the Lung Cancer Panel 1 Complete Molecular is usually 1-2 weeks. However, this can vary depending on the laboratory processing the sample.</p>

	<h2 class="test-h2 mt-3">What if my test shows a mutation? </h2>
<p>If your test results show a mutation, your doctor will discuss the results with you and explain what they mean for your treatment options. Certain mutations may make you eligible for targeted therapies, which can be more effective and cause fewer side effects than traditional chemotherapy.</p>

	<h2 class="test-h2 mt-3">Can this test be used to monitor my response to treatment? </h2>
<p>While this test can provide valuable information about the genetic makeup of your cancer, it is not typically used to monitor response to treatment. Other tests, such as imaging studies or blood tests, may be used for this purpose.</p>

	<h2 class="test-h2 mt-3">What does it mean if no mutation is found?</h2> 
<p>If no mutation is found, it means that none of the genetic changes tested in this panel are present in your tumor. However, this doesn't mean that no mutations exist. There are many other genetic changes not included in this panel that could still be driving your cancer.</p>

	<h2 class="test-h2 mt-3">Is there anyone who shouldn't have this test? </h2>
<p>This test is most useful for patients with non-small cell lung cancer, particularly adenocarcinoma. It may not provide useful information for other types of cancer or other lung diseases.</p>

	<h2 class="test-h2 mt-3">Are the results of this test always accurate?</h2> 
