<p>This test can detect the DNA of Mycobacterium leprae, indicating an active infection. However, it cannot
    differentiate between active and past infection.</p>
<h2 class="test-h2 mt-3">What factors might affect the test results?</h2>
<p>The test result might be affected by improper sample collection, handling, or contamination.</p>
