<h2 class="test-h2 mt-3">Why would my doctor order a Leptin test?</h2>
<p>Your doctor might order a leptin test if you have unexplained weight gain or difficulty losing weight. The test can
    also be used to help diagnose disorders of the hypothalamus, or to monitor the effectiveness of treatments for
    obesity.</p>
<h2 class="test-h2 mt-3">What does it mean if my Leptin levels are high?</h2>
<p>High levels of leptin can be indicative of leptin resistance, where your body does not respond properly to the
    hormone. This can lead to an increased appetite and is often associated with obesity.</p>
