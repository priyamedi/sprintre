<h2 class="test-h2 mt-3">Why is the Lamotrigine Level test done?</h2>
	<p>The Lamotrigine Level test is done to monitor the amount of lamotrigine in the blood
to ensure that it is within the therapeutic range that helps control symptoms without
causing side effects.</p>

<h2 class="test-h2 mt-3">What do the test results mean?</h2>
<p>The results are given in micrograms per milliliter (mcg/mL). The therapeutic range
for lamotrigine when used for epilepsy is typically considered to be about 3-14
mcg/mL. If levels are too high, it might cause side effects; if levels are too low, it may
not effectively control your symptoms.</p>
