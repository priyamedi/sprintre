<h2 class="test-h2 mt-3">Why is the Fungus Culture [URINE] test performed?</h2>
<p>The test is performed when a healthcare provider suspects a fungal urinary tract infection (UTI). Symptoms may include frequent urination, discomfort or burning during urination, lower abdominal pain, and cloudy or strong-smelling urine.</p>

<h2 class="test-h2 mt-3">How is the urine sample collected?</h2>
<p>A urine sample is collected in a clean, sterile container. It's important to collect a "clean catch" sample, which means the sample should be collected mid-stream, to avoid contamination with organisms at the urethral opening.</p>

