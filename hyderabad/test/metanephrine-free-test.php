<h1 class="test-h1">Metanephrine-Free Test </h1>
<div class="post-text">
<p>Metanephrine-Free test is a diagnostic tool that measures the amount of metanephrine, a metabolite of
adrenaline, in the urine. This test is generally used to diagnose or monitor conditions such as
pheochromocytoma or paraganglioma, which are rare tumors of the adrenal glands that can
overproduce catecholamines like adrenaline.
</p>
<p>Adrenaline is a hormone that your body releases when you're under stress, and metanephrine is what
remains when your body breaks down this hormone. The levels of metanephrines in your body can
fluctuate, which is why this test measures the amounts excreted in your urine over 24 hours. High levels
can be indicative of a problem with your adrenal glands.  </p>
