<h2 class="test-h2 mt-3"> What is the purpose of HOCM screening?</h2>
	<p>
The screening aims to identify individuals who have HOCM, particularly those without symptoms, so
that treatment can be initiated to manage the condition and prevent complications.</p>
<h2 class="test-h2 mt-3">How is HOCM screening done?</h2>
<p>
<strong>HOCM</strong> screening involves a combination of blood tests and imaging tests. Genetic testing can identify
specific mutations associated with HOCM, and an echocardiogram can detect abnormal thickness in the
heart muscle, a characteristic of HOCM.</p>
