<p> DMD primarily affects males and is inherited in an X-linked recessive pattern. The detection of DMD mutations through chorionic villus sampling enables families to understand the genetic status of the fetus and engage in counseling to comprehend the implications and options associated with a DMD diagnosis.</p>
