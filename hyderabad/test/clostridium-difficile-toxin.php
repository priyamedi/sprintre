	<h2 class="test-h2 mt-3">What is the Clostridium Difficile Toxin test?</h2> 
<p>The Clostridium Difficile Toxin test is a diagnostic test that detects the presence of toxins produced by the C. difficile bacterium in a stool sample. The presence of these toxins indicates an active infection.</p>
<h2 class="test-h2 mt-3">Why is the Clostridium Difficile Toxin test done?</h2> 
