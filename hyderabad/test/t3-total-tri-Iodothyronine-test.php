<h2 class="test-h2 mt-3">Why is the Total T3 test done?</h2>
<p>The Total T3 test is used to help diagnose thyroid diseases and to monitor the effectiveness of treatments for
    conditions such as hyperthyroidism.</p>
<h2 class="test-h2 mt-3">Is fasting required for the Total T3 test?</h2>
<p>No, fasting is not required for this test.</p>
