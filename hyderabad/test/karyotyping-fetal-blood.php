<h2 class="test-h2 mt-3">What is the Karyotyping - Fetal Blood test?</h2>
<p>The Karyotyping - Fetal Blood test is a diagnostic procedure that analyses the number and structure of chromosomes in
    fetal blood cells. It is used to detect chromosomal abnormalities that can lead to genetic disorders.</p>
<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is performed to detect any chromosomal abnormalities in a fetus. It is often recommended when other
    screening tests suggest a potential problem or if there's a known family history of genetic disorders.</p>
