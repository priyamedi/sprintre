	<p>The CLL Extended 1 test uses Flow Cytometry to study the physical and chemical characteristics of cells, Karyotyping to analyze the chromosomes in the CLL cells, and FISH to detect the 17p deletion. </p>
	<h2 class="test-h2 mt-3">What is the significance of the 17p deletion in CLL? </h2>
	<p>The 17p deletion is associated with a more aggressive form of CLL and often indicates resistance to standard chemotherapy. Knowing if this deletion is present helps the doctor in deciding the most effective treatment. </p>
