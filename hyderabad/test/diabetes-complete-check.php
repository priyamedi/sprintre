<h2 class="test-h2 mt-3">What is HbA1c and why is it significant?</h2>
<p>HbA1c is a test that measures the average blood sugar level over the past 2-3 months. It is significant as it gives a
    broader picture of how well diabetes is being managed over time.</p>
