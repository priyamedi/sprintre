<h2 class="test-h2 mt-3">Why is the urine test for cannabinoids done?</h2>
<p>This test is often done to detect the use of marijuana. It may be required for employment drug screenings, by legal
    authorities, or for medical purposes to monitor the use of cannabinoids.</p>
<h2 class="test-h2 mt-3">What does a positive test result mean?</h2>
<p>A positive result indicates the presence of cannabinoids in the urine. This can be due to the use of marijuana or
    medications containing cannabinoids.</p>
