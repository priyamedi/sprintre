<div class="post-text">
<p>The Tyrosinemia (FAH gene) test for the index case is a diagnostic test that identifies mutations in the FAH gene, which is linked to Tyrosinemia Type I. This disease is a genetic disorder that results in an inability to effectively break down the amino acid Tyrosine. The accumulation of tyrosine and its byproducts can lead to severe liver and kidney disease, growth retardation, and neurological crises. Early diagnosis and treatment can significantly improve the prognosis and quality of life for individuals with this disorder. The test is performed on a blood sample and is typically ordered when Tyrosinemia is suspected based on clinical symptoms or family history.</p>

<p>This test is considered an "index case" test, meaning it is typically ordered for individuals who have symptoms of the disease. If a mutation is found, genetic testing can be offered to other family members to determine their risk of having or passing on the disease.</p>
