<h2 class="test-h2 mt-3">What is FISH for 1p/19q co-deletion?</h2>
<p>FISH for 1p/19q co-deletion is a test that uses fluorescent probes to detect the simultaneous loss of
genetic material on the short arm of chromosome 1 (1p) and the long arm of chromosome 19 (19q). This
co-deletion is often found in oligodendrogliomas.</p>

<h2 class="test-h2 mt-3">Why is FISH for 1p/19q co-deletion performed?</h2>
<p>This test is performed to help diagnose oligodendrogliomas, to determine prognosis, and to guide
treatment decisions. The presence of the 1p/19q co-deletion is associated with a better response to
treatment and a more favorable outcome.</p>
