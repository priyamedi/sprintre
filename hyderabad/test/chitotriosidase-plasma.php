<h2 class="test-h2 mt-3">What is a Chitotriosidase test?</h2>
<p>A Chitotriosidase test measures the level of Chitotriosidase enzyme in plasma. This enzyme is often elevated in
	conditions where there is increased activity of macrophages, including lysosomal storage disorders.</p>
<h2 class="test-h2 mt-3">Why is the Chitotriosidase test important?</h2>
<p>Chitotriosidase test is often used as a biomarker to monitor the severity and progression of Gaucher's disease. It
