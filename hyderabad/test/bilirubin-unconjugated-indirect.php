<h2 class="test-h2 mt-3">What is the difference between unconjugated and conjugated bilirubin?</h2>
<p>Unconjugated bilirubin is produced from the breakdown of hemoglobin and is not water-soluble. It travels to the liver where it is converted into a water-soluble form, known as conjugated bilirubin, which can be excreted from the body.</p>

<h2 class="test-h2 mt-3">What causes high levels of unconjugated bilirubin?</h2>
<p>High levels can be due to increased breakdown of red blood cells, impaired uptake of bilirubin by the liver, or reduced conjugation of bilirubin.</p>

