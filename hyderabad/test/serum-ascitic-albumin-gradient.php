<h2 class="test-h2 mt-3">Why is a Serum Ascitic Albumin Gradient (SAAG) test done?</h2>
<p>This test is done to help determine the cause of ascites, an abnormal buildup of fluid in the abdomen.</p>
<h2 class="test-h2 mt-3">Do I need to fast before this test?</h2>
<p>No, fasting is not required for this test.</p>

