<h1 class="test-h1">IHC - diagnostic panel; sarcoma panel  </h1>
<div class="post-text">
<p>Immunohistochemistry (IHC) is a crucial tool used by pathologists to diagnose various types of cancers, including sarcomas. Sarcomas are a diverse group of cancers that start in the bones and soft tissues. An IHC Sarcoma Panel utilizes a set of markers, including Vimentin, SMA, Desmin, Myogenin, S100, CD34, CD31, CKIT, Caldesmon, NSE, CD 99, FLI1, AE1/3, CK, EMA, and TLE-1, to help pathologists identify and classify these cancers more accurately. This test aids in identifying the tissue of origin, differentiating between subtypes, and can be vital in formulating a precise treatment plan.</p>

<p>The Sarcoma Panel is typically ordered when a soft tissue tumor is suspected, either from the patient's symptoms, imaging tests, or an initial biopsy report. It works by detecting the presence and distribution of certain proteins (antigens) in the tissue sample that are specific to certain types of cells. The panel includes a range of markers because sarcomas can arise from different types of cells, and each marker corresponds to a specific cell type.</p>
