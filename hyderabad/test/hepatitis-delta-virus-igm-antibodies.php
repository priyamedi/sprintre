<h2 class="test-h2 mt-3">What is the Hepatitis Delta Virus IgM Antibodies test?</h2>
<p>The Hepatitis Delta Virus IgM Antibodies test is a blood test that detects the presence of Hepatitis D IgM
    antibodies. A positive result indicates a recent or ongoing Hepatitis D infection.</p>
<h2 class="test-h2 mt-3">Who should get this test?</h2>
<p>Individuals diagnosed with Hepatitis B, especially those experiencing symptoms of liver disease, or those at a high
    risk of Hepatitis D exposure such as healthcare workers, people with multiple sexual partners, and intravenous drug
    users, should get this test.</p>
