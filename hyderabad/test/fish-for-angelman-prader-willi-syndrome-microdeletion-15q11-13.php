<h2 class="test-h2 mt-3">Why is the FISH for Angelman Prader Willi Syndrome test done? </h2>
	<p>Thetest is done to detect microdeletions in chromosome 15, which are responsible for Angelman
Syndrome and Prader-Willi Syndrome. Early detection can aid in better management and intervention
for these genetic disorders.</p>
<h2 class="test-h2 mt-3"> Who should consider this test? </h2>
<p>Individuals with developmental delays, intellectual disabilities, or other symptoms suggestive of
Angelman Syndrome or Prader-Willi Syndrome, as well as their family members, should consider this
test.</p>
