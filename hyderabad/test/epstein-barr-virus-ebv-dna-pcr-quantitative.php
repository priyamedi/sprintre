<h2 class="test-h2 mt-3">What is the EBV DNA PCR - Quantitative test?</h2>
<p>The test quantitatively measures the amount of Epstein-Barr Virus DNA in the blood, helping doctors
diagnose an active infection and monitor its progression or response to treatment.</p>

<h2 class="test-h2 mt-3">How does this test differ from the qualitative version?</h2>
<p>While both tests detect EBV DNA, the quantitative version provides the amount of virus in the blood,
which can be essential for monitoring and treatment decisions.</p>
