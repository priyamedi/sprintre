								<p>Individuals at risk for hepatitis B, such as healthcare workers, people with multiple
									sexual partners, IV drug users, people with liver disease, and those traveling to
									areas where hepatitis B is common, should consider getting screened.</p>

								<h2 class="test-h2 mt-3">How is the HBsAg screening test performed?</h2>
								<p>A healthcare professional will take a blood sample, usually from a vein in the arm.
