<h2 class="test-h2 mt-3"> Why is the Bacterial Meningitis Screen - Antigen Detection test ordered?</h2>
The test is usually ordered when a patient presents symptoms suggestive of meningitis, such as severe
headache, fever, neck stiffness, nausea, vomiting, sensitivity to light, or altered mental status.</p>
<h2 class="test-h2 mt-3">What does a positive result mean?</h2>
	<p>
A positive result indicates the presence of bacterial antigens in the CSF, suggesting bacterial meningitis.
However, the test alone cannot determine the specific type of bacteria causing the infection.</p>
