<h2 class="test-h2 mt-3">What is a Ganglioside - IgG Antibody Panel by Immunoblot?</h2>
<p>This is a blood test that identifies the presence of IgG antibodies against gangliosides in the blood, which can be
    indicative of certain neurological disorders.</p>
<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is ordered when a patient shows signs of a neurological disorder and the doctor suspects an autoimmune
    cause. The presence of these antibodies can assist in the diagnosis of conditions such as Guillain-Barré syndrome or
    Miller Fisher syndrome.</p>
