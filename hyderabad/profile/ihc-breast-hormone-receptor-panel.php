	<h2 class="test-h2 mt-3">Is the test result affected by other medical conditions? </h2>
<p>No, the result of the Immunohistochemistry - Breast Hormone Receptor Panel is specific to breast cancer and is not typically affected by other medical conditions.</p>

	<h2 class="test-h2 mt-3">What are ER, PR, and HER2Neu? </h2>
<p>ER, PR, and HER2Neu are proteins found on the surface of some breast cancer cells. They can influence the growth of the cancer and its response to treatment. ER stands for estrogen receptor, PR stands for progesterone receptor, and HER2Neu is a human epidermal growth factor receptor.</p>

