<h2 class="test-h2 mt-3">Why is the GAD-65 IgG Antibodies Test performed?</h2>
<p>This test is usually performed to help diagnose Type 1 Diabetes or to differentiate it from Type 2 Diabetes. It is
    also used to assess autoimmune neurological disorders, such as Stiff Person Syndrome, where antibodies against
    GAD-65 are present.</p>
<h2 class="test-h2 mt-3">How is the GAD-65 IgG Antibodies Test performed?</h2>
<p>A healthcare professional will draw a blood sample from a vein in your arm. The blood sample is then sent to a
    laboratory where it will be tested for the presence and levels of antibodies against GAD-65.</p>
