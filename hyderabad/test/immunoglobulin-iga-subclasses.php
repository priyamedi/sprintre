<h2 class="test-h2 mt-3"> Why is the Immunoglobulin - IgA Subclasses test done?</h2>
<p>This test is typically done when an individual exhibits recurrent infection, particularly those involving mucosal surfaces such as the respiratory or gastrointestinal tract. It can help diagnose conditions such as Selective IgA Deficiency and Common Variable Immunodeficiency. It's also ordered when an immune deficiency is suspected based on the clinical picture.</p>
<h2 class="test-h2 mt-3"> Is fasting required for this test?</h2>
<p>No, fasting is not typically required for the Immunoglobulin - IgA Subclasses test.</p>
