<h2 class="test-h2 mt-3">Why is the Typhoid DNA (By Real Time PCR) - Blood test done?</h2>
<p>This test is done to detect the presence of Salmonella typhi bacteria, the cause of typhoid fever, in the blood. It
    is often ordered when a patient presents with symptoms of typhoid fever, such as high fever, weakness, stomach pain,
    headache, or loss of appetite.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test involves taking a blood sample from a vein in your arm. The blood sample is then sent to a laboratory, where
    real-time PCR is performed to amplify and detect any Salmonella typhi DNA present in the sample.</p>
