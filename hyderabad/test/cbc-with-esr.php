<p>A Complete Blood Count (CBC) with Erythrocyte Sedimentation Rate (ESR) is a combination of tests that
provides vital information about the number and types of cells in the blood. The CBC measures the
count of red blood cells, white blood cells, and platelets. It also checks the amount of hemoglobin in the
blood, and hematocrit, which is the proportion of blood volume that is occupied by red blood cells. </p>
