<h2 class="test-h2 mt-3">What is a Gastrin test?</h2>
<p>The Gastrin test is a blood test that measures the level of gastrin in your blood. Gastrin is a hormone that
    stimulates the stomach to produce gastric acid.</p>
<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>This test is usually ordered if a patient has recurrent peptic ulcers or an unusually high amount of gastric acid. It
    can help diagnose conditions like Zollinger-Ellison syndrome.</p>
