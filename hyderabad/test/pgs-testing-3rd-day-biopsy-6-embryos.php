<div class="post-text">
<p>PGS or Preimplantation Genetic Screening is a diagnostic procedure applied to embryos prior to their transfer into the uterus during an in-vitro fertilization (IVF) treatment. PGS tests embryos for chromosomal abnormalities or aneuploidies and is carried out on the third day after fertilization, at the stage when the embryos have about 6-8 cells.</p>
<p>This test is especially recommended for couples who have experienced recurrent miscarriages, failed IVF cycles, advanced maternal age, severe male factor infertility, or those who want to avoid passing on a chromosomal disorder. PGS testing can increase the chances of a successful pregnancy and a healthy baby by ensuring only embryos with a normal chromosomal count are transferred into the uterus.</p>

