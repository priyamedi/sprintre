<h2 class="test-h2 mt-3">What is ANA by IFA Reflex to ANA Profile?</h2>
<p>"ANA" stands for Antinuclear Antibodies. These are proteins produced by the immune system that target the body's own cells, specifically the nuclei. "IFA" stands for Indirect Immunofluorescence Assay, a method used to detect these antibodies. If the ANA by IFA test is positive, the lab will automatically perform further testing to identify specific types of ANA that can help identify a particular autoimmune disease – this is referred to as the ANA Profile.</p>

<h2 class="test-h2 mt-3">Why is the ANA by IFA Reflex to ANA Profile test performed?</h2>
<p>This test is primarily used to help diagnose autoimmune diseases, including lupus, rheumatoid arthritis, Sjogren's syndrome, scleroderma, polymyositis, and dermatomyositis. It may also help diagnose other conditions that cause similar symptoms, such as fibromyalgia and chronic fatigue syndrome.</p>

