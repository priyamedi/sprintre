<h2 class="test-h2 mt-3"> What is an Eye Swab Culture and Sensitivity test?</h2>
<p> It is a diagnostic test in which a sample is taken from the eye using a sterile swab. The sample is
cultured in the laboratory to identify any bacteria present. Sensitivity testing is done to find out which
antibiotics are effective against the bacteria.</p>

<h2 class="test-h2 mt-3"> Why is this test important?</h2>
<p> Eye infections can be severe and may affect vision. Identifying the causative bacteria and knowing
which antibiotics are effective against them is vital in managing the infection effectively and preventing
complications.</p>
