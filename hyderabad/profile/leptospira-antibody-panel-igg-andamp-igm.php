<h2 class="test-h2 mt-3">What are the symptoms of Leptospirosis?</h2>
<p>Leptospirosis symptoms may include high fever, chills, headache, muscle aches, nausea, vomiting, jaundice (yellowing
    of the skin and eyes), and sometimes a rash. Severe cases can lead to kidney or liver failure and meningitis.</p>
<h2 class="test-h2 mt-3">How is the Leptospira Antibody Panel test performed?</h2>
<p>A blood sample is taken from a vein in the arm. This sample is sent to a laboratory where it will be tested for the
    presence of IgG and IgM antibodies against Leptospira bacteria.</p>
