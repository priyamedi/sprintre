	<h2 class="test-h2 mt-3">What is ANA and why is it part of the BOH Panel?</h2>
<p>ANA stands for Antinuclear Antibodies. It's a test used to detect autoimmune disorders, such as lupus, which can impact pregnancy.</p>

	<h2 class="test-h2 mt-3">What is Lupus Anticoagulant, and how does it affect pregnancy?</h2>
<p>Lupus Anticoagulant is an antibody that increases the risk of blood clots. During pregnancy, this can lead to miscarriage or other complications such as preeclampsia.</p>

	<h2 class="test-h2 mt-3">How does TSH affect pregnancy?</h2>
<p>TSH (Thyroid Stimulating Hormone) levels are crucial during pregnancy. An imbalance in thyroid hormones can affect the development of the fetus and may increase the risk of miscarriage.</p>

	<h2 class="test-h2 mt-3">What is TORCH-10 testing?</h2>
<p>TORCH-10 is a series of tests that check for ten different infections that can affect pregnancy outcomes. These include infections like toxoplasmosis, rubella, cytomegalovirus, and herpes simplex virus.</p>

	<h2 class="test-h2 mt-3">Are there any risks associated with the BOH Panel?</h2>
<p>The risks associated with the BOH Panel are minimal and similar to those of a standard blood draw, such as light bruising or infection at the needle site.</p>

	<h2 class="test-h2 mt-3">What should I do if any of my results are abnormal?</h2>
<p>If any of the results are abnormal, it is essential to consult your doctor. They will interpret the results and guide you regarding the next steps or treatment options.</p>

	<h2 class="test-h2 mt-3">Can lifestyle modifications improve the chances of a successful pregnancy?</h2>
<p>Yes, lifestyle modifications such as maintaining a healthy weight, eating a balanced diet, managing stress, and avoiding tobacco and alcohol can improve the chances of a successful pregnancy.</p>

	<h2 class="test-h2 mt-3">What are modifiable factors affecting pregnancy outcomes?</h2>
<p>Modifiable factors include lifestyle choices such as diet, exercise, tobacco and alcohol use, and medication management.</p>

	<h2 class="test-h2 mt-3">What are non-modifiable factors affecting pregnancy outcomes?</h2>
<p>Non-modifiable factors include genetic predispositions, chromosomal abnormalities, and certain autoimmune diseases.</p>

	<h2 class="test-h2 mt-3">Is fasting required for the BOH Panel?</h2>
<p>Fasting is not required for the BOH Panel.</p>

	<h2 class="test-h2 mt-3">How is the BOH Panel performed?</h2>
<p>The BOH Panel is performed using a blood sample taken from a vein in the arm.</p>

	<h2 class="test-h2 mt-3">Which doctor should I consult if the results are abnormal?</h2>
<p>You should consult an obstetrician or a fertility specialist if the results are abnormal.</p>

	<h2 class="test-h2 mt-3">Can the BOH Panel prevent future pregnancy losses?</h2>
<p>While the BOH Panel cannot prevent pregnancy losses, it can provide valuable information that helps in making informed decisions and managing future pregnancies more effectively.</p>

