<div class="post-text">
<p><b>Spinal Muscular Atrophy (SMA)</b> is a genetic disorder characterized by muscle weakness and atrophy due to the degeneration of motor neurons in the spinal cord. SMN1 testing is a genetic test that looks for mutations or deletions in the SMN1 gene, which is responsible for the production of a protein essential for motor neurons. Identifying mutations in this gene can confirm a diagnosis of SMA. </p>
<p>Early diagnosis of SMA is crucial, as the condition can severely impact quality of life and may be life-threatening in severe cases. SMA is a spectrum disorder, meaning that the severity and progression can vary widely among individuals. SMN1 testing is an important tool for diagnosis and guiding treatment decisions. </p>
