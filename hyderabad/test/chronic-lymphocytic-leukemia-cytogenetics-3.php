	<h2 class="test-h2 mt-3">What information does the CLL Cytogenetics 3 panel provide? </h2>
	<p>This panel provides information on the presence of specific chromosomal abnormalities including del17p, del11q (ATM deletion), Trisomy 12, and del13q. These abnormalities have been associated with different responses to therapy and varied prognoses. </p>
	<h2 class="test-h2 mt-3">What are the normal values for this test? </h2>
	<p>Normal values would show no chromosomal abnormalities. The presence of del17p, del11q, Trisomy 12, or del13q is indicative of CLL. </p>
	<h2 class="test-h2 mt-3">How is this test performed? </h2>
	<p>The test is performed on a blood sample which is examined in the laboratory for chromosomal abnormalities using karyotyping and FISH techniques. </p>
	<h2 class="test-h2 mt-3">How do the results of this test affect my treatment options? </h2>
	<p>The results provide information that helps your doctor determine the aggressiveness of your CLL and select the most appropriate treatment options. Certain chromosomal abnormalities are associated with resistance to certain therapies and knowing this information can guide treatment decisions. </p>
	<h2 class="test-h2 mt-3">Which doctor should I consult if I have abnormal test results? </h2>
	<p>If the test results are abnormal, consulting a hematologist or oncologist who specializes in blood cancers is advisable. </p>
	<h2 class="test-h2 mt-3">What is del17p and why is it significant? </h2>
	<p>Del17p is a deletion of the short arm of chromosome 17. It is associated with poor prognosis in CLL and resistance to standard chemotherapy regimens. </p>
	<h2 class="test-h2 mt-3">What is del11q (ATM deletion) and why is it significant? </h2>
	<p>Del11q is a deletion of a part of chromosome 11, which includes the ATM gene. This is associated with more aggressive CLL and indicates a high risk of disease progression. </p>
	<h2 class="test-h2 mt-3">What is Trisomy 12 and why is it significant? </h2>
	<p>Trisomy 12 means there is an extra copy of chromosome 12. It is one of the common chromosomal abnormalities in CLL and is associated with variable prognosis. </p>
	<h2 class="test-h2 mt-3">What is del13q and why is it significant? </h2>
	<p>Del13q is a deletion of the long arm of chromosome 13. It is the most common chromosomal abnormality in CLL and is generally associated with a more favorable prognosis. </p>
	<h2 class="test-h2 mt-3">What is karyotyping? </h2>
	<p>Karyotyping is a laboratory technique that allows for the visualization of an individual’s chromosomes. It is used to detect structural abnormalities in chromosomes. </p>
	<h2 class="test-h2 mt-3">Are there any factors that can affect the levels of these chromosomal abnormalities? </h2>
	<p>No, these chromosomal abnormalities are genetic and are not affected by lifestyle or environmental factors. </p>
	<h2 class="test-h2 mt-3">How often should this test be done? </h2>
	<p> This test is generally done at diagnosis to help with risk stratification. It might be repeated if there is a change in disease behavior or before initiating therapy.</p>
	<h2 class="test-h2 mt-3">Is there any preparation required for the test? </h2>
