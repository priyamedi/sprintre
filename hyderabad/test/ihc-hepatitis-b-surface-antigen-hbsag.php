	<h2 class="test-h2 mt-3">What type of sample is used for this test? </h2>
<p>The test is performed on a tissue sample, usually taken from the liver.</p>

	<h2 class="test-h2 mt-3">How is the tissue sample obtained? </h2>
<p>A tissue sample is usually obtained through a biopsy procedure, where a small piece of liver tissue is removed using a needle or during a surgical procedure.</p>

	<h2 class="test-h2 mt-3">Do I need to fast before providing the tissue sample?</h2>
<p>No, there is no need for fasting before the procedure for obtaining the tissue sample.</p>

	<h2 class="test-h2 mt-3">How long does it take to get the results?</h2>
<p>The results usually take about 1 to 3 days.</p>

	<h2 class="test-h2 mt-3">What does a positive result mean? </h2>
<p>A positive result indicates the presence of Hepatitis B Surface Antigen in the tissue sample, which suggests an active hepatitis B infection.</p>

	<h2 class="test-h2 mt-3">Are there any risks associated with the biopsy procedure?</h2>
<p>There are minimal risks associated with a biopsy procedure such as bleeding, infection, or pain at the biopsy site. Your doctor will discuss these risks with you.</p>

	<h2 class="test-h2 mt-3">How is hepatitis B treated?</h2>
<p>Hepatitis B treatment depends on the severity of the infection. Acute hepatitis B may not require treatment, while chronic hepatitis B might require antiviral medications.</p>

	<h2 class="test-h2 mt-3">Can hepatitis B be prevented? </h2>
<p>Yes, hepatitis B can be prevented through vaccination and by taking precautions such as using condoms during sex, not sharing needles, and avoiding contact with infected blood.</p>

	<h2 class="test-h2 mt-3">What are the normal values for HBsAg in tissue? </h2>
<p>Normally, HBsAg should not be present in liver tissue. The presence of HBsAg indicates an infection.</p>

	<h2 class="test-h2 mt-3">What factors can affect the results of this test? </h2>
<p>The results of the test can be affected by the quality of the tissue sample and the presence of antibodies to HBsAg.</p>

	<h2 class="test-h2 mt-3">What is the difference between chronic and acute hepatitis B infection? </h2>
<p>Acute hepatitis B is a short-term infection that may resolve on its own, while chronic hepatitis B is a long-term infection that can lead to liver damage or cancer.</p>

	<h2 class="test-h2 mt-3">Can a person be infected with hepatitis B more than once?</h2>
<p>Once a person has been infected and cleared the virus, they usually develop immunity to it. However, if the infection becomes chronic, it can reactivate.</p>

	<h2 class="test-h2 mt-3">Which doctor should I consult if I have abnormal levels of HBsAg in tissue? </h2>
<p>You should consult a hepatologist or an infectious disease specialist if you have abnormal levels of HBsAg in tissue.</p>

