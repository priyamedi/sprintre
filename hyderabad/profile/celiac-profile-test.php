<h1 class="test-h1">Celiac Profile Test (TTG IgA ABS, Gliadin IgG and IgA ABS; Endomysial Ab) </h1>
<div class="post-text">
<p>The Celiac Profile Test is a group of blood tests used to diagnose celiac disease, an autoimmune disorder triggered by gluten ingestion. Gluten is a protein found in wheat, barley, and rye. In people with celiac disease, gluten ingestion leads to inflammation and damage in the small intestine.</p>

<p>This profile includes testing for Tissue Transglutaminase (TTG) IgA antibodies, Gliadin IgG and IgA antibodies, and Endomysial antibodies (EMA). These markers are essential in identifying the immune response that leads to intestinal damage in celiac disease. This guide will answer frequently asked questions about the Celiac Profile Test.</p>
