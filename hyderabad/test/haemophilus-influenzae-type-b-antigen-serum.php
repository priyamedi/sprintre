<p>Haemophilus influenzae type b (Hib) is a bacterium responsible for severe pneumonia, meningitis and other invasive diseases primarily in children. Less commonly, Hib causes disease in adults with underlying chronic illnesses. The bacteria can be identified through different laboratory tests, and one such test is the detection of Hib antigen in a patient's blood serum. </p>
