<h2 class="test-h2 mt-3"> What is pleural effusion?</h2>
	<p>
Pleural effusion is a condition characterized by the build-up of excess fluid in the pleural space, the area
between the lungs and the chest wall.</p>
<h2 class="test-h2 mt-3"> What are the symptoms of pleural effusion?</h2>
<p>
Symptoms of pleural effusion can include shortness of breath, chest pain, dry cough, and fever.
However, some people with pleural effusion may not experience any symptoms.</p>
