<h2 class="test-h2 mt-3">Why is it important to test for PCP?</h2>
<p>Testing for PCP is crucial because the drug has dangerous and unpredictable effects on both mental and physical
    health. It can help in identifying abuse or overdose and is vital for providing appropriate intervention and
    treatment.</p>
<h2 class="test-h2 mt-3">How long can PCP be detected in urine?</h2>
<p>PCP can typically be detected in urine for about 7 to 14 days after use, but this can vary depending on factors such
    as dosage, frequency of use, metabolism, and the individual’s health.</p>
