<h2 class="test-h2 mt-3"> What is the Rubella Virus IgM Antibody Test?</h2>
	<p>
This test measures the presence of IgM antibodies against the Rubella virus in your blood. It helps to
determine whether you have been recently or are currently infected with rubella.</p>
<h2 class="test-h2 mt-3"> Do I need to fast before this test?</h2>
<p>
No, fasting is not required for this test.</p>
