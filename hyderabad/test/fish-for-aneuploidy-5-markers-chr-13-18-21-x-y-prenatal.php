<h2 class="test-h2 mt-3"> What is the purpose of the FISH for Aneuploidy test with 5 markers?</h2>
	<p>This test is used to detect chromosomal abnormalities in the fetus by examining the number of
chromosomes 13, 18, 21, X, and Y. It aids in early diagnosis of conditions like Down syndrome, Patau
syndrome, Edwards syndrome, and sex-linked disorders.</p>

<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>The test requires a sample of amniotic fluid or chorionic villus, which is obtained through amniocentesis
or CVS. Fluorescent probes are then used in the lab to bind to the chromosomes in the sample, making
them visible under a microscope.</p>
