<h2 class="test-h2 mt-3">Why is the Tacrolimus test done?</h2>
<p>The Tacrolimus test is done to monitor the amount of the drug in the blood, to ensure it's at a therapeutic level
    that's sufficient to prevent organ rejection but not so high as to cause unwanted side effects or toxicity.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test involves a simple blood draw. A healthcare provider will collect a small amount of blood from a vein in your
    arm, which will be sent to a laboratory for analysis.</p>
