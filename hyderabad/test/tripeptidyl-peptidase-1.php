<h2 class="test-h2 mt-3">Why is the Tripeptidyl Peptidase 1 (TPP1) test done?</h2>
<p>The TPP1 test is done to diagnose Late Infantile Neuronal Ceroid Lipofuscinosis (LINCL), a type of Batten disease. It
    can help doctors confirm a diagnosis and guide treatment decisions.</p>
<h2 class="test-h2 mt-3">How is the TPP1 test performed?</h2>
<p>The test is done using a blood sample. The sample is sent to a laboratory where the level of TPP1 enzyme is measured.
</p>
