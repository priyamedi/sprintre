<h2 class="test-h2 mt-3">What are the symptoms of a lentil allergy?</h2>
<p>Symptoms of a lentil allergy can range from mild to severe and include itching or tingling in the mouth, swelling of the lips, face, tongue, or throat, hives, stomach cramps, nausea, vomiting, diarrhea, and, in severe cases, anaphylaxis.</p>
<h2 class="test-h2 mt-3">How is the Allergen, Individual - Food Lentil test performed?</h2>
<p>The test is conducted using a blood sample. A healthcare professional will draw blood from a vein, typically in the arm, and the sample is sent to a laboratory for analysis.</p>
