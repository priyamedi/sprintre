<h2 class="test-h2 mt-3"> What is FISH for Aneuploidy – 3 Markers Chr 13, 18, 21?</h2>
	<p>This test uses Fluorescence In Situ Hybridization to detect the presence of an abnormal number of
chromosomes 13, 18, and 21 in a fetus, which are associated with certain genetic disorders.</p>

<h2 class="test-h2 mt-3">Why is this test important during pregnancy?</h2>
<p>This test is critical for early detection of chromosomal abnormalities, allowing parents and doctors to
make well-informed decisions regarding the management of the pregnancy and the health of the baby.</p>
