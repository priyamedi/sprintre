	<h2 class="test-h2 mt-3">What is Fluorescence In Situ Hybridization (FISH) and why is it used in this test? </h2>
	<p>FISH is a molecular cytogenetic technique that uses fluorescent probes to bind to specific DNA sequences on chromosomes. It is a highly sensitive and specific method that allows for the detection of genetic abnormalities such as deletions, translocations, and amplifications, which are common in multiple myeloma. </p>
	<h2 class="test-h2 mt-3"> What is the significance of del17p in multiple myeloma? </h2>
	<p>Del17p refers to a deletion of part of chromosome 17. This particular deletion is associated with an aggressive form of multiple myeloma and is often linked to resistance to standard therapies. </p>
