<h2 class="test-h2 mt-3"> What is the IHC- ER, PR test?</h2>
<p>
The IHC- ER, PR test is a pathology test that identifies the presence of estrogen and progesterone
receptors in a tissue sample. The presence or absence of these receptors can significantly impact the
treatment strategy for diseases like breast cancer.</p>
<h2 class="test-h2 mt-3"> How is the IHC- ER, PR test performed?</h2>
<p>
This test is performed on a tissue biopsy sample. The sample is treated with specific antibodies designed
to bind to the estrogen and progesterone receptors. If these receptors are present in the tissue, the
antibodies will attach, and this interaction can be visualized under a microscope.</p>

