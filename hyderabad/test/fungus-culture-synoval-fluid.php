<h2 class="test-h2 mt-3">Why is the Fungus Culture [SYNOVIAL FLUID] test performed?</h2>
<p>The test is performed when a healthcare provider suspects a fungal infection within a joint. Symptoms may include joint swelling, pain, stiffness, or reduced range of motion.</p>

<h2 class="test-h2 mt-3">How is the synovial fluid sample collected for the test?</h2>
<p>The sample is collected through a procedure called arthrocentesis, usually performed under local anesthesia. The healthcare provider inserts a needle into the joint space to withdraw the synovial fluid.</p>

