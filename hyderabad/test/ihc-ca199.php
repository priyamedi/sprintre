<div class="post-text">
<p>The <strong>IHC - CA19.9</strong> test, or immunohistochemistry for Carbohydrate Antigen 19.9, is a diagnostic investigation primarily used in oncology. CA19.9 is a tumor marker commonly associated with pancreatic and gastrointestinal cancers. This test uses the immunohistochemistry technique, which involves using antibodies that bind to specific antigens in the tissue sample. This binding is then visualized under a microscope, allowing doctors to determine the presence and level of CA19.9 in the tissue.
 </p>
<p>While CA19.9 levels are most commonly elevated in pancreatic and gastrointestinal cancers, this marker can also be elevated in other conditions, including benign disorders of the liver, gallbladder, and pancreas. Therefore, this test should be used alongside other diagnostic measures to confirm a diagnosis. It is also important to remember that not all cancers cause an increase in CA19.9, and not all increases in CA19.9 are due to cancer.
 </p>
