    during the urine collection period.</p>
<h2 class="test-h2 mt-3">When should I get this test done?</h2>
<p>This test is typically recommended if you have had kidney stones in the past, especially calcium oxalate stones. It
    may also be recommended if you have certain health conditions that can affect oxalate levels.</p>
<h2 class="test-h2 mt-3">What information does this test provide?</h2>
<p>This test measures the amount of oxalate excreted in your urine over a 24-hour period. High levels can indicate an
    increased risk of kidney stones or may suggest a condition that affects oxalate production or elimination.</p>
<h2 class="test-h2 mt-3">How often should I get this test done?</h2>
<p>The frequency of this test depends on your individual risk factors and your doctor's recommendations. If you're being
    treated for high oxalate levels or kidney stones, your doctor may recommend regular testing to monitor your response
    to treatment.</p>
<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
