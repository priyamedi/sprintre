	<h2 class="test-h2 mt-3">How is this test performed? </h2>
	<p>A bone marrow sample is obtained through aspiration. The sample is then subjected to CD138 Magnetic Bead Enrichment to isolate plasma cells. The enriched cells are analyzed using FISH, where fluorescent probes specific for the regions of interest in chromosomes 4, 14, and 16 bind to the DNA, allowing the visualization of translocations under a fluorescence microscope. </p>
	<h2 class="test-h2 mt-3">How will the results of this test impact my treatment? </h2>
	<p>The presence of t(4;14) or t(14;16) translocations may indicate a high-risk form of multiple myeloma, which might require more aggressive treatment. Your doctor will consider these results to customize the most effective treatment plan for you. </p>
