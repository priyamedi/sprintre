<h2 class="test-h2 mt-3"> Why is the Mercury - Serum test performed?</h2>
	<p>
The Mercury - Serum test is done to determine the amount of mercury present in the serum. It is
generally performed when an individual shows symptoms of mercury poisoning or has been exposed to
an environment suspected of containing high levels of mercury.</p>
<h2 class="test-h2 mt-3"> Is fasting required for this test?</h2>
<p>
No, fasting is not required for the Mercury - Serum test. You can continue your regular diet and daily
routine unless specifically directed otherwise by your healthcare provider.</p>
