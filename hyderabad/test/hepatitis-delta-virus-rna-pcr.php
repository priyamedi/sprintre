<h2 class="test-h2 mt-3">What is Hepatitis D and why is it significant?</h2>
<p>Hepatitis D is a viral infection caused by the Hepatitis D virus (HDV). It's only present in individuals who are also
    infected with Hepatitis B, and it tends to worsen the liver disease associated with Hepatitis B, leading to more
    severe outcomes like cirrhosis and liver cancer.</p>
<h2 class="test-h2 mt-3">Who should get the Hepatitis Delta Virus - RNA PCR test?</h2>
<p>This test is recommended for individuals who have been diagnosed with Hepatitis B, especially if they have symptoms
    of a more severe liver disease, or for those at risk of Hepatitis D exposure, such as healthcare workers,
    individuals with multiple sexual partners, and intravenous drug users.</p>
