<h2 class="test-h2 mt-3">What is <strong>Maple Syrup Urine Disease (MSUD)</strong>?</h2>
	<p>
MSUD is an inherited metabolic disorder in which the body is unable to effectively break down certain
amino acids. This leads to the buildup of these amino acids and their metabolites in the blood and urine,
which can be toxic to the brain and other organs.</p>
<h2 class="test-h2 mt-3"> What are the symptoms of Maple Syrup Urine Disease?</h2>
<p>
Symptoms of MSUD typically present shortly after birth and may include poor feeding, vomiting,
lethargy, developmental delay, seizures, and the characteristic maple syrup smell in the urine and
earwax.</p>
