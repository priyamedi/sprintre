<h2 class="test-h2 mt-3">Is any specific preparation required for this test?</h2>
<p>There is no specific preparation required for the patient. The tissue sample is usually collected by a healthcare
    provider during a surgical procedure or biopsy.</p>
<h2 class="test-h2 mt-3">When should I get this test done?</h2>
<p>You may be advised to undergo this test if you have been diagnosed with colorectal cancer, especially if your family
    has a history of the disease, or if your doctor suspects Lynch syndrome based on certain clinical criteria.</p>
<h2 class="test-h2 mt-3">What information does the IHC - PMS2 test provide?</h2>
<p>The IHC - PMS2 test helps to detect the presence or absence of PMS2 protein in the cells of a tissue sample. The
    absence of PMS2 can indicate a high likelihood of microsatellite instability, which is associated with a higher risk
    of colorectal carcinoma and suggests the possibility of Lynch syndrome.</p>
<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>The frequency of this test depends on individual circumstances and your doctor's recommendations. It's typically
    performed when colorectal cancer is diagnosed to help determine the best treatment strategy.</p>
<h2 class="test-h2 mt-3">What are the normal values for the IHC - PMS2 test?</h2>
<p>In a normal test result, the PMS2 protein is present in the cells. Absence or reduced expression of PMS2 protein is
    considered abnormal and can indicate a likelihood of microsatellite instability.</p>
<h2 class="test-h2 mt-3">Are there any precautions to be taken for the IHC - PMS2 test?</h2>
<p>No special precautions are required for this test as the tissue sample is collected by a healthcare provider.</p>
<h2 class="test-h2 mt-3">What are the factors affecting the PMS2 levels?</h2>
<p>Mutations in the PMS2 gene that lead to a loss or reduction in the production of PMS2 protein can affect the levels.
    This is often a non-modifiable factor as it is related to one's genetic makeup.</p>
<h2 class="test-h2 mt-3">Who should I consult if my IHC - PMS2 test values are abnormal?</h2>
<p>If your test values are abnormal, you should consult your treating oncologist or a genetic counselor who can guide
    you about the next steps, which may include further testing or a discussion about potential treatment options.</p>
<h2 class="test-h2 mt-3">What happens after an abnormal IHC - PMS2 test result?</h2>
<p>An abnormal result can suggest a higher risk of colorectal carcinoma due to microsatellite instability. Your doctor
    will discuss the results and possibly suggest further genetic testing to confirm Lynch syndrome and develop a
    personalized treatment plan.</p>
<h2 class="test-h2 mt-3">Is the IHC - PMS2 test definitive for diagnosing Lynch syndrome?</h2>
<p>While the IHC - PMS2 test is an important tool for diagnosing Lynch syndrome, further genetic testing is usually
    required to confirm the diagnosis.</p>
<h2 class="test-h2 mt-3">How reliable is the IHC - PMS2 test?</h2>
<p>The IHC - PMS2 test is a reliable and established method for checking the presence of the PMS2 protein. However,
    results should always be interpreted in conjunction with clinical <a href="https://www.sprintdiagnostics.in/symptoms"><b style="color:#2C53A5">symptoms</b></a> and other laboratory findings.</p>
<h2 class="test-h2 mt-3">Can I take this test at home?</h2>
<p>No, this test requires a tissue sample, which needs to be collected by a healthcare provider through a biopsy or
    surgical procedure. It cannot be done at home.</p>
<h2 class="test-h2 mt-3">Can this test determine if my cancer is hereditary?</h2>
<p>The absence of PMS2 protein may suggest a genetic form of colorectal cancer known as Lynch syndrome. However, further
    genetic testing is usually required to confirm this diagnosis.</p>
<p>In conclusion, the <strong>IHC - PMS2 test</strong> serves as a crucial tool for early detection of Lynch
    syndrome and to guide appropriate treatment strategies in patients with colorectal carcinoma. Always consult with
    your healthcare provider for any questions or concerns you might have about this test.</p>





