<h2 class="test-h2 mt-3">What is the significance of the FISH for Aneuploidy test with 6 markers?</h2>
	<p>This test is vital for detecting chromosomal abnormalities in the fetus, particularly for chromosomes 13,
16, 18, 21, X, and Y. Early diagnosis can provide crucial information regarding conditions like Down
syndrome, Patau syndrome, Edwards syndrome, and sex-linked disorders.</p>
<h2 class="test-h2 mt-3">How is this test conducted?</h2>
<p>This test requires a sample of amniotic fluid or chorionic villus, which is obtained through amniocentesis
or CVS. Fluorescent probes are used in the lab to bind to specific chromosomes in the sample, which can
then be visualized under a microscope.</p>
