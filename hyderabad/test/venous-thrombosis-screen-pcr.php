<h2 class="test-h2 mt-3">What is Venous Thrombosis?</h2>
	<p>
<strong>Venous Thrombosis</strong> is a condition characterized by the formation of blood clots in the veins,
often in the deep veins of the leg, known as Deep Vein Thrombosis (DVT). These clots can
dislodge and travel to the lungs causing a life-threatening condition called Pulmonary Embolism
(PE).</p>
<h2 class="test-h2 mt-3">What are the risk factors for Venous Thrombosis?</h2>
<p>
Risk factors for Venous Thrombosis include a family history of blood clots, prolonged bed rest or
immobility, injury to veins, certain surgeries, pregnancy, oral contraceptive use, hormone
replacement therapy, cancer, <a href="https://www.sprintdiagnostics.in/hyderabad/test/obesity-capsule-1"><b style="color:#2C53A5">obesity</b></a>, and certain genetic conditions.</p>
