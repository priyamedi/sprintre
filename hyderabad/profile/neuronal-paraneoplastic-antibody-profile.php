<div class="post-text">
<p>Paraneoplastic syndromes are a group of disorders that are associated with malignant diseases yet are
not directly caused by the tumor itself. They are believed to result from an abnormal immune response
to the presence of a cancer. The Neuronal (Paraneoplastic) Antibody Profile test is a type of blood test
used to detect the presence of specific antibodies associated with these syndromes. The antibodies this
test looks for include Amphiphysin, PNMA2 (Ma2/Ta), Ri/ANNA-2, Yo/PCA-1, HU/ ANNA-1, and CV2.
 </p>
<p> This test is typically ordered when a person presents with neurological symptoms that cannot be
explained by other diagnoses and the physician suspects a paraneoplastic syndrome. Neurological
symptoms may include ataxia (loss of control over body movements), vertigo (dizziness), numbness or
tingling in limbs, difficulty swallowing or speaking, memory loss, or even changes in personality or mood.</p>
