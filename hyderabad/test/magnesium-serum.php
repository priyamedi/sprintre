<p>An imbalance in magnesium levels may lead to several health conditions such as muscle twitching, fatigue, high blood pressure, asthma, and heart disease. Therefore, this test serves as an important diagnostic tool that helps physicians detect any abnormalities and outline an appropriate treatment plan.
