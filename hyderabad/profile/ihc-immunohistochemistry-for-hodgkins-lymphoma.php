<h2 class="test-h2 mt-3">What is the purpose of this test?</h2>
<p>The IHC panel for Hodgkin's Lymphoma is used to confirm the diagnosis of Hodgkin's lymphoma and differentiate it from
    other similar conditions. It also helps to define the subtype of Hodgkin's lymphoma, which can guide treatment
    options.</p>
<h2 class="test-h2 mt-3">Is fasting required for this test?</h2>
<p>No, fasting is not required for this test. It is performed on a tissue sample obtained through a biopsy.</p>
