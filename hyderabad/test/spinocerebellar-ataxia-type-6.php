<p>No special preparation is needed for this genetic test for SCA6.</p>
<h2 class="test-h2 mt-3">When should I get this test?</h2>
<p>If you have symptoms like uncoordinated movements, <a href="https://www.sprintdiagnostics.in/symptoms/difficulty-with-coordination-balance"><b style="color:#2C53A5">difficulty with balance</b></a>, or <a href="https://www.sprintdiagnostics.in/symptoms/speech-difficulties"><b style="color:#2C53A5">dysarthria</b></a> (difficulty articulating
