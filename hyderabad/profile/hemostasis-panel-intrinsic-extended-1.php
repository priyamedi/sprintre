<h1 class="test-h1">Hemostasis Panel - Intrinsic - Extended 1 </h1>
<div class="post-text">
<p>Hemostasis is the process by which the body stops bleeding through blood clotting. This process involves various proteins and factors, and it’s essential for preventing excessive blood loss from injuries. However, an imbalance in this process can lead to bleeding disorders or abnormal clotting. The Hemostasis Panel - Intrinsic - Extended 1 includes tests that are crucial in evaluating the intrinsic pathway of the clotting process. It includes tests for Activated Partial Thromboplastin Time (APTT) with mixing studies, and the levels of clotting Factor VIII, Factor IX, and Factor XI.</p>

<p>APTT measures the time it takes for blood to clot and can help diagnose blood clotting disorders. The mixing studies involve mixing the patient’s blood with normal plasma to identify if there is an inhibitor affecting clotting. Factor VIII, IX, and XI are proteins essential for blood clotting, and this panel measures their levels, helping in the diagnosis of hemophilia and other bleeding disorders.</p>
