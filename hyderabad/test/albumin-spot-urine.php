<h1 class="test-h1">Albumin - Spot Urine </h1>
<div class="post-text">
<p>Albumin is a type of protein that is typically present in the blood but not usually detected in urine. It's the most abundant protein in human blood and it helps to regulate blood pressure, blood volume, and keeps your blood vessels from leaking. The kidney filters albumin and reabsorbs it, preventing it from passing into the urine. When the kidneys aren't working properly, albumin can leak into the urine, a condition known as albuminuria or proteinuria.
 </p>
<p>A <strong>Spot Urine Albumin test</strong> is done to measure the amount of albumin present in a urine sample. This test is most commonly used to screen for, help diagnose, and monitor kidney disease. It is particularly useful for individuals who are at high risk of kidney disease, such as those with diabetes or high blood pressure. </p>
