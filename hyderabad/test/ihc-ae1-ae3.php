 	<h2 class="test-h2 mt-3">What does a positive result mean?</h2>
<p>A positive result indicates the presence of cytokeratins in the tissue sample. This is a common finding in carcinomas, a type of cancer that begins in the epithelial cells.</p>

 	<h2 class="test-h2 mt-3">What does a negative result mean?</h2>
<p>A negative result indicates that no cytokeratins were detected in the tissue sample. This result may rule out certain diseases, but further testing may be needed for a definitive diagnosis.</p>

  	<h2 class="test-h2 mt-3">Are there any factors that can affect the test results?</h2>
