<h2 class="test-h2 mt-3">Why might I need a NOTCH 3 Gene Cadasil (Deletion - Duplication) test?</h2>
<p>If you have a family history of CADASIL or are experiencing symptoms such as recurrent strokes, migraines, mood
    disturbances, cognitive decline, or early-onset dementia, your healthcare provider may recommend this test.</p>
<h2 class="test-h2 mt-3">How is the NOTCH 3 Gene Cadasil (Deletion - Duplication) test performed?</h2>
<p>The test is conducted using a sample of your blood. The DNA is extracted and examined for any alterations,
    specifically deletions or duplications, in the NOTCH3 gene.</p>
