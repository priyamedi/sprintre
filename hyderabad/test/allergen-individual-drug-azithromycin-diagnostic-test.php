								<h2 class="test-h2 mt-3"> When should I get this test done?</h2>
								<p>If you experience allergy-like symptoms, especially after taking azithromycin or a similar antibiotic, you should consider getting this test done. Symptoms to watch for include rash, itching, breathing difficulties, or severe reactions like anaphylaxis.</p>

								<h2 class="test-h2 mt-3">What information does the test provide?</h2>
								<p>The test detects and measures the levels of specific IgE antibodies in your blood. These antibodies are produced by your immune system in response to azithromycin. Elevated levels indicate an allergic reaction to azithromycin.</p>

								<h2 class="test-h2 mt-3"> How often should this test be done?</h2>
								<p>This test is typically done when symptoms suggest an allergy to azithromycin. The frequency of retesting depends on whether you need to take azithromycin or a similar antibiotic again. Your doctor will provide guidance based on your individual circumstances.</p>

