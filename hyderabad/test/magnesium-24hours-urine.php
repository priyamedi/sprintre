<h2 class="test-h2 mt-3">Why is the Magnesium - 24 hours urine test ordered?</h2>
<p>This test is usually ordered when a healthcare provider suspects that you may have an imbalance of magnesium in your body. Symptoms of a magnesium imbalance can include muscle weakness, cramps, irregular heart rhythms, or changes in mental status such as confusion or seizures.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test requires you to collect all urine passed in a 24-hour period. This collection is started after you empty your bladder in the morning and continues for the next 24 hours. The collected urine is then sent to a laboratory for analysis.</p>
