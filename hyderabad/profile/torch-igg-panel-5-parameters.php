<h2 class="test-h2 mt-3">What are IgG antibodies?</h2>
<p>IgG antibodies are a type of antibody produced by the immune system in response to an infection. These antibodies
    usually persist in the blood long after the infection has been resolved, indicating past infection or immunity.</p>
<h2 class="test-h2 mt-3">How is the sample collected for this test?</h2>
<p>A healthcare professional will draw a small volume of blood from a vein in your arm using a needle. This procedure is
    quick and typically causes minimal discomfort.</p>
