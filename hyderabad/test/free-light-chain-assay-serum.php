<h1 class="test-h1">Free Light Chain Assay (Kappa & Lambda) - Serum (with Kappa Lambda Ratio) </h1>
<div class="post-text">
<p>Free light chain assay, often known as the serum free light chain (SFLC) test, measures the free (unbound) kappa and lambda light chains in the blood. Light chains are small proteins produced by plasma cells, a type of white blood cell. They can combine with heavy chains to form immunoglobulins, or antibodies, which help fight infections. When light chains are produced in excess, they can be found in the blood in a "free" state, unbound to heavy chains.
 </p>
<p>The balance or ratio of kappa to lambda light chains in the blood (kappa:lambda ratio) can provide valuable information about disorders related to plasma cells, such as multiple myeloma, a type of bone marrow cancer. It is also useful in monitoring treatment response and disease progression in these conditions.
 </p>
