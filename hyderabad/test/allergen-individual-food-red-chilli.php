<h2 class="test-h2 mt-3">What are the symptoms of red chili allergy?</h2>
<p>Symptoms of red chili allergy can range from mild to severe and may include itching or tingling in the mouth, hives,
    eczema, difficulty breathing, swelling of the face, lips, tongue, or throat, nausea, vomiting, abdominal pain,
    diarrhea, and in severe cases, anaphylaxis.</p>
<h2 class="test-h2 mt-3">What triggers red chili allergy?</h2>
<p>Red chili allergy is triggered by the proteins found in red chili peppers. Capsaicin, the compound that gives chilies
    their heat, may also contribute to allergic reactions in some individuals.</p>
