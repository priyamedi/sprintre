<h2 class="test-h2 mt-3"> Why is the Metanephrine-Free test performed?</h2>
	<p>
The Metanephrine-Free test is often used when a pheochromocytoma or paraganglioma is suspected.
These are rare adrenal gland tumors that produce excessive adrenaline.</p>
<h2 class="test-h2 mt-3"> How is the Metanephrine-Free test performed?</h2>
<p>
The test requires a 24-hour urine collection. This means you'll need to collect all your urine in a special
container over a 24-hour period.</p>
