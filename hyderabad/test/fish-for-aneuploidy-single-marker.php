<h2 class="test-h2 mt-3"> Why is the FISH for Aneuploidy with a single marker test done?</h2>
	<p>This test is conducted to detect if there is an abnormal number of a specific chromosome in the fetus,
which may indicate a chromosomal disorder. Early detection allows for better planning and decision-
making regarding the pregnancy.</p>
<h2 class="test-h2 mt-3"> How is the sample for this test collected?</h2>
<p>The sample is usually collected through an amniocentesis, where a small amount of amniotic fluid is
extracted, or through chorionic villus sampling (CVS), which involves taking a small sample of the
placenta.</p>
