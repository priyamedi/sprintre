<h2 class="test-h2 mt-3">What is the HSV-1 IgM antibody test?</h2>
<p>The HSV-1 IgM antibody test is a serological test that is used to detect the presence of Immunoglobulin M (IgM)
    antibodies against the herpes simplex virus type 1 (HSV-1) in the serum of a patient.</p>
<h2 class="test-h2 mt-3">Why is the test performed?</h2>
<p>This test is performed to identify recent or acute infection with the HSV-1 virus. HSV-1 typically causes infections
    around the mouth, leading to cold sores or fever blisters. However, it can also cause genital infections.</p>
