	<p>EGFR sequencing is a laboratory method used to read the genetic code of the EGFR gene in cancer cells. This method can identify mutations in the EGFR gene, which can be targeted by specific treatments.
 </p>
	<h2 class="test-h2 mt-3">What is ALK-1 IHC? 
 </h2>
