<h2 class="test-h2 mt-3">What is the Clostridium Difficile PCR test? </h2>
<p>The Clostridium Difficile PCR test is a diagnostic tool used to detect the presence of the C. difficile bacterium in a stool sample. This test uses a method called Polymerase Chain Reaction (PCR) to identify the DNA of the bacteria.</p>
<h2 class="test-h2 mt-3">Why is a Clostridium Difficile PCR test done? </h2>
<p>A Clostridium Difficile PCR test is done if a person has diarrhea or other symptoms that suggest a C. difficile infection, especially if they have been taking antibiotics or have been in a healthcare setting for a prolonged period.</p>
