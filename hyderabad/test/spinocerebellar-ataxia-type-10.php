<h2 class="test-h2 mt-3">When should I get this test?</h2>
<p>If you exhibit symptoms of ataxia, such as <a href="https://www.sprintdiagnostics.in/symptoms/difficulty-with-coordination-balance"><b style="color:#2C53A5">impaired coordination</b></a>, balance issues, and <a href="https://www.sprintdiagnostics.in/symptoms/seizures"><b style="color:#2C53A5">epileptic seizures</b></a>, especially
