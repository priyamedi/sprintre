<h2 class="test-h2 mt-3">What is DiGeorge Syndrome?</h2>
<p> DiGeorge Syndrome is a genetic disorder caused by a deletion in chromosome 22. It affects the development of several body systems and can cause heart defects, poor immune system function, a cleft palate, and developmental delays.</p>

<h2 class="test-h2 mt-3">How is the FISH test for DiGeorge Syndrome performed? </h2>
<p>This test is performed using a sample of amniotic fluid obtained through amniocentesis. The sample is then analyzed in the laboratory using fluorescent probes that bind to the specific region of chromosome 22. If the region is missing, it indicates that the fetus has DiGeorge Syndrome.</p>

