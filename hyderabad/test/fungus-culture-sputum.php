<h2 class="test-h2 mt-3">Why is a fungus culture test from sputum performed?</h2>
<p>This test is performed when a healthcare provider suspects a fungal infection in the lungs or airways. Symptoms of a fungal infection may include a persistent cough, chest pain, fever, and difficulty breathing.</p>

<h2 class="test-h2 mt-3">How is the sputum sample collected for this test?</h2>
<p>Sputum is typically collected in the morning as you tend to produce more sputum after lying down for a period. You may be asked to cough deeply to bring up sputum from your lungs.</p>

