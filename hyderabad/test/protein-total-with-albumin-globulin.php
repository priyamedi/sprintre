<h2 class="test-h2 mt-3">Why might my doctor order this test?</h2>
	<p> Your doctor might order this test if they suspect that you have a condition affecting your liver or kidneys. It might also be ordered to evaluate your nutritional status, especially if you've been experiencing unexplained weight loss or malnutrition.</p>
	<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>This test is performed via a simple blood draw. The sample is then sent to the lab for analysis.</p>
