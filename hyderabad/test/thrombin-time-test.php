<h2 class="test-h2 mt-3">Why is the Thrombin Time test done?</h2>
<p>The Thrombin Time test is performed to evaluate your body's ability to form blood clots and to monitor the
    effectiveness of treatment with blood-thinning medications. If the results are abnormal, it may indicate a bleeding
    disorder or blood clotting disorder.</p>
<h2 class="test-h2 mt-3">What does the Thrombin Time test measure?</h2>
<p>The Thrombin Time test measures the time it takes for a clot to form in a sample of your blood after thrombin has
    been added. This helps evaluate your blood’s ability to clot properly.</p>
