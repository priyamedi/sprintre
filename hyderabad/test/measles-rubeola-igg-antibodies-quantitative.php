<h2 class="test-h2 mt-3">Why is this test important?</h2>
<p>This test helps determine if you have immunity to measles, either from a previous infection or from
vaccination.</p>

<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test is performed using a blood sample, which is collected via a simple venipuncture procedure.</p>
