 </h2>
	<p>The Liver Function Test is used to assess the overall health and functionality of the liver. It can help diagnose liver diseases, monitor disease progression, determine the severity of a disease, and monitor the efficacy of treatment. </p>
	<h2 class="test-h2 mt-3">How is the Liver Function Test performed? </h2>
	<p>The Liver Function Test is performed using a blood sample, which is analyzed in a laboratory. </p>
