	<h2 class="test-h2 mt-3">What is peritoneal or ascitic fluid?</h2>
	<p> Peritoneal fluid is a small amount of liquid that is present in the abdominal cavity to lubricate the
surfaces of the tissue lining the abdomen and pelvis. Ascitic fluid is an excess accumulation of liquid in
the abdominal cavity, which can occur for various reasons, including infection, liver disease, or cancer.</p>
	<h2 class="test-h2 mt-3"> How is the sample collected for Culture and Sensitivity - Aerobic [Peritoneal/Ascitic Fluid]?</h2>
	<p> A procedure called paracentesis is used to collect peritoneal or ascitic fluid. In this procedure, a
needle is carefully inserted through the abdominal wall into the peritoneal cavity. Ultrasound may be
used to guide the needle.</p>
