<h2 class="test-h2 mt-3">Why is the PNH RBC Screening - CD55 & CD59 test important?</h2>
<p>The PNH RBC Screening test is crucial for diagnosing PNH and monitoring the disease progression. Identifying the
    absence or deficiency of CD55 and CD59 can confirm a diagnosis of PNH and provide valuable insights into the
    severity of the condition.</p>
<h2 class="test-h2 mt-3">Is fasting required for this test?</h2>
