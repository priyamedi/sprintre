<h2 class="test-h2 mt-3">What are heterophile antibodies?</h2>
<p>Heterophile antibodies are a type of antibody that reacts not only to the inducing antigen but also to unrelated
    antigens. They are typically formed in response to an infection with the Epstein-Barr virus, which causes infectious
    mononucleosis.</p>
<h2 class="test-h2 mt-3">Why is the test performed?</h2>
<p>The heterophile antibody test is performed to help diagnose infectious mononucleosis. If you have symptoms such as
    fever, sore throat, and swollen lymph glands, your healthcare provider may order this test to confirm a diagnosis of
    mono.</p>
