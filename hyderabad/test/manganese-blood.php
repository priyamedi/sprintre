<h2 class="test-h2 mt-3">Why is the manganese blood test done?</h2>
<p>The manganese blood test is used to detect the levels of manganese in the body to monitor exposure,
evaluate the body's ability to excrete manganese or to confirm a diagnosis of manganese poisoning.</p>

<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>This test is performed on a blood sample. The blood is drawn from a vein, typically a vein in your arm,
using a needle.</p>
