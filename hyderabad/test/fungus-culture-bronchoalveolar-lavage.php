<h2 class="test-h2 mt-3">What is the purpose of a fungus culture of BAL fluid? </h2>
<p>This test is performed to identify and diagnose fungal infections in the lungs. The results can guide healthcare professionals in choosing the most appropriate treatment options.</p>
<h2 class="test-h2 mt-3">How is the BAL fluid collected?</h2>
<p> BAL fluid is collected through a procedure known as bronchoalveolar lavage. A bronchoscope is passed through the mouth or nose into the lungs, and a saline solution is then introduced into a small part of the lung. The fluid is collected for further analysis.</p>
