	<h2 class="test-h2 mt-3">Can this test be repeated over time? </h2>
<p>Yes, the test can be repeated if necessary, particularly if the characteristics of the cancer appear to change or if the response to treatment is not as expected.</p>

	<h2 class="test-h2 mt-3">What does it mean if the receptors are not found? </h2>
<p>If these receptors are not present, it means the cancer is "triple-negative". Triple-negative breast cancers do not respond to hormonal therapies or therapies that target HER2Neu, but other treatments are available.</p>

