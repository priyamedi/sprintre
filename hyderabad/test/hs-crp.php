<p>The <strong>hs CRP (C-Reactive Protein high sensitive) test</strong> is a blood test that measures the level of C-reactive
protein (CRP) in your body. CRP is a protein produced by the liver in response to inflammation, and its
level rises in the blood when there's inflammation in the body. </p>
<p> The <strong>hs CRP test</strong> is more sensitive than the standard CRP test and can detect lower levels of the protein.
It's often used to assess the risk of developing cardiovascular disease (CVD), as higher levels of CRP can
indicate a higher risk of CVD. However, high levels of CRP can also be seen in other conditions, including
infections and autoimmune diseases.</p>
<p>This test is typically used alongside other tests and risk factors (such as high blood pressure and
cholesterol levels) to assess an individual's risk of CVD. It may also be used to monitor disease activity in
inflammatory conditions and to check the effectiveness of treatments.</p>
