<h2 class="test-h2 mt-3"> What is a Bence Jones Protein (BJ Protein) Test?</h2>
<p>This test measures the amount of Bence Jones proteins, a type of light chain antibody, in your urine. It is primarily used to help diagnose and monitor multiple myeloma, a type of blood cancer.</p>
<h2 class="test-h2 mt-3"> Why is this test important?</h2>
<p>This test is important because it helps to detect multiple myeloma, a type of cancer that affects plasma cells in the bone marrow. The presence of Bence Jones proteins in the urine is a common sign of this disease.</p>
