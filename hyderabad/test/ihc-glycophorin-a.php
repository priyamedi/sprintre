<p> This test detects the presence of Glycophorin-A, a protein found on the surface of red blood cells, in tissue samples.</p>
<h2 class="test-h2 mt-3">Why is the IHC - Glycophorin-A test done?</h2>
<p> It is used to diagnose certain blood disorders, such as anemia, and to help differentiate between different types of tumors, especially if the origin of the tumor is unclear.</p>
