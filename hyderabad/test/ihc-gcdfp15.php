<p>The GCDFP15 IHC test is done to detect the presence of breast carcinoma cells in a tissue sample, especially when investigating metastatic cancer of unknown primary origin.</p>
<h2 class="test-h2 mt-3">Do I need to fast for this test?</h2>
<p> No, fasting is not required for this test.</p>
