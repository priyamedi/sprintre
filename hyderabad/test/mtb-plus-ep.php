<h2 class="test-h2 mt-3">What is the MTB Plus EP test?</h2>
<p>The MTB Plus EP test is a comprehensive diagnostic panel that includes an AFB smear, GeneXpert-EP, and AFB/TB
    Culture. These tests work together to detect and identify Mycobacterium tuberculosis, the bacteria causing TB, as
    well as test for drug resistance.</p>
<h2 class="test-h2 mt-3">Who should get the MTB Plus EP test?</h2>
<p>This test is generally recommended for individuals who exhibit symptoms of TB, such as persistent cough, unexplained
    weight loss, fever, night sweats, and fatigue. It's also indicated for those who have been in contact with TB
    patients or who live in or have traveled to areas with high TB incidence.</p>
