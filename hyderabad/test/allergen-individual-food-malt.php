<h2 class="test-h2 mt-3">What are the symptoms of a malt allergy?</h2>
<p>Symptoms of a malt allergy can include itching or tingling in the mouth, hives, eczema, swelling of the lips, face, tongue, or throat, nasal congestion, wheezing, shortness of breath, stomach pain, diarrhea, nausea, vomiting, dizziness, lightheadedness, or anaphylaxis in severe cases.</p>
<h2 class="test-h2 mt-3">How is the Allergen, Individual - Food Malt test performed?</h2>
<p>The test is performed using a blood sample. A healthcare professional will draw a small sample of blood from your arm, which will be sent to a laboratory. The laboratory will then analyze the blood sample for specific IgE antibodies that react to proteins in malt.</p>
