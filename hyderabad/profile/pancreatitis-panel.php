<p>This panel is important for diagnosing and monitoring pancreatitis. Abnormal levels of liver enzymes, amylase, or
    lipase can indicate a problem with your pancreas, such as inflammation or damage.</p>
<h2 class="test-h2 mt-3">Do I need to fast for this test?</h2>
<p>Yes, fasting for 8-12 hours is typically required before this test. You should also avoid alcohol and strenuous
    exercise for 24 hours before the test.</p>

