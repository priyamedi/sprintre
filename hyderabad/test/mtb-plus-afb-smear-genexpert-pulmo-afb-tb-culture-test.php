<h2 class="test-h2 mt-3">Why would I need the MTB Plus test?</h2>
<p>If you have symptoms such as a persistent cough, weight loss, fatigue, fever, night sweats, or chest pain, and your
    healthcare provider suspects tuberculosis, they may recommend the MTB Plus test.</p>
<h2 class="test-h2 mt-3">How is the sputum sample collected for this test?</h2>
<p>You will be instructed to cough deeply to bring up the sputum from your lungs. The sample is collected in a sterile
    container.</p>
<h2 class="test-h2 mt-3">What does a positive result mean?</h2>
<p>A positive result in any of the three tests included in MTB Plus suggests the presence of Mycobacterium tuberculosis,
    indicating a likely case of tuberculosis.</p>
