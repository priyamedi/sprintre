<h2 class="test-h2 mt-3">What is the Aldosterone/Plasma Renin Activity Ratio test?</h2>
<p>This test measures the levels of aldosterone and renin in the blood to calculate their ratio, aiding in the diagnosis of primary aldosteronism or other disorders related to the adrenal glands.</p>
<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>It's usually done when a patient has high blood pressure and low potassium levels that are not adequately controlled by medication, or when an adrenal mass has been detected during an imaging study.</p>
