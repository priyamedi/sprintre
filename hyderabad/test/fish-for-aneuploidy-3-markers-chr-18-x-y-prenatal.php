<h2 class="test-h2 mt-3"> How is the test performed?</h2>
<p>A sample of amniotic fluid or placental tissue is obtained via amniocentesis or CVS. The sample is
analyzed in the laboratory where fluorescent probes are used to bind to the chromosomes, making
them visible under a microscope.</p>

<h2 class="test-h2 mt-3">When should this test be done?</h2>

<p>This test is typically conducted in the second trimester of pregnancy, but can be performed earlier
through chorionic villus sampling.</p>

<h2 class="test-h2 mt-3"> What do abnormal results indicate?</h2>
<p>Abnormal results indicate an atypical number of chromosomes 18, X, or Y, suggesting the possibility of
chromosomal disorders such as Edwards syndrome or sex-linked disorders.</p>

<h2 class="test-h2 mt-3">Are there any risks associated with this test?</h2>
<p>The test itself does not pose risks, but the sample collection methods (amniocentesis or CVS) carry a
slight risk of infection or miscarriage.</p>

<h2 class="test-h2 mt-3"> What should be done if the test results are abnormal?</h2>
<p>Consult your doctor for a thorough understanding of the results and discuss the options and steps for
managing the pregnancy.</p>

<h2 class="test-h2 mt-3"> Is this test conclusive for the diagnosis of chromosomal disorders?</h2>
<p>While FISH is highly accurate, it is often used in conjunction with other genetic tests for a
comprehensive diagnosis.</p>

<h2 class="test-h2 mt-3">Can medication or lifestyle choices affect the results?</h2>
<p>No, the number of chromosomes in fetal cells is not affected by medications or lifestyle.</p>

<h2 class="test-h2 mt-3"> Are there alternatives to the FISH test?</h2>
<p>Yes, alternatives include karyotyping and non-invasive prenatal testing (NIPT) which analyze cell-free
fetal DNA in the mother's blood.</p>

<h2 class="test-h2 mt-3"> Is this test recommended for all pregnancies?</h2>
<p>This test is usually recommended in cases of high-risk pregnancies, such as maternal age over 35, a
family history of genetic disorders, or abnormal findings in other prenatal tests.</p>

<h2 class="test-h2 mt-3"> Is genetic counseling recommended if abnormal results are obtained?</h2>
<p>Yes, genetic counseling is highly recommended to understand the implications and explore options for
managing the pregnancy.</p>

<h2 class="test-h2 mt-3">What are the limitations of the FISH test?</h2>
<p>FISH testing only targets specific chromosomes and does not provide information on structural
abnormalities or other chromosomes.</p>

<h2 class="test-h2 mt-3">Can this test tell the sex of the baby?</h2>

<p>Yes, by analyzing the X and Y chromosomes, this test can also determine the sex of the baby.</p>

<h2 class="test-h2 mt-3"> Is the FISH test painful?</h2>
<p>The test itself is not painful, but the collection of amniotic fluid or placental tissue may cause discomfort
or mild pain.</p>

