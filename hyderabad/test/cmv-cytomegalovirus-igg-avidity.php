<h2 class="test-h2 mt-3">What is a CMV (Cytomegalovirus) IgG Avidity test?</h2>
<p>A CMV IgG Avidity test measures the strength of the binding between IgG antibodies and the CMV virus.
A high avidity indicates a past infection, while a low avidity suggests a recent infection, generally within
the last three to six months.</p>

<h2 class="test-h2 mt-3">Why might I need this test?</h2>
<p>This test is particularly helpful in situations where it's necessary to distinguish between a recent and a
past infection, such as in pregnancy. If a woman gets infected with CMV for the first time during
pregnancy, there's a risk the virus can be passed to the unborn baby, possibly leading to serious
complications.</p>
