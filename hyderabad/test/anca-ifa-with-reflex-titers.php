<h2 class="test-h2 mt-3">What is the ANCA - IFA with Reflex Titers test?</h2>
<p>ANCA stands for Antineutrophil Cytoplasmic Antibodies. These are autoantibodies—immune proteins that mistakenly target and react with a person's own tissues or organs—that target proteins within neutrophils, a type of white blood cell. The ANCA test detects and measures the amount of ANCA in the blood. If the initial IFA (Indirect Fluorescent Antibody) test is positive, reflex titer testing is done to determine the level of these antibodies.</p>

<h2 class="test-h2 mt-3">Why is the ANCA - IFA with Reflex Titers test performed?</h2>
<p>The ANCA test is primarily used to help diagnose diseases that cause inflammation of the blood vessels (vasculitis), particularly when a person has signs and symptoms that suggest systemic vasculitis. It may also help monitor the effectiveness of treatment in a person diagnosed with a condition associated with ANCA.</p>

