	<h2 class="test-h2 mt-3">Why is the IgG Synthesis Index test performed?
 </h2>
	<p>The <strong>IgG Synthesis Index test</strong> is performed when a neurological disorder like Multiple Sclerosis is suspected. It aids in the diagnosis by measuring the levels of IgG in the CSF and comparing it with the levels in the blood.
 </p>
	<h2 class="test-h2 mt-3">How is the test performed?
 </h2>
	<p>The test requires a sample of cerebrospinal fluid (CSF), usually obtained through a lumbar puncture (spinal tap) procedure. The sample is then sent to the laboratory for analysis.
 </p>
