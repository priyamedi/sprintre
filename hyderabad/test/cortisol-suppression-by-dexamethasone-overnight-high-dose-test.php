<p>The Cortisol Suppression by Dexamethasone, Overnight High Dose Test, commonly known as the Overnight Dexamethasone Suppression Test, is a diagnostic procedure used to check for Cushing's syndrome, a condition characterized by excessive cortisol levels. Cortisol is a hormone produced by the adrenal glands, and it plays a crucial role in various bodily functions, such as metabolism, immune response, and stress management.</p>

<p>Dexamethasone is a medication that typically suppresses cortisol production in healthy individuals. In this test, a high dose of dexamethasone is administered, and cortisol levels are measured the following morning. If the cortisol levels do not decrease significantly, it might indicate the presence of Cushing's syndrome or another disorder affecting cortisol production.</p>


