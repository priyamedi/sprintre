						<h2 class="test-h2 mt-3">What are the normal values?</h2>
						<p>Normal values can vary between labs, but generally, the absolute eosinophil count is between 30 to 350 cells/µL.</p>
						<h2 class="test-h2 mt-3">What factors can affect my AEC levels?</h2>
						<p><b>Modifiable Factors: </b>Allergen exposure, certain medications.</p>
						<p><b>Nonmodifiable Factors: </b>Genetics, pre-existing conditions like allergies or asthma.</p>
						<h2 class="test-h2 mt-3">Can certain medications affect my AEC levels?</h2>
