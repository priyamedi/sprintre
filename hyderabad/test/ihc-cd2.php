<h2 class="test-h2 mt-3">What is the purpose of the IHC - CD2 test?</h2>
<p>The <strong>IHC - CD2</strong> test is used to detect the presence of the CD2 antigen, commonly found on the surface of T cells and natural killer cells. It helps in distinguishing T cell lymphomas from other lymphoid malignancies.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test involves applying antibodies specific to the CD2 antigen on a section of tissue. If CD2 is present, it will bind to the antibodies and can be visualized under a microscope.</p>
