<h2 class="test-h2 mt-3"> Why would I need a Routine Examination with Cytology - Body Fluid test?</h2>
	<p>
This test may be required if you are experiencing symptoms such as unexplained fluid build-up, pain,
inflammation or if your doctor suspects an infection, <ahref="https://www.sprintdiagnostics.in/our-services/nuclear-medicine/pet-ct-scan/"><b style="color:#2C53A5">cancer</b></a>, or another disease.</p>
<h2 class="test-h2 mt-3"> How is the body fluid sample collected?</h2>
<p>
The method for collecting the <strong>body fluid</strong> depends on the type of fluid to be tested. It might be collected
via a lumbar puncture, thoracentesis, paracentesis, or arthrocentesis.</p>
