<h1 class="test-h1">Thyroid Profile - IV (Anti-TG, Anti-TPO, FT3, FT4, T3, T4, & TSH) </h1>
<div class="post-text">
<p>The Thyroid Profile - IV is a comprehensive blood test that measures a range of hormones and antibodies to assess the overall function of your thyroid gland. This test panel includes measurements for Anti-Thyroglobulin (Anti-TG), Anti-Thyroid Peroxidase (Anti-TPO), Free Triiodothyronine (FT3), Free Thyroxine (FT4), Total Triiodothyronine (T3), Total Thyroxine (T4), and Thyroid-Stimulating Hormone (TSH).</p>

<p>The thyroid, a small, butterfly-shaped gland located in the front of your neck, produces T3 and T4 hormones, which play a significant role in body metabolism. The free forms of these hormones, FT3 and FT4, represent the active, unbound portion available for use by cells. TSH, produced by the pituitary gland in your brain, regulates the production and release of thyroid hormones. Anti-TG and Anti-TPO are antibodies that target thyroid proteins, and their presence can indicate autoimmune thyroid diseases like Hashimoto's thyroiditis and Graves' disease.</p>
