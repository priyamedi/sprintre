<h2 class="test-h2 mt-3">What does the Chikungunya IgM Rapid Test do?</h2>
<p>This test detects IgM antibodies that the body produces in response to a Chikungunya virus infection. The presence of
	these antibodies typically indicates a recent or current infection.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>A healthcare professional will take a blood sample, usually from a vein in your arm. The test can typically be
	performed at a healthcare facility and results are usually available within 1-2 hours.</p>
