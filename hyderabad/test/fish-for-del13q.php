<h2 class="test-h2 mt-3">What does the FISH for del(13q) test detect?</h2>
	<p>This test detects the deletion of a portion of the long arm of chromosome 13, which is known as 13q
deletion. This deletion can be associated with chronic lymphocytic leukemia, multiple myeloma, and
other hematologic disorders.</p>

<h2 class="test-h2 mt-3">How is the sample for this test collected?</h2>
<p>The sample is usually collected through a blood draw or a bone marrow biopsy, depending on the
condition being tested for and the doctor's recommendation.</p>
