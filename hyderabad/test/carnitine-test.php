<p> Carnitine is a nutrient that plays a crucial role in energy production in the body. It is synthesized in the
body from the amino acids lysine and methionine. Carnitine is responsible for the transport of long-
chain fatty acids into the mitochondria, the powerhouses of cells, where they are oxidized to produce
energy. It also helps in the removal of short and medium-chain fatty acids from the mitochondria that
accumulate during metabolism.</p>
<p> A test for carnitine can be done on blood or urine samples to measure the levels of free carnitine and
total carnitine in the body. It can help diagnose primary carnitine deficiency, a genetic disorder that
prevents the body from using certain fats for energy, particularly during periods without food, and
secondary carnitine deficiency, which can be caused by certain metabolic disorders, malnutrition, or side
effects of medications.</p>
