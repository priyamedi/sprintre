<h1 class="test-h1">Friedreich's Ataxia Mutation Analysis </h1>
<div class="post-text">
<p>Friedreich's ataxia (FA) is a rare genetic disorder that mainly affects the nervous system and results in movement problems. Over time, the muscle coordination (ataxia) worsens, leading to changes in gait, impaired speech, and abnormal eye movements. The disorder also affects the heart, causing hypertrophic cardiomyopathy that can result in heart failure.
 </p>
<p>A Friedreich's Ataxia Mutation Analysis is a type of genetic test that checks for mutations in the FXN gene, which has been linked to the condition. This gene provides instructions for the production of a protein called frataxin. Frataxin is critical in cells' energy factories, known as mitochondria.
 </p>
 <p>In people with Friedreich's ataxia, a specific area of the FXN gene (a sequence of "GAA") is repeated too many times. This test, therefore, primarily identifies the presence of these excessive GAA repeats in the FXN gene.
</p>
