	<h2 class="test-h2 mt-3">What is the Chloride - Spot Urine test?
 </h2>
	<p>The Chloride - Spot Urine test is a simple diagnostic test that measures the concentration of chloride, an important electrolyte, in a random urine sample.
 </p>
	<h2 class="test-h2 mt-3">Why is this test done? </h2>
	<p>This test is often done to evaluate the body's electrolyte balance, assess kidney function, and help diagnose various health conditions, such as adrenal gland disorders, dehydration, and certain types of metabolic acidosis or alkalosis.
 </p>
