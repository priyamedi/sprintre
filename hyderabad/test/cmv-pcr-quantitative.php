<h2 class="test-h2 mt-3">What is the CMV - PCR Quantitative test?</h2>
<p>The CMV - PCR Quantitative test is a diagnostic test that measures the amount of CMV DNA in a blood
sample, which indicates the severity of an active CMV infection.</p>

<h2 class="test-h2 mt-3">Who might need this test?</h2>
<p>This test is often recommended for people with weakened immune systems, such as organ transplant
recipients, individuals with HIV/AIDS, or patients undergoing chemotherapy. It may also be used for
newborns suspected of having a congenital CMV infection.</p>
