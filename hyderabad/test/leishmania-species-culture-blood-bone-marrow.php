<h2 class="test-h2 mt-3">What is Leishmaniasis?</h2>
<p>Leishmaniasis is a parasitic infection caused by the Leishmania species. It is transmitted through the bite of
    infected sandflies. The disease can manifest in different forms, the most severe of which is visceral leishmaniasis
    that affects internal organs.</p>
<h2 class="test-h2 mt-3">What is the purpose of the Leishmania Species Culture test?</h2>
<p>This test aims to culture and isolate the Leishmania parasites from blood or bone marrow samples. It is crucial in
    diagnosing visceral leishmaniasis and determining the appropriate course of treatment.</p>
