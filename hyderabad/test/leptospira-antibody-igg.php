<h2 class="test-h2 mt-3">How is the Leptospira Antibody IgG test performed?</h2>
<p>The test is performed using a blood sample. A healthcare professional will draw blood from a vein, usually in your
    arm. The blood sample will then be sent to a laboratory for analysis.</p>
<h2 class="test-h2 mt-3">What does a positive result mean?</h2>
<p>A positive result indicates that you have antibodies against Leptospira bacteria in your blood, suggesting a current
    or past infection.</p>
