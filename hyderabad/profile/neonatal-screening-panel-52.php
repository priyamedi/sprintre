<h2 class="test-h2 mt-3"> What is Tandem Mass Spectrometry?</h2>
	<p>
Tandem Mass Spectrometry (MS/MS) is a highly sensitive and specific technique used to identify and
quantify molecules in a sample. It works by breaking down molecules into smaller fragments and
analyzing their mass and charge. This technique can identify multiple metabolites simultaneously,
making it highly useful for neonatal screening panels. </p>
<h2 class="test-h2 mt-3"> Why does my newborn need this test?</h2>
<p>
Neonatal screening tests are designed to identify conditions that could lead to serious health problems if
not detected and treated early. While your baby might appear healthy, they could still have a metabolic
or genetic disorder that isn't immediately apparent. Early detection allows for timely treatment and can
significantly improve health outcomes. </p>
