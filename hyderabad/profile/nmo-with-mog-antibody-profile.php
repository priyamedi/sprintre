<h1 class="test-h1">Neuromyelitis Optica (NMO) with Myelin Oligodendrocyte Glycoprotein (MOG) Antibody Profile</h1>
<div class="post-text">
<p>Neuromyelitis Optica (NMO), also known as Devic's disease, is a rare neurological condition characterized by inflammation and damage to the optic nerve (optic neuritis) and the spinal cord (myelitis). An important aspect of NMO diagnosis involves testing for specific autoantibodies, like anti-aquaporin-4 (AQP4) antibodies and Myelin Oligodendrocyte Glycoprotein (MOG) antibodies.</p>
<p>The Myelin Oligodendrocyte Glycoprotein (MOG) Antibody Profile test measures the levels of MOG antibodies in the blood. MOG is a protein located on the surface of myelin sheaths in the central nervous system. Antibodies against MOG are found in a proportion of patients with NMO, as well as other demyelinating diseases like multiple sclerosis (MS).</p>
<p>This test is valuable in helping to distinguish NMO from conditions with similar clinical presentations such as multiple sclerosis (MS). It also provides important information for prognosis and choice of treatment strategies.</p>
