	<h2 class="test-h2 mt-3"> What is the purpose of the DVT-Deep Vein Thrombosis Panel?</h2>
	<p>The panel is used to assess the risk of developing DVT, diagnose DVT, and to identify potential
underlying causes of abnormal blood clotting, which could lead to DVT. </p>
	<h2 class="test-h2 mt-3">Why is Factor VIII included in this panel? </h2>
	<p> Factor VIII is a blood clotting factor. High levels of Factor VIII can increase the risk of excessive clotting,
hence it is tested to see if it is contributing to the risk of DVT.</p>
