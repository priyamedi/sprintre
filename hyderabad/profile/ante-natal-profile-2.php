<h2 class="test-h2 mt-3"> What is the Ante Natal Profile II?</h2>
	<p>
Ante Natal Profile II is a comprehensive set of tests conducted during pregnancy to monitor the health
of both the mother and baby. It includes the tests from Ante Natal Profile I along with Hemoglobin
Variant Analysis.</p>
<h2 class="test-h2 mt-3">What is the significance of Hemoglobin Variant Analysis in the Ante Natal Profile II?</h2>
<p>
Hemoglobin Variant Analysis is important to detect any genetic disorders related to hemoglobin, such as
thalassemia or sickle cell disease. These disorders can lead to complications during pregnancy and could
potentially affect the health of the baby. Early detection helps in better management of these
conditions.</p>
