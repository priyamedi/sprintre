<h2 class="test-h2 mt-3">When should I get the Flowcytometry - CD123 test done?</h2>
<p> Your doctor may recommend this test if they suspect a hematological disorder, especially leukemia or lymphoma. It is also commonly used in research studies.</p>
<h2 class="test-h2 mt-3">What does the Flowcytometry - CD123 test measure? </h2>
<p>This test measures the expression of CD123, a protein found on the surface of certain immune cells. High levels can suggest an abnormal proliferation of these cells, as seen in certain types of blood cancers.</p>
<h2 class="test-h2 mt-3">How often should this test be done? </h2>
<p>The frequency of the test depends on your specific medical condition. Your doctor will guide you based on your health status and treatment plan.</p>
<h2 class="test-h2 mt-3">What are the normal values for this test? </h2>
<p>Normal values may vary depending on the specific lab and method used for the test. Typically, a normal result is the absence of an abnormally high level of CD123-expressing cells.</p>
<h2 class="test-h2 mt-3">Which doctor should I consult if I have abnormal results?</h2>
<p> You should consult your doctor or a hematologist (a specialist in blood disorders) if your test results are abnormal.</p>
<h2 class="test-h2 mt-3">Can medications affect the test results? </h2>
<p>Yes, some medications may affect the test results. It is essential to inform your doctor about any medication you are currently taking.</p>
<h2 class="test-h2 mt-3">Is the test painful?</h2>
<p> The test involves a routine blood draw, which may cause minor discomfort at the injection site. Any discomfort is generally short-lived and manageable.</p>
<h2 class="test-h2 mt-3"> What precautions are needed after the test? </h2>
<p>There are no specific post-test precautions. However, it's important to keep the injection site clean to prevent infection.</p>
<h2 class="test-h2 mt-3"> Can the Flowcytometry - CD123 test help diagnose cancer? </h2>
<p>Yes, the test can aid in diagnosing certain types of blood cancers, like acute leukemia and blastic plasmacytoid dendritic cell neoplasm (BPDCN), which typically show a high level of CD123 expression.</p>
<h2 class="test-h2 mt-3"> Are there any modifiable factors that can affect CD123 levels? </h2>
<p>Certain medications may affect CD123 levels. However, it's best to discuss this with your doctor.</p>
<h2 class="test-h2 mt-3"> Are there any non-modifiable factors that can affect CD123 levels? </h2>
<p>Non-modifiable factors that can affect CD123 levels include genetic factors and certain diseases like leukemia and lymphoma.</p>
<h2 class="test-h2 mt-3"> What happens if I have high levels of CD123?</h2>
<p> High levels of CD123 may suggest the presence of certain blood cancers. Your doctor will interpret your test results in the context of your overall health and other test results.</p>
<h2 class="test-h2 mt-3"> Can this test be done at home?</h2>
<p> No, this test requires specialized equipment and trained professionals to perform and interpret the results. Therefore, it needs to be done in a medical lab or a healthcare facility.</p>
