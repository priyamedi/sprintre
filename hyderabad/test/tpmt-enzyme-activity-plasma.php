<p>This test measures the activity level of the Thiopurine Methyl Transferase enzyme in your plasma. It helps doctors
    understand how your body will metabolize certain medications and guide appropriate dosage.</p>
<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>A healthcare provider will draw a blood sample from a vein in your arm. This blood is then processed to separate the
    plasma, and the TPMT enzyme activity is measured.</p>
