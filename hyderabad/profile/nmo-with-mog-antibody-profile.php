<h2 class="test-h2 mt-3">Why is the MOG Antibody Profile test done? </h2>
<p>The MOG Antibody Profile test is done to aid in the diagnosis of Neuromyelitis Optica (NMO) and to distinguish it from other conditions with similar symptoms such as multiple sclerosis (MS).</p>

<h2 class="test-h2 mt-3">What are the symptoms of NMO? </h2>
<p>Symptoms of NMO include sudden loss of vision, weakness or paralysis of the limbs, loss of sensation, uncontrollable vomiting and hiccups, and problems with balance and coordination.</p>

