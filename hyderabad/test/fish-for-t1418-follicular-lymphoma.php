<h1 class="test-h1">FISH for t(14;18) - Follicular Lymphoma </h1>
<div class="post-text">
<p>Follicular lymphoma is one of the most common types of non-Hodgkin's lymphoma and is characterized by the slow-growing presence of cancerous cells in the lymph system. One of the defining features of follicular lymphoma is a chromosomal translocation between chromosomes 14 and 18, known as t(14;18). This translocation results in the juxtaposition of the immunoglobulin heavy chain gene (IGH) on chromosome 14 to the BCL2 gene on chromosome 18. As a consequence, there is an overexpression of the BCL2 protein which prevents the normal process of cell death, allowing the lymphoma cells to survive and accumulate. <br><br>
Fluorescence in situ hybridization, commonly known as FISH, is a test that is used to visualize and map the genetic material in an individual's cells, including the chromosomes. The FISH test for t(14;18) is particularly used in the diagnosis of follicular lymphoma, helping in not only confirming the disease but also in evaluating prognosis and guiding treatment decisions. </p>

