<p>The John Cunningham virus, or JC virus, is a type of human polyomavirus that is very common and harmless in most people. However, in individuals with compromised immune systems, such as those with HIV/AIDS or those undergoing immunosuppressive therapy following an organ transplant, the virus can reactivate and cause a serious brain disease known as progressive multifocal leukoencephalopathy (PML).</p>
