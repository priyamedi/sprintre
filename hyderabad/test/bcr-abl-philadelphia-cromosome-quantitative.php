	<h2 class="test-h2 mt-3">When is the BCR/ABL - Philadelphia Chromosome (IS - International Scale) Quantitative test ordered?
 </h2>
	<p>This test is typically ordered when CML is suspected based on symptoms, complete blood count results, and bone marrow examination. It is then repeated periodically to monitor the disease progression and response to treatment.
 </p>
	<h2 class="test-h2 mt-3">How does the BCR/ABL - Philadelphia Chromosome (IS - International Scale) Quantitative test work?
 </h2>
	<p>The test uses real-time quantitative PCR to amplify and detect the BCR-ABL1 fusion gene in a sample of the patient's blood or bone marrow. The amount of BCR-ABL1 gene present is compared to a control gene to calculate the ratio, which is then reported on an International Scale (IS).
 </p>
