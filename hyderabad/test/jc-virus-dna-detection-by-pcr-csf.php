<h2 class="test-h2 mt-3">What is Progressive Multifocal Leukoencephalopathy (PML)?</h2>
<p>PML is a rare and severe brain infection caused by the JC virus. It affects individuals with weakened immune systems,
    causing symptoms such as clumsiness, progressive weakness, and visual, speech, and sometimes personality changes.
</p>
<h2 class="test-h2 mt-3">How is the sample for this test collected?</h2>
<p>A healthcare professional collects the CSF sample for this test using a procedure called a lumbar puncture, or spinal
    tap, where a needle is inserted into the lower back to withdraw fluid from the spinal canal.</p>
