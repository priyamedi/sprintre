<h1 class="test-h1">Herpes Simplex Virus 1 (HSV-1) IgG - Serum</h1>
<div class="post-text">
<p>The Herpes Simplex Virus 1 (HSV-1) IgG - Serum test is a blood test that detects the presence of antibodies to the HSV-1 virus in the serum. It is primarily used to confirm a past or recent infection with HSV-1, often referred to as oral herpes because it typically causes cold sores and fever blisters around the mouth and on the face.</p>
<p>The body produces two types of antibodies in response to an HSV infection: IgM and IgG. IgM antibodies are the first line of defense and indicate a recent or active infection. IgG antibodies are produced later and persist in the body, providing long-term immunity. Therefore, the presence of IgG antibodies to HSV-1 in the blood typically indicates a past infection with HSV-1.
</p>

