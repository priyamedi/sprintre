	<h2 class="test-h2 mt-3">What are the common symptoms of cotton allergy?
 </h2>
	<p>Symptoms of cotton allergy can vary based on how you're exposed. They may include skin reactions like redness, hives, itching, or eczema when you wear cotton clothing. Inhaling cotton dust can cause respiratory symptoms like sneezing, runny nose, and asthma. Eating foods containing cottonseed oil might lead to symptoms like itching mouth, swelling of lips or tongue, nausea, and diarrhea.
 </p>
	<h2 class="test-h2 mt-3">How is cotton allergy diagnosed?
 </h2>
	<p>A doctor can diagnose a cotton allergy through a skin prick test, where small amounts of potential allergens are pricked into the skin and any reaction is observed. Blood tests that measure the amount of specific antibodies to cotton can also help diagnose a cotton allergy.
 </p>
