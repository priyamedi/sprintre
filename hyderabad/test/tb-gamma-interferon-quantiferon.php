<h2 class="test-h2 mt-3">What is a TB Gamma Interferon - Quantiferon test?</h2>
<p>A TB Gamma Interferon - Quantiferon test is a blood test that helps diagnose tuberculosis. It measures the immune
    response to TB bacteria in your blood.</p>
<h2 class="test-h2 mt-3">Who should get this test?</h2>
<p>This test is recommended if you have symptoms of tuberculosis, you have been in close contact with someone with
    active tuberculosis, or you have a medical condition that weakens your immune system. It is also used when
    traditional TB tests may give false results.</p>
