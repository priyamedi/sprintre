					<h2 class="test-h2 mt-3">What is the Cryoglobulin-Qualitative Test?</h2>
<p>The Cryoglobulin-Qualitative Test is a blood test that determines the presence or absence of cryoglobulins in the
    blood. Cryoglobulins are proteins that precipitate out of the blood serum at temperatures below 37°C.</p>
	<div class="mt-3"></div>
<h2 class="test-h2 mt-3">Why is the Cryoglobulin-Qualitative Test done?</h2>
<p>This test is done to diagnose cryoglobulinemia and to monitor and manage diseases associated with cryoglobulins
    such as hepatitis C, autoimmune disorders, and certain types of cancer.</p>

	



					
