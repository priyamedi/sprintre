<h2 class="test-h2 mt-3">Why is the IHC - MDM2 test done?</h2>
	<p>
The IHC - MDM2 test is used to detect the overexpression of the MDM2 protein, which can be indicative
of certain types of cancer. The test can aid in the diagnosis of these conditions and provide essential
information to guide treatment planning.</p>
<h2 class="test-h2 mt-3"> Is there any specific preparation required for the test?</h2>
<p>
No specific preparation is required for the IHC - MDM2 test.</p>
