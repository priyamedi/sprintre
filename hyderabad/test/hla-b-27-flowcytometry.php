<div class="post-text">
<p><strong>Human Leukocyte Antigen B27 (HLA-B27)</strong> is a protein located on the surface of your white blood cells. The HLA-B27 test identifies the presence of this specific protein. An HLA-B27 test is often used to support a diagnosis of ankylosing spondylitis, reactive arthritis, or other autoimmune conditions. The flow cytometry method is a powerful tool used to measure the physical and chemical characteristics of HLA-B27 protein in a population of cells. It involves the use of a beam of light, usually from a laser, which is directed onto a hydrodynamically-focused stream of fluid that carries a cell or particle suspension through it.
