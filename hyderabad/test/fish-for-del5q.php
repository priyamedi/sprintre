<p>Fluorescence In Situ Hybridization, commonly referred to as FISH, for del(5q) is a diagnostic test that
detects the deletion of a part of the long arm of chromosome 5. This deletion is associated with a variety
of hematological disorders, particularly Myelodysplastic Syndromes (MDS), which are a group of
disorders caused by poorly formed blood cells or ones that do not work properly. Recognizing the
presence of this deletion is vital for the accurate diagnosis and management of certain blood disorders,
and it has prognostic implications as well.
 </p>
<p>The deletion of the long arm of chromosome 5, del(5q), is known to be associated with specific subtypes
of MDS. FISH for del(5q) is a molecular cytogenetic technique that uses fluorescent probes to target and
bind to specific DNA sequences on chromosome 5. Under a fluorescence microscope, a trained
technician can determine whether or not there is a deletion in the long arm of chromosome 5. </p>
