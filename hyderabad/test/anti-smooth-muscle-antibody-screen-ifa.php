	<h2 class="test-h2 mt-3">What is the Anti-Smooth Muscle Antibody (ASMA) Screen?
 </h2>
	<p>The ASMA Screen is a blood test that uses the Immunofluorescence Assay (IFA) method to detect the presence of antibodies that target smooth muscle cells in the body. Elevated levels of ASMA are often associated with autoimmune hepatitis and other autoimmune disorders.
 </p>
	<h2 class="test-h2 mt-3">Why is the ASMA Screen important?
 </h2>
	<p>The ASMA Screen is crucial for the early detection and diagnosis of autoimmune hepatitis, a condition where the immune system attacks the liver. Early diagnosis can help in initiating timely treatment to control the disease and prevent complications such as cirrhosis or liver failure.
 </p>
