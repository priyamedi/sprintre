<h2 class="test-h2 mt-3">What is the purpose of lymphocyte subsetting?</h2>
<p>Lymphocyte subsetting helps to evaluate the proportions and numbers of different lymphocyte populations, such as B
    cells, T cells, and natural killer (NK) cells. It provides important information about the functioning of the immune
    system and can aid in diagnosing certain immunodeficiency disorders.</p>
<h2 class="test-h2 mt-3">How is the lymphocyte subsetting test performed?</h2>
<p>The lymphocyte subsetting test is performed on a blood sample. Specialized laboratory techniques are used to identify
    and quantify the different lymphocyte populations present in the sample.</p>
