	<p>AML is a form of cancer that begins in the bone marrow and results in rapid production of abnormal myeloblasts, a type of white blood cell. These abnormal cells interfere with the production of normal blood cells. </p>
	<h2 class="test-h2 mt-3">Why is the AML Cytogenetics 1 panel important? </h2>
	<p>This panel is essential for the proper diagnosis and categorization of AML. By detecting specific chromosomal abnormalities, the panel can help identify the subtype of AML, which is crucial for determining prognosis and guiding treatment decisions. </p>
