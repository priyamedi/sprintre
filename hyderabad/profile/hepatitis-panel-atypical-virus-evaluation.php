<h1 class="test-h1">Hepatitis Panel - Atypical Virus Evaluation (CMV IgM, HSV IgM, Varicella Zoster IgM, EBV(VCA) IgM)</h1>
<div class="post-text">
<p>The Hepatitis Panel - Atypical Virus Evaluation includes a series of tests designed to detect the presence of certain atypical viral infections that may cause hepatitis or liver inflammation. These tests include Cytomegalovirus (CMV) IgM, Herpes Simplex Virus (HSV) IgM, Varicella Zoster Virus (VZV) IgM, and Epstein-Barr Virus (EBV) VCA IgM.</p>
<p>Atypical viral hepatitis is a type of liver disease that is caused by viruses other than the traditional hepatitis viruses (A, B, C, D, E). Some of these atypical viruses include cytomegalovirus (CMV), herpes simplex virus (HSV), varicella-zoster virus (VZV), and Epstein-Barr virus (EBV).</p>
<p>The Hepatitis Panel - Atypical Virus Evaluation is a comprehensive test panel that evaluates the presence of antibodies against these atypical viruses in the bloodstream. These antibodies are proteins produced by the immune system in response to an infection.</p>

