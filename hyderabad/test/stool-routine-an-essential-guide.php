<h2 class="test-h2 mt-3">What is a routine stool test?</h2>
<p>A routine stool test, also known as stool analysis or fecal analysis, is a set of tests performed on a stool sample
	to check for abnormalities that might indicate certain illnesses or conditions.</p>
<h2 class="test-h2 mt-3">Why do I need a routine stool test?</h2>
<p>A routine stool test can help your doctor identify possible causes of abdominal pain, diarrhea, bloody stool, or
	unexpected weight loss. It can detect infections, digestive problems, or screen for colorectal cancer.</p>
