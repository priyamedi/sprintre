<h2 class="test-h2 mt-3">What is the FISH t(8;14) test, and why is it important?</h2>
<p>The FISH t(8;14) test is a specialized test that uses fluorescent probes to detect the translocation of
genetic material between chromosomes 8 and 14. This translocation is a hallmark of Burkitt's lymphoma
and is used in its diagnosis.</p>

<h2 class="test-h2 mt-3">How is the tissue sample for the FISH t(8;14) test collected?</h2>
<p>A tissue biopsy, typically from a lymph node or other affected tissues, is required for the FISH t(8;14)
test. The sample is collected by a doctor using a needle or through a surgical procedure.</p>
