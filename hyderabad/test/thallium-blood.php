<h2 class="test-h2 mt-3">Why is a Thallium Blood Test done?</h2>
<p>This test is generally conducted if you are showing symptoms of thallium poisoning such as abdominal pain, nausea,
	vomiting, diarrhea, hair loss, and nerve damage. It can also be useful for those who work in industries where
	thallium exposure is possible.</p>
<h2 class="test-h2 mt-3">Is there any risk associated with this test?</h2>
<p>This is a standard blood test, so the risks are minimal and similar to those of other blood draws. These include
	slight pain or bruising at the puncture site, lightheadedness, and, rarely, infection.</p>
