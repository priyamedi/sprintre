<h2 class="test-h2 mt-3">Why is the T4 Antibody test done?</h2>
<p>The T4 Antibody test is used when a patient has symptoms suggestive of a thyroid disorder and the doctor wants to
    rule out interference in T4 assays due to the presence of these antibodies. It can be part of a larger panel of
    tests used to diagnose and monitor thyroid disorders.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test involves a normal blood sample. A healthcare professional will collect blood from a vein in your arm using a
    small needle. After the needle is inserted, a small amount of blood is collected into a test tube or vial.</p>
