<h2 class="test-h2 mt-3">What is the Hepatitis A Virus (HAV) Panel?</h2>
<p>The HAV Panel is a combination of two tests: HAV IgM and HAV IgG. The IgM antibodies are the first to develop after exposure to the virus and indicate a recent infection. In contrast, IgG antibodies develop later and either indicate a past infection or that immunity has developed due to vaccination or previous infection.</p>
<h2 class="test-h2 mt-3">What are the symptoms of Hepatitis A?</h2>
<p>Symptoms of Hepatitis A can include fatigue, nausea, abdominal pain, loss of appetite, and jaundice. However, some people, especially children, may have no symptoms at all.</p>
