<h2 class="test-h2 mt-3">What is the Albumin - Serum test?</h2>
<p>The Albumin - Serum test measures the amount of albumin in your blood. Albumin is a protein made by your liver that helps keep fluid in your bloodstream, so it doesn't leak into other tissues.</p>
<h2 class="test-h2 mt-3">Why is the Albumin - Serum test done?</h2>
<p>This test is done to evaluate your nutritional status, to check liver function, or to assess and monitor the status of a kidney disease or intestinal protein loss.</p>
