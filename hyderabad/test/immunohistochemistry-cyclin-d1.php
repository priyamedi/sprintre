<h2 class="test-h2 mt-3">What does an IHC - Cyclin D1 test measure?</h2>
<p>This test detects the presence and amount of Cyclin D1 protein in tissue samples. Overexpression of Cyclin D1 can be
    indicative of certain types of cancer, such as mantle cell lymphoma.</p>
<h2 class="test-h2 mt-3">Why would I need an IHC - Cyclin D1 test?</h2>
<p>Your doctor may recommend this test if you have symptoms or other test results that suggest you may have a type of
    cancer that commonly overexpresses Cyclin D1. This test can help confirm a diagnosis and guide treatment decisions.
</p>
