<h2 class="test-h2 mt-3">What is a Streptococcus Group A antigen detection test?</h2>
<p>A Streptococcus Group A antigen detection test is a quick diagnostic tool used to identify Group A Streptococcus
	(GAS) bacteria, typically associated with throat infections like strep throat.</p>
<h2 class="test-h2 mt-3">Why do I need this test?</h2>
<p>You may need this test if you have symptoms of a strep throat infection, such as sore throat, fever, headache, and
	swollen lymph nodes. Early diagnosis helps in timely treatment and prevents complications.</p>
