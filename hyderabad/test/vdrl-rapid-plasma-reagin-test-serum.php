<h2 class="test-h2 mt-3">Why is the VDRL / RPR test ordered?</h2>
	<p>
The test may be ordered if you are showing symptoms of syphilis, as part of routine prenatal screening,
or if you are at high risk for the disease due to certain behaviors like unprotected sex, multiple sexual
partners, or illicit drug use.</p>
<h2 class="test-h2 mt-3">How is the VDRL / RPR test performed?</h2>
<p>
The test involves taking a blood sample from a vein in your arm. The serum portion of your blood is then
tested in a lab for antibodies that may indicate a syphilis infection.</p>
