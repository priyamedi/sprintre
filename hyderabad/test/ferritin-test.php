<h2 class="test-h2 mt-3">Why is the Ferritin Test done?</h2> 
<p>The Ferritin Test is done to check the iron levels stored in the body. It helps in diagnosing conditions such as iron deficiency anemia, inflammation, liver disease, chronic disease anemia, or hemochromatosis.</p>
<h2 class="test-h2 mt-3">What does a low ferritin level indicate?</h2> 
<p>A low ferritin level might indicate that a person has iron deficiency, which could lead to iron deficiency anemia if not addressed.</p>
