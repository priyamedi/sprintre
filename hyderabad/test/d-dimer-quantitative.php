<h1 class="test-h1">D-Dimer - Quantitative Test</h1>
<div class="post-text">
<p>D-Dimer - Quantitative is a blood test that measures the concentration of D-Dimer in the blood. D-Dimer is a small protein fragment that is present in the blood after a blood clot dissolves. Normally, the levels of D-Dimer are very low. However, when blood clots form and dissolve in the body, the levels of D-Dimer can significantly increase. This test is often used to help diagnose conditions such as deep vein thrombosis (DVT), pulmonary embolism (PE), or to rule out the presence of blood clots. It is also used in monitoring patients with certain blood disorders. </p>
<p>D-Dimer levels can be elevated in a number of conditions besides blood clot formation, including infection, inflammation, and certain types of cancer. Therefore, while an elevated D-Dimer level can indicate the presence of a clot, it is not definitive and must be considered alongside other diagnostic tests and clinical information.</p>
 
 
