<h1 class="test-h1">IHC - Cytokeratin 20 - CK20 </h1>
<div class="post-text">
<p>Immunohistochemistry (IHC) is a powerful diagnostic tool in pathology that uses antibodies to identify specific proteins in cells from a tissue section. One such application is the IHC for Cytokeratin 20 (CK20), which identifies the presence of CK20, a protein expressed in the cytoplasm of cells.</p>

<p>CK20 is a type of cytokeratin, a family of intermediate filament proteins that are a component of the cytoskeleton in epithelial cells. CK20 is normally present in the gastrointestinal epithelium, urothelium, and Merkel cells. An IHC-CK20 test is thus particularly useful in the diagnosis of cancers originating from these tissues, helping to differentiate them from other types of malignancies. It is often performed along with other IHC tests, such as CK7, to provide a more comprehensive understanding of the tumor's origin.</p>

