<h2 class="test-h2 mt-3">What is GLUT-1? </h2>
<p>Glucose Transporter 1 (GLUT-1) is a protein that helps transport glucose into cells. It is present in many tissues but plays a significant role in carrying glucose across the blood-brain barrier.</p>
<h2 class="test-h2 mt-3">Why is the IHC for GLUT-1 test done? </h2>
<p>The IHC for GLUT-1 test is performed to help diagnose various malignancies. It is also used to detect GLUT-1 deficiency syndrome, which affects the nervous system.</p>
