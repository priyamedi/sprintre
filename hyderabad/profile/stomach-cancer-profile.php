<h2 class="test-h2 mt-3">What is the Stomach Cancer Profile?</h2>
<p>The Stomach Cancer Profile is a set of blood tests that help in the detection and monitoring of stomach cancer. They
	measure levels of three tumor markers: CEA, CA19.9, and CA72.4.</p>
<h2 class="test-h2 mt-3">Why is the Stomach Cancer Profile done?</h2>
<p>This profile is typically ordered when a person has symptoms suggesting stomach cancer or when someone has been
	diagnosed with stomach cancer to monitor the effectiveness of treatment.</p>
