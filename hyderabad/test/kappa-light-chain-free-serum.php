<h2 class="test-h2 mt-3">Why is the Kappa Light Chain Free - Serum test done?</h2>
<p>The test is done to monitor conditions that cause an overproduction of free light chains, such as multiple myeloma, a
    type of bone marrow cancer. It may also be used to assess kidney function as the kidney filters these proteins out
    of the blood.</p>
<h2 class="test-h2 mt-3">What preparation is required for the test?</h2>
<p>No specific preparation is required for this test. No fasting or water restrictions are needed.</p>
