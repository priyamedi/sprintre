					<h2 class="test-h2 mt-3">What is the C-Reactive Protein (CRP) - Quantitative Test?</h2>
<p>The CRP - Quantitative Test is a blood test that measures the concentration of C-Reactive Protein, a marker of
    inflammation or infection in the body.</p>
	<div class="mt-3"></div>
<h2 class="test-h2 mt-3">Why is the CRP test performed?</h2>
<p>The CRP test is used to detect and monitor infection or inflammation in the body. It is also used in assessing the
    risk of heart disease and monitoring treatment effectiveness for inflammatory diseases.</p>

	



					
