	<h2 class="test-h2 mt-3">Why is Fertility Capsule I done?</h2>
 <p>This test is done to assess the levels of FSH, LH, and Prolactin, which are important in understanding reproductive health and fertility in both men and women.</p>
<h2 class="test-h2 mt-3">What does high or low FSH level indicate?</h2>
 <p>In women, a high FSH level may indicate a reduced ovarian reserve or menopause, while a low level may signal problems with ovulation. In men, high levels might indicate testicular dysfunction, while low levels might indicate issues with sperm production.</p>
