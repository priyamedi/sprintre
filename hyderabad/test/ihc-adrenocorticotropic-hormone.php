<h1 class="test-h1">IHC - Adrenocorticotropic Hormone (ACTH) </h1>
<div class="post-text">
<p>The <strong>Adrenocorticotropic Hormone (ACTH) immunohistochemistry (IHC) test</strong> is a type of diagnostic study that is performed on tissue samples to identify and measure the presence of ACTH. ACTH is a hormone produced and released by the pituitary gland, a small gland located at the base of the brain. This hormone plays a crucial role in regulating levels of the steroid hormone cortisol, released by the adrenal gland.
 </p>
<p>ACTH acts by stimulating the adrenal glands to produce and release cortisol. Cortisol is a vital hormone that plays diverse roles throughout the body, including managing how the body uses carbohydrates, fats, and proteins, suppressing inflammation, regulating blood pressure, increasing blood sugar, and controlling the sleep/wake cycle.
 </p>
