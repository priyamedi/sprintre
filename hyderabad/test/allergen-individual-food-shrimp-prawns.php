<h2 class="test-h2 mt-3">What symptoms can indicate a shrimp or prawn allergy?</h2>
<p>Symptoms of shrimp or prawn allergy can range from mild to severe and include hives, itching, eczema, shortness of
    breath, wheezing, coughing, stomach pain, nausea, vomiting, dizziness, or anaphylaxis in severe cases.</p>
<h2 class="test-h2 mt-3">What is the importance of this test?</h2>
<p>This test is important to confirm if someone has an allergy to shrimp or prawns. Knowing this helps in taking the
    necessary steps to avoid these foods and manage the allergy effectively.</p>
