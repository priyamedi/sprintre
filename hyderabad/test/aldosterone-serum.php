<h2 class="test-h2 mt-3">What is the Aldosterone Serum Test?</h2>
<p>The Aldosterone Serum Test is a blood test that measures the level of the hormone aldosterone in your blood.</p>
<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is typically done to check for adrenal gland disorders and to help determine the cause of high blood pressure.</p>
