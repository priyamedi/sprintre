<p>The AFB/TB Susceptibility: 10 Drug Panel test is a comprehensive evaluation to determine the sensitivity or resistance of the bacteria Mycobacterium tuberculosis, the causative organism of Tuberculosis (TB), to a panel of first and second-line anti-TB drugs. This test is crucial to formulate an effective treatment plan, especially in the cases of drug-resistant TB.
 </p>
