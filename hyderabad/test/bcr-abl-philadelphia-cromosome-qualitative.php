<h2 class="test-h2 mt-3">When is the BCR/ABL - Philadelphia Chromosome - Qualitative (Breakpoint Analysis) - PCR test ordered?</h2>
<p>This test is typically ordered when a healthcare provider suspects a diagnosis of CML or certain types of ALL based on symptoms, blood counts, and other laboratory findings.</p>
<h2 class="test-h2 mt-3">What does the test involve?</h2>
<p>The test involves the extraction of a sample of blood or bone marrow from the patient. The sample is then analyzed in the lab using PCR to detect the presence of the BCR-ABL fusion gene.</p>
