<h2 class="test-h2 mt-3">Why is the Thyroglobulin-Fluid test ordered?</h2>
<p>This test is often ordered when a doctor wants to investigate an unexplained growth, nodule or cyst, especially in
    the neck region. High levels of thyroglobulin in the aspirated fluid may suggest a thyroid origin of the fluid.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test involves collecting a sample of fluid from a cyst or other fluid-filled structure in the body. This is
    usually done using a fine needle aspiration procedure, where a thin, hollow needle is inserted into the cyst to
    withdraw some fluid.</p>
