<h2 class="test-h2 mt-3">What is the Anti-Aquaporin-4 / NMO Antibodies test?</h2>
<p> The Anti-Aquaporin-4 / NMO Antibodies test is a blood test that detects the presence of antibodies against the aquaporin-4 protein, which is found on cells in the brain and spinal cord. The presence of these antibodies is associated with Neuromyelitis Optica, an autoimmune disorder.</p>
<h2 class="test-h2 mt-3">What are the symptoms of Neuromyelitis Optica?</h2>
<p> Symptoms of Neuromyelitis Optica include vision loss, pain in the eyes, muscle weakness, impaired coordination, and, in severe cases, paralysis. These symptoms occur due to inflammation and damage to the optic nerves and spinal cord.</p>
