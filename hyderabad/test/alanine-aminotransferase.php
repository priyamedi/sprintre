<h1 class="test-h1">Alanine Aminotransferase - (ALT / SGPT) </h1>
<div class="post-text">
<p>Alanine aminotransferase (ALT), also known as serum glutamic-pyruvic transaminase (SGPT), is an enzyme found primarily in the liver and kidneys. The ALT test measures the amount of this enzyme in the blood, and it is used to determine if there is damage to the liver.
 </p>
<p>Liver damage releases ALT into the bloodstream, causing levels to rise. Therefore, an ALT test can help identify liver diseases like hepatitis or cirrhosis, or conditions like jaundice. It can also monitor the effectiveness of treatments for liver disease and measure the extent of damage caused by diseases such as alcohol-related liver disease.
 </p>
