<h2 class="test-h2 mt-3">What is Clinical Exome Sequencing?</h2>
<p>Clinical Exome Sequencing is a sophisticated genetic test that sequences and examines the exome – the portion of the genome that codes for proteins. This test can identify changes or mutations in genes associated with genetic disorders.</p>
<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is done to diagnose genetic disorders when other tests have been inconclusive, and to provide information for treatment and management of genetic conditions. It can also identify the genetic cause of a condition in a patient, allowing for precise diagnosis.</p>
