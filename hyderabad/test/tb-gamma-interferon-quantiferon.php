<p>Quantiferon, or Interferon Gamma Release Assays (IGRAs), is a blood test used to help diagnose tuberculosis (TB). It works by measuring the immune response to TB bacteria in the blood. When your body is exposed to TB bacteria, your immune cells release a substance called interferon-gamma. This test is often used when standard TB tests, like the skin test, could give false-positive results.</p>

