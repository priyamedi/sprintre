<p>The T3 - Free (Tri-iodothyronine-Free) test is a blood test that measures the level of Free T3, which is the unbound portion of triiodothyronine hormone circulating in the bloodstream. T3 is a thyroid hormone that plays a vital role in metabolism, energy production, and the regulation of body temperature. The free T3 test is particularly important because it can provide information about thyroid function without the influence of protein levels, which can affect the total T3 levels.</p>

<p>This test is often used to diagnose and monitor thyroid disorders, such as hyperthyroidism (an overactive thyroid) or hypothyroidism (an underactive thyroid). It is also an essential test for evaluating how well the thyroid gland is working and can be used in conjunction with other thyroid tests such as TSH (thyroid-stimulating hormone) and T4 (thyroxine).</p>

