<h2 class="test-h2 mt-3">What is dander?</h2>
<p>Dander is tiny particles of skin that had been shed from animals with fur or feathers. For people with
allergies, exposure to dander can lead to various allergic reactions, including sneezing, wheezing, and
itchiness.</p>
<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>These tests are performed to determine whether an individual has an allergy to certain animals. They
are done when a person has allergy-like symptoms after exposure to cats, dogs, horses, cows, or guinea
pigs.</p>
