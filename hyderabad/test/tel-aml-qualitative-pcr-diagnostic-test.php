<h1 class="test-h1">TEL/AML -Qualitative PCR Diagnostic Test </h1>
<div class="post-text">
<p>The TEL/AML - Qualitative PCR Diagnostic Test is a laboratory procedure used to detect the presence of a specific type of genetic rearrangement. This involves the fusion of two genes, the TEL (also known as ETV6) and the AML1 (also known as RUNX1). This particular genetic fusion is often associated with certain types of leukemia, including acute lymphoblastic leukemia (ALL) and acute myeloid leukemia (AML). </p>
<p>The presence of the TEL/AML fusion gene doesn't necessarily mean a person has leukemia, but it is a strong risk factor and is often used in conjunction with other tests to confirm a diagnosis. The test involves analyzing a sample of your blood using polymerase chain reaction (PCR), a technique that amplifies small amounts of DNA to make it easier to study. </p>
