viral infection that generally causes mild symptoms like a rash and fever. However, if contracted during
pregnancy, it can result in serious harm to the developing fetus, including congenital rubella syndrome.
 </p>
<p>This test is generally ordered when an individual displays symptoms of rubella or when a healthcare
provider wants to confirm a recent or ongoing rubella infection. The presence of Rubella IgM antibodies
generally indicates a recent or current infection. </p>
