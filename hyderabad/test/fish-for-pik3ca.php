<h2 class="test-h2 mt-3">Who should consider taking this test?</h2>
<p>Patients diagnosed with cancer, especially breast cancer, colorectal cancer, or lung cancer, may benefit from the
    FISH for PIK3CA test as it can provide information that helps in making treatment decisions.</p>
<h2 class="test-h2 mt-3">Is any preparation needed before the test?</h2>
<p>There is no special preparation needed for the patient. The test is performed on tissue that has been previously
    biopsied – usually as part of the diagnostic process.</p>
