<h2 class="test-h2 mt-3">What is the Coagulation Screen - Basic test?</h2>
<p>The Coagulation Screen - Basic test comprises the PT, INR, and aPTT tests, which assess how well your
blood is clotting. These tests can detect problems in the coagulation process, which could lead to
excessive bleeding or clotting.</p>

<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>This test is performed to investigate unexplained bleeding or bruising, to monitor individuals who are
taking blood-thinning medications, and to assess a patient's ability to form a blood clot before surgery.</p>
