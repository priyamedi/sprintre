<h2 class="test-h2 mt-3">Why is a 24-hour urine lead test done?</h2>
<p>The 24-hour urine lead test is done to assess chronic exposure to lead. It’s especially useful when blood lead levels
	are not conclusive or when the exposure is occupational or environmental.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The patient must collect all urine passed in a 24-hour period. This usually begins after the first urine of the day
	is discarded and continues with all subsequent specimens being collected including the first one of the next day.
</p>
