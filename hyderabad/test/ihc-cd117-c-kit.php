<h2 class="test-h2 mt-3">What is the IHC - CD117 (C-KIT) test used for? </h2>
<p>This test is used primarily to help diagnose certain types of cancers, such as GISTs and some leukemias. It can help to confirm a diagnosis or rule out other conditions.</p>
<h2 class="test-h2 mt-3">How is the test performed? </h2>
<p>The test is performed on a tissue sample, usually obtained through a biopsy. The sample is examined under a microscope to check for the presence and extent of the CD117 protein.</p>
