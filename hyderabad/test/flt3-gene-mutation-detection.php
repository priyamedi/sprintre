<p>Fms-like tyrosine kinase 3 (FLT3) gene mutation is one of the most common genetic alterations in Acute Myeloid Leukemia (AML), occurring in approximately one-third of patients. There are two main types of FLT3 mutations: internal tandem duplication (ITD) and point mutations in the tyrosine kinase domain (TKD), including D835Y. FLT3-ITD mutations are associated with high relapse rates and poor overall survival, whereas the prognosis related to FLT3-TKD mutations is less clear. Identifying these mutations is essential for prognosis and therapeutic decisions in AML.</p>
