	<h2 class="test-h2 mt-3">What is a chromosomal translocation? </h2>
	<p>A chromosomal translocation is a genetic abnormality where a part of one chromosome breaks off and attaches to a different chromosome. In the case of t(4;14) and t(14;16), parts of chromosomes 4 and 16 are translocated to chromosome 14. </p>
	<h2 class="test-h2 mt-3">How does FISH analysis work in Myeloma IgH Characterization? </h2>
	<p>FISH analysis uses fluorescent probes that specifically bind to the IgH gene regions on chromosome 14 and the corresponding regions on chromosomes 4 and 16. Under a fluorescence microscope, this binding can be visualized, and the presence of translocations can be detected. </p>
