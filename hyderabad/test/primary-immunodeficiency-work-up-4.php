    and assesses for Leukocyte Adhesion Deficiency.</p>
<h2 class="test-h2 mt-3">When should I get this test?</h2>
<p>You should get this test if you have recurrent, severe, or unusual infections, indicating a possible immunodeficiency
    disorder.</p>
<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>The frequency of this test is based on your individual health condition and advice from your healthcare provider.</p>
<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
