<h2 class="test-h2 mt-3">What is the Kappa Light Chain Quantitative, Urine Random test?</h2>
<p>This test measures the levels of kappa light chains in a random urine sample. Kappa light chains are components of
    antibodies, produced in excess by plasma cells, and usually filtered out by the kidneys.</p>
<h2 class="test-h2 mt-3">Why would my doctor order this test?</h2>
<p>Your doctor might order this test if they suspect you may have a condition that affects plasma cells and results in
    the overproduction of light chains, like multiple myeloma or certain types of lymphoma. It may also be ordered to
    monitor your response to treatment for these conditions.</p>
