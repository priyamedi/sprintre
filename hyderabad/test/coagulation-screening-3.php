<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is done to evaluate your blood's clotting ability. It's crucial for diagnosing blood clotting
disorders, managing anticoagulant therapy, and assessing bleeding risk before surgical procedures.</p>

<h2 class="test-h2 mt-3">Do I need to fast for this test?</h2>
<p>Fasting is usually not required for this test. However, always follow the specific instructions provided by
your doctor or the testing lab.</p>
