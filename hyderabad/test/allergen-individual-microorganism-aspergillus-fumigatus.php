<h2 class="test-h2 mt-3">What are the common symptoms of an allergy to Aspergillus fumigatus? </h2>
<p>Common symptoms include coughing, wheezing, shortness of breath, nasal congestion, sneezing, and in severe cases, fever, and chest pain.</p>
<h2 class="test-h2 mt-3">How is an allergy to Aspergillus fumigatus diagnosed? </h2>
<p>An allergy to Aspergillus fumigatus is diagnosed through a blood test that measures the level of specific antibodies to this fungus. Sometimes, skin prick tests may also be used.</p>
