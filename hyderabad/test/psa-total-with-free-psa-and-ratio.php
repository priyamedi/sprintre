<h2 class="test-h2 mt-3"> What is the purpose of the total PSA, free-PSA, and ratio test?</h2>
	<p>
This test helps detect prostate cancer at an early stage and assists doctors in differentiating between
cancerous and non-cancerous conditions of the prostate.</p>
<h2 class="test-h2 mt-3"> What do my test results mean?</h2>
<p>
A higher total PSA level along with a lower free to total PSA ratio may indicate a higher risk of prostate
cancer. However, these results alone are not diagnostic and further tests may be required.</p>
