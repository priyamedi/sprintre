<h2 class="test-h2 mt-3"> What is an Ante Natal Profile I? </h2>
	<p>
The Ante Natal Profile I is a series of tests conducted during pregnancy to monitor the health of the
mother and baby. It aids in identifying conditions that could potentially affect the mother or the child.</p>
<h2 class="test-h2 mt-3"> Why is the Ante Natal Profile I necessary? </h2>
<p>
These tests are essential to ensure the well-being of both mother and baby. They can detect conditions
such as hepatitis C, HIV, anemia, thyroid disorders, kidney issues, and gestational diabetes early,
enabling timely treatment and management.</p>
