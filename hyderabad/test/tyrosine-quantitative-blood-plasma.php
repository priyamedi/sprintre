<h2 class="test-h2 mt-3">What is Tyrosine?</h2>
<p>Tyrosine is a non-essential amino acid, which means it can be made by the body. It is a precursor to substances like
    neurotransmitters and hormones, playing a vital role in brain function and other bodily processes.</p>
<h2 class="test-h2 mt-3">Why might my doctor order a Tyrosine Quantitative Blood Plasma test?</h2>
<p>Your doctor might order this test if they suspect a metabolic disorder, a nutritional deficiency, or a problem with
    your liver. It can also be used to monitor treatment for these conditions.</p>
