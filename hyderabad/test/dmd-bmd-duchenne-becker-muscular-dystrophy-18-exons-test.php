<h2 class="test-h2 mt-3">What is the DMD/BMD - 18 Exons Test?</h2>
<p>The DMD/BMD - 18 Exons Test is a genetic test that analyzes 18 exons of the DMD gene to detect mutations responsible
    for Duchenne and Becker Muscular Dystrophy.</p>
<h2 class="test-h2 mt-3">Why is this test important?</h2>
<p>This test is crucial for diagnosing Duchenne or Becker Muscular Dystrophy, determining carrier status in female
    relatives, and making informed decisions during pregnancy if there is an increased risk for DMD or BMD.</p>
