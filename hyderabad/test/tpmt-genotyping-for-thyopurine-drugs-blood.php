<h2 class="test-h2 mt-3">What is TPMT Genotyping?</h2>
<p>TPMT Genotyping is a genetic test that identifies specific variations in the TPMT gene that can affect how your body
    metabolizes thiopurine medications.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test is performed on a blood sample that is drawn from a vein in your arm. This sample is then sent to a
    laboratory where it is examined for specific genetic variations in the TPMT gene.</p>
