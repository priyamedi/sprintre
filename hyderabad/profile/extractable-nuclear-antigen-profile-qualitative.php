	<h2 class="test-h2 mt-3">What is the ENA profile test used for? </h2>
	<p>The ENA profile test is used to help diagnose and monitor autoimmune diseases. It is often used when a
patient has unexplained symptoms such as fatigue, joint pain, and muscle weakness that may be
indicative of an autoimmune disorder. </p>
	<h2 class="test-h2 mt-3">How is the ENA profile test performed? </h2>
	<p>The test is performed using a blood sample. A healthcare professional will draw blood from a vein,
usually in your arm. The blood sample is then sent to a laboratory for analysis. </p>
