<h2 class="test-h2 mt-3">What is the Hepatitis B Envelope Antigen test?</h2>
<p>The Hepatitis B Envelope Antigen test is a blood test that detects the presence of HBeAg. It helps in assessing the infectivity and understanding the phase of Hepatitis B infection.</p>
<h2 class="test-h2 mt-3">Why is the HBeAg test important?</h2>
<p>The HBeAg test is important because its presence is usually associated with high levels of HBV in the blood. This indicates that the infected person is highly contagious and can easily spread the virus to others.</p>
