<div class="post-text">
<p>Lung cancer, specifically non-small cell lung cancer (NSCLC), often involves various genetic changes that contribute to the development and progression of the disease. Identification of these changes is critical for determining the most effective treatment approach, as some therapies target specific mutations. The Lung Cancer Panel 1 Complete Molecular is a comprehensive test designed to detect several of these genetic alterations, providing valuable information to guide treatment decisions.
 </p>
<p>The panel uses Amplification Refractory Mutation System (ARMS) PCR for EGFR mutation detection, one of the most common genetic changes in lung adenocarcinoma, a subtype of NSCLC. For the identification of ALK, ROS1, RET gene rearrangements, and MET gene amplification, Fluorescence In Situ Hybridization (FISH) technique is used. These gene alterations are less common but are associated with specific subtypes of NSCLC and can be targeted by certain therapies.
 </p>
