	<h2 class="test-h2 mt-3">Why is the Allergen, Individual - Food Legume Mix (Fx32) test done?
 </h2>
	<p>This test is done to determine if an individual is allergic to legumes. It is usually ordered if someone has experienced symptoms of an allergic reaction after eating legumes, such as hives, itching, difficulty breathing, or digestive issues.
 </p>
	<h2 class="test-h2 mt-3">How is the test performed?
 </h2>
	<p>A healthcare professional will draw a blood sample from a vein in your arm. This blood sample will be sent to a laboratory where it will be tested for the presence of IgE antibodies to legumes.
 </p>
