<h1 class="test-h1">PNH (Paroxysmal Nocturnal Hemoglobinuria) RBC Screening - CD55 & CD 59</h1>
<div class="post-text">
<p>Paroxysmal Nocturnal Hemoglobinuria (PNH) is a rare and potentially life-threatening blood disorder that leads to the premature destruction of red blood cells, blood clots, and impaired bone marrow function. It is caused by a genetic mutation in a certain type of bone marrow stem cell.</p>
<p>The PNH RBC Screening - CD55 & CD59 is a test that screens red blood cells (RBCs) for the presence of specific proteins - CD55 and CD59. These proteins play a critical role in protecting red blood cells from being destroyed by the body's immune system. In PNH, these proteins are deficient or absent on some or all RBCs, leading to their premature destruction, a condition called hemolysis.</p>


