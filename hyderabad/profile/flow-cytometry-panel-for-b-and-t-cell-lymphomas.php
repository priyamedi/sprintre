<h2 class="test-h2 mt-3">What does this panel measure?</h2>
<p> This panel measures the presence and amount of specific cellular markers on lymphocytes in a lymph node sample. It helps to differentiate between B-cell and T-cell lymphomas and can identify the subtype of lymphoma.</p>

<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p> The frequency of testing depends on your doctor's advice, which is based on factors like your diagnosis, treatment response, and potential recurrence risk.</p>

<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
<p> Normal values can vary as the test measures various cellular markers. The results will be interpreted in the context of your overall health, clinical symptoms, and other test results.</p>

<h2 class="test-h2 mt-3">Are there any precautions to take before the test?</h2>
<p> There are no specific precautions. However, inform your doctor about any medications or supplements you're taking, as they might influence the test results.</p>

<h2 class="test-h2 mt-3">What factors can affect the test results?</h2>
<p> Factors affecting test results can include certain medications, underlying health conditions, and the method of sample collection. Some of these are modifiable, like medication use, while others, like genetic factors, are not.</p>

<h2 class="test-h2 mt-3">Whom should I consult if my test results are abnormal? </h2>
<p>If your test results are abnormal, consult a hematologist or an oncologist. They can provide further guidance based on your specific health status and test results.</p>

<h2 class="test-h2 mt-3">Are there any risks associated with this test? </h2>
<p>The test involves a lymph node biopsy, which carries some risks, such as infection or bleeding at the biopsy site. However, serious complications are rare.</p>

<h2 class="test-h2 mt-3">Does an abnormal result indicate I have lymphoma? </h2>
<p>An abnormal result does not necessarily mean you have lymphoma. The results should be interpreted in conjunction with your symptoms, clinical history, and other diagnostic tests by your doctor.</p>

<h2 class="test-h2 mt-3">What treatment options are available if I'm diagnosed with lymphoma?</h2>
<p> Treatments for lymphoma vary depending on the type and stage of lymphoma, as well as your overall health. Common treatments include chemotherapy, radiation therapy, targeted therapy, and sometimes stem cell transplant.</p>

<h2 class="test-h2 mt-3">How can I prevent lymphomas? </h2>
<p>While there are no guaranteed ways to prevent lymphomas, certain lifestyle choices, such as maintaining a healthy weight, regular exercise, a balanced diet, and avoiding tobacco and excessive alcohol use, may reduce overall cancer risk.</p>

<h2 class="test-h2 mt-3">Can lifestyle changes affect the outcome of lymphomas? </h2>
<p>A healthy lifestyle can support your overall health, boost your immune system, and enhance your body's response to lymphoma treatment.</p>

<h2 class="test-h2 mt-3">Can I take this test during pregnancy? </h2>
<p>Consult your doctor about the necessity and timing of this test if you are pregnant. Some elements of the test, such as a lymph node biopsy, might be postponed until after delivery if possible.</p>

<h2 class="test-h2 mt-3">Does this test involve radiation exposure? </h2>
<p>No, flow cytometry does not involve radiation exposure. The test uses light and electronic sensors to measure cell properties.</p>

