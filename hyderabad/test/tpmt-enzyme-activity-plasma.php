<h2 class="test-h2 mt-3">Can I perform this test at home?</h2>
<p>No, this test requires a blood draw, which must be performed by a healthcare professional in a clinical setting.</p>
<h2 class="test-h2 mt-3">Can I take this test while I'm on medication?</h2>
<p>Yes, you can take this test while on medication. However, some medicines might interfere with the test results. It is
