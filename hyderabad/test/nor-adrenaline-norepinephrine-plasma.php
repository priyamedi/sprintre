<h2 class="test-h2 mt-3">Why is the Nor-Adrenaline Plasma Test done?</h2>
<p>The test is primarily used to diagnose conditions that cause an overproduction of adrenaline and noradrenaline,
    including certain types of tumors (pheochromocytoma, neuroblastoma, paraganglioma), and adrenal gland disorders. It
    can also help in the evaluation of persistent hypertension.</p>
<h2 class="test-h2 mt-3">What is the significance of noradrenaline?</h2>
<p>Noradrenaline is a hormone and neurotransmitter instrumental in the body's 'fight or flight' response to stress. It
    stimulates the heart rate, constricts blood vessels, and raises blood glucose levels, preparing the body for action.
</p>
