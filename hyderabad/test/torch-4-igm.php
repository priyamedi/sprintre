							<h2 class="test-h2 mt-3">When should I get tested?</h2>
							<p> The test is usually recommended in early pregnancy, but your doctor might recommend it
								at any stage of pregnancy if there is a suspicion of infection.</p>

							<h2 class="test-h2 mt-3">What does the test measure? </h2>
							<p>The test measures the level of IgM antibodies to the TORCH pathogens in the blood, which
								indicates a recent infection with these pathogens.</p>

							<h2 class="test-h2 mt-3">What do the results mean? </h2>
							<p>A positive result indicates a recent infection with one of the TORCH pathogens. A
								negative result suggests no recent infection.</p>

							<h2 class="test-h2 mt-3">What happens if the test comes back positive?</h2>
							<p> If the test is positive, your doctor will guide you through the next steps, which may
