<h2 class="test-h2 mt-3">Why is the Selenium, Urine Spot test done?</h2>
<p>The test is done to check the levels of selenium in your body. It helps in detecting deficiencies, which could lead
    to health problems like heart disease, infertility, cognitive decline, and weak immune system. Conversely, it can
    also detect selenium toxicity resulting from excessive intake.</p>
<h2 class="test-h2 mt-3">Do I need to fast before this test?</h2>
<p>No, fasting is not necessary before the Selenium, Urine Spot test. You can follow your regular diet and drink water
    as usual.</p>

