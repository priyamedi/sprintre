	<h2 class="test-h2 mt-3">Why is the ENA profile ordered? </h2>
	<p>The ENA profile is ordered when a doctor suspects an autoimmune disorder based on a patient’s symptoms. It helps in diagnosing conditions like lupus, Sjögren’s syndrome, and scleroderma. It can also be used to monitor disease activity and response to treatment. </p>
	<h2 class="test-h2 mt-3">How is the sample for the ENA profile collected? </h2>
	<p>A blood sample is collected from a vein in the arm using a needle. The procedure is quick and relatively painless. </p>
