<h2 class="test-h2 mt-3">What is the Chyle - Qualitative Detection test?</h2>
<p>This test checks for the presence of chyle, a type of lymphatic fluid, in a body fluid sample. It helps in identifying lymphatic system abnormalities and thoracic duct injuries.</p>
<h2 class="test-h2 mt-3">Why might I need this test?</h2>
<p>You might need this test if you have symptoms such as abdominal pain, chest discomfort, or difficulty breathing that suggest a problem with your lymphatic system or thoracic duct.</p>
