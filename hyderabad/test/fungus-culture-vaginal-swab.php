<h2 class="test-h2 mt-3"> Why is the Fungus Culture [VAGINAL SWAB] test done?</h2>
<p>This test is usually done when a woman has symptoms of a vaginal infection, such as itching, burning, abnormal discharge, discomfort during sex, or redness and swelling of the vulva.</p>

<h2 class="test-h2 mt-3"> How is the vaginal swab collected?</h2>
<p>The vaginal swab is collected by a healthcare professional during a pelvic examination. The professional will insert a speculum into your vagina to hold the vaginal walls apart, then use a sterile swab to collect a sample from your vaginal wall and/or cervix.</p>

