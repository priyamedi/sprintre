<h1 class="test-h1">MTB Plus- (AFB Smear + GeneXpert (Pulmo)+ AFB/TB Culture) Test </h1>
<div class="post-text">
<p>MTB Plus is a comprehensive test that uses three different techniques - AFB Smear, GeneXpert, and AFB/TB Culture - to diagnose tuberculosis (TB). Tuberculosis is an infectious disease caused by the bacterium Mycobacterium tuberculosis (MTB). This test is performed on a sputum sample - a type of thick mucus produced in the lungs and in the airways leading to the lungs. </p>
<p>The Acid Fast Bacilli (AFB) smear is a quick test to detect mycobacteria, including the Mycobacterium tuberculosis bacteria, in the sputum. The GeneXpert test is a molecular test that uses a sample of sputum to detect the DNA of Mycobacterium tuberculosis and can also determine if the bacteria are resistant to rifampicin, one of the first-line drugs used to treat TB. AFB/TB culture is a more time-consuming test but it is the most accurate way to diagnose TB and can also identify drug resistance. </p>
