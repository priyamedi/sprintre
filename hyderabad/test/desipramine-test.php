<h2 class="test-h2 mt-3">What does a high level of Desipramine in the blood indicate?</h2>
<p>A high level of desipramine in the blood could indicate that the dosage of the medication is too high, which may
    increase the risk of side effects and toxicity. Symptoms of desipramine toxicity can include irregular heartbeat,
