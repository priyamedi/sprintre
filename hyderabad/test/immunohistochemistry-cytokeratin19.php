<h1 class="test-h1">IHC - Cytokeratin 19 (CK19)</h1>
<div class="post-text">
<p>Immunohistochemistry (IHC) is a diagnostic laboratory procedure used in the detection of different types of cancer. One such IHC test is for Cytokeratin 19 (CK19), a type of keratin, which are proteins that provide structure to epithelial cells. Cytokeratins are typically found in epithelial cells, which line the body's surfaces, both inside and out. They play an integral role in cellular functions like cell structure and movement, and the presence and pattern of cytokeratins can give valuable information about the health and type of a cell.</p>
<p>CK19, in particular, is an intermediate filament protein, a component of the cytoskeleton in epithelial cells. It's often used as a marker to detect certain types of cancer, as it's usually overexpressed in carcinoma cells. The IHC - CK19 test is typically performed on a tissue sample, which can be obtained through a biopsy or surgery, and the presence or absence of CK19 can help identify the presence and type of carcinoma.
</p>

