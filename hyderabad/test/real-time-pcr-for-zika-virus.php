<h1 class="test-h1">Real Time PCR for Zika Virus</h1>
<div class="post-text">
<p>The Real Time PCR for Zika Virus is a test used to detect the Zika virus in a person's blood. This diagnostic tool uses the method of Real Time Polymerase Chain Reaction (PCR) that amplifies the genetic material of the virus making it detectable.</p>
<p>Zika virus is a mosquito-borne flavivirus closely related to the dengue, yellow fever, and West Nile viruses. It was first identified in the Zika forest of Uganda in 1947. Its mild symptoms and limited geographical spread kept it from being recognized as a significant health threat until the major outbreak in Brazil in 2015 that linked the virus to a spike in birth defects.</p>
<p>Though the majority of individuals infected with the Zika virus do not experience symptoms, those that do often report fever, rash, joint pain, and conjunctivitis. More severe complications can include Guillain-Barre syndrome and congenital abnormalities in infants born to infected mothers.</p>
