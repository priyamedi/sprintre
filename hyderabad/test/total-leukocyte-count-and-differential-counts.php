<h2 class="test-h2 mt-3">What is the purpose of the Total Leukocyte Count and Differential Counts test?</h2>
<p>The Total Leukocyte Count and Differential Counts test provides information about the number and type of white blood
    cells in your blood. This information can help your healthcare provider diagnose various conditions, such as
    infections, allergies, and diseases of the blood or immune system.</p>
<h2 class="test-h2 mt-3">How is the sample collected for this test?</h2>
<p>A healthcare provider collects the sample for this test through a standard blood draw, usually from a vein on the
    inside of your elbow.</p>
