	<div class="mt-3"></div>
<h2 class="test-h2 mt-3">Why is the CPK - Isoenzymes Electrophoresis Test performed?</h2>
<p>This test is performed to diagnose muscle diseases, to determine if there is muscle damage after a heart attack,
    or to monitor muscle damage in other conditions.</p>

	



					
