<h2 class="test-h2 mt-3"> Why is a Beta-HCG quantitative test done during pregnancy?</h2>
	<p>
The Beta-HCG quantitative test is performed to confirm pregnancy, estimate the age of the fetus, and
assess the health of the pregnancy. It can also be used to detect an ectopic pregnancy or to monitor a
woman after a miscarriage to ensure that all tissue has been expelled.</p>
<h2 class="test-h2 mt-3"> How is the Beta-HCG test performed?</h2>
<p>
The Beta-HCG test is performed using a blood sample, which is drawn from a vein in the arm. The blood
sample is then analyzed in a laboratory to measure the levels of Beta-HCG.</p>
