<h2 class="test-h2 mt-3"> Why is the Metabolic Disorder Panel - Urine performed?</h2>
	<p> 
This test is performed to detect metabolic disorders. It's generally recommended when a person has
symptoms of a metabolic disorder or has a family history of such conditions.</p>
<h2 class="test-h2 mt-3"> How is this test performed?</h2>
<p> 
This test is performed on a urine sample. The sample is collected in a clean, sterile container, then sent
to the laboratory for analysis.</p>
