<h2 class="test-h2 mt-3">What is the Anti-Aquaporin-4 / NMO Antibodies test in CSF? </h2>
<p>This test detects the presence of antibodies against the aquaporin-4 protein in the cerebrospinal fluid, which is the fluid surrounding the brain and spinal cord. The presence of these antibodies in the CSF is associated with Neuromyelitis Optica.</p>
<h2 class="test-h2 mt-3">How is the CSF sample collected?</h2>
<p> The CSF sample is collected through a lumbar puncture (spinal tap) procedure, where a needle is inserted into the lower back to collect a small amount of cerebrospinal fluid.</p>
