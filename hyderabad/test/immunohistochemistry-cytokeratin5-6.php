<h1 class="test-h1">IHC - Cytokeratin 5/6 - CK5/6 </h1>
<div class="post-text">
<p>Cytokeratin 5/6 (CK5/6) are types of intermediate filaments found in the intracytoplasmic cytoskeleton of epithelial tissue, which add strength and stability to the cells. They are usually found in the basal cells of the epidermis and are hence also known as basal cell markers.</p>

<p>The Immunohistochemistry - Cytokeratin 5/6 (IHC - CK5/6) test is an immunohistochemical analysis used to detect the presence of CK5/6 in tissue samples. The presence of CK5/6 can help identify certain types of tumors, especially those originating from squamous cells, such as squamous cell carcinomas. Conversely, lack of CK5/6 expression might suggest a non-squamous origin for the tumor.</p>

