<h2 class="test-h2 mt-3">What is a conventional fungal culture used for?</h2>
<p> A conventional fungal culture is used to identify the fungus that may be causing an infection. This information is essential for determining the most effective treatment.</p>
<h2 class="test-h2 mt-3">What type of sample is used for a fungal culture?</h2>
<p> Depending on the suspected location of the infection, a variety of samples can be used for a fungal culture, including body fluids, skin scrapings, nail clippings, hair, sputum, or tissue.</p>
