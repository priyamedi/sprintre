<h2 class="test-h2 mt-3">What is a Transferrin Saturation Index (TSI) test?</h2>
<p>A TSI test is a blood test that calculates the percentage of transferrin, a protein that transports iron in the
    blood, which is saturated with iron.</p>
<h2 class="test-h2 mt-3">Why might my doctor order a TSI test?</h2>
<p>Your doctor might order a TSI test if you show symptoms of anemia or iron overload. These symptoms might include
    fatigue, weakness, joint pain, or abdominal pain.</p>
