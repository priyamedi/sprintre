<h2 class="test-h2 mt-3">What is a 24-Hour Uric Acid Urine Test?</h2>
<p>A 24-Hour Uric Acid Urine Test measures the amount of uric acid that is excreted in the urine over a 24-hour period.
</p>
<h2 class="test-h2 mt-3">Why is the 24-Hour Uric Acid Urine Test done?</h2>
<p>The test is used to determine if your body is producing too much uric acid or not excreting enough of it. It helps in
	diagnosing gout and kidney disorders, and monitoring individuals undergoing chemotherapy or radiation treatment.</p>
