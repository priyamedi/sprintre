<h2 class="test-h2 mt-3">What is a Kidney Stone Analysis?</h2>
<p>A Kidney Stone Analysis, especially one conducted by Fourier-Transform Infrared Spectroscopy (FTIR), is a laboratory
	test that determines the composition of a kidney stone. The findings can help your doctor determine the cause of the
	stone and guide the treatment strategy.</p>
<h2 class="test-h2 mt-3">Why is a Kidney Stone Analysis necessary?</h2>
<p>This test is necessary to identify the composition of kidney stones. The results can help guide dietary
	modifications, lifestyle changes, and medical treatments to prevent future stone formation.</p>
