	<h2 class="test-h2 mt-3">What is the Allergen, Individual - Tree Pollen White Pine test?
 </h2>
	<p>This test identifies the presence of specific antibodies called Immunoglobulin E (IgE) in your blood. These antibodies are produced by your body in response to allergens, in this case, White Pine pollen. A high level of IgE indicates an allergic response. </p>
	<h2 class="test-h2 mt-3">Do I need to fast before this test? </h2>
	<p>No fasting is required before this test. You can eat and drink as usual unless your doctor advises otherwise.
 </p>
