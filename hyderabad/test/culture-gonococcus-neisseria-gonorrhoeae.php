<h2 class="test-h2 mt-3">How is the sample collected?</h2>
<p>Depending on the suspected site of infection, a healthcare professional will take a swab from your
throat, cervix, rectum, or urethra. Alternatively, a urine sample may be collected.</p>
<h2 class="test-h2 mt-3">What does a positive result indicate?</h2>
<p>A positive result indicates that Neisseria gonorrhoeae bacteria are present, confirming a diagnosis of
gonorrhea.</p>
<h2 class="test-h2 mt-3">How is gonorrhea treated?</h2>
<p>Gonorrhea is usually treated with antibiotics. It is important to take all the prescribed medication and
to refrain from sexual activity until the infection is completely cleared to prevent spreading it to others.</p>
<h2 class="test-h2 mt-3">What are the symptoms of gonorrhea?</h2>
<p>Symptoms of gonorrhea can include pain or burning during urination, increased vaginal or penile
discharge, vaginal bleeding between periods, and anal discomfort. However, many people with
gonorrhea do not have noticeable symptoms.</p>

<h2 class="test-h2 mt-3">Can gonorrhea be prevented?</h2>
<p>Gonorrhea can be prevented by using condoms consistently and correctly, being in a mutually
monogamous relationship with a partner who does not have STIs, or by abstaining from sexual activity.</p>
<h2 class="test-h2 mt-3">Who should get tested for gonorrhea?</h2>
<p>Anyone who is sexually active and at risk for STIs should be tested for gonorrhea. This includes
individuals with multiple sexual partners, those who do not consistently use condoms, and those who
have been diagnosed with another STI.</p>
<h2 class="test-h2 mt-3">What complications can result from untreated gonorrhea?</h2>
<p>Untreated gonorrhea can lead to pelvic inflammatory disease in women, which can cause infertility
and ectopic pregnancy. In men, it can cause epididymitis, which can lead to infertility. It also increases
the risk of contracting HIV.</p>
<h2 class="test-h2 mt-3">Should my partner be tested as well?</h2>
<p>Yes, if you have been diagnosed with gonorrhea, it's important that all recent sexual partners are
informed and get tested as well.</p>
<h2 class="test-h2 mt-3">Can I get gonorrhea more than once?</h2>
<p>Yes, having had gonorrhea does not provide immunity against future infections. It is possible to get
the infection again if exposed to the bacteria.</p>
<h2 class="test-h2 mt-3">Which doctor should I consult if I am diagnosed with gonorrhea?</h2>
<p>If you are diagnosed with gonorrhea, it is important to consult a doctor who specializes in infectious
diseases or a gynecologist for women and a urologist for men.</p>

<p>Gonorrhea is a common sexually transmitted infection that can have serious consequences if left
untreated. It is important for individuals who are at risk to be regularly tested for gonorrhea and other
STIs. Early detection and treatment are key to preventing complications and the spread of the infection
to others. Always practice safe sex and communicate openly with your doctor and sexual partners about
your sexual health.</p>
