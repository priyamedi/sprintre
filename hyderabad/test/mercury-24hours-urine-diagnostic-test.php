<h2 class="test-h2 mt-3"> What is the purpose of the Mercury - 24 Hours Urine test? </h2>
	<p>
The Mercury - 24 Hours Urine test is used to measure the level of mercury in the body. It is usually
ordered when a person has symptoms suggestive of acute or chronic mercury exposure, such as
neurological changes, mood swings, memory loss, or skin rash.</p>
<h2 class="test-h2 mt-3"> Do I need to fast before this test? </h2>
<p>
No fasting is required for the Mercury - 24 Hours Urine test. You can follow your regular diet and
activities.</p>
