<h2 class="test-h2 mt-3">Why would I need a Thyroid Profile - II test?</h2>
<p>A Thyroid Profile - II test is typically ordered when symptoms of a thyroid disorder are present, such as abnormal
    weight changes, feelings of extreme tiredness, heart palpitations, or variations in skin and hair quality.</p>
<h2 class="test-h2 mt-3">How is this test carried out?</h2>
<p>The test requires a small blood sample, usually drawn from a vein in your arm.</p>
