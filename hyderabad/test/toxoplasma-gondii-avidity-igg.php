<p>This test is performed to help determine the timing of a Toxoplasma gondii infection. It can indicate whether an
    infection is recent or occurred in the past, which is particularly important for pregnant women.</p>
<h2 class="test-h2 mt-3">How is the blood sample collected for this test?</h2>
<p>A healthcare provider will collect a blood sample from a vein in your arm using a small needle.</p>
