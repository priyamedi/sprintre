<h2 class="test-h2 mt-3">What does this test measure?</h2>
<p>This test measures the sensitivity of the Mycobacterium tuberculosis to Capreomycin. It determines if Capreomycin
    would be effective in treating the TB infection.</p>
<h2 class="test-h2 mt-3">How often should I take this test?</h2>
<p>The frequency of this test will be determined by your healthcare provider, usually when there is a confirmed
    diagnosis of TB and potential drug resistance.</p>
<h2 class="test-h2 mt-3">What is the normal value for this test?</h2>
<p>A normal result would show that M.TB is sensitive to Capreomycin, suggesting that this antibiotic could be
    effectively used to treat the infection.</p>
<h2 class="test-h2 mt-3">What precautions should I take?</h2>
<p>This test should be performed under the guidance of a healthcare provider. The sample collection process needs to be
