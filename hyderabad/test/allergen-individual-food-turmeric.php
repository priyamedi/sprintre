	<h2 class="test-h2 mt-3">What are the common symptoms of a turmeric allergy?
 </h2>
	<p>Common symptoms include itching, hives, redness, difficulty breathing, runny nose, and in severe cases, anaphylaxis. Gastrointestinal symptoms such as nausea, diarrhea, or stomach pain might also occur.
 </p>
	<h2 class="test-h2 mt-3">Can turmeric cause skin reactions?
 </h2>
	<p>Yes, in some individuals, turmeric can cause contact dermatitis, where the skin becomes inflamed and itchy after coming in contact with turmeric.
 </p>
