<h2 class="test-h2 mt-3">What is a Uric Acid Test?</h2>
<p>The Uric Acid Test is a blood test that measures the amount of uric acid present in your blood.</p>
<h2 class="test-h2 mt-3">Why is the Uric Acid Test performed?</h2>
<p>This test is typically ordered to help diagnose gout, kidney stones, or kidney disorders. It may also be ordered to
	monitor uric acid levels when undergoing chemotherapy or radiation treatment.</p>
