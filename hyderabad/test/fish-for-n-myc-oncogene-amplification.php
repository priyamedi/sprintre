<h1 class="test-h1">FISH for N-myc Oncogene Amplification </h1>
<div class="post-text">
<p>Fluorescence in situ hybridization (FISH) for N-myc oncogene amplification is a molecular diagnostic test employed to detect the amplification of the N-myc oncogene in cells. The N-myc oncogene is a gene that can contribute to the transformation of a cell into a cancer cell when it is over-expressed or amplified. Amplification of the N-myc oncogene is associated with several types of cancer, most notably neuroblastoma.</p>

<p>The N-myc oncogene belongs to the Myc family of genes, which are known to be involved in the regulation of cell proliferation, differentiation, and apoptosis. Amplification of N-myc is an abnormality in which there are multiple copies of the gene. This is often associated with an aggressive tumor phenotype, and has been shown to have prognostic significance especially in neuroblastoma.</p>
