<h1 class="test-h1">IHC - For confirming Metastatic tumor of Breast origin [ER, PR, GCDFP-15, Mammoglobin] </h1>
<div class="post-text">
<p>Immunohistochemistry (IHC) is an essential laboratory technique used in the diagnosis and management of cancer. It involves the use of antibodies to detect specific proteins in tissue samples, thus providing information about the type of cancer and its characteristics. For breast cancer, especially metastatic breast cancer where the cancer has spread to other parts of the body, the IHC plays a crucial role in confirming the origin of the tumor.
 </p>
<p>One of the challenges in cancer treatment is identifying the primary site of cancer when it has metastasized. Identifying whether a metastatic tumor is of breast origin is important for determining the most appropriate course of treatment. In this context, IHC for ER (Estrogen Receptor), PR (Progesterone Receptor), GCDFP-15 (Gross Cystic Disease Fluid Protein-15), and Mammaglobin is performed.
 </p>
 <p>ER and PR are receptors found in some breast cancer cells. If the cancer cells have one or both of these receptors, they may be fueled by the hormones estrogen and/or progesterone. GCDFP-15 and Mammaglobin are proteins that are generally only present in breast cells, so their presence can strongly suggest that the tumor is of breast origin.
</p>
