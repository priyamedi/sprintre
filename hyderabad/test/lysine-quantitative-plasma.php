<h2 class="test-h2 mt-3">What is a Lysine - Quantitative Plasma test?</h2>
<p>A Lysine - Quantitative Plasma test is a blood test that measures the amount of lysine in your
blood. It helps to assess your body's protein nutritional status and detect any deficiencies or
excesses of this essential amino acid.</p>
<h2 class="test-h2 mt-3">Why would I need a Lysine - Quantitative Plasma test?</h2>
<p>You might need this test if your doctor suspects that you may have a deficiency or excess of
lysine, which can occur due to certain diet or metabolic conditions. Symptoms of lysine
deficiency include fatigue, nausea, dizziness, loss of appetite, agitation, bloodshot eyes, slow
growth, anemia, and reproductive disorders.</p>
