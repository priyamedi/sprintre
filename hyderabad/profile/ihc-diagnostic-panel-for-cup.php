<h1 class="test-h1">IHC - diagnostic panel; Metastases from unknown primary-customised panels of antibodies from the list based on morphology of the tumour </h1>
<div class="post-text">
<p>Immunohistochemistry (IHC) has played an essential role in the categorization and identification of different tumor types in clinical oncology. When a patient presents with metastatic cancer, but the primary site remains elusive despite a thorough examination, these cases are referred to as cancers of unknown primary (CUP). To tackle this diagnostic challenge, an IHC diagnostic panel, customized from a list of antibodies based on the morphology of the tumor, can be employed. This panel utilizes five markers tailored to the patient's individual case, helping identify the primary tumor type, which is vital for appropriate treatment planning.</p>

<p>An essential premise of the IHC diagnostic panel is its adaptability, the capacity to choose the most relevant markers based on the tumor's histological and morphological features. The chosen markers target specific proteins that are frequently expressed in certain types of cancer. The expression of these markers, or lack thereof, provides valuable insights into the possible origin of the metastatic cells. By interpreting these patterns, the pathologist can suggest a probable primary site, leading to a more targeted approach in managing the patient's condition.</p>

<p>However, it is crucial to note that no IHC panel, no matter how comprehensive, can replace the importance of a thorough clinical evaluation, radiological investigations, and a detailed pathology review. The IHC panel for CUP should be viewed as an adjunct tool that helps in the diagnostic process, particularly in challenging cases where conventional methods have not provided sufficient information.</p>
