<h2 class="test-h2 mt-3">Why do I need a Lipoprotein a (Lp-a) test?</h2>
<p>If you have a family history of early heart disease or if you have high cholesterol that doesn't respond to diet,
    exercise, or cholesterol-lowering medications, your doctor might recommend an Lp-a test.</p>
<h2 class="test-h2 mt-3">How is the Lp-a test performed?</h2>
<p>The test requires a standard blood draw, typically from a vein in your arm.</p>
