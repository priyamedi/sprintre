	<h2 class="test-h2 mt-3"> What is a CBC with ESR?</h2>
	<p>A CBC with ESR is a blood test that measures the count of different types of cells in the blood and the
rate at which red blood cells settle at the bottom of a test tube. This information can help your doctor
diagnose and monitor many different health conditions. </p>
	<h2 class="test-h2 mt-3"> Why is this test performed?</h2>
	<p> This test is often used to evaluate your overall health and detect a variety of disorders, such as anemia,
