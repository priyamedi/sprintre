<h2 class="test-h2 mt-3">When should this test be done?</h2>
<p>The test is typically ordered when a patient has a soft tissue mass, and a biopsy is conducted. The IHC panel aids in
    characterizing the tumor.</p>
<h2 class="test-h2 mt-3">What information does this test provide?</h2>
<p>This test provides information about the specific proteins present in the tissue sample, helping to identify the cell
    type from which the tumor originated and distinguish between different sarcoma subtypes.</p>
<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>This test is typically conducted once, during the diagnostic process. However, it may be repeated if the patient has
    a recurrence or a new tumor.</p>
<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
<p>The 'normal' values for this test are not straightforward because it is not a quantitative test. It is about whether
    certain markers are present (positive) or absent (negative), helping to indicate the type of sarcoma.</p>
<h2 class="test-h2 mt-3">What precautions should be taken before and after this test?</h2>
<p>No specific precautions are required before or after this test.</p>
<h2 class="test-h2 mt-3">What factors can affect the levels of these markers?</h2>
<p>The presence or absence of these markers is inherent to the cells from which the sarcoma originated. It's not
    affected by lifestyle or environmental factors, but by the inherent nature of the tumor.</p>
<h2 class="test-h2 mt-3">Which doctor should be consulted in case of abnormal values?</h2>
<p>In case of abnormal or positive results, consultation with an oncologist or a cancer specialist is recommended.</p>
<h2 class="test-h2 mt-3">What other tests might be performed in conjunction with this IHC panel?</h2>
<p>Other tests that might be performed include imaging studies like CT scan or MRI, blood tests, and possibly a
    molecular or genetic test depending on the type of sarcoma suspected.</p>
<h2 class="test-h2 mt-3">Does the test cause any discomfort or pain?</h2>
<p>There's usually no discomfort associated with the test itself, but there might be discomfort or pain from the biopsy
    procedure to get the tissue sample.</p>
<h2 class="test-h2 mt-3">What if my test results are negative for all markers?</h2>
<p>If all markers are negative, it might suggest that the mass is not a sarcoma, or it is a type of sarcoma not covered
    by these markers. Your doctor will interpret the results based on your symptoms, medical history, and other tests.
</p>
<h2 class="test-h2 mt-3">Can this test be used to monitor the effectiveness of treatment?</h2>
<p>No, this test is mainly used for diagnosis and classification of sarcomas. Other tests would be used to monitor
    treatment response.</p>
<h2 class="test-h2 mt-3">Can this test be done during pregnancy?</h2>
<p>Yes, if a pregnant woman has a suspicious mass, a biopsy and this test can be done. However, any risks related to the
    biopsy procedure would be discussed with the patient.</p>
<h2 class="test-h2 mt-3">What is the next step after the IHC sarcoma panel test?</h2>
<p>The next step after this test would depend on the results. If a specific type of sarcoma is identified, your doctor
    will discuss the treatment options with you.</p>
<h2 class="test-h2 mt-3">Are there any risks involved in this test?</h2>
<p>This test itself doesn't pose any risk to the patient. The risks involved are more related to the biopsy procedure to
    collect the tissue sample. These could include bleeding, infection, or pain at the biopsy site. Your doctor will
    explain all potential risks before the procedure.</p>
<h2 class="test-h2 mt-3">Can a sarcoma be diagnosed without this test?</h2>
<p>Yes, it is possible to diagnose a sarcoma without this test; however, the IHC Sarcoma Panel provides more specific
    information that can help to better classify the sarcoma and guide treatment options. Therefore, it is generally
    recommended in the diagnostic process.</p>
<h2 class="test-h2 mt-3">What are the modifiable factors affecting the test?</h2>
<p>There are no modifiable factors that affect the IHC Sarcoma Panel as the markers used in the test are inherent
    characteristics of the sarcoma cells.</p>
<h2 class="test-h2 mt-3">What does a positive test result mean?</h2>
<p>A positive test result means that the tissue sample has tested positive for one or more of the markers included in
    the panel, which helps to identify the type of sarcoma.</p>
<h2 class="test-h2 mt-3">Can the test results be affected by medications I'm taking?</h2>
<p>The test results should not be affected by medications. However, you should always inform your doctor of any
    medications you are taking.</p>
<h2 class="test-h2 mt-3">Can I undergo this test if I have a known allergy to any dyes or substances used in the
    process?</h2>
<p>Most likely, yes, but it's important to discuss any known allergies with your healthcare provider before the test.
</p>
<h2 class="test-h2 mt-3">What happens if the test results are inconclusive?</h2>
<p>If the test results are inconclusive, additional testing may be recommended. This could include more specific IHC
    tests, molecular testing, or a repeat biopsy.</p>
 
