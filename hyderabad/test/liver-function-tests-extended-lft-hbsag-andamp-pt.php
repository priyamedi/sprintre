	<h2 class="test-h2 mt-3">Why is the Extended Liver Function Test ordered?  </h2>
	<p>The test is ordered when a patient exhibits symptoms indicative of a liver disease or has a history of conditions known to impact the liver, including jaundice, abdominal pain, fatigue, and swelling.
 </p>
	<h2 class="test-h2 mt-3">What is the Hepatitis B surface antigen test?  </h2>
	<p>The Hepatitis B surface antigen (HBsAg) test is a blood test that identifies the presence of HBsAg, a protein found on the surface of the hepatitis B virus (HBV). If positive, it signifies an active hepatitis B infection.
 </p>
