<h2 class="test-h2 mt-3">How is the FISH Test for t(11;18) performed?</h2>
<p>The test uses a blood or tissue sample. The sample is treated with fluorescent probes specific for the BIRC3 and MALT1 genes. The probes bind to these genes if present, and their fluorescence can be detected under a microscope.</p>
