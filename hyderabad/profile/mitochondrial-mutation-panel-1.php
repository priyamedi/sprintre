<h2 class="test-h2 mt-3">What is MELAS, MERRF, MM, MMC, ME, PEM, EI?</h2>
<p>These abbreviations represent various types of mitochondrial diseases that result from specific genetic mutations.
    They include MELAS (Mitochondrial Encephalopathy, Lactic Acidosis, and Stroke-like episodes), MERRF (Myoclonic
    Epilepsy with Ragged Red Fibers), MM (Mitochondrial Myopathy), MMC (Mitochondrial Myopathy, Cardiomyopathy), ME
    (Mitochondrial Encephalopathy), PEM (Progressive External Ophthalmoplegia), and EI (Exercise Intolerance).</p>
<h2 class="test-h2 mt-3">What does this test involve?</h2>
<p>The test involves a simple blood draw. The blood sample is then sent to a laboratory, where advanced genetic testing
    techniques are used to screen the mitochondrial DNA for specific mutations related to the diseases covered by this
    panel.</p>
