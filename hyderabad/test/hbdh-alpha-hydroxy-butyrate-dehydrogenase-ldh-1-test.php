<h1 class="test-h1">HBDH - Alpha Hydroxy Butyrate Dehydrogenase (LDH-1) Test </h1>
<div class="post-text">
<p>Alpha Hydroxy Butyrate Dehydrogenase (HBDH), also known as lactate dehydrogenase-1 (LDH-1), is an enzyme predominantly found in the heart and red blood cells. It is one of the five isoenzymes of lactate dehydrogenase (LDH), a critical enzyme that helps convert glucose into energy.
 </p>
<p>HBDH (LDH-1) test is performed to measure the amount of this particular isoenzyme in the blood. It is a specific marker for myocardial injury and is used in the diagnosis and management of acute myocardial infarction (heart attack). It can also provide useful information in conditions affecting the red blood cells and kidneys.
 </p>
