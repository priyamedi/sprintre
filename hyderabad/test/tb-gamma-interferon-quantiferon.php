<h2 class="test-h2 mt-3">Why is this test important?</h2>
<p>The TB Gamma Interferon - Quantiferon test can help detect latent or active TB infection. Early detection of TB is
    crucial to prevent its spread and to start treatment as soon as possible.</p>
<h2 class="test-h2 mt-3">What does the test involve?</h2>
<p>The test involves taking a blood sample, which will then be sent to a laboratory for analysis.</p>
<h2 class="test-h2 mt-3">How is this test different from the TB skin test?</h2>
<p>Unlike the TB skin test, which involves injecting a substance into the skin and checking for a reaction 48 to 72
    hours later, the Quantiferon test requires only one patient visit and gives results within 24 to 48 hours. It's also
    less likely to give false-positive results due to BCG vaccination.</p>
<h2 class="test-h2 mt-3">Do I need to fast before this test?</h2>
<p>No, fasting is not required for this test.</p>
<h2 class="test-h2 mt-3">What do the results mean?</h2>
<p>A positive result suggests that you have been infected with the TB bacteria. It does not tell whether the infection
    is latent or active. Additional tests will be needed to confirm active TB disease. A negative result generally means
    you are not infected.</p>
<h2 class="test-h2 mt-3">What factors can affect the test results?</h2>
<p>Recent infection with TB, very old TB infection, or a very low amount of TB bacteria in the body may cause a
    false-negative result. Also, some diseases can cause a false-positive result.</p>
<h2 class="test-h2 mt-3">Can this test be done at home?</h2>
<p>No, this test requires a blood sample to be drawn by a healthcare professional, and the analysis must be done in a
    laboratory.</p>
<h2 class="test-h2 mt-3">What should I do if I get a positive result?</h2>
<p>If you get a positive result, your doctor will likely order more tests to determine if you have latent or active TB
    disease. It's crucial to follow your doctor's instructions to prevent the spread of the disease and start treatment.
</p>
<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>This test should be done if you have symptoms suggestive of TB, or if you've been in contact with someone with TB.
    Your doctor will advise on the frequency depending on your health and risk factors.</p>
<h2 class="test-h2 mt-3">Does a positive test mean I have tuberculosis?</h2>
<p>A positive result indicates that you've been infected with TB bacteria. However, it doesn't distinguish between
    latent TB infection and active TB disease. More tests will be needed to confirm the diagnosis.</p>
<h2 class="test-h2 mt-3">Can this test detect drug-resistant TB?</h2>
<p>No, the Quantiferon test can't detect drug-resistant TB. If you are diagnosed with TB, further tests will be required
    to determine if the TB bacteria are resistant to any antibiotics.</p>
<h2 class="test-h2 mt-3">Can the Quantiferon test replace the TB skin test?</h2>
<p>The Quantiferon test and the TB skin test are both used to detect TB infection. However, the Quantiferon test may be
    more accurate and is less likely to give a false-positive result due to BCG vaccination.</p>
<h2 class="test-h2 mt-3">Is there a cure for TB?</h2>
<p>Yes, TB is curable with a course of antibiotics that usually lasts 6 to 9 months. The specific medications and length
    of treatment will depend on whether you have latent TB infection or active TB disease, your age and overall health,
    and whether the TB bacteria are resistant to any antibiotics.</p>
<h2 class="test-h2 mt-3">How can I prevent getting TB?</h2>
<p>Preventing TB involves avoiding close contact with people who have the disease, practicing good hygiene such as
    covering your mouth when you cough or sneeze, and getting vaccinated with the BCG vaccine, which can prevent severe
    TB in children.</p>
<h2 class="test-h2 mt-3">Can the Quantiferon test be used in children?</h2>
<p>Yes, the Quantiferon test can be used in children over the age of two years. However, it may not be as accurate in
    children under five years of age.</p>
<h2 class="test-h2 mt-3">Does BCG vaccination affect the Quantiferon test result?</h2>
<p>Unlike the TB skin test, BCG vaccination does not cause a false-positive result with the Quantiferon test.</p>
<h2 class="test-h2 mt-3">What is the cost of the Quantiferon test?</h2>
<p>The cost of the test may vary depending on the laboratory. Please contact the healthcare provider for the exact
    price.</p>
<h2 class="test-h2 mt-3">Where can I get the Quantiferon test done?</h2>
<p>The Quantiferon test can be done at hospitals, medical centers, and laboratories that provide diagnostic services.
    It's always best to consult with your healthcare provider for further information.</p>
