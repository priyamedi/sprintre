<div class="post-text">
<p>Measles, also known as rubeola, is a highly contagious viral disease that primarily affects children. The
disease is characterized by a high fever, cough, runny nose, conjunctivitis, and a distinctive rash. The
measles IgG antibodies test is a blood test that detects the presence of Immunoglobulin G (IgG)
antibodies to the measles virus. </p>
<p>The body produces two types of immunoglobulin in response to an infection: IgM and IgG. IgM is
produced first and disappears after a few weeks. IgG is produced later but lasts for a long time, often for
life, providing immunity against future infections. This test primarily determines whether a person has
immunity to measles, either from vaccination or a past infection. </p>
