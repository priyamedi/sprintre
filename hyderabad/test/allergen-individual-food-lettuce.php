<h2 class="test-h2 mt-3">What are the symptoms of a lettuce allergy?</h2>
<p>The symptoms of a lettuce allergy can include itching or tingling in the mouth, swelling of the lips, tongue, or throat, hives, nasal congestion, nausea, vomiting, diarrhea, and in severe cases, anaphylaxis, which is a life-threatening reaction that requires immediate medical attention.</p>
<h2 class="test-h2 mt-3">How is the Allergen, Individual - Food Lettuce test performed?</h2>
<p>The test is performed using a blood sample. A healthcare professional will draw blood from your arm, and the sample will be sent to a laboratory for analysis to detect the presence of specific IgE antibodies that react to lettuce.</p>
