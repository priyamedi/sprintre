<h2 class="test-h2 mt-3">Why is a TSH-ultrasensitive test ordered?</h2>
<p>The TSH-ultrasensitive test is ordered when a person has symptoms of a thyroid disorder or to monitor the
    effectiveness of treatment in individuals diagnosed with thyroid disease. It is also used to screen for thyroid
    disorders in newborns.</p>
<h2 class="test-h2 mt-3">What do the results of a TSH-ultrasensitive test mean?</h2>
<p>Elevated TSH levels may suggest an underactive thyroid (hypothyroidism), while decreased TSH levels may indicate an
    overactive thyroid (hyperthyroidism). The results should be interpreted in the context of your overall health and
    other diagnostic tests by a healthcare professional.</p>
