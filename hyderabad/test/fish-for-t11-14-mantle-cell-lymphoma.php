<h2 class="test-h2 mt-3">Why is the FISH for t(11;14) test done?</h2>
<p>FISH for t(11;14) is performed to confirm the diagnosis of Mantle Cell Lymphoma (MCL). It helps to differentiate MCL
    from other types of lymphoma, which is essential for determining the appropriate treatment.</p>
<h2 class="test-h2 mt-3">What does the presence of t(11;14) indicate?</h2>
<p>The presence of t(11;14) indicates a chromosomal abnormality that is characteristic of Mantle Cell Lymphoma. This
    translocation leads to the overexpression of the cyclin D1 gene, which is involved in cell cycle regulation, and
    contributes to the uncontrolled growth of lymphocytes.</p>
