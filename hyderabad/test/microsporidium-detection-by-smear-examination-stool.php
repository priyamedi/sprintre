<p>Typically, you can expect the test results within 1-3 days after the lab receives your sample.</p>
<h2 class="test-h2 mt-3">If my test result is negative, can I stop taking precautions?</h2>
<p>Even if your test result is negative, you should still practice good hygiene to prevent infections, including
    microsporidiosis.</p>
<h2 class="test-h2 mt-3">How reliable is this test for detecting microsporidia?</h2>
<p>The test is generally reliable, but its accuracy can depend on several factors, including the quality of the stool
