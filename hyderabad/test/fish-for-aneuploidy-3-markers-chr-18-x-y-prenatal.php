<h2 class="test-h2 mt-3">What does FISH for Aneuploidy – 3 Markers Chr 18, X, Y entail?</h2>
	<p>The test uses Fluorescence In Situ Hybridization to detect the number of chromosomes 18, X, and Y in
fetal cells, helping in identifying chromosomal disorders like Edwards syndrome and sex-linked
disorders.</p>

<h2 class="test-h2 mt-3"> Why is it necessary to perform this test during pregnancy?</h2>
<p>Early detection of chromosomal abnormalities assists parents and doctors in making informed decisions
regarding pregnancy management and preparation for the special care that might be needed for the
child.</p>

