<p>Modifiable factors affecting GH levels include lifestyle changes like diet, exercise, sleep patterns, and medication
    use.</p>
<h2 class="test-h2 mt-3">What are the non-modifiable factors affecting GH levels?</h2>
<p>Non-modifiable factors include age and genetic predisposition.</p>
