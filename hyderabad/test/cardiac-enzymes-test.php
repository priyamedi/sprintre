	<h2 class="test-h2 mt-3"> What is the purpose of the Cardiac Enzymes test?</h2>
	<p>The Cardiac Enzymes test helps to detect heart muscle damage. Elevated levels of these enzymes can
indicate a heart attack or other heart damage. </p>
	<h2 class="test-h2 mt-3">How is this test performed? </h2>
	<p>This test requires a blood sample, which is drawn from a vein in your arm by a healthcare professional. </p>
