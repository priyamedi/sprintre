	<h2 class="test-h2 mt-3">What is a stool culture and sensitivity test? </h2>
	<p>A stool culture and sensitivity test is a laboratory procedure where a stool sample is cultured to
identify any harmful bacteria present. The bacteria are then tested against different antibiotics to
determine which ones are effective in treating the infection </p>
	<h2 class="test-h2 mt-3">Why might a doctor order this test? </h2>
	<p>A doctor might order this test if you have symptoms of a gastrointestinal infection, such as diarrhea,
abdominal pain, fever, or blood in the stool. It helps in identifying the specific bacteria causing the
infection and in choosing the appropriate antibiotics for treatment </p>
