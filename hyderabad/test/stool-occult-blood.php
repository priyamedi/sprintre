<h2 class="test-h2 mt-3">What is a Fecal Occult Blood Test (FOBT)?</h2>
<p>The FOBT is a lab test used to check your stool (feces) for hidden blood. Occult blood in the stool may indicate
	colon polyps or colorectal cancer.</p>
<h2 class="test-h2 mt-3">Why is an FOBT necessary?</h2>
<p>An FOBT is often done to screen for early colon or rectal cancer. Hidden blood in your stool can be a sign of these
	diseases.</p>
