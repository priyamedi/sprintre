<h2 class="test-h2 mt-3">How is the sample collected?</h2>
<p>The sample is usually collected through a biopsy of the lung tissue. Your healthcare provider will give you specific
    instructions based on the biopsy procedure type.</p>
<h2 class="test-h2 mt-3">What do these markers mean?</h2>
<p>Each marker has specific significance. For instance, Napsin A and TTF1 are often positive in adenocarcinomas of lung
    origin. CK7 and CK20 can help differentiate lung cancer from other cancers. P63 and CK 5/6 are typically seen in
    squamous cell carcinoma. CD56, Synapto, and Chromogranin are markers for neuroendocrine tumors.</p>
<h2 class="test-h2 mt-3">How long does it take to get the results?</h2>
<p>The turnaround time is typically 5-7 days but may vary based on the laboratory.</p>
<h2 class="test-h2 mt-3">What preparation is needed for this test?</h2>
<p>There is no specific preparation required for this test. However, because a biopsy is involved, your healthcare
    provider will give you instructions based on your overall health and the specific biopsy procedure.</p>
<h2 class="test-h2 mt-3">When should one get this test done?</h2>
<p>This test is done when lung cancer is suspected based on clinical symptoms, imaging studies, and other preliminary
    tests.</p>
<h2 class="test-h2 mt-3">What information does this test provide?</h2>
<p>The IHC panel for lung cancer provides crucial information about the type and subtype of the lung cancer, which
    influences the treatment options and prognosis.</p>
<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>This test is typically performed once for diagnosis. However, if treatment is given and then symptoms reappear, the
    test may be repeated to assess for recurrence or metastasis.</p>
<h2 class="test-h2 mt-3">What are the normal values?</h2>
<p>In normal lung tissue, there is a specific pattern of marker expression. Significant deviation from this pattern,
    such as the increased expression of certain markers, can indicate lung cancer.</p>
<h2 class="test-h2 mt-3">What precautions must be taken?</h2>
<p>The primary precaution is related to the biopsy procedure, as it carries a risk of complications such as bleeding or
    infection. Your healthcare provider will discuss these risks with you and give you specific instructions to minimize
    them.</p>
<h2 class="test-h2 mt-3">What factors can affect these markers?</h2>
<p>Non-modifiable factors include genetic predisposition to lung cancer. Modifiable factors are limited, as these
    markers are specific to the cells and not influenced by environmental or lifestyle factors.</p>
<h2 class="test-h2 mt-3">Which doctor should be consulted in case of abnormal results?</h2>
<p>If results are abnormal, you should consult with your oncologist or pulmonologist. They can interpret the results and
    guide the next steps in your care.</p>
<h2 class="test-h2 mt-3">What other tests might be done along with this?</h2>
<p>Additional tests might include PET/CT scans, molecular testing for genetic mutations, and other blood tests to assess
    your overall health and organ function.</p>
<h2 class="test-h2 mt-3">How reliable are the results?</h2>
<p>The results are generally very reliable but should be interpreted in conjunction with your medical history, symptoms,
    and other diagnostic findings.</p>
<p>In conclusion, the IHC diagnostic panel for lung cancer is a vital tool in diagnosing and differentiating lung cancer
    subtypes. These findings can significantly influence the treatment course and potentially affect the prognosis. As
    with all medical procedures and tests, it is essential to discuss the results and implications thoroughly with your
    healthcare provider.</p>
