<p> This test is typically ordered when a healthcare provider suspects a patient may have alkaptonuria, particularly if the patient has dark-colored urine or a family history of the condition.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p> The test involves the collection of a urine sample. The sample will then be sent to a lab to be analyzed for the presence of homogentisic acid.</p>
