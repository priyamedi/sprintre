<h1 class="test-h1">IHC - CD45/LCA </h1>
<div class="post-text">
<p>The Immunohistochemistry (IHC) test for CD45, also known as the Leukocyte Common Antigen (LCA), is a specialized laboratory test that uses specific antibodies to identify certain proteins, called antigens, on cells from a tissue sample. This test is typically used in a pathology lab to help identify and classify lymphomas and leukemias.
 </p>
<p>The CD45 antigen is found on the surface of all white blood cells, including B cells, T cells, and granulocytes, making it a valuable diagnostic tool for diseases that involve these cells. The presence or absence of CD45 can provide crucial information for distinguishing between different types of lymphomas and leukemias, as well as other diseases.
 </p>
 <p>The IHC CD45 test is not a routine test but is performed when a doctor suspects a patient has a lymphoproliferative disorder, like lymphoma or leukemia. The sample required for the test is usually obtained from a biopsy, in which a small piece of tissue is surgically removed for examination. The exact procedure depends on the location of the tissue being tested.</p>
