<p>Venous Thrombosis Screen - PCR is a comprehensive blood test that investigates the presence of specific
gene mutations known to increase the risk of venous thromboembolism (VTE). Venous
thromboembolism is a condition where blood clots form in the veins, often in the deep veins of the leg
