	<h2 class="test-h2 mt-3">What are the symptoms of an allergy to sunflower seeds? 
 </h2>
	<p>Symptoms of a sunflower seed allergy can include itching, hives, swelling of the lips, tongue, or throat, difficulty breathing, stomach cramps, nausea, or vomiting. In severe cases, anaphylaxis, a life-threatening reaction, may occur.
 </p>
	<h2 class="test-h2 mt-3">How is an allergy to sunflower seeds diagnosed? </h2>
	<p>An allergy to sunflower seeds is diagnosed through a blood test that looks for specific antibodies (IgE) that indicate an allergic reaction to proteins in sunflower seeds. </p>
