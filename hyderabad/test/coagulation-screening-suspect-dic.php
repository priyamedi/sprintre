<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is conducted to diagnose and manage Disseminated Intravascular Coagulation, a severe
condition that results in blood clots throughout the body's small blood vessels. This condition can lead
to organ damage and severe bleeding.</p>

<h2 class="test-h2 mt-3">Do I need to fast for this test?</h2>
<p>There is no requirement to fast before this test. However, it is important to stay hydrated for the blood
draw.</p>
