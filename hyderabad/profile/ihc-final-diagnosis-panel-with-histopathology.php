	<h2 class="test-h2 mt-3">How is the test performed? </h2>
<p>The test is performed on a tissue sample, which is usually obtained through biopsy. The tissue is then treated with specific antibodies and examined under a microscope.</p>

	<h2 class="test-h2 mt-3">What does the test measure? </h2>
<p>The test measures the presence, location, and abundance of specific proteins in the tissue sample.</p>

	<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>The frequency of this test depends on the healthcare provider's recommendations, which are based on the patient's medical history and current health condition.</p>

	<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
<p>Normal values can vary depending on the specific antibodies used. In general, normal tissues will show expected patterns of protein distribution, while abnormal or diseased tissues may show different patterns.</p>

	<h2 class="test-h2 mt-3">What factors can affect the results? </h2>
<p>Several factors can affect the results, including the quality of the tissue sample, the technique used for the test, and the specific antibodies used.</p>

	<h2 class="test-h2 mt-3">Who should be consulted in case of abnormal results?</h2>
<p>In case of abnormal results, a pathologist or the treating physician who ordered the test should be consulted.</p>

	<h2 class="test-h2 mt-3">Is this test used only for diagnosing cancer? </h2>
<p>While this test is frequently used in diagnosing different types of cancers, it's also used for diagnosing other conditions such as autoimmune diseases and infectious diseases.</p>

	<h2 class="test-h2 mt-3">Is this test conclusive for diagnosis? </h2>
<p>While the results of this test provide valuable information, they should be interpreted in combination with other clinical and diagnostic findings for a conclusive diagnosis.</p>

	<h2 class="test-h2 mt-3">Does the test cause any discomfort or side effects? </h2>
<p>The discomfort associated with this test is usually related to the tissue sampling or biopsy procedure, not the IHC test itself.</p>

