<p> Calprotectin is a protein released by certain types of white blood cells when there is inflammation in the
body. It is found in high concentrations in the stools (feces) of individuals with gastrointestinal disorders
such as Crohn's disease and ulcerative colitis. The fecal calprotectin test is a non-invasive laboratory test
used to measure the amount of calprotectin in the stool.</p>
<p>This test helps doctors distinguish between inflammatory bowel diseases (IBD) like Crohn's disease and
ulcerative colitis, which cause inflammation in the intestines, and non-inflammatory bowel diseases like
irritable bowel syndrome (IBS). High levels of fecal calprotectin suggest an IBD, while normal levels
suggest IBS or other non-inflammatory conditions. </p>
