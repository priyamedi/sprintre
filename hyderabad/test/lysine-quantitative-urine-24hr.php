<h2 class="test-h2 mt-3">What is the purpose of this test? </h2>
<p>The Lysine - Quantitative Urine 24 Hr test is used to measure the amount of lysine your body is excreting over a 24-hour period. It can help your healthcare provider evaluate your nutritional status or diagnose certain metabolic disorders related to lysine.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p> This test requires a 24-hour urine sample. You will be given a container to collect your urine at home over a 24-hour period, which you will then return to the laboratory for analysis.</p>
