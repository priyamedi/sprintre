<p>Extractable Nuclear Antigen (ENA) refers to a group of antigens that are found in the nucleus of a cell.
The ENA profile is a group of blood tests that looks for specific antibodies against these antigens. This
test is primarily used to help diagnose and monitor autoimmune disorders such as lupus, Sjogren’s
syndrome, scleroderma, and mixed connective tissue disease. </p>
