<h1 class="test-h1">FISH for Aneuploidy - 2 Markers Chr 13, 21 (Prenatal) </h1>
<div class="post-text">
<p>FISH, standing for Fluorescence In Situ Hybridization, for aneuploidy involving 2 markers, chromosomes
13 and 21, is a prenatal test used to detect chromosomal abnormalities in unborn babies. Chromosomal
abnormalities such as Trisomy 13 (Patau syndrome) and Trisomy 21 (Down syndrome) are genetic
disorders in which an extra chromosome is present. Patau syndrome is associated with severe
intellectual disability and physical abnormalities, while Down syndrome is one of the most common
chromosome abnormalities in humans. FISH is a molecular cytogenetic technique that uses fluorescent
probes binding to only those parts of a nucleic acid sequence with a high degree of sequence
complementarity. It is used to detect and localize the presence or absence of specific DNA sequences on
chromosomes.</p>
