<h2 class="test-h2 mt-3">What are the symptoms of a mackerel allergy?</h2>
<p>Common symptoms of a mackerel allergy include itching or tingling in the mouth, hives, swelling of the lips, face, tongue, or throat, nasal congestion, difficulty breathing, stomach pain, nausea, vomiting, and diarrhea. In severe cases, anaphylaxis, a life-threatening allergic reaction, may occur.</p>
<h2 class="test-h2 mt-3">How is the Allergen, Individual - Food Mackerel test performed?</h2>
<p>A healthcare professional will draw a small sample of blood from your arm. This blood sample is then sent to a laboratory, where it will be analyzed for specific IgE antibodies that react to proteins in mackerel.</p>
