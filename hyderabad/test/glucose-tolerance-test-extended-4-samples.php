<h2 class="test-h2 mt-3">Why is the Glucose Tolerance Test (GTT) done?</h2>
<p>The GTT is primarily used to check for type 2 diabetes and gestational diabetes. It measures how quickly your body
    clears sugar from the bloodstream.</p>
<h2 class="test-h2 mt-3">How is the GTT performed?</h2>
<p>You will be asked to fast for 8 to 12 hours. Then, a blood sample is taken to measure your fasting glucose level. You
    will then drink a high-glucose solution, and additional blood samples will be taken at hourly intervals.</p>
