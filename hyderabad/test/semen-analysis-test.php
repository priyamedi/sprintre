<h2 class="test-h2 mt-3">Why is a Semen Analysis done?</h2>
<p>A semen analysis is primarily done to evaluate male fertility. It's also used to confirm the success of a vasectomy
    or reverse vasectomy, or to help diagnose disorders in the male reproductive system.</p>
<h2 class="test-h2 mt-3">Do I need to fast before this test?</h2>
<p>Fasting is not required for a semen analysis.</p>

