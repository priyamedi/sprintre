	<h2 class="test-h2 mt-3">When should I get this test done?</h2>
<p>Your doctor may recommend this test if you have been diagnosed with TB, and your condition is not improving despite treatment, or if you have been exposed to someone with drug-resistant TB.</p>

	<h2 class="test-h2 mt-3">What does this test measure?</h2>
<p>This test measures the susceptibility of Mycobacterium tuberculosis to streptomycin. It helps in identifying whether the TB bacteria are resistant or sensitive to this drug.</p>

	<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>The frequency of this test depends on the recommendations of your doctor, usually when drug-resistant TB is suspected.</p>

	<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
<p>A sensitive result indicates that the TB bacteria are likely to respond to treatment with streptomycin. A resistant result suggests that the bacteria are not likely to respond to this drug.</p>

	<h2 class="test-h2 mt-3">What precautions should I take?</h2>
<p>The main precaution is to provide an appropriate sample for the test. Follow the instructions provided by the healthcare facility or your doctor.</p>

	<h2 class="test-h2 mt-3">What factors can affect the levels?</h2>
<p>Factors that can affect the test results include improper collection of the sample, contamination of the sample, and technical issues with the testing process.</p>

	<h2 class="test-h2 mt-3">What are the modifiable and non-modifiable factors?</h2>
<p>Modifiable factors include the method of sample collection and handling. Non-modifiable factors include the biological properties of the TB bacteria, such as natural genetic mutation leading to drug resistance.</p>

	<h2 class="test-h2 mt-3">Which doctor should I consult in case of abnormal values?</h2>
<p>If your test results indicate streptomycin resistance, you should consult a specialist in infectious diseases or a pulmonologist for further treatment.</p>

	<h2 class="test-h2 mt-3">Is there any specific preparation required for this test?</h2>
<p>No specific preparation is required. You should follow the instructions given by your healthcare professional for sample collection.</p>

	<h2 class="test-h2 mt-3">Can the test results vary in different labs?</h2>
<p>The test should be highly standardized, but slight differences may occur due to technical variances between labs.</p>

	<h2 class="test-h2 mt-3">Does this test pose any risk?</h2>
<p>The test itself poses no risk, but sample collection might cause minor discomfort.</p>

	<h2 class="test-h2 mt-3">Can I eat and drink normally before the test?</h2>
<p>Yes, you can eat and drink normally before the test.</p>

	<h2 class="test-h2 mt-3">Can streptomycin-resistant TB be treated?</h2>
<p>Yes, streptomycin-resistant TB can be treated. However, it requires different combinations of anti-TB drugs and a longer duration of treatment.</p>

<p>This test plays a critical role in managing tuberculosis effectively, especially in areas where drug-resistant TB is prevalent. Identifying the resistance profile of the TB bacteria helps in customizing the treatment plan, thereby aiding in the patient's recovery and preventing the spread of drug-resistant TB. Prompt and accurate results from this test can significantly impact the treatment outcome and prognosis of the disease.</p>

