<h2 class="test-h2 mt-3">What is a Chromosomal Array - Peripheral Blood test?</h2>
<p>A Chromosomal Array - Peripheral Blood test is a genetic test performed on a blood sample. It is designed to detect small imbalances in the chromosomes that could be linked to genetic disorders.</p>
<h2 class="test-h2 mt-3">Why would I need a Chromosomal Array test on Peripheral Blood?</h2>
<p>This test is typically recommended if there are concerns about developmental delays, intellectual disabilities, or physical abnormalities that can't be explained by standard genetic tests.</p>
