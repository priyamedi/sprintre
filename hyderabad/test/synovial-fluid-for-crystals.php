<h2 class="test-h2 mt-3">What is a Synovial Fluid for Crystals test?</h2>
<p>It is a laboratory test that identifies the presence and type of crystals in the synovial (joint) fluid.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test is performed using a sample of synovial fluid obtained from the joint through a procedure called
    arthrocentesis or joint aspiration.</p>
