<p>Alpha-1-Antitrypsin (AAT) is a protein produced by the liver and is essential in protecting the lungs from
the harmful effects of enzymes released during inflammation. In cases where AAT levels are low or the
protein is dysfunctional, an individual is at risk of developing chronic obstructive pulmonary disease
(COPD) and liver diseases. The deficiency or dysfunction of AAT is caused by mutations in the SERPINA1
gene and is known as Alpha-1-Antitrypsin Deficiency (AATD). Detecting levels of AAT and diagnosing
AATD is important for managing lung and liver diseases and developing a suitable treatment plan. </p>
