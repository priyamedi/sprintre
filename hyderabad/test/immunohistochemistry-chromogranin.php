<h2 class="test-h2 mt-3">What is an IHC - Chromogranin test?</h2>
<p>The IHC - Chromogranin test is a diagnostic test that uses immunohistochemistry to detect the presence of the
    Chromogranin A protein in tissue samples. It is often used to identify and monitor neuroendocrine tumors.</p>
<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>This test is performed when a neuroendocrine tumor is suspected. The presence of Chromogranin A is a strong indicator
    of such tumors.</p>
