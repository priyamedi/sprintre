<h2 class="test-h2 mt-3">What is a c-myc Breakapart- FISH test?</h2>
<p>The c-myc Breakapart- FISH (Fluorescence In Situ Hybridization) test is a molecular diagnostic test that
can identify changes or rearrangements in the c-myc gene. These changes can cause uncontrolled cell
growth and lead to cancer.</p>

<h2 class="test-h2 mt-3">Why might I need this test?</h2>
<p>Your doctor may order this test if you have symptoms suggestive of cancer, such as persistent fatigue,
unexplained weight loss, and changes in the color or size of moles. The test can help confirm a diagnosis
of cancer and determine the type of cancer.</p>
