<h2 class="test-h2 mt-3">What is the FISH ALK Gene Rearrangement test?</h2>
<p>The FISH ALK Gene Rearrangement test is a molecular diagnostic test that detects alterations or
rearrangements in the ALK gene. This test is commonly used to identify specific genetic changes in
patients with non-small cell lung cancer (NSCLC).</p>

<h2 class="test-h2 mt-3">How is the sample for this test collected?</h2>
<p>The sample used for this test is typically a tissue sample from the tumor. This can be obtained through a
biopsy procedure where a small piece of tissue is extracted, often with a needle. The tissue is then
embedded in a paraffin block for examination.</p>
