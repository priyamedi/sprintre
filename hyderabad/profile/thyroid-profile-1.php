<h2 class="test-h2 mt-3">Why do I need a Thyroid Profile - I test?</h2>
<p>A Thyroid Profile - I test is typically ordered when you exhibit symptoms of a thyroid disorder, such as unexplained
    weight loss or gain, fatigue, palpitations, or changes in skin and hair.</p>
<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>The test requires a small sample of blood, which is typically drawn from a vein in your arm.</p>
