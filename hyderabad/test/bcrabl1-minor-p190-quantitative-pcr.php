<h2 class="test-h2 mt-3">What is the BCR/ABL1 - Minor (p190) Quantitative PCR test?</h2>
<p>This test is used to detect and measure the quantity of the BCR-ABL1 fusion gene in the blood or bone marrow, specifically the "minor" or p190 type, which is often associated with Acute Lymphoblastic Leukemia (ALL) and sometimes Chronic Myeloid Leukemia (CML).</p>
<h2 class="test-h2 mt-3">When should this test be performed?</h2>
<p>The test is typically ordered at the time of diagnosis to confirm ALL or CML and can be repeated at intervals to monitor disease progression or response to treatment.</p>
