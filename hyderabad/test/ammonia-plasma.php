<h2 class="test-h2 mt-3">What is the Ammonia - Plasma test?</h2>
<p>The Ammonia - Plasma test measures the concentration of ammonia in the blood. Ammonia is produced when proteins are broken down by the body. The liver normally converts ammonia into urea, which is then excreted through the urine.</p>

<h2 class="test-h2 mt-3">Why is the Ammonia - Plasma test performed?</h2>
<p>The test is performed to evaluate liver function, especially in cases of suspected liver disease or damage. It's also used to diagnose and manage conditions that cause elevated blood ammonia levels, such as Reye’s syndrome or hepatic encephalopathy.</p>

