<h2 class="test-h2 mt-3"> What is the purpose of the Vasculitis Panel?</h2>
	<p>
The Vasculitis Panel helps in diagnosing vasculitis and monitoring its treatment. By identifying the
presence of certain autoantibodies in the blood, it helps doctors understand the type and severity of
vasculitis a patient has.</p>
<h2 class="test-h2 mt-3"> What are ANA, cANCA/pANCA, and Anti ds-DNA?</h2>
<p>
These are all types of autoantibodies, or antibodies that mistakenly attack your own cells. ANA refers to
antinuclear antibodies, cANCA and pANCA are antibodies against neutrophil cytoplasmic antigens, and
Anti ds-DNA are antibodies against double-stranded DNA.</p>
