<h1 class="test-h1">Neonatal Screening Panel - 52 (52 Conditions by Tandem Mass Spectrometry) </h1>
<div class="post-text">
<p>Neonatal Screening Panel - 52 is a comprehensive diagnostic test that screens for 52 different conditions
using a technique called Tandem Mass Spectrometry (MS/MS). This test is generally performed on a
small sample of blood obtained through a heel prick from a newborn baby. It's a routine part of neonatal
care in many countries and is typically conducted within the first few days after birth.
 </p>
<p> The test works by identifying abnormal levels of certain metabolites in the blood, which can indicate the
presence of various metabolic or genetic disorders. Early detection of these conditions is crucial, as it
allows for immediate initiation of treatment or management strategies to prevent long-term
complications or even life-threatening situations. The disorders tested in this panel include but are not
limited to phenylketonuria, maple syrup urine disease, congenital adrenal hyperplasia, and various
forms of amino acid, organic acid, and fatty acid metabolism disorders.</p>
