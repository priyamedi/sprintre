<h2 class="test-h2 mt-3">What is the Allergen, Individual - Food Bayleaf test?</h2>
<p>The test is a blood analysis that measures the presence and quantity of specific IgE antibodies in response to bay
    leaf proteins. These antibodies, when detected in high amounts, suggest an allergy to bay leaves.</p>
<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>This test is conducted when an individual shows symptoms of a food allergy after consuming or coming into contact
    with bay leaves. It helps in the confirmation of a bay leaf allergy.</p>
