<h2 class="test-h2 mt-3">What is the purpose of the IHC - CDX2 test?</h2>
<p>The IHC - CDX2 test is used to determine the presence and location of the CDX2 protein in tissue samples. Its
    detection is valuable in diagnosing and distinguishing gastrointestinal tumors, primarily colorectal cancer, from
    other types of cancer.</p>
<h2 class="test-h2 mt-3">How is the IHC - CDX2 test performed?</h2>
<p>This test is performed on a tissue sample, usually taken during a biopsy, surgery, or autopsy. The sample is treated
    with antibodies that bind to the CDX2 protein, if present. The antibodies are tagged with a dye to make the protein
    visible under a microscope.</p>
