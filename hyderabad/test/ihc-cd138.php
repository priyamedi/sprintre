<h2 class="test-h2 mt-3">What is the CD138 antigen and why is it important?</h2>
<p>CD138, also known as syndecan-1, is a protein that is present on the surface of certain cells. It is often overexpressed in multiple myeloma, a type of blood cancer, and its detection can assist in the diagnosis and monitoring of this disease.</p>
<h2 class="test-h2 mt-3">How is the IHC - CD138 test performed?</h2>
<p>The test is performed on a tissue sample, usually taken through a biopsy. The sample is treated with specific antibodies that bind to the CD138 antigen, if present. The bound antibodies can then be visualized under a microscope.</p>
