	<h2 class="test-h2 mt-3">What is a respiratory tract infection?</h2>
	<p>A respiratory tract infection is an infection that can affect the sinuses, throat, airways, or lungs. They can be caused by a variety of viruses and bacteria. These infections range from the common cold and flu to pneumonia and bronchitis. Symptoms may include a cough, sore throat, runny nose, fever, and difficulty breathing.</p>
