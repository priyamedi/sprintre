<h2 class="test-h2 mt-3">What is the IHC - CK19 test?</h2>
<p>This test uses immunohistochemistry to detect the presence or absence of Cytokeratin 19 in a tissue sample, which can
    help identify certain types of cancer.</p>
<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is done to identify and classify tumors, particularly carcinomas. It can help determine the origin of a
    tumor when the primary site is unknown.</p>
