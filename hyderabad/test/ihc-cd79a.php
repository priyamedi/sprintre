<h2 class="test-h2 mt-3">What is the IHC - CD79a test used for?</h2>
<p>The IHC - CD79a test is primarily used to aid in the diagnosis of B-cell lymphomas and leukemias, as the CD79a
    protein is expressed on the surface of B cells.</p>
<h2 class="test-h2 mt-3">How is the IHC - CD79a test performed?</h2>
<p>The test is performed on a tissue biopsy sample, usually obtained through a minimally invasive procedure. The sample
    is then stained with a specific antibody that binds to the CD79a protein. The binding is then visualized under a
    microscope, confirming the presence and location of the protein.</p>
