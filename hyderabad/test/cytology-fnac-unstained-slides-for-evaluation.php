<h2 class="test-h2 mt-3">Why would FNAC unstained slides be used for evaluation?</h2>
<p>FNAC unstained slides are used when a laboratory or pathologist wishes to apply specific stains that are best suited
    to highlight certain cell structures or elements relevant to the diagnosis.</p>
<h2 class="test-h2 mt-3">How are the unstained slides prepared?</h2>
<p>During FNAC, cells are aspirated from a lump or mass using a fine needle. These cells are then placed on glass
    slides. Instead of immediately staining them, the slides are left unstained and are often fixed to preserve the
    cellular material for later staining and evaluation.</p>

