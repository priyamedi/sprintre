	<h2 class="test-h2 mt-3">What is the role of cytokeratins in the body?</h2>
<p>Cytokeratins are proteins found in the cytoskeleton of epithelial cells. They play a key role in maintaining the structural integrity of these cells.</p>

	<h2 class="test-h2 mt-3">What does a 'borderline' result mean?</h2>
<p>A borderline result means that the test has detected a level of cytokeratins that falls between the established ranges for 'negative' and 'positive.' This can happen in early stages of disease or in conditions where the level of cytokeratins is slightly elevated. It's essential to discuss such results with your healthcare provider who may suggest further testing or monitoring.</p>

