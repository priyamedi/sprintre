<h2 class="test-h2 mt-3">Why is the Immunoglobulin IgG - CSF test done?</h2>
<p>The Immunoglobulin IgG - CSF test is performed to assess the IgG levels in the CSF. This helps in the diagnosis and management of neurological conditions such as multiple sclerosis, meningitis, or other autoimmune or inflammatory disorders affecting the nervous system.</p>
<h2 class="test-h2 mt-3">Is fasting required for the Immunoglobulin IgG - CSF test?</h2>
<p>No, fasting is not required for the Immunoglobulin IgG - CSF test. The test requires a CSF sample which is usually collected via a lumbar puncture.</p>
