<h2 class="test-h2 mt-3"> When should this test be done?</h2>
<p>
This test is generally performed when a patient has been diagnosed with a tumor, and further
characterization or classification of the tumor type is required.</p>
<h2 class="test-h2 mt-3"> What information does this test provide?</h2>
<p>
The test identifies the presence or absence of more than eight specific proteins (antigens) in tissue
samples. The presence or absence of these proteins can give valuable information about the type and
origin of the tumor, aiding in its classification and guiding the treatment approach.</p>
<h2 class="test-h2 mt-3"> How often should this test be done?</h2>
<p>
The frequency of this test is based on the individual's specific clinical circumstances and is typically
guided by their healthcare provider.</p>
<h2 class="test-h2 mt-3"> What are the normal values for this test?</h2>
<p>
In tissue samples, 'normal' values can vary significantly and depend on the specific antigens being
tested. Interpretation should be made by a healthcare provider considering the overall clinical picture.</p>
<h2 class="test-h2 mt-3"> What precautions should I take before the test?</h2>
<p>

No special precautions are necessary before this test. Continue with your usual diet and medications
unless otherwise instructed by your healthcare provider.</p>
