<h2 class="test-h2 mt-3"> What is the purpose of the Autoimmune Encephalitis Panel?</h2>
	<p>
The primary purpose of the panel is to detect the presence of specific antibodies in the blood associated
with different types of autoimmune encephalitis. This assists in accurate diagnosis and appropriate
treatment of the condition.</p>
<h2 class="test-h2 mt-3"> What are the symptoms of autoimmune encephalitis?</h2>
<p>
Symptoms can vary and may include memory loss, cognitive changes, seizures, hallucinations, psychosis,
and movement disorders. The onset might be gradual or sudden, and symptoms can fluctuate in
intensity.</p>
