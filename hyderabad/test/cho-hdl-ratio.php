	<h2 class="test-h2 mt-3">What is the Cho/HDL Ratio?
 </h2>
	<p>The Cho/HDL Ratio is a calculation based on the total cholesterol and HDL cholesterol levels in your blood. It helps determine your risk of developing heart disease. The higher the ratio, the higher the risk.
 </p>
	<h2 class="test-h2 mt-3">Why is this test done?
 </h2>
	<p>This test is performed to assess the risk of developing heart disease. A higher ratio indicates a higher risk of heart disease. It provides more information about your heart health than a total cholesterol test alone.
 </p>
