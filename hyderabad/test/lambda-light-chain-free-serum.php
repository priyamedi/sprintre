<h1 class="test-h1">Lambda Light Chain Free - Serum </h1>
<div class="post-text">
<p>Lambda Light Chain Free in serum refers to a diagnostic test that measures the levels
of free lambda light chains in the blood. Light chains are proteins produced by
immune cells called B lymphocytes. They are part of antibodies, which are used by
the immune system to identify and neutralize foreign objects such as bacteria and
viruses. Normally, light chains combine with heavy chains to form antibodies.
However, some light chains are not bound to heavy chains and are therefore termed
as “free” light chains. Among the free light chains, there are two types, namely
kappa and lambda. </p>
<p>In certain medical conditions, such as multiple myeloma, light chain levels may be
abnormally high. Multiple myeloma is a type of blood cancer that affects the plasma
cells, a type of white blood cell that makes antibodies. In this cancer, the plasma cells
become abnormal and start producing a large number of monoclonal light chains,
either kappa or lambda. These can accumulate in the blood and sometimes cause
damage to the kidneys.</p>
