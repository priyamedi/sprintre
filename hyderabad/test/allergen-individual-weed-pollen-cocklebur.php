<h2 class="test-h2 mt-3">What is the Allergen, Individual - Weed Pollen Cocklebur test?</h2>
<p>This test is a blood test that identifies whether or not an individual is allergic to the pollen of the Cocklebur weed. It does so by detecting the presence of specific Immunoglobulin E (IgE) antibodies, which the body produces in response to allergens.</p>
<h2 class="test-h2 mt-3">Is fasting required for this test?</h2>
<p>No fasting is required for this test. You can eat and drink as you normally would unless your doctor advises otherwise.</p>
