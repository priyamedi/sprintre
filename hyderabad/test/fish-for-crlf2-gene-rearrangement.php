<h2 class="test-h2 mt-3">Why is the FISH for CRLF2 Gene Rearrangement test performed?</h2>
	<p>This test is performed to detect the presence of rearrangements in the CRLF2 gene, which are
associated with certain types of leukemia. It helps in diagnosis, prognosis, and deciding treatment
strategies for hematologic malignancies.</p>

<h2 class="test-h2 mt-3">How is the sample collected for this test?</h2>
<p>The sample for this test is usually collected through a blood draw or a bone marrow biopsy.</p>
