<h2 class="test-h2 mt-3">What does this neonatal screening panel test for?</h2>
<p>This screening panel tests for four conditions - Congenital Hypothyroidism, Congenital Adrenal Hyperplasia (CAH),
    Glucose-6-phosphate dehydrogenase (G6PD) deficiency, and Galactosemia. These conditions can lead to significant
    health complications if not detected and managed early.</p>
<h2 class="test-h2 mt-3">How is this screening test performed?</h2>
<p>The blood sample for the test is obtained using a heel prick, a procedure where a small puncture is made in the
    baby's heel to collect a few drops of blood. The sample is then sent to a laboratory for further testing.</p>
