	<h2 class="test-h2 mt-3">What is Mucopolysaccharidosis VI (Maroteaux-Lamy Syndrome)?
 </h2>
	<p>Mucopolysaccharidosis VI (MPS VI) is one of a group of inherited metabolic disorders where there is an absence or malfunctioning of specific enzymes needed to break down molecules called glycosaminoglycans. In MPS VI, there is a deficiency of the enzyme Aryl Sulfatase B, which leads to the accumulation of dermatan sulfate, a type of GAG, in various tissues and organs. The accumulation of these molecules causes progressive damage and affects appearance, physical abilities, organ function, and, in severe cases, lifespan.
 </p>
