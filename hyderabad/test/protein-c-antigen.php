<h1 class="test-h1">Protein C Antigen </h1>
<div class="post-text">
<p>Protein C is a protein in your blood that plays a crucial role in preventing blood clot formation. It is one of the natural anticoagulants your body produces to balance the clotting process. A Protein C Antigen test measures the amount of protein C in your blood, which helps determine if you have a deficiency or excess of this protein. This test is particularly significant for individuals with a history of abnormal clot formation or a family history of clotting disorders.</p>

<p>The test works by measuring the antigenic level of protein C - that is, the quantity of the protein present, regardless of its functionality. It is worth noting that a normal protein C antigen level does not necessarily mean the protein is functioning correctly, and additional tests may be required to confirm its activity.</p>
