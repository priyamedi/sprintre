<p>The PML/RARA t(15:17) Gene Rearrangement Quantitative - PCR test is a genetic test that detects the presence of a specific chromosomal translocation between chromosomes 15 and 17. This translocation results in the fusion of the PML gene on chromosome 15 and the RARA gene on chromosome 17, creating a PML-RARA fusion gene. This fusion gene is a hallmark of acute promyelocytic leukemia (APL), a subtype of acute myeloid leukemia (AML).</p>

