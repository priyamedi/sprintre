<h2 class="test-h2 mt-3">Why do I need to undergo a UTI Screen? </h2>
<p>A UTI Screen is done to diagnose a urinary tract infection (UTI) and determine the type of bacteria causing the infection. It can also help monitor the effectiveness of treatment for a UTI.</p>

<h2 class="test-h2 mt-3">What does a Complete Blood Count (CBC) test indicate in a UTI Screen? </h2>
<p>The CBC test measures the concentration of white blood cells, red blood cells, and platelets in the blood. An increase in white blood cells may indicate an infection.</p>

