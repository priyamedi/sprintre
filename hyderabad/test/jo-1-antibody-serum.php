<p>There are minimal risks associated with blood draw for the test. You may experience a slight prick or sting at the
    site of needle insertion.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test is performed by drawing a blood sample from a vein in your arm. The blood is then sent to the lab for
    analysis.</p>
<h2 class="test-h2 mt-3">Are there any side effects of the test?</h2>
<p>The side effects of the test are minimal and usually associated with the blood draw, such as slight pain or bruising
    at the needle site.</p>
<h2 class="test-h2 mt-3">Can the test results be affected by medications?</h2>
<p>Certain medications might affect the test results. Therefore, it's important to inform your doctor about all the
