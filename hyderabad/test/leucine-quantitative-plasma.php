<h2 class="test-h2 mt-3">Why is the Leucine Quantitative Plasma test done?</h2>
<p>This test is performed to assess the levels of the amino acid leucine in the plasma. It can be used for nutritional
    evaluation, diagnosing metabolic disorders, and monitoring the effect of diet or medication on leucine levels.</p>
<h2 class="test-h2 mt-3">What are the sources of leucine?</h2>
<p>Leucine can be found in foods high in protein such as meat, dairy products, eggs, nuts, and legumes. It is also
    available as a dietary supplement.</p>
