<h2 class="test-h2 mt-3">What is the Allergen, Individual - Weed Pollen Nettle test?</h2>
<p>This is a blood test that measures the level of specific Immunoglobulin E (IgE) antibodies against Nettle pollen. The presence of these antibodies in the blood indicates an allergic reaction.</p>
<h2 class="test-h2 mt-3">Do I need to fast before this test?</h2>
<p>No, there is no need to fast before this test. Maintain your normal diet and water intake unless otherwise directed by your doctor.</p>
