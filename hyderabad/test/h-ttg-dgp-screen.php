<h2 class="test-h2 mt-3"> What is the h-TTG/Dgp Screen?</h2>

<p>
The <strong>h-TTG/Dgp Screen</strong> is a blood test that measures the levels of anti-tissue transglutaminase (tTG)
antibodies and Deamidated Gliadin Peptide (Dgp) antibodies. It is primarily used to help diagnose celiac
disease. </p>
<h2 class="test-h2 mt-3">Why is this test performed?</h2>

<p>
This test is performed to help diagnose celiac disease, especially in individuals who have symptoms such
as abdominal pain, bloating, diarrhea, or malabsorption. It can also be used to monitor individuals with
celiac disease and evaluate their response to a gluten-free diet. </p>
