<h2 class="test-h2 mt-3">Why would I need a Urobilinogen - Urine test?</h2>
<p>This test is usually ordered when you have signs and symptoms of liver disease or if your healthcare provider
	suspects you may have a condition that affects the production or excretion of bilirubin. Symptoms may include
	jaundice (yellowing of the skin or eyes), dark urine, abdominal pain, unexplained weight loss, and fatigue.</p>
<h2 class="test-h2 mt-3">How is the Urobilinogen - Urine test performed?</h2>
<p>The test is performed on a urine sample. Typically, you will be asked to collect a sample of your urine in a clean,
	sterile container. The sample is then analyzed in a laboratory for the presence and amount of urobilinogen.</p>
