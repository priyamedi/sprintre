<h2 class="test-h2 mt-3">Why is the G6PD Neonatal Screen done?</h2>
<p>This screening test is done to determine if a newborn has G6PD deficiency. Early detection can help avoid substances and medications that may cause complications like hemolytic anemia.</p>

<h2 class="test-h2 mt-3">How is the test conducted?</h2>
<p>A small blood sample is taken from the newborn's heel, which is then sent to a laboratory for analysis.</p>

