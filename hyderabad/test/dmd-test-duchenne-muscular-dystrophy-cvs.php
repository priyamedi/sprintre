<h2 class="test-h2 mt-3">What is the DMD Test for CVS?</h2>
<p>The DMD Test for CVS is a prenatal diagnostic test that analyzes a sample of chorionic villi to detect mutations in
    the DMD gene responsible for Duchenne Muscular Dystrophy.</p>
<h2 class="test-h2 mt-3">Why is the test performed?</h2>
<p>This test is conducted to determine if a fetus is affected by Duchenne Muscular Dystrophy, particularly in cases with
    a known family history of the disorder.</p>
