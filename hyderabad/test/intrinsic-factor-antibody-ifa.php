                                <h2 class="test-h2 mt-3">When should this test be done?</h2>
                                <p>This test is typically done when a person presents symptoms of anemia (like fatigue,
                                    pale skin, or shortness of breath), and a doctor suspects that pernicious anemia may
                                    be the cause.</p>

                                <h2 class="test-h2 mt-3">What information does the IFA Test provide?</h2>
                                <p>The IFA Test detects antibodies against intrinsic factor. The presence of these
