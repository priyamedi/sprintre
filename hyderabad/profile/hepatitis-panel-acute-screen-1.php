<h1 class="test-h1">Hepatitis Panel - Acute Screen-1</h1>
<div class="post-text">
<p>The Hepatitis Panel - Acute Screen-I is a series of blood tests used to detect current or past infections with viral hepatitis types A and B. The panel includes Hepatitis A Virus IgM Antibody (HAV-IgM), Anti-Hepatitis B Core Antibody IgM (Anti-HBc IgM), and Hepatitis B Surface Antigen (HBsAg). Each of these tests provides important information to help your doctor understand your hepatitis status and guide treatment, if necessary.</p>
<p>Hepatitis is a condition characterized by inflammation of the liver, often caused by a viral infection. Different viruses are responsible for different types of hepatitis, each with its unique transmission mode and clinical implications. Hepatitis A and B are two common types that can cause both acute and chronic disease, with varying severity levels. This panel is particularly helpful in the acute phase, where it can aid in the diagnosis and help rule out other types.</p>

