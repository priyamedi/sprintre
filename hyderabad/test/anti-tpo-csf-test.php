<h2 class="test-h2 mt-3">What is the Anti-TPO in CSF test? </h2>
<p>The Anti-TPO in CSF test is an analysis that measures the levels of anti-thyroid peroxidase antibodies in the cerebrospinal fluid. This test can provide information about the involvement of the central nervous system in autoimmune thyroid diseases.</p>
<h2 class="test-h2 mt-3">Why would a doctor order an Anti-TPO test on CSF instead of blood? </h2>
<p>While blood tests are more common for assessing thyroid function, a doctor may order an Anti-TPO test on CSF if there is suspicion that an autoimmune thyroid disorder is affecting the central nervous system, or if there are unexplained neurological symptoms.</p>
