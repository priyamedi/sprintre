<h2 class="test-h2 mt-3">What is the CD8 protein?</h2>
<p>The CD8 protein is found on the surface of cytotoxic T cells, a type of white blood cell that can kill cancer cells,
    cells infected with a virus, or cells that are damaged in other ways.</p>
<h2 class="test-h2 mt-3">Why is the IHC - CD8 test performed?</h2>
<p>The IHC - CD8 test can help diagnose and monitor certain health conditions, such as autoimmune diseases, infections,
    and cancers. By revealing the presence and location of CD8 T cells in a tissue sample, the test can provide insights
    into the immune system's response to these conditions.</p>
