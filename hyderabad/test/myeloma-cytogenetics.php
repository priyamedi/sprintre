<p>Multiple Myeloma is a cancer of plasma cells, which are a type of white blood cell responsible for producing antibodies. In myeloma, these cells become abnormal and multiply uncontrollably. Myeloma Cytogenetics involves using Karyotyping and Fluorescence In Situ Hybridization (FISH) to analyze the genetic changes in plasma cells. This testing looks for specific chromosomal abnormalities, namely deletion 17p (del17p), immunoglobulin heavy chain (IGH) gene rearrangements, deletion 13q (del13q), translocation t(4;14), and translocation t(14;16), which are known to be associated with multiple myeloma. </p>
<p>Multiple Myeloma is a complex disease that arises from the uncontrolled proliferation of plasma cells in the bone marrow. Plasma cells are essential for the immune system as they produce antibodies to fight infections. In multiple myeloma, genetic mutations lead to the overproduction of abnormal plasma cells, which accumulate in the bone marrow and disrupt the production of normal blood cells. Myeloma Cytogenetics is essential for the diagnosis, prognosis, and management of multiple myeloma. It involves using Karyotyping and FISH to detect chromosomal abnormalities that are characteristic of the disease. This information is crucial for risk stratification and guiding therapeutic decisions. </p>
