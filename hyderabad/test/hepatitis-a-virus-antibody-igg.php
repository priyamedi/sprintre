<h2 class="test-h2 mt-3">What is the importance of getting the Hepatitis A Virus (HAV) Antibody - IgG test done?</h2>
<p>Getting tested for HAV IgG is important for determining whether an individual has immunity to hepatitis A. This can
    be vital information for people who are traveling to areas where hepatitis A is common, or for healthcare workers
    and others who may be at an increased risk of exposure to the virus.</p>
<h2 class="test-h2 mt-3">What does it mean if I have HAV IgG antibodies?</h2>
<p>If HAV IgG antibodies are detected in your blood, it indicates that you have been exposed to the hepatitis A virus in
    the past or that you have been vaccinated against it. The presence of IgG antibodies generally means you are immune
    to the virus.</p>
