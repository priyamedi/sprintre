<h2 class="test-h2 mt-3">What does the Neonatal Screening for Maple Syrup Urine Disorders test for?</h2>
<p>This test screens for Maple Syrup Urine Disease (MSUD), an inherited disorder that prevents the body from breaking
    down certain amino acids. The condition, if untreated, can lead to neurological damage and developmental delays.</p>
<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>The test involves a simple heel prick procedure where a small blood sample is collected from the newborn's heel. The
    blood sample is placed on a special filter paper and sent to the lab for further testing.</p>
