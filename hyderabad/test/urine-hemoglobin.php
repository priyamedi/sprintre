<h2 class="test-h2 mt-3">What is the Urine Hemoglobin Test?</h2>
<p>The Urine Hemoglobin Test is a diagnostic procedure used to detect the presence of hemoglobin in urine, which is not
	normally present in healthy individuals.</p>
<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>This test is conducted to help diagnose conditions such as kidney disease, urinary tract infections, or other medical
	problems that could lead to the presence of hemoglobin in the urine.</p>
