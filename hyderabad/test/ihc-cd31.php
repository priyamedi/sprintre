<h1 class="test-h1">IHC - CD31 </h1>
<div class="post-text">
<p>CD31, also known as platelet endothelial cell adhesion molecule (PECAM-1), is a protein that in humans is encoded by the PECAM1 gene. CD31 is normally found on endothelial cells (the cells lining the inside of blood vessels), platelets, certain types of immune cells, and cells involved in the immune response. In the medical field, the CD31 antigen is used as a marker for endothelial cells, making it useful in the identification of angiosarcomas and other tumors of vascular origin. </p>
<p>Immunohistochemistry (IHC) is a laboratory procedure that uses antibodies to check for certain antigens (markers) in a sample of tissue. The process of staining these antigens allows pathologists to visualize and determine the presence and distribution of the specific protein, in this case, CD31. The <strong>IHC - CD31</strong> test can provide valuable insights into disease mechanisms, contributing to the diagnosis and management of various medical conditions. </p>
