<h2 class="test-h2 mt-3">What is the FISH for del(7q) test used for?</h2>
<p> FISH for del(7q) is used to detect deletions in the long arm of chromosome 7, which can be associated with hematological disorders such as Myelodysplastic Syndromes (MDS) and acute myeloid leukemia (AML). It is essential for diagnosis and planning appropriate treatment.</p>

<h2 class="test-h2 mt-3">How is the test performed? </h2>
<p>The test is performed on a sample of blood or bone marrow. The sample is treated with fluorescent probes specific for chromosome 7. Under a special microscope, a technician can determine if there is a deletion in the long arm of chromosome 7.</p>

