<h2 class="test-h2 mt-3">What is the Anti-PR3 antibodies - cANCA test? </h2>
<p>The Anti-PR3 antibodies - cANCA test is a blood test that detects antibodies against the Proteinase 3 (PR3) enzyme in neutrophils. It is used to help diagnose Granulomatosis with Polyangiitis (GPA) and other vasculitis disorders.</p>
<h2 class="test-h2 mt-3">Why is the Anti-PR3 antibodies - cANCA test important? </h2>
<p>This test is important for diagnosing and monitoring diseases like Granulomatosis with Polyangiitis, which can have serious complications if not treated. Early diagnosis and treatment can prevent organ damage and improve prognosis.</p>
