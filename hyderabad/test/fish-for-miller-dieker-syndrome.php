<h2 class="test-h2 mt-3">What is Miller-Dieker Syndrome (MDS)?</h2>
<p>Miller-Dieker Syndrome is a genetic disorder characterized by severe brain malformations, intellectual disability,
    seizures, and distinctive facial features. The syndrome is caused by a deletion of genes in the 17p13.3 region of
    chromosome 17.</p>
<h2 class="test-h2 mt-3">How is FISH for Miller-Dieker Syndrome performed?</h2>
<p>A blood sample or amniotic fluid is collected from the patient. The DNA is then denatured, and fluorescently labeled
    probes specific to the 17p13.3 region are added. These probes will bind to complementary DNA sequences. Under a
    fluorescence microscope, the absence or reduction of the fluorescent signal in the specific region indicates the
    deletion associated with MDS.</p>
