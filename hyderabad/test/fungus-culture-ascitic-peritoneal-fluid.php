<h2 class="test-h2 mt-3">What is the purpose of a fungal culture of ascitic or peritoneal fluid? </h2>
<p>The purpose of this test is to detect and identify fungal infections in the peritoneal cavity, which can help determine the cause of symptoms and guide the most effective treatment strategy.</p>
<h2 class="test-h2 mt-3">What kind of sample is needed for this test? </h2>
<p>The test requires a sample of ascitic or peritoneal fluid, which is collected by a healthcare professional using a needle and syringe. This procedure is typically performed under local anesthesia.</p>
