<h2 class="test-h2 mt-3">Why is TB Culture on Löwenstein-Jensen medium done?</h2>
<p>TB Culture is done to diagnose tuberculosis infection by isolating the Mycobacterium tuberculosis bacteria from a
    patient’s sample. This test is more reliable than smear microscopy as it can detect even small numbers of bacteria.
</p>
<h2 class="test-h2 mt-3">How is the sample collected?</h2>
<p>The sample type may vary. Sputum samples are the most common for pulmonary TB. For suspected TB in other parts of the
    body, samples such as urine, pleural fluid, or tissue may be collected.</p>
