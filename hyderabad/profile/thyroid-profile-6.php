<h2 class="test-h2 mt-3">Why is a Thyroid Profile - VI test recommended?</h2>
<p>This test is often ordered when you have symptoms that suggest a thyroid disorder, such as weight gain or loss,
    fatigue, tremors, or changes in heart rate.</p>
<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>This test requires a small blood sample, which is typically drawn from a vein in your arm.</p>
