<h2 class="test-h2 mt-3">What is the purpose of PGS testing?</h2>
<p>The main purpose of PGS testing is to identify any chromosomal abnormalities in embryos before they are transferred
    to the uterus. This can increase the chances of a successful pregnancy and reduce the risk of miscarriage.</p>
<h2 class="test-h2 mt-3">How is the biopsy performed?</h2>
<p>The biopsy is performed by an embryologist in a laboratory setting. On the 5th day of development, a small number of
    cells are carefully removed from each embryo. These cells are then analyzed to identify any chromosomal
    abnormalities.</p>
