<h2 class="test-h2 mt-3">What is the D-Dimer - Quantitative test used for?</h2>
<p>This test is used to help diagnose or rule out the presence of blood clots, which can be a sign of conditions such as
    deep vein thrombosis, pulmonary embolism, or disseminated intravascular coagulation. It is also sometimes used to
    monitor the effectiveness of treatment for these conditions.</p>
<h2 class="test-h2 mt-3">How is the D-Dimer test performed?</h2>
<p>A healthcare provider will take a blood sample from a vein in your arm. The blood sample is then sent to a
    laboratory, where it is analyzed for D-Dimer levels.</p>

