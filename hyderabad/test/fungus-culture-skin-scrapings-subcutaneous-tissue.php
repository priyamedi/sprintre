	<h2 class="test-h2 mt-3">Why is the Fungus Culture [Skin Scrapings/Subcutaneous Tissue] test performed? 
 </h2>
	<p>This test is performed when a fungal infection of the skin is suspected. Symptoms that may lead to this test include itching, redness, scaling, blisters, or changes in skin color.
 </p>
	<h2 class="test-h2 mt-3">How are the skin scrapings or subcutaneous tissue samples collected?
 </h2>
	<p> Skin scrapings are usually collected using a sterile blade to scrape off a small amount of skin. Subcutaneous tissue samples might require a small biopsy under local anesthesia.
 </p>
