	<h2 class="test-h2 mt-3">What do the results mean? </h2>
<p>The results provide information about specific genetic mutations in your tumor. This information can help your doctor determine the most effective course of treatment for your cancer.</p>

	<h2 class="test-h2 mt-3">Are there any risks associated with this test? </h2>
<p>The risks are those associated with the biopsy procedure used to obtain the tissue sample. These may include pain, bleeding, or infection at the biopsy site.</p>

	<h2 class="test-h2 mt-3">Can this test predict how well I will respond to treatment? </h2>
<p>Yes, certain mutations in the EGFR, KRAS, and ALK genes can predict how well you may respond to targeted therapies.</p>

	<h2 class="test-h2 mt-3">Can this test tell me if I have lung cancer? </h2>
<p>The Lung Cancer Mutation Panel is not a screening test for lung cancer. It's intended for individuals who have already been diagnosed with the disease.</p>

	<h2 class="test-h2 mt-3">How quickly will I receive my results? </h2>
<p>The turnaround time for this test is usually 7-10 days, but it can vary depending on the laboratory.</p>

	<h2 class="test-h2 mt-3">How often should I get this test? </h2>
<p>The test is typically performed at the time of diagnosis to guide treatment decisions. However, if you're undergoing targeted therapy, your doctor may order the test again if your disease progresses to see if new mutations have developed.</p>

	<h2 class="test-h2 mt-3">Is this test covered by insurance? </h2>
<p>Coverage varies by provider and individual insurance plan. It's important to check with your insurance provider to understand your coverage for this test.</p>

	<h2 class="test-h2 mt-3">Can this test detect other types of cancer? </h2>
<p>This specific panel is designed to detect mutations commonly found in lung cancer. Other tests are available for different types of cancer.</p>

	<h2 class="test-h2 mt-3">What is targeted therapy for lung cancer?</h2> 
<p>Targeted therapy is a type of cancer treatment that targets the cancer's specific genes, proteins, or the tissue environment that contributes to cancer growth and survival.</p>

	<h2 class="test-h2 mt-3">Is the lung cancer mutation panel test always necessary for lung cancer patients? </h2>
<p>While it's not always necessary, it's increasingly being used due to the rise of targeted therapies for lung cancer.</p>

	<h2 class="test-h2 mt-3">Can a negative result mean I do not have these mutations? </h2>
<p>A negative result means that the specific mutations this test was designed to detect were not found in the sample tested.</p>

	<h2 class="test-h2 mt-3">What happens if my results show I have one of these mutations? </h2>
<p>If your results show a mutation, your doctor will discuss what these findings mean in the context of your overall health and treatment plan.</p>

	<h2 class="test-h2 mt-3">Are these mutations hereditary? </h2>
<p>Most mutations associated with lung cancer, including EGFR, KRAS, and ALK, occur during a person's lifetime and are not inherited.</p>

	<h2 class="test-h2 mt-3">Can this test be performed on blood samples? 
<p>Yes, certain tests similar to this panel are performed on blood samples. Thes</e are often referred to as "liquid biopsies."</

	<h2 class="test-h2 mt-3">Are EGFR, KRAS and ALK the only genes of interest in lung cancer?</h2> 
<p>While these are three significant genes, lung cancer is a complex disease that can involve mutations in several other genes. Comprehensive genomic profiling may be beneficial for some patients.</p>

	<h2 class="test-h2 mt-3">Can this test tell me the stage of my lung cancer? </h2>
<p>No, this test does not provide information about cancer stage. It only gives information about specific genetic mutations present in the cancer cells.</p>

	<h2 class="test-h2 mt-3">What is ALK-D5F3 immunohistochemistry (IHC)?</h2> 
<p>ALK-D5F3 IHC is a type of laboratory test used to detect ALK gene rearrangements in cancer cells. It uses an antibody, D5F3, that binds to ALK protein in cells where the gene is rearranged and overactive.</p>

	<h2 class="test-h2 mt-3">What does it mean if I have an ALK gene rearrangement? </h2>
<p>ALK gene rearrangements can lead to uncontrolled cell growth, contributing to cancer. Some lung cancers with ALK rearrangements respond well to specific targeted therapies.</p>

	<h2 class="test-h2 mt-3">Why do some people with lung cancer not have any of these mutations? </h2>
<p>Lung cancer is a complex disease that can occur due to many different genetic changes. Not all lung cancers will have mutations in the EGFR, KRAS, or ALK genes.</p>

	<h2 class="test-h2 mt-3">If I have a mutation in one of these genes, does it mean other members of my family will get lung cancer?</h2> 
<p>Most mutations found in lung cancer are not inherited and occur during a person's lifetime. It's unlikely that a mutation in these genes will directly impact the risk of lung cancer in family members.</p>

	<h2 class="test-h2 mt-3">What other tests might I need if I have lung cancer?</h2>
<p> The tests you might need depend on the type of lung cancer, its stage, and your overall health. These might include imaging tests, additional blood tests, bronchoscopy, and others.</p>

	<h2 class="test-h2 mt-3">How will the Lung Cancer Mutation Panel results affect my treatment options? </h2>
<p>If your cancer has a mutation in one of the tested genes, you might be a candidate for targeted therapies. These treatments specifically target cancer cells with these mutations, often leading to more effective and less harmful treatment.</p>

	<h2 class="test-h2 mt-3">Can the results of this test change over time? </h2>
<p>In some cases, the genetic profile of your cancer can change over time, particularly if the cancer becomes resistant to treatment. If this happens, your doctor may recommend repeating the testing.</p>

