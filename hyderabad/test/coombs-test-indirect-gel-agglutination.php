								<h2 class="test-h2 mt-3">What is the Indirect Coombs Test with Gel Agglutination?</h2>
								<p>The Indirect Coombs Test using Gel Agglutination is a sensitive method to detect antibodies in the plasma that can bind to red blood cells. It's mainly used in blood transfusion medicine and to screen for antibodies in pregnant women that may affect the fetus.</p>
								
							
								<h2 class="test-h2 mt-3">How is the Indirect Coombs Test performed using Gel Agglutination?</h2>
								<p> A blood sample is taken and the plasma is separated. The plasma is then mixed with red blood cells of known antigenic makeup in the presence of a special gel containing anti-human globulin. If antibodies are present in the plasma, they will bind to the red blood cells and clump together in the gel. This clumping is detected under a microscope.</p>
								
