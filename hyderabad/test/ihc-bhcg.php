<h2 class="test-h2 mt-3">Why is the IHC - BHCG test performed?</h2>
<p>The <strong>IHC-BHCG test</strong> is primarily performed to confirm a diagnosis of gestational trophoblastic disease, germ cell tumors, or testicular cancer. It is also sometimes used to evaluate the tissue in cases of abnormal pregnancies, such as ectopic pregnancies or molar pregnancies.</p>
<h2 class="test-h2 mt-3">What does the test measure?</h2>
<p>The IHC-BHCG test detects and visualizes the BHCG protein in tissue samples. It can indicate whether the tissue is from a normal placenta, a trophoblastic tumor, a germ cell tumor, or another kind of cancer that produces BHCG.</p>
