<h2 class="test-h2 mt-3">What is a Varicella Zoster Virus IgG test? </h2>
<p>A Varicella Zoster Virus IgG test is a blood test that checks for the presence of IgG antibodies to the varicella-zoster virus. These antibodies indicate past exposure to the virus or vaccination.</p>

<h2 class="test-h2 mt-3">Why would I need this test? </h2>
<p>This test is typically ordered if you are unsure of your chickenpox vaccination status or if you have never had chickenpox. It can also be used to confirm immunity to the virus in healthcare workers or other individuals in high-risk settings.</p>

