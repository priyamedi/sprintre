<h2 class="test-h2 mt-3">What is the FLT3 Gene Mutation (ITD, D835Y) Detection test?</h2>
<p> This is a genetic test used to detect mutations in the FLT3 gene, specifically ITD and D835Y, commonly found in Acute Myeloid Leukemia (AML) patients. This test plays a significant role in prognosis and determining treatment strategy.</p>

<h2 class="test-h2 mt-3">Why is this test performed? </h2>
<p>The FLT3 gene mutation test is performed to guide the prognosis and treatment options for AML patients. Patients with FLT3-ITD mutations are often at a higher risk of relapse and have a poorer overall survival rate.</p>

