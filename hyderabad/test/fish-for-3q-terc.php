<h2 class="test-h2 mt-3">What is FISH for 3q (TERC)?</h2>
<p>FISH for 3q (TERC) is a diagnostic test that uses fluorescent probes to detect changes in the TERC gene
located on the long arm (q) of chromosome 3. It is mainly used to identify alterations associated with
cervical cancer.</p>

<h2 class="test-h2 mt-3">Why is the FISH for 3q (TERC) test performed?</h2>
<p>This test is performed to detect early chromosomal changes that are associated with the progression of
cervical dysplasia to cervical cancer. It helps in risk assessment and guides treatment decisions for
patients with cervical abnormalities.</p>
