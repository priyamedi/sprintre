<h1 class="test-h1">BOH (Bad Obstetric History) Panel - Comprehensive </h1>
<div class="post-text">
<p>The BOH (Bad Obstetric History) Panel is an extensive set of blood tests that are aimed at identifying potential causes for recurrent pregnancy losses. “Bad Obstetric History” is a term used to describe the history of multiple spontaneous miscarriages. The information gathered through these tests is essential in helping doctors devise a personalized management plan for future pregnancies.
 </p>
<p>This comprehensive panel includes several different tests such as ANA (Antinuclear Antibodies), APA (Antiphospholipid Antibodies) IgG/IgM, Anti Cardiolipin - IgG/IgM/IgA, Lupus Anticoagulant (using Dilute Russell’s Viper Venom Test, DRVVT), TSH (Thyroid-Stimulating Hormone), and TORCH-10 (testing for a group of infectious diseases). Each of these tests provides insight into different aspects and conditions that can affect pregnancy outcomes, including autoimmune disorders, thyroid function, and infections.
 </p>
