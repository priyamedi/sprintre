<h2 class="test-h2 mt-3"> Why is a Rheumatic Fever Panel necessary?</h2>
	<p>
The panel is necessary to identify if a patient is suffering from <strong>rheumatic fever</strong>, a condition that usually
follows a streptococcal throat infection and can lead to severe complications like heart disease if not
treated promptly.</p>
<h2 class="test-h2 mt-3">How is the sample collected for the panel?</h2>
<p>
A blood sample is drawn from a vein in the arm for the <a href="https://www.sprintdiagnostics.in/our-services/laboratory/hematology/"><b style="color:#2C53A5">blood tests</b></a>, and a swab is taken from the throat
for the culture.</p>
