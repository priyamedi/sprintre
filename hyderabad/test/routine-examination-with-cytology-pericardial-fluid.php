<h2 class="test-h2 mt-3"> Why is the Routine Examination with Cytology - Pericardial Fluid done?</h2>
	<p>
This test is conducted to identify the underlying cause of pericardial effusion or to evaluate the
effectiveness of the ongoing treatment for diseases causing an increase in <strong>pericardial fluid</strong>.</p>
<h2 class="test-h2 mt-3"> What does the test involve?</h2>
<p>
The test involves a procedure called pericardiocentesis, where a needle is inserted into the pericardial
sac to collect a fluid sample. This sample is then sent to the lab for cytological examination.</p>
