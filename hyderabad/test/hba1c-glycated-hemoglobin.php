<h2 class="test-h2 mt-3">What is the HbA1c test?</h2>
<p>The HbA1c test measures the percentage of glycated hemoglobin in your blood. Glycated hemoglobin is formed when sugar in the blood attaches to hemoglobin. This test reflects the average blood sugar levels over the past two to three months.</p>
<h2 class="test-h2 mt-3">Why is the HbA1c test important?</h2>
<p>The HbA1c test is essential for people with diabetes to see if their blood sugars are staying within the target range. It can also be used for the diagnosis of diabetes and prediabetes.</p>
