<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is performed to evaluate the body's immune response. It can help diagnose conditions related to immune deficiencies, autoimmune diseases, allergies, and infections. It's also useful in evaluating the immune system's response to vaccines and monitoring the progression of diseases like HIV.</p>
<h2 class="test-h2 mt-3">Is fasting necessary for this test?</h2>
<p>No, fasting is not required for this test.</p>
