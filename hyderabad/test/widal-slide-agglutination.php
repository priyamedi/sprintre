						<h2 class="test-h2 mt-3">What does this test measure?</h2>
						<p>The Widal Slide Agglutination Test measures the presence and quantity of specific antibodies produced by your body in
							response to Salmonella typhi infection.</p>
						<h2 class="test-h2 mt-3">How often should this test be done?</h2>
						<p>This test is usually done when you display symptoms of typhoid fever. Repeat testing is not typically necessary
