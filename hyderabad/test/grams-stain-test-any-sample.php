<h2 class="test-h2 mt-3">When should this test be done?</h2>
<p>This test is done when a bacterial infection is suspected, based on your symptoms or other diagnostic tests. The
    exact timing will depend on your doctor's assessment.</p>
<h2 class="test-h2 mt-3">What information does the Gram's Stain Test provide?</h2>
<p>The Gram's Stain Test helps identify the presence of bacteria and classifies them into Gram-positive or
    Gram-negative, offering valuable initial information on the type of infection present.</p>
<h2 class="test-h2 mt-3">How often should this test be performed?</h2>
<p>The frequency of the test is situation-dependent and is typically performed when a bacterial infection is suspected.
