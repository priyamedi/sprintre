<h1 class="test-h1">IHC - CD5 </h1>
<div class="post-text">
<p>Immunohistochemistry (IHC) - CD5 is a diagnostic test used in pathology labs to help classify various types of lymphomas and leukemias. The CD5 marker is often expressed on the surface of certain immune cells like T cells and a subset of B cells. Its presence or absence can help in diagnosing specific types of lymphomas and leukemias.
 </p>
<p>This test uses antibodies designed to detect the presence of the CD5 protein in tissue samples. If the protein is present, the antibodies bind to it, allowing for its visualization under a microscope. CD5 plays a critical role in the activation and differentiation of T cells, which are crucial components of our immune system.
 </p>
 <p>The test is performed on a biopsy sample, which can be obtained from any part of the body where the suspected lymphoma or leukemia is present. This test doesn't require any specific preparation like fasting or restriction of certain medications. However, patients should always inform their doctor about their medical history and the medications they're currently taking.</p>
