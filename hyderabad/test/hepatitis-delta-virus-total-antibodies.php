<h2 class="test-h2 mt-3">Why is the Hepatitis Delta Virus Total Antibodies test done?</h2>
<p>The test is done to determine whether you have been infected with HDV, either currently or in the past. It can also
    help monitor the effectiveness of treatment in patients diagnosed with HDV.</p>
<h2 class="test-h2 mt-3">Is fasting required for this test?</h2>
<p>No, fasting is not required for the Hepatitis Delta Virus Total Antibodies test. You can eat and drink normally
    before the test.</p>
