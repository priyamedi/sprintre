<h2 class="test-h2 mt-3">What is the Albumin - Spot Urine test?</h2>
<p><strong>The Albumin - Spot Urine test</strong> measures the amount of albumin in a single, or "spot," sample of urine.</p>
<h2 class="test-h2 mt-3">Why is the Albumin - Spot Urine test done?</h2>
<p>This test is used to detect and monitor kidney disease. It is especially important for people at high risk of kidney disease, such as those with diabetes or high blood pressure.</p>
