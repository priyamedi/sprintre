<h2 class="test-h2 mt-3">What is the MRD-AML Panel?</h2>
<p> The MRD-AML Panel is a sophisticated diagnostic test that uses flow cytometry to detect minimal residual disease (MRD) in patients with Acute Myeloid Leukemia (AML).</p>

<h2 class="test-h2 mt-3">Do I need to fast or prepare for this test? </h2>
<p>No special preparation or fasting is necessary for this test. However, it's important to inform your doctor about any ongoing medications or health conditions prior to the test.</p>

