<h2 class="test-h2 mt-3"> How is this test performed?</h2>

<p>
This test is performed using a sample of blood. The blood sample is sent to a laboratory, where it is
analyzed for the presence of tTG and Dgp antibodies. </p>
<h2 class="test-h2 mt-3"> Do I need to fast before this test?</h2>

<p>
No, fasting is not typically required before this test. </p>
<h2 class="test-h2 mt-3"> What preparation is needed for this test?</h2>

<p>
No specific preparation is needed for this test. However, it is important to inform your doctor of any
medications or supplements you are taking as these may affect the test results. </p>
<h2 class="test-h2 mt-3"> How often should I get tested?</h2>

<p>

The frequency of testing will depend on your <a href="https://www.sprintdiagnostics.in/symptoms"><b style="color:#2C53A5">symptoms</b></a> and the recommendations of your healthcare
provider. </p>
<h2 class="test-h2 mt-3">How are the test results interpreted?</h2>

<p>
High levels of tTG and Dgp antibodies in the blood can be an indication of celiac disease. However,
further diagnostic tests may be needed to confirm the diagnosis. </p>
<h2 class="test-h2 mt-3">Are there any risks associated with this test?</h2>

<p>
There are minimal risks associated with this test as it simply involves a routine blood draw. </p>
<h2 class="test-h2 mt-3"> Can this test be done at home?</h2>

<p>
No, this test requires a blood sample to be taken by a healthcare professional and needs to be analyzed
in a laboratory. </p>
<h2 class="test-h2 mt-3"> What factors can affect the test results?</h2>

<p>
Certain factors such as recent gluten consumption, use of certain medications, and other autoimmune
conditions can affect the test results. </p>
<h2 class="test-h2 mt-3">What should I do if my test results are positive?</h2>

<p>
If your test results are positive, it means that you may have celiac disease. Your doctor will likely
recommend additional tests to confirm the diagnosis and discuss potential treatment options with you. </p>
<h2 class="test-h2 mt-3"> How is celiac disease treated?</h2>

<p>
The main treatment for celiac disease is a strict, lifelong gluten-free diet. </p>
<h2 class="test-h2 mt-3"> Can other conditions cause elevated tTG or Dgp levels?</h2>

<p>
Yes, other conditions such as type 1 diabetes, thyroid disease, and liver disease can also cause elevated
tTG or Dgp levels. </p>
<h2 class="test-h2 mt-3"> What is the difference between tTG and Dgp antibodies?</h2>

<p>
tTG antibodies are produced by the immune system in response to tissue transglutaminase, an enzyme
found in the gut. Dgp antibodies are produced in response to deamidated gliadin peptides, a component
of gluten. Both types of antibodies are commonly elevated in people with celiac disease. </p>
<h2 class="test-h2 mt-3"> Are tTG and Dgp tests the same?</h2>

<p>
Both tests measure the levels of specific antibodies in the blood that are commonly elevated in people
with celiac disease, but they are not the same test. The tTG test measures anti-tissue transglutaminase
antibodies, while the Dgp test measures Deamidated Gliadin Peptide antibodies. </p>
<h2 class="test-h2 mt-3"> How reliable is this test for diagnosing celiac disease?</h2>

<p>
The <strong>h-TTG/Dgp Screen</strong> is considered very reliable for diagnosing celiac disease, especially when
combined with other diagnostic methods such as a small intestine biopsy. </p>
<h2 class="test-h2 mt-3">Can I have celiac disease even if my test results are negative?</h2>

<p>

Yes, it is possible to have celiac disease even if your test results are negative. If you have symptoms of
celiac disease and your test results are negative, your doctor may recommend further testing. </p>
<h2 class="test-h2 mt-3"> Can I eat gluten before this test?</h2>

<p>
Yes, it is actually recommended to continue eating a normal diet that includes gluten before this test. If
you stop eating gluten before the test, it can affect the test results. </p>
<h2 class="test-h2 mt-3"> Can this test diagnose non-celiac gluten sensitivity?</h2>

<p>
No, this test cannot diagnose non-celiac gluten sensitivity. It is specifically designed to detect antibodies
associated with celiac disease. </p>
<h2 class="test-h2 mt-3"> Is there a cure for celiac disease?</h2>

<p>
While there is currently no cure for celiac disease, the condition can be effectively managed with a
strict, lifelong gluten-free diet.
