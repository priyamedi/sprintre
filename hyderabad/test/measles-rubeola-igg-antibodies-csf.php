<div class="post-text">
<p>Measles, or rubeola, is a highly infectious disease caused by a virus. While vaccination has significantly
reduced the prevalence of measles worldwide, it can still occur in unvaccinated populations or in
individuals with impaired immunity. Common symptoms include fever, cough, rash, and in severe cases,
it can cause inflammation of the brain known as encephalitis. </p>
<p>This test is used to identify the presence of Immunoglobulin G (IgG) antibodies against the measles virus
in the cerebrospinal fluid (CSF), the fluid found within the brain and spinal cord. The presence of these
antibodies in CSF can indicate that the virus has infected the central nervous system. </p>
