<h1 class="test-h1">Autoimmune Hepatitis Panel II (AMA M2, M2- 3E (Bpo), Sp100, PML, gp210,LKM1, LC1, SLA/LP, Ro52) </h1>
<div class="post-text">
<p>Autoimmune Hepatitis Panel II provides an in-depth evaluation of autoimmune hepatitis, a disease
characterized by the body's immune system launching an attack on liver cells. This results in
inflammation and possible liver damage. The panel includes a range of antibodies that can point towards
different facets of autoimmune hepatitis, aiding in diagnosis, and treatment planning. Understanding
the ins and outs of this test can empower patients and medical professionals in managing liver health
effectively. </p>
<p> The Autoimmune Hepatitis Panel II uses an immunofluorescence assay (IFA) to detect the presence of
different types of antibodies, including Anti-Mitochondrial Antibody M2 (AMA M2), M2-3E, Sp100,
Promyelocytic Leukaemia (PML), gp210, Anti-Liver Kidney Microsome Type 1 (Anti-LKM1), Liver Cytosol
Type 1 (LC1), Soluble Liver Antigen/Liver Pancreas (SLA/LP), and Ro52. These antibodies can signify
various forms of autoimmune hepatitis and related conditions, making this panel a comprehensive
diagnostic tool for autoimmune liver diseases.</p>
<p>Autoimmune hepatitis is categorized into two types based on the antibodies present. Type 1 is
associated with anti-nuclear antibodies (ANA), Anti-Smooth Muscle Antibody (ASMA), and at times,
SLA/LP. Type 2, though less common, is signified by Anti-LKM1 and LC1 antibodies. Certain antibodies
like AMA M2, Sp100, gp210, and PML are primarily linked to primary biliary cholangitis, another
autoimmune liver disease. The detection of these antibodies through the Autoimmune Hepatitis Panel II
contributes to a more precise diagnosis and helps establish an appropriate treatment plan.</p>
