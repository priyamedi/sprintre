	<h2 class="test-h2 mt-3">What does a high or low LH level indicate?</h2>
<p>In women, high LH levels can indicate Polycystic Ovary Syndrome (PCOS) or premature menopause, while low levels might indicate poor egg health. In men, high levels might indicate testicular failure, and low levels might indicate low testosterone.</p>
<h2 class="test-h2 mt-3">What does a high or low Prolactin level indicate? </h2>
<p>High prolactin levels can cause irregular menstrual cycles in women and low testosterone levels in men. Low prolactin levels are rare but might indicate pituitary gland problems.</p>
<h2 class="test-h2 mt-3">What does a high or low Estradiol (E2) level indicate?</h2> 
<p>High levels of Estradiol in women might indicate ovarian tumors or hyperthyroidism, while low levels might indicate menopause or poor ovarian reserve. In men, high levels might indicate gynecomastia, and low levels might indicate hypogonadism.</p>
<h2 class="test-h2 mt-3">What does a high or low TSH level indicate? </h2>
<p>A high TSH level typically indicates an underactive thyroid (hypothyroidism), while a low level indicates an overactive thyroid (hyperthyroidism).</p>
<h2 class="test-h2 mt-3">How is the Fertility Capsule II test performed? </h2>
<p>A healthcare professional will draw a blood sample from a vein in your arm using a small needle. The sample is then sent to a laboratory for analysis.</p>
<h2 class="test-h2 mt-3">Are there any risks associated with this test?</h2> 
<p>There is minimal risk associated with having blood drawn. Some people might experience slight pain or bruising at the site where the needle was inserted.</p>
<h2 class="test-h2 mt-3">What are the normal values for these hormones?</h2>
<p>Normal levels can vary, but generally, FSH is around 4.5-21.5 mIU/mL for women and 1.5-12.4 mIU/mL for men. LH is typically 1.9-12.5 mIU/mL for women and 1.24-7.8 mIU/mL for men. Prolactin varies throughout menstrual cycles but is generally under 20 ng/mL for women and under 15 ng/mL for men. Estradiol is 30-400 pg/mL for women and 10-50 pg/mL for men. TSH is typically 0.4-4.0 mIU/L.</p>
<h2 class="test-h2 mt-3"> Is fasting required for the test?</h2> 
<p>No, fasting is not required for the Fertility Capsule II test.</p>
<h2 class="test-h2 mt-3"> How often should this test be done? </h2>
<p>The frequency of the test can vary based on individual circumstances and should be determined by your doctor.</p>
<h2 class="test-h2 mt-3"> Which doctor should I consult if my levels are abnormal? </h2>
<p>If your levels are abnormal, you should consult your doctor, who may refer you to an endocrinologist or fertility specialist.</p>
<h2 class="test-h2 mt-3">Are there any factors that can affect the test results?</h2> 
<p>Yes, factors such as medications, stress, illness, and the phase of the menstrual cycle can affect hormone levels.</p>
<h2 class="test-h2 mt-3"> What is the next step if fertility issues are identified?</h2>
 <p>If fertility issues are identified, your doctor will guide you through the appropriate next steps, which may include additional testing, treatment, or referral to a specialist.</p>
<h2 class="test-h2 mt-3"> Can this test be done during pregnancy?</h2> 
<p>This test is generally not done during pregnancy, as hormone levels change significantly during this period.</p>
<p>Understanding the hormone levels in your body is crucial in assessing your reproductive health. If you are facing issues related to fertility or if you just want to have a check-up, the Fertility Capsule II test can be instrumental. It is always important to discuss the results and any potential health concerns with your doctor. Being proactive about your reproductive health can make all the difference in early diagnosis and effective treatment.</p>

