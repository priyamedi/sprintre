<h2 class="test-h2 mt-3">Why would a doctor order a JC Virus - DNA Detection by PCR - Plasma test?</h2>
<p>A doctor may order this test if you are exhibiting neurological symptoms suggestive of PML, especially if you are at
    high risk, such as those with HIV/AIDS, or individuals receiving immunosuppressive drugs after organ
    transplantation. It can also be ordered to monitor the status of the virus in patients known to be at risk.</p>
<h2 class="test-h2 mt-3">What is Progressive Multifocal Leukoencephalopathy (PML)?</h2>
<p>PML is a rare and serious brain infection caused by the reactivation of JC virus in people with severely weakened
    immune systems. It leads to inflammation and damage to the white matter of the brain, causing neurological symptoms
    that can worsen over time.</p>
