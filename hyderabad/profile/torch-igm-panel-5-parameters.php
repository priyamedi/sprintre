<h2 class="test-h2 mt-3">What is the purpose of the Torch IgM Panel (5 parameters) test?</h2>
<p>The purpose of this test is to detect recent or ongoing infections with Toxoplasma, CMV, Rubella, HSV-1, and HSV-2 by
    identifying specific IgM antibodies in your blood.</p>
<h2 class="test-h2 mt-3">Who should get the Torch IgM Panel (5 parameters) test?</h2>
<p>This test is typically recommended for pregnant women, individuals with suspected infections with one of these
    pathogens, or newborns with signs of a congenital infection.</p>
