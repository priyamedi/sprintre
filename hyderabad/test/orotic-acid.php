<h2 class="test-h2 mt-3">Why is the Orotic Acid test done?</h2>
<p>The Orotic Acid test is primarily used to detect inherited metabolic disorders such as Orotic Aciduria. In such
    conditions, an excess of orotic acid is present in the body, which is then excreted in the urine.</p>
<h2 class="test-h2 mt-3">When should one get this test?</h2>
<p>If a person is exhibiting symptoms of metabolic disorders like lethargy, failure to thrive, developmental delay,
    seizures, or has a family history of these disorders, an orotic acid test may be recommended.</p>
