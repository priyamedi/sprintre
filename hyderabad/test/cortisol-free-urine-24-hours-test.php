<h2 class="test-h2 mt-3">What is the Cortisol Free, Urine 24 Hours Test?</h2>
<p>The Cortisol Free, Urine 24 Hours Test is a diagnostic test that measures the amount of cortisol in the urine
    collected over a 24-hour period to assess adrenal function and detect disorders related to cortisol levels.</p>
<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>You will be provided with a special container to collect your urine over a 24-hour period. All the urine passed in
    the 24-hour period should be collected, starting from the second voided urine after waking up. The collected sample
    is then sent to a laboratory for analysis.</p>
