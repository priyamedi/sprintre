<h2 class="test-h2 mt-3">What is the Herpes Simplex Virus (HSV) Types 1 and 2 IgM - Serum test?</h2>
<p>The HSV Types 1 and 2 IgM test is a blood test that detects the presence of IgM antibodies against HSV-1 and HSV-2. A
    positive test result indicates a recent infection, while a negative result can suggest the absence of a recent
    infection.</p>
<h2 class="test-h2 mt-3">Why is this test ordered?</h2>
<p>The test is usually ordered when an individual presents with symptoms suggestive of an HSV infection such as cold
    sores or genital sores. It is also performed when a person is at risk of the infection due to their lifestyle or
    certain medical conditions.</p>
