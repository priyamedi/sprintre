<h2 class="test-h2 mt-3">What does the Niemann Pick Disease Quantitative Blood Test involve? </h2>
<p>This test involves drawing a blood sample from the patient, which is then analyzed in the laboratory for the presence and activity level of the sphingomyelinase enzyme. Low levels of this enzyme suggest a diagnosis of Niemann-Pick disease Type A or B.</p>

<h2 class="test-h2 mt-3">How long will it take to get the results? </h2>
<p>The turnaround time for the Niemann Pick Disease Quantitative Blood Test is typically 5-10 days, although this can vary depending on the specific laboratory.</p>

