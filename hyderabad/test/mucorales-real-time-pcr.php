<h2 class="test-h2 mt-3">Why is the Mucorales Real-Time PCR test done?</h2>
<p>The test is conducted when a patient shows signs of Mucormycosis, particularly in individuals with weakened immune
    systems. Symptoms can vary depending on which part of the body is affected but may include sinus pain or congestion,
    coughing, shortness of breath, skin lesions, and neurological symptoms.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The Mucorales Real-Time PCR test typically involves drawing a blood sample from a vein in your arm. This sample is
    then sent to the laboratory, where it undergoes the PCR process to detect the presence of Mucorales DNA.</p>
