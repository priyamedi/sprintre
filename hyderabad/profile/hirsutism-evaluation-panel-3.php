<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is primarily used to determine the cause of hirsutism, a condition that causes excessive hair growth in
    women. It can help to diagnose conditions like polycystic ovary syndrome (PCOS), adrenal gland disorders, and other
    hormonal imbalances.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test involves a simple blood draw. A healthcare professional will collect a blood sample from a vein in your arm
    using a small needle. The sample is then sent to a lab for analysis.</p>
