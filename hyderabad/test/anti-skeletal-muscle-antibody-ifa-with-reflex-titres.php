	<h2 class="test-h2 mt-3">Why is the ASKA test important? </h2>
	<p>The ASKA test is important because it helps to diagnose and monitor autoimmune diseases that affect the skeletal muscles, such as myasthenia gravis. It is especially valuable for identifying cases where thymoma (a tumor of the thymus gland) is present, which is often associated with high levels of these antibodies.
 </p>
	<h2 class="test-h2 mt-3">How is the test performed?
 </h2>
	<p>The ASKA test is performed using a blood sample. The blood is sent to a laboratory where the levels of anti-skeletal muscle antibodies are measured using immunofluorescence assay (IFA) with reflex titres.
 </p>
