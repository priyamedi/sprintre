<h2 class="test-h2 mt-3"> Why is the Anti-Cardiolipin Antibody IgG test done?</h2>
	<p>
This test is often used to help diagnose antiphospholipid syndrome, especially in patients who have had
unexplained blood clotting or recurrent miscarriages. It can also be used to evaluate patients with
systemic lupus erythematosus (SLE) or other autoimmune disorders.</p>
<h2 class="test-h2 mt-3"> How is the test performed?</h2>
<p>
A blood sample is taken from a vein in the arm. This is a standard procedure and poses minimal risks,
such as slight pain or bruising at the injection site.</p>
