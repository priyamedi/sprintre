	<h2 class="test-h2 mt-3">What role does Factor XII play in blood clotting? </h2>
<p>Factor XII is involved in the initiation of the intrinsic pathway of blood clotting. It gets activated upon contact with negatively charged surfaces and, in turn, activates other proteins that eventually lead to clot formation.</p>
<h2 class="test-h2 mt-3">Why is the Factor XII Activity test important?</h2>
 <p>
This test is important for diagnosing Factor XII deficiency and understanding its impact on the blood clotting process. It is particularly useful when there is an unexplained prolongation of clotting time in laboratory tests.</p>
