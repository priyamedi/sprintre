<h2 class="test-h2 mt-3"> Why is the AMH test done?</h2>
	<p>
The <strong>AMH test</strong> is primarily used to evaluate a woman’s ovarian reserve and fertility potential. It is also
used to predict the response of ovaries to fertility treatment, and in the diagnosis of polycystic ovary
syndrome (PCOS). In males, it is sometimes used to evaluate testicular function.</p>
<h2 class="test-h2 mt-3"> What do the levels of AMH indicate?</h2>
<p>
In women, high levels of AMH indicate a high number of remaining eggs, while low levels suggest a
lower ovarian reserve. Extremely high levels might be indicative of PCOS. In men, abnormal levels of
AMH can be associated with fertility issues and testicular dysfunction.</p>
