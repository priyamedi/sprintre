<h2 class="test-h2 mt-3">What is the Neisseria Gonorrhoeae - PCR test?</h2>
<p>The Neisseria Gonorrhoeae PCR test is a diagnostic method used to detect the bacterium Neisseria Gonorrhoeae, which
    causes the sexually transmitted infection known as gonorrhea. PCR, or Polymerase Chain Reaction, is a technique used
    to amplify small samples of DNA for easier detection and identification.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test is performed by collecting a swab sample from the patient's throat, rectum, or genital area, depending on
    where the infection is suspected. This sample is then sent to a lab for testing.</p>
