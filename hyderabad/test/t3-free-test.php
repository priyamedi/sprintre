<h2 class="test-h2 mt-3">Why is the Free T3 test ordered?</h2>
<p>The Free T3 test is ordered when a person has symptoms of hyperthyroidism or hypothyroidism, or to monitor the
    effectiveness of treatment for a thyroid disorder.</p>
<h2 class="test-h2 mt-3">What are the symptoms of hyperthyroidism?</h2>
<p>Symptoms of hyperthyroidism include nervousness, tremors, increased heart rate, weight loss, excessive sweating, and
    intolerance to heat.</p>
