	<h2 class="test-h2 mt-3">Why is a Bone Marrow Biopsy performed?
 </h2>
	<p>Bone Marrow Biopsy is performed to diagnose and monitor various diseases and conditions affecting the blood cells and bone marrow, such as anemia, cancers like leukemia, and other infections.
 </p>
	<h2 class="test-h2 mt-3">How is the procedure done?
 </h2>
	<p>The patient is made to lie down on a table. A local anesthetic is used to numb the area where the biopsy will be taken. A special needle is inserted through the skin and into the bone to extract a sample of the marrow. The procedure takes about 30 minutes.
 </p>
