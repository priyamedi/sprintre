<h2 class="test-h2 mt-3">What is the process of the test?</h2>
<p>The test requires a lumbar puncture (spinal tap) to collect a sample of cerebrospinal fluid. This procedure involves
    inserting a needle into the lower back to extract fluid. The sample is then analyzed in a laboratory for MBP.</p>
<h2 class="test-h2 mt-3">Is the test painful?</h2>
<p>The lumbar puncture procedure may cause some discomfort. An anesthetic is typically used to numb the area and
    minimize pain.</p>
