<h2 class="test-h2 mt-3">What does the IHC - Beta Catenin Test measure?</h2>
<p>The <strong>IHC - Beta Catenin test</strong> identifies the presence and distribution of the beta-catenin protein in a tissue sample. The pattern of beta-catenin can provide insight into the type and behavior of the cancer.</p>
<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is often done when a doctor suspects a specific type of cancer that is known to exhibit abnormalities in beta-catenin, such as colorectal cancer or hepatocellular carcinoma.</p>
