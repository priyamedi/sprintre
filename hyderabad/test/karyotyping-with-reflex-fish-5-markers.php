<p>Fluorescence In Situ Hybridization (FISH) is combined with traditional karyotyping in this procedure. FISH utilizes fluorescent probes that attach to specific chromosome segments. This is particularly useful for detecting particular genetic abnormalities that might not be visible through conventional karyotyping.</p>
<p>This test is usually performed after a miscarriage or termination to understand if chromosomal abnormalities were a contributing factor. </p>
