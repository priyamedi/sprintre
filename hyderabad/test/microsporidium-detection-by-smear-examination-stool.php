<h2 class="test-h2 mt-3">Why is the Microsporidium Detection by Smear Examination performed?</h2>
<p>This test is performed when a person exhibits symptoms consistent with microsporidiosis, such as chronic diarrhea,
    malabsorption, weight loss, and muscle pain. It is particularly important for people with compromised immune systems
    who are more susceptible to these infections.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test is performed on a stool sample, which is examined under a microscope. The sample is prepared with special
    stains to make the microsporidia easier to see.</p>
