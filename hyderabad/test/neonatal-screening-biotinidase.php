<h2 class="test-h2 mt-3">What does the Neonatal Screening for Biotinidase test for?</h2>
<p>The test screens for Biotinidase Deficiency, a rare genetic disorder that hampers the body's ability to recycle the
    vitamin biotin. The condition, if untreated, can cause developmental delays, hearing loss, and skin rashes.</p>
<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>The test involves a simple heel prick procedure where a small blood sample is collected from the newborn's heel. The
    sample is placed on a special filter paper and sent to the lab for further testing.</p>
