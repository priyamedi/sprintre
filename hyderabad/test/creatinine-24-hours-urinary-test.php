					<h2 class="test-h2 mt-3">What is Creatinine - 24 Hours Urinary Test?</h2>
<p>It is a test that measures the amount of creatinine excreted in the urine over a 24-hour period to assess kidney
    function.</p>
	<div class="mt-3"></div>
<h2 class="test-h2 mt-3">Why is the 24 Hours Urinary Creatinine Test performed?</h2>
<p>The test is performed to assess how well the kidneys are filtering wastes from the blood. It is useful in
    diagnosing kidney diseases, monitoring kidney function in patients with known kidney conditions, and evaluating
    potential kidney donors.</p>

	



					
