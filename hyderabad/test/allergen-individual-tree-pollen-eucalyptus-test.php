<h2 class="test-h2 mt-3"> What are the typical symptoms of an allergy to eucalyptus tree pollen?</h2>
	<p>
Eucalyptus tree pollen allergy can cause symptoms like sneezing, <b><a href="https://www.sprintdiagnostics.in/symptoms/dry-or-itchy-eyes">itchy or watery eyes</a></b>, runny or stuffy
nose, <b><a href="https://www.sprintdiagnostics.in/symptoms/persistent-cough">coughing</a></b>, and in some severe cases, <b><a href="https://www.sprintdiagnostics.in/symptoms/dyspnea-breath-shortness">breathing difficulties</a></b>.</p>
<h2 class="test-h2 mt-3">How is the Allergen, Individual - Tree Pollen Eucalyptus Test performed?</h2>
<p>
The test involves taking a blood sample from the patient, usually from a vein in the arm. This sample is
then sent to a laboratory where it's analyzed for IgE antibodies specific to eucalyptus pollen.</p>
