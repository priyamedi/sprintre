<h2 class="test-h2 mt-3">What is the Allergen, Individual - Food Apple test?</h2>
<p>The Allergen, Individual - Food Apple test is a blood test that checks for a specific type of allergy - apple
    allergy. It does this by measuring the quantity of specific IgE antibodies produced in response to apple proteins in
    your body.</p>
<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is done if you exhibit symptoms of an allergic reaction after consuming apples. These symptoms may include
    hives, itching, swelling, diarrhea, vomiting, difficulty breathing, or even anaphylaxis in severe cases.</p>
