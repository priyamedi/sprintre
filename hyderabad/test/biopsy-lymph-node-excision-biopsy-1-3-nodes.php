<h2 class="test-h2 mt-3">Why would I need a lymph node excision biopsy involving 1-3 nodes?</h2>
<p>If you have swollen or tender lymph nodes, or if imaging tests reveal abnormal lymph nodes, your doctor may recommend a biopsy to determine the cause. This can be important for diagnosing diseases like cancer, infections, or immune system disorders.</p>

<h2 class="test-h2 mt-3">What does the procedure entail?</h2>
<p>The procedure involves making a small incision in the skin to access the lymph nodes. The nodes are then removed and sent to a lab for analysis. The incision is closed with stitches. The procedure is usually performed under local anesthesia but can also be done under general anesthesia depending on the location of the lymph nodes and the patient's health.</p>

