certain forms of leukemia and lymphoma.</p>
<h2 class="test-h2 mt-3">Is fasting required before the test?</h2>
<p>
No, fasting is not required before the Lysozyme IHC test. The test is performed on a tissue sample that is
usually obtained through a biopsy.</p>
