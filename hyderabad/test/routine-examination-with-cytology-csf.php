<h2 class="test-h2 mt-3"> What conditions can a Routine Examination with Cytology - CSF help diagnose?</h2>
	<p>
This test can help diagnose a wide range of conditions affecting the nervous system, such as meningitis,
encephalitis, multiple sclerosis, and brain tumors. It can also help detect <ahref="https://www.sprintdiagnostics.in/our-services/nuclear-medicine/pet-ct-scan/"><b style="color:#2C53A5">cancers</b></a>
 that have spread to the
brain or spinal cord.</p>
<h2 class="test-h2 mt-3"> Is the collection procedure for this test painful?</h2>
<p>
While the procedure might cause discomfort, the area is numbed with a local anesthetic to minimize
pain. After the procedure, you may feel some soreness at the puncture site.</p>
