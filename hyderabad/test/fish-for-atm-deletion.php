<h2 class="test-h2 mt-3">  What is the purpose of the FISH test for ATM deletion?</h2>
	<p>This test is used to detect the deletion of the ATM gene, which can be associated with Ataxia-
telangiectasia (A-T) and an increased risk of several types of cancer.</p>
<h2 class="test-h2 mt-3"> Who should consider undergoing this test?</h2>
<p>Individuals with symptoms of Ataxia-telangiectasia, a family history of A-T, or a known ATM mutation in
the family should consider this test. Additionally, individuals with a family history of breast, pancreatic,
or other cancers linked to the ATM gene might benefit from this test.</p>
