<h2 class="test-h2 mt-3">What is the Allergy - Asthma / Rhinitis Screening Panel 1 - Adult test?</h2>
<p>This test measures the level of specific Immunoglobulin E (IgE) antibodies in the blood against a range of common allergens. A high level of these antibodies indicates an allergic reaction to the tested substances.</p>
<h2 class="test-h2 mt-3">Do I need to fast before this test?</h2>
<p>No, fasting is not necessary for this test. You can maintain your regular diet and hydration.</p>
