<h2 class="test-h2 mt-3">What are the symptoms of a melon allergy?</h2>
<p>Symptoms of a melon allergy can include itching or tingling in the mouth, swelling of the face, lips, tongue, or throat, hives, abdominal pain, nausea, vomiting, diarrhea, and in severe cases, anaphylaxis.</p>
<h2 class="test-h2 mt-3">How is the Allergen, Individual - Food Melon test performed?</h2>
<p>A healthcare professional will take a blood sample from your arm. The sample is then sent to a lab, where it's analyzed for the presence of specific IgE antibodies that react against melon proteins.</p>
