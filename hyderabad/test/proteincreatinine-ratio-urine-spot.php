<h2 class="test-h2 mt-3"> What is a Protein/Creatinine Ratio - Urine Spot test?</h2>
	<p>
This test measures the amount of protein and creatinine in a urine sample and calculates their ratio.
The result helps estimate the amount of protein that is being lost through the kidneys.</p>
<h2 class="test-h2 mt-3"> Why is the Protein/Creatinine Ratio - Urine Spot test ordered?</h2>
<p>
The test is usually ordered when a doctor suspects kidney disease or when you have conditions like
diabetes or hypertension which can affect the kidneys. It can also be used to monitor kidney function in
patients already diagnosed with kidney disease.</p>
