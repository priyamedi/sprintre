<h2 class="test-h2 mt-3"> What does a hypertension profile test measure?</h2>
	<p>
A <strong>hypertension profile</strong> measures various blood parameters that can be affected by high blood pressure.
These include kidney function, blood sugar levels, electrolytes, and cholesterol levels. All these
components together give a detailed overview of how your body is managing the high blood pressure. </p>
<h2 class="test-h2 mt-3">Why do I need a hypertension profile test?</h2>
<p>
If you have been diagnosed with hypertension, this profile helps your healthcare provider monitor the
effectiveness of your treatment and understand how hypertension is affecting your body. It helps to
detect any complications early on and adjust your treatment plan accordingly. </p>
