<h2 class="test-h2 mt-3"> Why is the Autoimmune Hepatitis Panel I ordered?</h2>
	<p>
The Autoimmune Hepatitis Panel I is ordered when a patient presents with signs or symptoms
suggestive of liver disease, such as jaundice, fatigue, abdominal discomfort, or abnormal liver function
tests, and autoimmune hepatitis is suspected.</p>
<h2 class="test-h2 mt-3"> What does the presence of specific antibodies mean?</h2>
<p>
The presence of specific antibodies can indicate different types of autoimmune hepatitis. For example,
ANA and ASMA are usually associated with type 1 autoimmune hepatitis, while Anti-LKM1 is related to
type 2.</p>
