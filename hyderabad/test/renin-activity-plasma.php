<h2 class="test-h2 mt-3">Why might I need a Renin Activity, Plasma test?</h2>
<p>Your doctor might order this test if you have high blood pressure that isn't responding well to treatment, especially
    if you also have low potassium levels. The test can help determine whether your high blood pressure is due to
    overactivity of the renin-angiotensin-aldosterone system.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test involves a simple blood draw. The blood sample is then sent to a laboratory where the renin activity in your
    plasma is measured.</p>
