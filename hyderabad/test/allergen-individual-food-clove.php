<h2 class="test-h2 mt-3">How is the Allergen, Individual - Food Clove test performed?</h2>
<p>A simple blood draw, usually from a vein in your arm, is all that's needed for this test. The extracted blood is then
    sent to a laboratory for analysis of specific IgE antibodies against clove proteins.</p>
<h2 class="test-h2 mt-3">Who should get this test?</h2>
<p>Anyone who experiences signs of an allergic reaction after eating clove or foods containing clove should consider
    taking this test. Symptoms might include skin rashes, itching, swelling of the face, difficulty breathing, stomach
    pain, or even anaphylaxis in severe cases.</p>
