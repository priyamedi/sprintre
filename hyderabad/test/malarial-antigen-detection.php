<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is done to diagnose malaria and differentiate between Plasmodium vivax and Plasmodium
falciparum infections.</p>

<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>A healthcare provider will take a small sample of your blood using a needle. This sample is then tested
for the presence of malaria antigens.</p>
