<h1 class="test-h1">NIPT test for Fetal Trisomy 13, 18, 21 and Other Chromosomal Aneuploidy</h1>
<div class="post-text">
<p>Non-Invasive Prenatal Testing (NIPT), also known as cell-free DNA screening, is a method of determining the risk that the fetus will be born with certain genetic abnormalities. This test looks for specific trisomies (conditions in which there is an extra chromosome) as well as other types of chromosomal aneuploidy (abnormal number of chromosomes). This test is usually performed on a sample of the mother's blood.</p>
<p>The primary focus of the NIPT test is on three specific chromosomal abnormalities: Trisomy 21 (Down syndrome), Trisomy 18 (Edwards syndrome), and Trisomy 13 (Patau syndrome). These are the most common trisomies that survive past birth. However, the NIPT test can also screen for other less common chromosomal abnormalities.</p>
