<h2 class="test-h2 mt-3">What is the Flow Cytometry Panel for MRD B-ALL? </h2>
<p>The Flow Cytometry Panel for MRD B-ALL is a diagnostic procedure that uses flow cytometry to detect and quantify the minimal residual disease (MRD), i.e., the cancer cells that remain after treatment in B-cell Acute Lymphoblastic Leukemia (B-ALL).</p>

<h2 class="test-h2 mt-3">Why is this test performed? </h2>
<p>This test is performed to assess the efficacy of the treatment and to detect any residual cancer cells that may lead to a relapse. The data obtained from the test also helps in determining the next course of treatment, if necessary.</p>

