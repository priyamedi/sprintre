<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>This test is performed to detect the presence of IgG and IgM antibodies that are indicative of a viral
infection causing meningitis.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test is performed on a sample of cerebrospinal fluid (CSF) which is collected via a lumbar puncture
or "spinal tap". This is a procedure where a small amount of fluid is extracted from the spinal canal for
testing.</p>
