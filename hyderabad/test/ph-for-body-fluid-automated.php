	<h2 class="test-h2 mt-3">How is the body fluid sample collected?</h2>
<p>The method of collecting the body fluid sample depends on the type of fluid. For example, a sample of cerebrospinal fluid is collected via a procedure known as a lumbar puncture, where a needle is inserted into the lower spine. A pleural fluid sample is collected via thoracentesis, a procedure that involves inserting a needle into the pleural space in the chest.</p>

