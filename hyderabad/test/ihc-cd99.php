<h1 class="test-h1">IHC - CD99</h1>
<div class="post-text">
<p>Immunohistochemistry (IHC) involves the process of detecting antigens (e.g., proteins) in cells of a tissue section by exploiting the principle of antibodies binding specifically to antigens in biological tissues. The IHC - CD99 test specifically targets and detects the CD99 antigen.</p>
<p>CD99 is a cell surface glycoprotein expressed in a variety of tissues and cells. Although it is found in many types of cells, its expression is particularly high in leukocytes and Ewing's sarcoma cells, a type of cancer that affects the bone or the soft tissue around the bones. Because of this, the IHC - CD99 test is often used to help diagnose and classify Ewing's sarcoma and other similar types of cancers. It can be a crucial tool in histopathology when diagnosing certain diseases.</p>

