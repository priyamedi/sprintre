<div class="post-text">
<p><strong>Alpha-fetoprotein (AFP)</strong> is a protein that is produced by the liver and yolk sac of a developing fetus during pregnancy. The IHC - AFP test is an immunohistochemical analysis that measures the amount of AFP in tissue samples. High levels of AFP can be a sign of certain types of cancers such as liver cancer (hepatocellular carcinoma) and germ cell tumors.
 </p>
<p>Immunohistochemistry (IHC) uses antibodies to detect the presence of specific proteins, such as AFP, in tissue samples. The test is useful in diagnosing, staging and monitoring the treatment of these cancers.
 </p>
