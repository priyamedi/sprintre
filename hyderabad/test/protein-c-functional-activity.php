	<h2 class="test-h2 mt-3">How is the Protein C Functional Activity test performed?</h2>
<p>This test is performed on a blood sample taken from a vein in your arm. The sample is then sent to a laboratory,
    where it is analyzed for Protein C activity.</p>
<h2 class="test-h2 mt-3">What does a high result mean?</h2>
<p>A high result in a Protein C Functional Activity test is generally not a concern and does not increase the risk of
    blood clots. It could reflect the body's response to excessive clotting, such as in conditions like disseminated
    intravascular coagulation (DIC).</p>
<h2 class="test-h2 mt-3">What does a low result mean?</h2>
<p>Low levels of Protein C activity can increase your risk of developing abnormal blood clots. This might be due to
    inherited Protein C deficiency, liver disease, Vitamin K deficiency, or use of certain medications.</p>
<h2 class="test-h2 mt-3">How often should I get tested?</h2>
<p>The frequency of testing largely depends on your medical and family history, and your doctor's recommendation. If you
    have a known Protein eficiency or a history of unexplained blood clots, regular testing might be advised.</p>
<h2 class="test-h2 mt-3">What are the normal ranges for Protein C activity?</h2>
<p>Normal ranges for Protein C activity can vary between laboratories, but generally, it's around 70-140% of normal.
    Your doctor will interpret your results based on the lab's reference range.</p>
<h2 class="test-h2 mt-3">Are there any precautions I should take before the test?</h2>
<p>No special precautions are needed before a Protein C Functional Activity test. However, make sure to inform your
    healthcare provider about any medications or supplements you're taking, as they may influence the test results.</p>
<h2 class="test-h2 mt-3">What factors could affect my test results?</h2>
<p>Various factors could affect the test results, including certain medications, pregnancy, liver disease, and Vitamin K
    status.</p>
<h2 class="test-h2 mt-3">Who should I consult if my results are abnormal?</h2>
