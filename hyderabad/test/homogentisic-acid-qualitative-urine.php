<h1 class="test-h1">Homogentisic Acid (Alkaptonuria) - Qualitative - Urine </h1>
<div class="post-text">
<p><strong>Homogentisic Acid (HGA)</strong> is a byproduct of the metabolism of two specific amino acids, tyrosine and phenylalanine. Normally, the body further breaks down HGA to be eliminated. However, individuals with alkaptonuria, a rare inherited genetic disorder, lack the necessary enzyme to complete this metabolic step, leading to an accumulation of HGA in the body. The excess HGA is excreted in urine. This test is used to detect the presence of HGA in the urine and thus diagnose alkaptonuria.
 </p>
<p><strong>Alkaptonuria</strong> is typically characterized by dark-colored urine and, over time, can cause a variety of health issues including arthritis, heart disease, and kidney stones. As it's a genetic condition, it's often detected in early childhood </p>
