<h1 class="test-h1">Osmolal Gap - Serum (Sodium, BUN, Glucose, Osmolality)</h1>
<div class="post-text">
<p>The osmolal gap is a calculation derived from the osmolality, which is a measure of the concentration of all the particles in a solution. It is used as a diagnostic tool to identify various conditions that can affect the balance of fluid and electrolytes in the body.</p>
<p>The Osmolal Gap - Serum (Sodium, BUN, Glucose, Osmolality) test measures the difference between the calculated osmolality (total concentration of dissolved particles in the blood) and the measured osmolality. The test includes the measurement of sodium, blood urea nitrogen (BUN), glucose, and osmolality levels in the blood.</p>
<p>In a healthy individual, the osmolal gap should be small, usually less than 10 milliosmoles per kilogram (mOsm/kg). A larger gap indicates the presence of osmotically active substances in the blood that are not being measured by the standard osmolality test, such as ethanol, methanol, ethylene glycol, or mannitol. These substances can alter the body's fluid balance and may be indicative of certain conditions like metabolic disorders, intoxication, or renal disease.</p>
<p>This test is especially useful in emergency situations, where it can help identify poisoning from substances like methanol or ethylene glycol. Additionally, it may be used to monitor patients with chronic diseases or disorders that can affect electrolyte balance.</p>
