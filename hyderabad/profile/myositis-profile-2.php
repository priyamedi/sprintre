<p>Myositis is a general term that represents inflammation of the muscles. The cause of myositis can be an autoimmune condition, infection, injury, or certain medications. The Myositis Profile-2 test is a comprehensive panel designed to detect specific antibodies that can provide vital information about the type and severity of myositis. This particular panel investigates eleven antigens: Mi2, Ku, PMScl 100, PM Scl 75, Jo1, SRP, PL7, PL-12, EJ, OJ, Ro52. These antibodies are often associated with different types of myositis, including dermatomyositis, polymyositis, and inclusion body myositis.</p>
