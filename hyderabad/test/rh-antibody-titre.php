<h2 class="test-h2 mt-3"> Why is the Rh Antibody Titre test performed?</h2>
	<p>
This test is performed to determine if a person has developed antibodies against the Rh antigen,
especially in an Rh-negative pregnant woman carrying an Rh-positive baby. It's also done after an Rh-
incompatible blood transfusion or organ transplant.</p>
<h2 class="test-h2 mt-3"> What is Rh sensitization?</h2>
<p>
Rh sensitization occurs when an Rh-negative individual is exposed to Rh-positive blood, stimulating their
immune system to produce antibodies against the Rh antigen. This can cause complications in
subsequent pregnancies or after Rh-incompatible blood transfusions.</p>
