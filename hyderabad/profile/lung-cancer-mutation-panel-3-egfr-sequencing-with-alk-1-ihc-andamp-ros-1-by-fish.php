<h1 class="test-h1">Lung Cancer Mutation Panel 3- EGFR Sequencing with ALK-1 IHC & ROS-1 by FISH </h1>
<div class="post-text">
<p>Lung cancer is a serious condition characterized by the uncontrolled growth of cells in the lungs. These cells can sometimes have mutations, or changes, in their DNA that allow them to grow and divide more rapidly. The Lung Cancer Mutation Panel 3 aims to identify some of these mutations. Specifically, it looks for changes in the EGFR gene by sequencing, ALK gene rearrangements by immunohistochemistry (IHC) using ALK-1 antibody, and ROS-1 gene rearrangements using Fluorescence in situ hybridization (FISH) technique.
 </p>
<p>EGFR, ALK, and ROS-1 are all genes that provide instructions for making proteins involved in cell growth and division. Mutations or changes in these genes can lead to an overactive protein, triggering the development of lung cancer. Identifying these mutations helps doctors understand more about each patient's unique form of cancer, potentially leading to more targeted and effective treatment strategies. </p>
