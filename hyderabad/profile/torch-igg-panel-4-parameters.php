<p>IgG antibodies are produced by the immune system in response to an infection. They remain in the blood for a long
    time after the infection has passed, indicating past infection or immunity.</p>
<h2 class="test-h2 mt-3">How is the sample collected for this test?</h2>
<p>A healthcare provider will draw a small amount of blood from a vein in your arm. The procedure is usually quick and
