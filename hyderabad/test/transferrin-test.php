<h2 class="test-h2 mt-3">What is a Transferrin test?</h2>
<p>A Transferrin test is a blood test that measures the amount of transferrin in your blood. Transferrin is a protein
    that transports iron throughout the body.</p>
<h2 class="test-h2 mt-3">Why might my doctor order a Transferrin test?</h2>
<p>Your doctor might order a Transferrin test if you have symptoms of anemia or iron overload. These symptoms might
    include fatigue, weakness, pale skin, or frequent infections.</p>
