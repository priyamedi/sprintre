<div class="post-text">
<p>DiGeorge Syndrome, also known as 22q11.2 deletion syndrome, is a disorder caused by the deletion of a small segment of chromosome 22. This deletion can result in the poor development of several body systems. FISH, or Fluorescence In Situ Hybridization, for DiGeorge Syndrome is a diagnostic test used to detect microdeletion in chromosome 22q11.2 in amniotic fluid. This test is usually done during pregnancy to determine whether the fetus has DiGeorge Syndrome.</p>
<p>When a fetus is suspected of having DiGeorge Syndrome, either due to family history, abnormal ultrasound findings, or other factors, a FISH test can be performed on a sample of amniotic fluid. This fluid surrounds the fetus in the womb and contains fetal cells that can be analyzed for chromosomal abnormalities.</p>
