	<h2 class="test-h2 mt-3">What are the symptoms of a lemon allergy?
 </h2>
	<p>The symptoms of a lemon allergy can range from mild to severe and may include itching or tingling in the mouth, hives, rash, nausea, vomiting, abdominal pain, or difficulty breathing. In severe cases, it may cause anaphylaxis, a life-threatening allergic reaction.
 </p>
	<h2 class="test-h2 mt-3">How is the Allergen, Individual - Food Lemon test performed?
 </h2>
	<p>The test is performed using a blood sample. A healthcare professional will draw blood from a vein, typically in the arm, and the sample is sent to a laboratory for analysis.
 </p>
