<h2 class="test-h2 mt-3">What are IgM antibodies?</h2>
<p>IgM antibodies are the first antibodies produced by the immune system in response to an infection. They typically
    indicate a recent or ongoing infection.</p>
<h2 class="test-h2 mt-3">How is the sample collected for this test?</h2>
<p>The sample is collected via a standard blood draw. A healthcare professional will insert a needle into a vein in your
    arm and collect a small amount of blood.</p>
