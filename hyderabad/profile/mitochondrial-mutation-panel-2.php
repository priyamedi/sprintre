<h2 class="test-h2 mt-3">What is the purpose of the Mitochondrial Mutation Panel-2?</h2>
<p>This panel is used to detect specific mutations in the mitochondrial DNA associated with a variety of mitochondrial
    disorders. If you exhibit symptoms of these disorders, this test can assist in the diagnosis and guide subsequent
    treatment plans.</p>
<h2 class="test-h2 mt-3">What should I expect during the test?</h2>
<p>This test requires a blood sample, which is usually taken from a vein in your arm. A healthcare provider will clean
    the site with an antiseptic, then insert a small needle to collect the sample. It is a quick and relatively painless
    procedure.</p>
