<h2 class="test-h2 mt-3">Why is the Total T4 test done?</h2>
<p>Total T4 test is used to help diagnose hyperthyroidism and hypothyroidism. It is a tool your doctor might use in
    conjunction with other thyroid tests (like TSH and T3) to understand the cause of your thyroid dysfunction.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test involves a normal blood sample. A healthcare professional will collect blood from a vein in your arm using a
    small needle. After the needle is inserted, a small amount of blood is collected into a test tube or vial.</p>
