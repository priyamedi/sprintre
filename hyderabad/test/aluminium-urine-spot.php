<h2 class="test-h2 mt-3"> Why is the <strong>Aluminium - Urine Spot test</strong> done?</h2>
	<p>
This test is used to determine the levels of aluminum in the urine, which can help to evaluate a person’s
exposure to aluminum and assess the risk of aluminum toxicity.</p>
<h2 class="test-h2 mt-3"> What are the symptoms of aluminum toxicity?</h2>
<p>
Symptoms of aluminum toxicity may include fatigue, muscle weakness, bone pain, anemia, impaired
kidney function, and neurological symptoms such as confusion and memory problems.</p>
