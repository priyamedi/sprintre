<h2 class="test-h2 mt-3">Why is a urine spot test for lead done?</h2>
<p>This test is done to check for lead exposure. A spot urine test can be used to monitor people who work in industries
	where lead is used, or individuals who live in older homes with lead-based paint or pipes.</p>
<h2 class="test-h2 mt-3">How is the urine spot test performed?</h2>
<p>For this test, a single urine sample is collected at any time during the day.</p>
