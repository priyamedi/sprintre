<h1 class="test-h1">IHC - CD20 </h1>
<div class="post-text">
<p>The <strong>IHC - CD20</strong> test is an immunohistochemical analysis used to detect CD20, a protein widely present on B-cell lymphomas, on the surface of cells in tissue samples. This test is a vital tool in the pathology laboratory and plays a critical role in diagnosing and classifying B-cell lymphomas, which are types of cancer that start in <a href="https://www.sprintdiagnostics.in/hyderabad/test/total-leukocyte-count"><b style="color:#2C53A5">white blood cells </b></a>(lymphocytes). </p>
<p>CD20 is a non-glycosylated phosphoprotein expressed on the surface of all B-cells beginning at the pro-B phase (CD45R+, CD117+) and progressively increasing in concentration until maturity. In normal and reactive conditions, CD20 demonstrates a strong, homogeneous staining pattern, making it a reliable marker for B cells. </p>
