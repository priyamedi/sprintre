<h2 class="test-h2 mt-3">What is Hepatitis B Core Antibody IgM (Anti-HBc IgM) test?</h2>
<p>The Anti-HBc IgM test detects the presence of Immunoglobulin M (IgM) antibodies that are produced in response to the hepatitis B core antigen. These antibodies usually appear in the early stages of hepatitis B infection and indicate a recent infection.</p>
<h2 class="test-h2 mt-3">Why is the Anti-HBc IgM test important?</h2>
<p>This test is crucial for the early detection of an acute Hepatitis B infection. Knowing whether an infection is acute or chronic helps in making decisions about the need for monitoring, treatment, and notifying contacts.</p>
