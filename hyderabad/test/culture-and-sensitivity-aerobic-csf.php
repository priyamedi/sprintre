<h2 class="test-h2 mt-3">What is a CSF culture?</h2>
<p> A CSF culture is a laboratory test that is used to detect the presence of bacteria in the cerebrospinal
fluid (CSF). It helps in diagnosing infections within the central nervous system, such as meningitis.</p>

<h2 class="test-h2 mt-3"> How is the CSF sample collected?</h2>
<p> The CSF sample is usually collected through a procedure known as a lumbar puncture or spinal tap,
where a needle is inserted into the lower back to draw fluid from the spinal canal.</p>
