								<h2 class="test-h2 mt-3">What does this test measure?</h2>
								<p>The test measures various blood biomarkers related to liver health. These include alpha2-macroglobulin, haptoglobin, apolipoprotein A1, bilirubin, and gamma-glutamyl transpeptidase (GGT). Based on these markers, the test provides an estimation of liver fibrosis and inflammation.</p>

								<h2 class="test-h2 mt-3">How frequently should this test be done?</h2>
								<p>The frequency of testing depends on the specific circumstances of each patient and the judgment of the healthcare provider. It's usually performed at the initial diagnosis of a liver disease and then periodically to monitor the disease's progression or response to treatment.</p>

								<h2 class="test-h2 mt-3">What are normal values for this test?</h2>
