	<h2 class="test-h2 mt-3">What is the purpose of a bone biopsy?</h2>
	<p>A bone biopsy helps to diagnose conditions affecting the bone such as infections, bone cancers, and other disorders. It can also help to evaluate the extent of diseases and assess the effectiveness of ongoing treatments.</p>
	<h2 class="test-h2 mt-3">How is a core bone biopsy performed?</h2>
	<p>A core bone biopsy is performed under local or general anesthesia. The doctor will make a small incision over the area where the bone sample will be taken. A needle is inserted through the skin and into the bone to remove a core of bone tissue. The procedure usually takes about 30 minutes to an hour.</p>
