<h2 class="test-h2 mt-3">What does a positive IHC - CD31 test result mean?</h2>
<p> A positive test result means that the cells tested show the presence of the CD31 antigen. This is often an indication of a tumor of vascular origin, such as an angiosarcoma.</p>
<h2 class="test-h2 mt-3">Are there any risks associated with the IHC - CD31 test?</h2>
<p> The <strong>IHC - CD31</strong> test is performed on a tissue sample, so the risks are mainly related to the tissue collection procedure, which might include <a   href="https://www.sprintdiagnostics.in/symptoms/frequent-infections"><b style="color:#2C53A5">infection</b></a>, bleeding, or other complications. The test itself does not pose any direct risks to the patient.</p>
<h2 class="test-h2 mt-3">What other tests might be done in conjunction with the IHC - CD31 test?</h2>
<p> Your doctor may order other IHC tests, or other types of tests such as a biopsy or imaging tests, to confirm a diagnosis or to gather more information about the tumor.</p>
<h2 class="test-h2 mt-3">What factors can affect the results of an IHC - CD31 test?</h2>
<p> The quality of the tissue sample and the specifics of how the test is performed can influence the results. It's also possible for certain medications or other medical conditions to affect the test results.</p>
<h2 class="test-h2 mt-3">Are there any special preparations required before the IHC - CD31 test?</h2>
<p> No special preparations are required before the <strong>IHC - CD31</strong> test. You can eat and drink normally unless your doctor gives you other instructions.</p>
