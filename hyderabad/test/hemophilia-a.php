<h2 class="test-h2 mt-3">What is Hemophilia A and why is screening for Inversion 22 important?</h2>
<p>Hemophilia A is caused by a deficiency in clotting factor VIII. Inversion 22, a specific genetic mutation, is
    responsible for nearly half of severe Hemophilia A cases. Screening for this mutation is vital for diagnosis,
    management, and genetic counseling.</p>
<h2 class="test-h2 mt-3">What does the screening process involve?</h2>
<p>The test involves drawing a blood sample from the patient. The sample is then analyzed in a laboratory for the
    presence of Inversion 22 in the F8 gene.</p>
