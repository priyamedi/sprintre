	<h2 class="test-h2 mt-3">What is the Albumin - CSF test?
 </h2>
	<p>The Albumin - CSF test is a diagnostic procedure that measures the level of albumin in the cerebrospinal fluid. </p>
	<h2 class="test-h2 mt-3">Why is the Albumin - CSF test done? </h2>
	<p>The test is done to detect abnormalities in the blood-brain barrier and the meninges. It can help diagnose conditions such as meningitis, multiple sclerosis, brain tumor, or traumatic injury to the brain or spinal cord.
 </p>
