<h2 class="test-h2 mt-3">Why is the Thyroid Profile - V test done?</h2>
<p>This test is done to evaluate your thyroid function and to help diagnose thyroid diseases. It may be ordered if you
    have symptoms of a thyroid disorder or to monitor the effectiveness of treatment if you have a known thyroid
    disease.</p>
<h2 class="test-h2 mt-3">How is the Thyroid Profile - V test performed?</h2>
<p>This test is performed using a blood sample. A healthcare provider will draw a small amount of blood from your vein,
    typically in your arm. The sample is then sent to a laboratory for analysis.</p>
