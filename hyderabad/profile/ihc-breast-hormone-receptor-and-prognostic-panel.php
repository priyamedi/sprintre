	<h2 class="test-h2 mt-3">When should I get this test done?</h2>
<p>The test is typically recommended after a diagnosis of breast cancer to help characterize the tumor and determine the most appropriate treatment.</p>

	<h2 class="test-h2 mt-3">What does the test measure?</h2>
<p>This test measures the presence and level of four key markers in breast cancer cells - Estrogen Receptor (ER), Progesterone Receptor (PR), Human Epidermal Growth Factor Receptor 2 (HER2Neu), and Ki67.</p>

	<h2 class="test-h2 mt-3">How often should this test be performed?</h2>
<p>This test is typically performed at the time of diagnosis to determine the characteristics of the tumor. Depending on the situation, it might be repeated if there is recurrence or if the nature of the tumor seems to have changed.</p>

	<h2 class="test-h2 mt-3">What are the normal values for these markers?</h2>
<p>The interpretation of the results depends on the type of cancer and the specific markers. Your doctor will discuss the results with you in relation to your specific situation.</p>

	<h2 class="test-h2 mt-3">What precautions need to be taken before and after this test?</h2>
<p>No specific precautions are needed before or after this test. However, you should follow your doctor's advice regarding the biopsy or surgery that provides the tissue sample.</p>

	<h2 class="test-h2 mt-3">What factors can affect the levels of these markers?</h2>
<p>Several factors can influence the levels of these markers, including the type and stage of cancer, menopausal status, and individual genetic factors. It's important to discuss these factors with your doctor.</p>

	<h2 class="test-h2 mt-3">Which doctor should I consult if the test values are abnormal?</h2>
<p>You should consult with a medical oncologist, who specializes in treating cancer with medications, including chemotherapy.</p>

	<h2 class="test-h2 mt-3">Can I do this test at home?</h2>
<p>No, this test requires a tissue sample that needs to be obtained in a medical setting, and the analysis should be performed in a specialized laboratory.</p>

	<h2 class="test-h2 mt-3">What are the modifiable factors affecting these markers?</h2>
<p>Modifiable factors that may affect these markers include body weight, physical activity, and alcohol consumption. Studies suggest that maintaining a healthy weight, regular exercise, and moderating alcohol intake can influence the biology of breast tumors.</p>

	<h2 class="test-h2 mt-3">What are the non-modifiable factors affecting these markers?</h2>
<p>Non-modifiable factors that can influence these markers include age, genetics, and menopausal status. For instance, the likelihood of having hormone receptor-positive breast cancer increases with age and it is often higher in postmenopausal women.</p>

	<h2 class="test-h2 mt-3">How will this test affect my treatment plan?</h2>
<p>The results of this test play a crucial role in deciding your treatment plan. For example, if your cancer is ER or PR positive, hormonal therapies may be effective. If it is HER2Neu positive, targeted therapies might be chosen. If Ki67 levels are high, it indicates a more aggressive tumor and may lead to a more intensive treatment approach.</p>

	<h2 class="test-h2 mt-3">Is this test painful?</h2>
<p>The pain associated with this test is not from the test itself, but from the procedure used to collect the tissue sample. You might feel discomfort or slight pain during the biopsy, but your healthcare provider will use local anesthesia to minimize any discomfort.</p>

	<h2 class="test-h2 mt-3">Are there any risks associated with this test?</h2>
<p>The test itself poses no risks. However, there may be slight risks associated with the biopsy or surgical procedure used to collect the tissue sample, such as infection, bleeding, or an adverse reaction to anesthesia. Your doctor will discuss these potential risks with you prior to the procedure.</p>

<p>Understanding your breast cancer at the molecular level through tests like the <strong>IHC - Breast Hormone Receptor and Prognostic Panel</strong> (ER, PR, HER2Neu, Ki67) can play a vital role in personalizing your treatment plan. This can ensure you receive the most effective therapies for your specific type of cancer. Always remember, every step taken in diagnosing and treating your cancer, including this test, is bringing you one step closer to recovery. If you have any further questions, please consult your healthcare provider.</p>

