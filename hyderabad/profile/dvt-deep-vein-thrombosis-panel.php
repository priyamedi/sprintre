	<h2 class="test-h2 mt-3">What is Antithrombin-III and why is it important? </h2>
	<p>Antithrombin-III is a protein that helps regulate blood clotting. A deficiency in Antithrombin-III can cause
excessive clotting, increasing the risk of DVT. </p>
	<h2 class="test-h2 mt-3">Why are Protein C and Free Protein S antigen included in this panel? </h2>
	<p>Protein C and Protein S are proteins that help prevent blood clotting. Low levels of these proteins can
lead to an increased risk of forming blood clots and therefore, DVT. </p>
	<h2 class="test-h2 mt-3">What is Lupus Anticoagulant and why is it tested? </h2>
	<p> Lupus Anticoagulant is an antibody that increases the risk of blood clotting. It is tested to determine if an
autoimmune disorder is contributing to an increased risk of DVT.</p>
	<h2 class="test-h2 mt-3">What are Cardiolipin IgG and IgM and Beta2 glycoprotein IgG &amp; IgM? </h2>
	<p> These are antibodies that can be associated with blood clotting disorders. High levels may indicate an
increased risk of DVT in individuals with autoimmune disorders like antiphospholipid syndrome.</p>
	<h2 class="test-h2 mt-3">How is the test performed? </h2>
	<p>A blood sample is drawn from a vein in the arm. This sample is then sent to the laboratory for analysis. </p>
	<h2 class="test-h2 mt-3">Can medications affect the results of this panel? </h2>
	<p>Yes, medications, especially anticoagulants or blood thinners, can affect the results of this panel. It&#39;s
important to inform the doctor of any medications you are taking. </p>
	<h2 class="test-h2 mt-3">What should I do if the results indicate an increased risk of DVT? </h2>
	<p>If the results indicate an increased risk of DVT, it's important to consult your doctor for further
evaluation and management. </p>
	<h2 class="test-h2 mt-3">Are there any lifestyle modifications that can reduce the risk of DVT? </h2>
	<p>Yes, maintaining a healthy weight, staying active, not smoking, and managing underlying conditions such
as high blood pressure can reduce the risk of DVT. </p>
	<h2 class="test-h2 mt-3">Can this test be used to monitor treatment for DVT? </h2>
	<p>Yes, the DVT panel can be used to monitor the effectiveness of treatment for DVT and to adjust
medication dosages if necessary. </p>
	<h2 class="test-h2 mt-3">Is this test only for people who have symptoms of DVT? </h2>
	<p> No, this test can also be used for individuals who may be at risk for DVT due to family history or other
risk factors, even if they do not have symptoms.</p>
	<h2 class="test-h2 mt-3"> What are the symptoms of DVT?</h2>
	<p>Symptoms of DVT may include swelling, pain, tenderness, and red or discolored skin in the affected limb,
usually the leg. </p>
	<h2 class="test-h2 mt-3"> Which doctor should I consult if my test results are abnormal?</h2>
	<p>If your test results are abnormal, you should consult your primary care doctor, who may refer you to a
hematologist or vascular specialist for further evaluation and treatment. </p>
	<h2 class="test-h2 mt-3">What are the complications of untreated DVT? </h2>
	<p> Untreated DVT can lead to serious complications such as pulmonary embolism, post-thrombotic
syndrome, and chronic thromboembolic pulmonary hypertension.</p>
