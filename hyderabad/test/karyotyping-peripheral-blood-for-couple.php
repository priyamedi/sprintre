<h2 class="test-h2 mt-3">Why is Karyotyping performed?</h2>
<p>Karyotyping is performed to detect chromosomal abnormalities that might contribute to recurrent miscarriages or
    fertility problems. The test can also determine if you or your partner carries a chromosomal alteration that can be
    passed on to your children.</p>
<h2 class="test-h2 mt-3">What does the test involve?</h2>
<p>The test involves a simple blood draw from the arm of each individual in the couple. The sample is then sent to a
    laboratory, where technicians will analyze the structure and number of chromosomes.</p>
