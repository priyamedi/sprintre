<h1 class="test-h1">FISH for Inv(16) </h1>
<div class="post-text">
<p>Fluorescence in situ hybridization (FISH) for inv(16) is a diagnostic test that is primarily used in the evaluation of acute myeloid leukemia (AML). AML is a type of cancer that affects the myeloid cells in the bone marrow. Inv(16) stands for inversion of chromosome 16, which is a specific chromosomal abnormality often associated with a subtype of AML known as acute myelomonocytic leukemia with eosinophilia (AML M4Eo).</p>

<p>In inv(16), a portion of chromosome 16 is inverted, such that genes that were previously in separate regions are now closer together. Specifically, this inversion brings the CBFB gene in close proximity to the MYH11 gene, resulting in a fusion gene. This fusion gene is believed to contribute to the development of AML.</p>

<p>The FISH test uses fluorescent probes that specifically bind to the regions of interest on chromosome 16. By visualizing these probes under a fluorescence microscope, it is possible to see whether the inversion is present.</p>
