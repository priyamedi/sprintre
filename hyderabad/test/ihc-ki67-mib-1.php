<h1 class="test-h1">IHC - KI67 (MIB-1) </h1>
<div class="post-text">
<p>Immunohistochemistry (IHC) - KI67 (MIB-1) is a test that is frequently used in the field of pathology to
evaluate the proliferation rate of cells in human tissues. The KI67 protein is a cellular marker for
proliferation. It is present during all active phases of the cell cycle (G1, S, G2, and mitosis), but is absent
from resting cells (G0), making it an excellent marker to determine the growth fraction of a given cell
population.
</p>
<p>The MIB-1 clone of the Ki67 antibody is commonly used in clinical practice because it can be used on
formalin-fixed, paraffin-embedded tissue, which is the most common form of tissue preservation. The
Ki67 protein was first identified by the monoclonal antibody Ki67, which was generated by immunizing
mice with nuclei of the Hodgkin lymphoma cell line L428. The name Ki67 is derived from the city of its
first description (Kiel, Germany) and the number of the original clone in the 96-well plate.  </p>
