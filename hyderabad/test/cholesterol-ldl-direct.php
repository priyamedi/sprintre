	<h2 class="test-h2 mt-3">What is the Cholesterol - LDL (Direct) test?
 </h2>
	<p>The Cholesterol - LDL (Direct) test measures the level of low-density lipoprotein cholesterol, also known as "bad" cholesterol, directly in your blood.
 </p>
	<h2 class="test-h2 mt-3">Why is this test done?
 </h2>
	<p>This test is performed to evaluate the risk of heart disease. High levels of LDL cholesterol can cause plaque to accumulate in the arteries, leading to a condition called atherosclerosis, which can result in heart disease and stroke.
 </p>
