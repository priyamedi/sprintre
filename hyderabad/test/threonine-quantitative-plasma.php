<h2 class="test-h2 mt-3">Why is this test ordered?</h2>
<p>The threonine quantitative plasma test may be ordered by your healthcare provider if there's suspicion of a metabolic
	disorder or a problem with your body's ability to metabolize (break down and use) certain nutrients. It can also be
	used to monitor individuals on certain types of parenteral (intravenous) nutrition.</p>
<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>The test is performed on a blood sample. A healthcare professional will draw a sample of your blood, typically from a
	vein in your arm, and send it to a laboratory for analysis.</p>
