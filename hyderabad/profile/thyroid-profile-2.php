<h1 class="test-h1">Thyroid Profile - II (FT3, FT4, & TSH) </h1>
<div class="post-text">
<p>The Thyroid Profile - II, encompassing the Free Triiodothyronine (FT3), Free Thyroxine (FT4), and Thyroid-Stimulating Hormone (TSH) tests, is a comprehensive blood test utilized to assess the functionality of your thyroid gland. Located in the front of your neck, the thyroid gland plays a key role in your metabolism, growth, and overall development.</p>

<p>FT3 and FT4 are the unbound fractions of the thyroid hormones Triiodothyronine and Thyroxine, respectively, that are available for use by the body's cells. TSH is a hormone secreted by the pituitary gland in the brain, tasked with regulating the production and release of FT3 and FT4. The balance of these three hormones in your bloodstream can provide valuable information about whether your thyroid is overactive (hyperthyroidism), underactive (hypothyroidism), or functioning optimally.</p>
