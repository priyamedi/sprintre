<h2 class="test-h2 mt-3">What are the common symptoms of mushroom allergy?</h2>
<p>Common symptoms of mushroom allergy include itching or tingling in the mouth, skin rash, hives, nasal congestion, difficulty breathing, stomach pain, nausea, vomiting, or diarrhea. In severe cases, it can lead to anaphylaxis, which is a life-threatening allergic reaction.</p>
<h2 class="test-h2 mt-3">How is the mushroom allergy test performed?</h2>
<p>The mushroom allergy test involves taking a blood sample from a vein in the arm. The blood sample is then sent to a laboratory, where it is analyzed for the presence of specific IgE antibodies that react to proteins in mushrooms.</p>
