<h1 class="test-h1">Auto Immune Encephalitis Panel (NMDA, AMPA-GluR1 & R2, GABA B receptor,VGKC (LGI1), CASPR2 by IFA): A Comprehensive Guide </h1>
<div class="post-text">
<p> Autoimmune encephalitis represents a group of conditions where the body's immune system mistakenly
attacks healthy brain cells, leading to inflammation of the brain. The Autoimmune Encephalitis Panel is a
crucial diagnostic tool used to detect specific antibodies associated with these conditions, guiding
accurate diagnosis and treatment. In the ensuing discussion, we will delve deeper into the test, its
implications, and how it aids in managing autoimmune encephalitis.</p>
<p>The <strong>Autoimmune Encephalitis Panel</strong> uses a laboratory technique called immunofluorescence assay (IFA)
to detect the presence of specific autoantibodies in the blood. These include antibodies against N-
methyl-D-aspartate (NMDA) receptor, α-amino-3-hydroxy-5-methyl-4-isoxazolepropionic acid (AMPA)
receptor GluR1 and GluR2, γ-aminobutyric acid B (GABA B) receptor, voltage-gated potassium channel
(VGKC) complex including leucine-rich glioma-inactivated 1 (LGI1), and contactin-associated protein 2
(CASPR2). </p>
<p>These antibodies are associated with different types of autoimmune encephalitis. For instance, NMDA
receptor encephalitis, one of the most common types, is linked with anti-NMDA receptor antibodies.
The presence of these antibodies can cause a range of neurological and psychiatric symptoms, including
<b><a href="https://www.sprintdiagnostics.in/symptoms/memory-loss">memory loss</a></b>, <b><a href="https://www.sprintdiagnostics.in/symptoms/seizures">seizures</a></b>, and psychosis. Similarly, anti-LGI1 and anti-CASPR2 antibodies are linked with
limbic encephalitis and Morvan's syndrome.</p>
<p>By identifying the presence of these specific antibodies, the <strong>Autoimmune Encephalitis Panel</strong> helps in the
precise diagnosis of the type of encephalitis, thereby facilitating appropriate and targeted treatment.</p>
