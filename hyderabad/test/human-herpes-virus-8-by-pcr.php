<h2 class="test-h2 mt-3"> Why would I need a Human Herpes Virus 8 by PCR test?</h2>
	<p>
This test is often ordered when your healthcare provider suspects that you might have an infection
caused by HHV-8. This could be due to <a href="https://www.sprintdiagnostics.in/symptoms"><b style="color:#2C53A5">symptoms</b></a> or if you are at high risk for the infection, such as
being HIV-positive or having received an organ transplant.</p>
<h2 class="test-h2 mt-3"> How is the test performed?</h2>
<p>
The test involves a simple blood draw which is then analyzed in the laboratory. The PCR technique is
used to amplify the DNA from the virus if it is present in your blood sample.</p>
