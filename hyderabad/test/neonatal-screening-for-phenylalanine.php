<h1 class="test-h1">Neonatal Screening for Phenylalanine </h1>
<div class="post-text">
<p>Phenylalanine is an essential amino acid, which means our bodies can't produce it, and it must be obtained through the diet. It is important for the production of proteins and other molecules in the body. However, high levels of phenylalanine can be harmful, especially to newborns.</p>

<p>Neonatal screening for phenylalanine is a vital diagnostic test performed on newborns to check for an inherited disorder called phenylketonuria (PKU). PKU is a rare metabolic disorder in which the body is unable to break down phenylalanine, causing a build-up of this amino acid in the bloodstream and potentially leading to severe intellectual disability and other health problems if not detected and treated early.</p>

<p>This test is part of the newborn screening program and is generally done within a few days of birth. The test involves taking a small blood sample from the baby's heel, which is then sent to a laboratory for analysis.</p>
