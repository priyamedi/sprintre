<h2 class="test-h2 mt-3">Why is Cytology for Bronchial Wash and Brushing done?</h2>
<p>This test is done to examine the cells of the bronchi to detect abnormalities like infections, inflammation, or
    cancer. It can also help in diagnosing chronic lung diseases like bronchitis.</p>
<h2 class="test-h2 mt-3">How is the sample collected?</h2>
<p>The sample is collected during a bronchoscopy. A thin tube is inserted through the nose or mouth into the bronchi.
    For bronchial wash, a saline solution is used to wash the bronchi, and the fluid is then collected. Brushing
    involves using a brush to collect cells from the bronchi lining.</p>

