<h1 class="test-h1">GeneXpert (Xpert MTB/RIF) - Extrapulmonary </h1>
<div class="post-text">
<p>GeneXpert MTB/RIF, also known as Xpert MTB/RIF, is a rapid molecular test that detects Mycobacterium tuberculosis complex (MTBC) DNA and rifampicin resistance. MTBC is the group of bacteria that causes tuberculosis (TB). The detection of rifampicin resistance serves as a surrogate marker for multidrug-resistant TB (MDR-TB). This test is specifically intended for samples from extrapulmonary sites, meaning areas outside of the lungs.</p>

<p>GeneXpert MTB/RIF has revolutionized TB diagnosis by offering a high degree of accuracy and much faster results than traditional culture-based tests. Additionally, the ability to simultaneously test for drug resistance allows for the immediate initiation of appropriate therapy, a key aspect in managing TB effectively, particularly MDR-TB.</p>

