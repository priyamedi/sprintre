<h2 class="test-h2 mt-3">Is the procedure painful?</h2>
<p>Local anesthesia is used to minimize pain during the procedure, and patients may feel pressure or slight discomfort.</p>

What conditions can be diagnosed using this test?
This test can help diagnose conditions such as pericarditis, cardiac tamponade, infections, and can also be used to assess trauma to the heart.</p>

<h2 class="test-h2 mt-3">What are the normal levels of glucose, protein, and chloride in pericardial fluid?</h2>
<p>
	<ul>
		<li><strong>Glucose:</strong> Usually similar to blood glucose levels</li>
