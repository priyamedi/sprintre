<h2 class="test-h2 mt-3">What is the purpose of the Peritoneal Equilibrium Test?</h2>
<p>The PET test helps to determine the transport characteristics of the peritoneum in individuals undergoing peritoneal
    dialysis. This information is useful in individualizing dialysis prescriptions for optimal waste removal and fluid
    balance.</p>
<h2 class="test-h2 mt-3">How is the Peritoneal Equilibrium Test performed?</h2>
<p>In a PET, the patient performs an exchange with a standard dialysis solution containing a known concentration of
    glucose. Samples of dialysis fluid are taken at intervals and tested to see how much glucose has been absorbed into
    the body and how much creatinine and other toxins have been removed into the dialysis fluid.</p>
