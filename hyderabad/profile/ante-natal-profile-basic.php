<h2 class="test-h2 mt-3"> What is an Ante Natal Profile Basic?</h2>
	<p>
The Ante Natal Profile Basic is a bundle of tests done during pregnancy to assess the mother's health
and detect conditions that might affect the pregnancy or the baby. It includes various tests, each of
which has its own significance.</p>
<h2 class="test-h2 mt-3"> Why is an Ante Natal Profile Basic ordered?</h2>
<p>
The tests in this profile are routinely ordered during pregnancy to ensure the well-being of both mother
and baby. They can help detect diseases like anemia, diabetes, infections, and sexually transmitted
diseases, all of which can impact the health of the mother and the baby.</p>
