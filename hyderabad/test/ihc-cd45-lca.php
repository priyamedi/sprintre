<h2 class="test-h2 mt-3">What does the IHC - CD45/LCA test measure?</h2>
<p> The <strong>IHC - CD45/LCA</strong> test uses antibodies to detect the presence of the CD45 protein, also known as the Leukocyte Common Antigen (LCA), on cells in a tissue sample. The CD45 protein is found on all white blood cells, and its presence or absence can help diagnose certain types of lymphomas and leukemias.</p>
<h2 class="test-h2 mt-3">What preparation is needed for the IHC - CD45/LCA test?</h2>
<p> As this test is performed on a tissue sample, no specific patient preparation like fasting or medication restriction is necessary. The specifics of the biopsy procedure will depend on the location from which the tissue sample is taken.</p>
