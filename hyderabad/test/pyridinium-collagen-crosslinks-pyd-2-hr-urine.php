<h2 class="test-h2 mt-3">What is the Pyridinium Crosslinks test?</h2>
<p>The Pyridinium Crosslinks test measures the level of Pyridinoline (PYD) in the urine. It is a biomarker used to
    assess the rate of bone turnover, providing useful information in the diagnosis and monitoring of bone diseases.</p>
<h2 class="test-h2 mt-3">Why do I need to take this test?</h2>
<p>Your doctor may recommend this test if they suspect you have a bone disease such as osteoporosis or Paget's disease,
