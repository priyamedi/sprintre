<h2 class="test-h2 mt-3">What are Parainfluenza viruses?</h2>
<p>Parainfluenza viruses are a group of viruses that can cause different types of respiratory illnesses, ranging from a
    mild cold to severe pneumonia. They are a common cause of respiratory infections in children.</p>
<h2 class="test-h2 mt-3">Why is this test ordered?</h2>
<p>This test is ordered when a person, especially a child, has symptoms of a respiratory tract infection, such as cough,
    fever, difficulty breathing, and wheezing.</p>
