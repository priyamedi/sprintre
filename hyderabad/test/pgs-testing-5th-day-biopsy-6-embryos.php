<h2 class="test-h2 mt-3">What is the purpose of PGS testing?</h2>
<p>The primary aim of PGS testing is to detect chromosomal abnormalities in embryos before they are implanted into the
    uterus. By doing so, the chances of a successful pregnancy can be significantly increased and the risk of
    miscarriage can be considerably reduced.</p>
<h2 class="test-h2 mt-3">How is PGS testing conducted?</h2>
<p>The test is conducted by an embryologist in a laboratory setting. On the 5th day of embryo development, a small
    number of cells are carefully extracted from each embryo. These cells are then examined for chromosomal
    abnormalities.</p>
