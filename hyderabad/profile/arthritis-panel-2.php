	<h2 class="test-h2 mt-3">What is the role of each test in Arthritis Panel - II? </h2>
	<p><ul>
		<li><b>ANA - IFA with reflex titers: </b>Detects antinuclear antibodies which are often present in autoimmune disorders.</li>
		<li><b>ASO: </b>Measures antibodies against the bacteria that cause strep throat, which might be linked to some forms of arthritis.
</li>
		<li><b>Calcium: </b>Monitors calcium levels which can be affected in certain arthritic conditions.</li>
		<li><b>CBC: </b>Gives a general overview of health and screens for a variety of disorders.</li>
		<li><b>ESR: </b>Measures how quickly red blood cells settle and is a non-specific marker of inflammation.
</li>
		<li><b>CRP: </b>A protein that increases in the blood with inflammation.
</li>
		<li><b>RF: </b>Measures the level of rheumatoid factor, an antibody present in rheumatoid arthritis.</li>
		<li><b>Uric acid:</b>Monitors levels of uric acid which can indicate gout.
