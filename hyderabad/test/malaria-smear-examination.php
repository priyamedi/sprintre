<h2 class="test-h2 mt-3">What is the purpose of the Malaria - Smear Examination test?</h2>
<p>The test is used to identify and differentiate the malaria parasites that may be present in a person's blood.</p>
<h2 class="test-h2 mt-3">How is the Malaria - Smear Examination performed?</h2>
<p>A healthcare provider collects a blood sample from a vein in your arm. The blood is then spread onto a glass slide to create both a thick and thin smear, which are stained and examined under a microscope to identify any malaria parasites.</p>
