<h2 class="test-h2 mt-3">What are the common symptoms of Leptospirosis?</h2>
<p>Common symptoms include high fever, chills, headache, muscle aches, vomiting, jaundice (yellowing of the skin and
    eyes), red eyes, abdominal pain, diarrhea, and a rash.</p>
<h2 class="test-h2 mt-3">How is the Leptospira DNA detection test performed?</h2>
<p>A blood sample is taken from the patient and sent to a laboratory. The DNA in the sample is extracted and amplified
    using the Polymerase Chain Reaction (PCR). The amplified DNA is then analyzed for the presence of Leptospira DNA.</p>
