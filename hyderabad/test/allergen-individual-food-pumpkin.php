<h2 class="test-h2 mt-3">What are the symptoms of pumpkin allergy?</h2>
<p>Symptoms of pumpkin allergy can vary from mild to severe, and may include itching or tingling in the mouth, hives,
    eczema, difficulty breathing, swelling of the face, lips, tongue, or throat, nausea, vomiting, abdominal pain,
    diarrhea, and, in severe cases, anaphylaxis.</p>
<h2 class="test-h2 mt-3">What are the risk factors for developing pumpkin allergy?</h2>
<p>Individuals with a family history of allergies or other allergic conditions such as asthma, eczema, or hay fever are
    at an increased risk of developing food allergies, including pumpkin allergy. Cross-reactivity with other allergens
    such as ragweed or melon may also increase the risk.</p>
