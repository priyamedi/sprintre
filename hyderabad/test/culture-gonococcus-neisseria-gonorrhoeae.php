<h2 class="test-h2 mt-3">What is a Culture for Gonococcus?</h2>
<p>Culture for Gonococcus is a laboratory test used to diagnose gonorrhea, an STI caused by the
bacterium Neisseria gonorrhoeae. The test involves analyzing a sample of urine or a swab from the
throat, cervix, rectum, or urethra for the presence of the bacteria.</p>
<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is done to diagnose gonorrhea, which is important for preventing complications such as
infertility, ectopic pregnancy, and an increased risk of HIV. It's also essential for preventing the spread of
the infection to others.</p>
