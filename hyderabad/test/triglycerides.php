<h2 class="test-h2 mt-3">What is a triglyceride test?</h2>
<p>The Triglycerides examination is a blood assessment that quantifies the concentration of triglycerides, a form of fat
    (lipid), in your bloodstream.</p>
<h2 class="test-h2 mt-3">Why might I need a Triglycerides examination?</h2>
<p>The examination may be ordered by your physician to assess your cardiac disease risk or if you exhibit symptoms or
    risk factors indicative of elevated triglycerides.</p>
