<p>Collagen Disease Antibody Panel is a comprehensive test that measures different autoantibodies present in the bloodstream. Autoantibodies are proteins produced by the immune system that mistakenly attack the body's own cells and tissues. These autoantibodies are commonly seen in individuals with autoimmune diseases, a group of disorders where the immune system misfires and targets the body's own cells.</p>
<p>This panel includes tests for several specific autoantibodies, including Antinuclear antibodies (ANA-IF), Anti-double stranded DNA (Anti-Ds-DNA), SS-A (Ro), SS-B (La), Smith (Sm) and ribonucleoprotein (nRNP), Scleroderma-70 (SCL-70), Jo1, and centromere. Each of these autoantibodies is associated with specific autoimmune disorders, and their detection in the bloodstream can help diagnose these conditions.</p>
