<h2 class="test-h2 mt-3">What is the importance of the Hepatitis Panel - Acute Screen-II test?</h2>
<p>This panel is critical for diagnosing and distinguishing between different types of viral hepatitis infections. Early
    detection and identification of the type of hepatitis virus causing the infection can help in starting the right
    treatment promptly and prevent complications like liver failure, cirrhosis, or liver cancer.</p>
<h2 class="test-h2 mt-3">Is fasting required for this test?</h2>
<p>No, fasting is not required for this test. You can have your regular meals before the test.</p>
