<p> This test may be ordered if you are at a high risk for heart disease or if you have a family history of heart disease. It can also be used to detect deficiencies in vitamins B6, B12, or folic acid.</p>
<h2 class="test-h2 mt-3">What does the test involve?</h2>
<p> The test involves a simple urine sample. Your healthcare provider will provide instructions on how to properly collect and store the sample.</p>
