<h2 class="test-h2 mt-3">Why would a doctor order an IHC - Alpha-1-Antitrypsin test?</h2>
<p>Your doctor might order this test if you have <a href="https://www.sprintdiagnostics.in/symptoms"><b style="color:#2C53A5">symptoms</b></a> of AATD, such as shortness of breath, reduced ability to exercise, wheezing, recurrent respiratory infections, or unexpected liver disease. The test can help confirm the diagnosis and guide treatment decisions.</p>
<h2 class="test-h2 mt-3">Are there any special preparations required for the test?</h2>
<p>No specific preparation is required for the <strong>IHC - Alpha-1-Antitrypsin test</strong>. However, it's important to inform your doctor about any medications or supplements you're taking, as some substances might interfere with the results.</p>
