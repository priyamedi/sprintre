	<h2 class="test-h2 mt-3">How is the Hib - IgG Serum test performed?</h2>
<p>The test is performed using a blood sample which is drawn from a vein in your arm.</p>

	<h2 class="test-h2 mt-3">What do the test results mean? </h2>
<p>A high level of Hib IgG antibodies in the blood can indicate a recent or past Hib infection, or a successful response to the Hib vaccine.</p>

	<h2 class="test-h2 mt-3">How often should this test be done? </h2>
<p>The frequency of this test depends on your individual health condition and is typically determined by your doctor. However, it may be recommended for individuals who are at high risk for Hib infection, such as those with certain immune disorders.</p>

	<h2 class="test-h2 mt-3">What factors can affect the test results?</h2>
<p>Some medications and underlying health conditions can affect your body's ability to produce antibodies, which may impact the test results. Always inform your doctor about any medications you are taking or health conditions you have before the test.</p>

	<h2 class="test-h2 mt-3">What type of doctor should I see if I have abnormal test results?</h2>
<p>If your test results are abnormal, it is recommended to consult an infectious disease specialist.</p>

