	<h2 class="test-h2 mt-3">What are the symptoms of a Phthalic Anhydride allergy?
 </h2>
	<p>Symptoms of a Phthalic Anhydride allergy can include skin rash, itching, hives, redness, swelling, and in severe cases, difficulty breathing. Workers frequently exposed to PA may experience respiratory symptoms such as wheezing, shortness of breath, and coughing.
 </p>
	<h2 class="test-h2 mt-3">How is a Phthalic Anhydride allergy diagnosed? </h2>
