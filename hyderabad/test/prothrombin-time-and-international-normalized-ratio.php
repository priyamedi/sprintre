<h2 class="test-h2 mt-3">Why is the PT/INR test ordered?</h2>
	<p>
The PT/INR test is often ordered to monitor the effectiveness of blood-thinning medications like
warfarin and to help diagnose bleeding or clotting disorders.</p>
<h2 class="test-h2 mt-3"> What does a prolonged PT/INR result mean?</h2>
<p>
A prolonged PT/INR result may indicate a higher risk of bleeding. It could also suggest that your blood-
thinning medication dosage may be too high.</p>
