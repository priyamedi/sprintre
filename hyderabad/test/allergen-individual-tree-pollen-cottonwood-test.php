<h2 class="test-h2 mt-3">What symptoms are typical of an allergy to cottonwood tree pollen?</h2>
	<p>
Symptoms of a <strong>cottonwood tree pollen allergy</strong> are similar to other pollen allergies and can include
sneezing, a runny or stuffy nose,<b><a href="https://www.sprintdiagnostics.in/symptoms/dry-or-itchy-eyes">itchy or watery eyes</a></b>, and occasionally, <b><a href="https://www.sprintdiagnostics.in/symptoms/dyspnea-breath-shortness">breathing difficulties</a></b>.</p>
<h2 class="test-h2 mt-3"> How is the Tree Pollen Cottonwood Allergy Test performed?</h2>
<p>
The test involves taking a blood sample from the patient, typically from a vein in the arm. This sample is
then sent to a laboratory for analysis.</p>
