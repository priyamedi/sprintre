<h2 class="test-h2 mt-3">What conditions can the Nor-Metanephrines - 24 hours Urine test help diagnose?</h2>
<p>This test is primarily used to diagnose pheochromocytomas and paragangliomas. These are rare tumors that originate
    from the adrenal glands or similar cells located elsewhere in the body, respectively.</p>
<h2 class="test-h2 mt-3">How is the Nor-Metanephrines - 24 hours Urine test performed?</h2>
<p>This test requires the collection of all urine passed over a 24-hour period. The collected urine is then sent to the
    lab, where it is analyzed for the presence and quantity of nor-metanephrines.</p>
