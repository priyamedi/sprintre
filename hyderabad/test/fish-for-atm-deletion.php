<h1 class="test-h1">FISH for ATM Deletion </h1>
<div class="post-text">
<p>The ATM gene, or Ataxia Telangiectasia Mutated gene, is situated on chromosome 11 and has a critical
role in the way cells respond to DNA damage, thereby playing a role in protecting cells from developing
into cancer. The Fluorescence In Situ Hybridization (FISH) test for ATM deletion is used to identify if
there is a deletion of the ATM gene. A deletion of this gene can be associated with various disorders
including Ataxia-telangiectasia (A-T), and an increased risk of several types of cancer.
 </p>
<p>Ataxia-telangiectasia is a rare genetic disorder characterized by progressive difficulty with coordinating
movements (ataxia), dilated blood vessels (telangiectasia), and an increased risk of developing cancer,
particularly lymphoma and leukemia. On the other hand, the deletion or mutation of the ATM gene has
also been linked to a higher susceptibility to breast, pancreatic, and other cancers. </p>
