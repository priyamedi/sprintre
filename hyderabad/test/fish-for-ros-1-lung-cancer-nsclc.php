<h2 class="test-h2 mt-3">What is the importance of getting the FISH for ROS-1 test done?</h2>
<p>Detecting ROS-1 rearrangements is crucial for the management of NSCLC. Patients with ROS-1 positive NSCLC may benefit
    from targeted therapies, which can be more effective and have fewer side effects compared to conventional
    chemotherapy.</p>
<h2 class="test-h2 mt-3">Is fasting required for this test?</h2>
<p>No, fasting is not required for the FISH for ROS-1 test. The test is performed on tissue samples, usually obtained
    through a biopsy of the lung tumor.</p>
