	<h2 class="test-h2 mt-3">What is the purpose of a Bone Marrow Evaluation Procedure?
 </h2>
	<p>Bone Marrow Evaluation is done to investigate the cause of blood disorders and to diagnose diseases such as leukemia, anemia, and infection. It can also be used to monitor the progress of treatment of these conditions. </p>
	<h2 class="test-h2 mt-3">How is the Bone Marrow Evaluation Procedure performed?
 </h2>
	<p>The procedure is typically performed in a hospital or clinic. A healthcare professional will clean and numb an area of your hipbone. A special needle is then inserted into the bone to extract a small sample of bone marrow. The procedure usually takes about 30 minutes.
 </p>
