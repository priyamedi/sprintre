<h1 class="test-h1">Histamine – Plasma </h1>
<div class="post-text">
<p>Histamine is an organic compound involved in local immune responses as well as regulating physiological function in the gut. It acts as a neurotransmitter, communicating important messages between different cells in your body. Histamine is most notably involved in allergic reactions and inflammation.</p>

<p>The histamine plasma test measures the amount of histamine in your blood. This helps your healthcare provider determine if you're experiencing an allergic reaction or have a condition that causes excess histamine production. The test can also assist in diagnosing and monitoring certain types of mast cell disorders, where mast cells (a type of white blood cell) produce too much histamine.</p>
