<h1 class="test-h1">Flowcytometry - CD117</h1>
<div class="post-text">
<p>The CD117 molecule, also known as c-Kit or Stem Cell Factor Receptor (SCFR), is a protein that is important in certain cellular processes, including cell differentiation, proliferation, and survival. The protein is encoded by the KIT gene. CD117 is a type of receptor tyrosine kinase, a class of proteins involved in transmitting chemical signals within cells.</p>
<p>Flow cytometry is a powerful tool that allows for the measurement of these and other cell properties. It is a technology that provides rapid multi-parametric analysis of single cells in a fluid stream. CD117 expression can be analyzed through the flow cytometry technique.</p>
<p>The use of CD117 expression in flow cytometry can help in the diagnosis and monitoring of certain conditions. CD117 is often overexpressed in several types of cancers, including gastrointestinal stromal tumors (GISTs), mastocytosis, and some forms of leukemia. Hence, CD117 can be used as a target for therapeutic intervention in these malignancies.</p>
<p>This test is performed when a doctor suspects these conditions and wants to confirm the diagnosis or monitor the effectiveness of treatment.</p>

