<h2 class="test-h2 mt-3">What is the Citrate - 24 hrs Urine test?</h2>
<p>This test measures the amount of citrate, a salt compound, in your urine over a 24-hour period. This can give doctors insights into your kidney function and metabolic health.</p>
<h2 class="test-h2 mt-3">Why might I need this test?</h2>
<p>Your doctor may order this test if you've been having recurring kidney stones. It can also be used to monitor the effectiveness of treatment for kidney stones.</p>
