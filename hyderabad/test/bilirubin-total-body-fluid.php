<h2 class="test-h2 mt-3">Why is the Bilirubin Total - Body Fluid test done?</h2>
<p>This test is performed to help diagnose and monitor liver diseases, to evaluate the health of the gallbladder, and to assess for conditions that cause increased breakdown of red blood cells.</p>

<h2 class="test-h2 mt-3">What does a high level of Bilirubin in body fluid indicate?</h2>
<p>High levels of Bilirubin in body fluid can indicate liver disease, gallstones, or a condition that causes the increased breakdown of red blood cells, such as hemolytic anemia.</p>

