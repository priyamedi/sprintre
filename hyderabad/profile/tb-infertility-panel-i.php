<h2 class="test-h2 mt-3">Who should get this panel?</h2>
<p>This panel is recommended for women who have been trying to conceive without success and have a history of TB or are
    at high risk of having TB.</p>
 
	<h2 class="test-h2 mt-3">What does each test in the panel signify?</h2>
	<p><ul>
	<li><b>Endometrial Biopsy:</b> It involves obtaining a small sample of the endometrial tissue for microscopic examination. This can show the presence of TB bacteria or other abnormalities.</li>
<li><b>MTB Culture:</b> This test involves culturing the biopsy tissue to see if TB bacteria grow. It helps in confirming the diagnosis of TB.</li>
<li><b>PCR - MTB/MOTT:</b> PCR (Polymerase Chain Reaction) is used to detect the genetic material of Mycobacterium tuberculosis. It is a highly sensitive and rapid test.</li>
<li><b>Endometrium for NK cell stromal index:</b> This test evaluates the presence and activity of natural killer cells in the endometrium. An elevated NK cell activity may be associated with infertility.</li>
<li><b>TNF alpha activity - endometrium:</b> TNF alpha is a cytokine involved in systemic inflammation. Elevated levels in the endometrial tissue can be indicative of an ongoing infection including TB.</li>
	</ul> </p>
	<h2 class="test-h2 mt-3">How is the sample collected?</h2>
<p>The endometrial biopsy is an in-office procedure. A speculum is used to open the vagina and then a small instrument
    is passed through the cervix into the uterus to collect a tissue sample.</p>
<h2 class="test-h2 mt-3">Is the endometrial biopsy painful?</h2>
<p>There might be some discomfort or pain during the procedure. Some women might experience cramping during and after
    the biopsy.</p>
<h2 class="test-h2 mt-3">How to prepare for the endometrial biopsy?</h2>
<p>It is recommended to schedule the biopsy around the 21st day of the menstrual cycle. Ensure to inform your healthcare
    provider of any medications you are on. They might provide specific instructions regarding medication and how to
    prepare.</p>
<h2 class="test-h2 mt-3">What does a positive result mean?</h2>
<p>A positive result for MTB culture or PCR indicates the presence of tuberculosis bacteria in the endometrial tissue.
    It suggests that tuberculosis might be contributing to infertility.</p>
<h2 class="test-h2 mt-3">How can TB cause infertility?</h2>
<p>Tuberculosis can cause damage to the reproductive organs, leading to scarring and blockage of the fallopian tubes,
    and changes in the endometrium. These changes can prevent the successful implantation of an embryo.</p>
<h2 class="test-h2 mt-3">What is the treatment for tuberculosis-related infertility?</h2>
<p>The treatment for tuberculosis-related infertility involves anti-tuberculosis medications to clear the infection. In
    some cases, surgical intervention might be necessary to repair or remove scarred tissues. Once the TB is
    successfully treated, fertility may improve, but in some cases, assisted reproductive techniques might be needed.
</p>
<h2 class="test-h2 mt-3">Can this panel detect other causes of infertility?</h2>
<p>No, this panel specifically looks for tuberculosis as a cause of infertility. There are many other causes of
    infertility that may need to be investigated separately.</p>
<h2 class="test-h2 mt-3">Can I get pregnant after TB treatment?</h2>
<p>Yes, many women are able to conceive after successful treatment of tuberculosis. However, the extent of the damage
    caused by TB to the reproductive organs may affect the chances of natural conception.</p>
<h2 class="test-h2 mt-3">How often should I get tested?</h2>
<p>Your healthcare provider will guide you on this based on your overall health, history of TB, and symptoms.</p>
<h2 class="test-h2 mt-3">Where can I get the TB Infertility Panel I done?</h2>
<p>The TB Infertility Panel I can be done at hospitals, medical centers, and laboratories that provide diagnostic
    services. Please consult with your healthcare provider for further information.</p>
<p>Remember, early detection and treatment are key to managing TB-induced infertility. Make sure
    to discuss your symptoms and concerns with your healthcare provider to ensure a correct and timely diagnosis.</p>
	<h2 class="test-h2 mt-3">What is MTB/MOTT?</h2>
<p>MTB stands for Mycobacterium tuberculosis, which is the bacterium responsible for tuberculosis. MOTT stands for
    Mycobacteria Other Than Tuberculosis, referring to other types of mycobacteria that do not cause tuberculosis but
    can cause other types of infections.</p>
<h2 class="test-h2 mt-3">What is the role of NK cells in infertility?</h2>
<p>Natural Killer (NK) cells are part of the immune system and are present in the endometrium (lining of the womb). They
    play a crucial role in early pregnancy, particularly in the process of embryo implantation. Elevated levels of NK
    cells or abnormal activity of these cells in the endometrium can negatively impact fertility.</p>
<h2 class="test-h2 mt-3">How long does it take to get the results of the TB Infertility Panel I?</h2>
<p>The turn around time for this panel varies, but typically it may take between 5 to 10 days to get the results.
    However, this can depend on the laboratory processing the tests.</p>
<h2 class="test-h2 mt-3">What are the side effects of an endometrial biopsy?</h2>
<p>Some women might experience light bleeding or cramping for a few days after the biopsy. It's also possible to have an
    infection or an allergic reaction to the local anesthesia, but these are rare.</p>
<h2 class="test-h2 mt-3">How reliable are these tests?</h2>
<p>These tests are generally highly reliable. The culture and PCR are especially sensitive and specific for detecting
    the presence of Mycobacterium tuberculosis in tissue samples. However, like all tests, they can have false positives
    and false negatives. That's why it's important to interpret the results in the context of the patient's symptoms and
    medical history.</p>
<h2 class="test-h2 mt-3">Are there other tests to detect TB in the reproductive organs?</h2>
<p>Yes, other tests can be done to detect TB in the reproductive organs. These include a hysterosalpingogram (an X-ray
    of the uterus and fallopian tubes), laparoscopy, and other imaging tests. However, these tests are more invasive and
    less specific for TB compared to the tests in the TB Infertility Panel I.</p>
 
