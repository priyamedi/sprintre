<h2 class="test-h2 mt-3">What is the Allergen, Individual - Weed Pollen English Plantain test?</h2>
<p>The Allergen, Individual - Weed Pollen English Plantain test is a blood test used to determine if an individual is allergic to the pollen of the English Plantain. It does this by identifying the presence of specific Immunoglobulin E (IgE) antibodies that the immune system produces in response to this allergen.</p>
<h2 class="test-h2 mt-3">Do I need to fast before this test?</h2>
<p>No, fasting is not required for this test. You can eat and drink as you normally would unless instructed otherwise by your doctor.</p>
