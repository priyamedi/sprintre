<h1 class="test-h1">Unsaturated Iron Binding Capacity (UIBC) </h1>
<div class="post-text">
<p>The Unsaturated Iron Binding Capacity (UIBC) test is a blood test that is used to assess the amount of iron that can be bound by proteins in the blood. Iron is an essential element that is carried through the bloodstream by a protein called transferrin. The UIBC test, along with other iron tests, helps to give a more complete picture of the amount of iron in the body and how it is being used.</p>

<p>UIBC is a part of the total iron binding capacity (TIBC) test. TIBC measures the total amount of iron that can potentially be bound by proteins in the blood. The UIBC test specifically measures the reserve capacity of transferrin, that is, how much more iron could be carried if necessary. It is calculated by subtracting the serum iron from the TIBC.</p>

