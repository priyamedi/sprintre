<h2 class="test-h2 mt-3">What is the Coxiella Burnetii - Q Fever (IgG Antibody) Test?</h2>
<p>This is a blood test used to detect antibodies against the Coxiella burnetii bacterium, which causes Q fever. The
    presence of IgG antibodies indicates a past or ongoing infection.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>A healthcare professional will draw blood from a vein in your arm using a needle. The blood sample will be sent to
    a laboratory for analysis.</p>
