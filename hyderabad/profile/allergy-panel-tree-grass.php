	<h2 class="test-h2 mt-3">What is the Allergy Panel - Grass test? </h2>
	<p>The Allergy Panel - Grass is a diagnostic test that measures the level of specific IgE antibodies in your blood in response to particular types of grass. If these antibodies are present in response to these grass types, it may indicate that you have an allergy to them.
 </p>
	<h2 class="test-h2 mt-3">How is the test performed? </h2>
	<p>The test involves drawing a blood sample, typically from a vein in your arm. This sample is then sent to a laboratory where it is analyzed for the presence of specific IgE antibodies to the grass types included in the panel.
 </p>
