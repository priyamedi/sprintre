								<h2 class="test-h2 mt-3">What does a positive HBsAg test mean?</h2>
								<p>A positive test means that the person is infected with the hepatitis B virus and can
									pass the virus to others through blood or other body fluids.</p>

								<h2 class="test-h2 mt-3">What does a negative HBsAg test mean?</h2>
								<p>A negative test generally means that the person is not currently infected with the
									hepatitis B virus. However, it doesn't rule out the possibility of a past infection
									or immunization.</p>

								<h2 class="test-h2 mt-3">Are there any symptoms of hepatitis B infection?</h2>
								<p>Many people with hepatitis B do not experience symptoms. When symptoms do occur, they
