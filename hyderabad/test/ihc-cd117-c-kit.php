<h1 class="test-h1">IHC - CD117 (C-KIT) </h1>
<div class="post-text">
<p>The IHC - CD117 (C-KIT) test is an immunohistochemistry procedure that detects the presence of the CD117 protein, also known as the c-KIT receptor, in tissue samples. CD117 is a type of protein known as a tyrosine kinase receptor, which plays a key role in cell communication and signal transduction. This protein is crucial for the growth and development of certain types of cells, including melanocytes, germ cells, mast cells, and interstitial cells of Cajal.
 </p>
<p>Alterations in the CD117 gene or overexpression of the CD117 protein have been associated with a number of cancers, including gastrointestinal stromal tumors (GISTs), mast cell diseases, and certain types of leukemia. As such, testing for CD117 can help with diagnosis and may guide treatment decisions, as targeted therapies exist for some conditions with CD117 involvement. </p>
