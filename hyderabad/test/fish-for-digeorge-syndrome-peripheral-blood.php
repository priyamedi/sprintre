<h2 class="test-h2 mt-3">What is DiGeorge Syndrome?</h2>
<p> DiGeorge Syndrome is a genetic disorder that occurs due to the deletion of a small segment on chromosome 22. This can result in a range of potential issues such as heart defects, immune system problems, learning disabilities, and distinct facial features.</p>

<h2 class="test-h2 mt-3">How does the FISH test for DiGeorge Syndrome work?</h2>
<p> FISH, or Fluorescence In Situ Hybridization, uses fluorescent probes that bind to specific DNA sequences on the chromosomes. In this case, the probe binds to the region on chromosome 22 that is often deleted in DiGeorge Syndrome. Under a special microscope, the presence or absence of this segment can be determined.</p>

