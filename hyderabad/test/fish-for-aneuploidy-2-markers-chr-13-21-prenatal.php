<h2 class="test-h2 mt-3"> What is the FISH for Aneuploidy - 2 Markers Chr 13, 21 (Prenatal) test? </h2>
	<p>The test uses fluorescent probes to identify if there are an abnormal number of chromosomes 13 and
21 in the cells of an unborn baby. This is important for diagnosing conditions such as Down syndrome
and Patau syndrome. </p>
<h2 class="test-h2 mt-3"> How is the test performed? </h2>
<p>This test is performed using a sample of amniotic fluid or tissue from the placenta, obtained through
amniocentesis or chorionic villus sampling. Fluorescent probes are applied to the sample, and under a
special microscope, the presence or absence of extra chromosomes can be detected.</p>
