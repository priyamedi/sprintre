<h2 class="test-h2 mt-3">What is the Allergen, Individual - Food Basil test?</h2>
<p>This is a blood test that checks for an allergic reaction to basil. It does so by detecting the amount of specific
    IgE antibodies produced in response to basil proteins.</p>
<h2 class="test-h2 mt-3">Why is this test important?</h2>
<p>The test is important for individuals who have experienced symptoms suggestive of a food allergy after consuming
    basil. The test can confirm if these reactions are indeed due to an allergy to basil.</p>
