<h2 class="test-h2 mt-3">What is a Chromosomal Array - Products of Conception test?</h2>
<p>This test analyzes genetic material from a miscarriage or fetal loss to identify chromosomal imbalances. It can provide insight into whether the loss was due to a genetic disorder.</p>
<h2 class="test-h2 mt-3">Why would I need this test?</h2>
<p>If you've experienced a miscarriage or fetal loss, this test can help determine if a chromosomal abnormality was a contributing factor. This information can be useful for future pregnancy planning and genetic counseling.</p>
