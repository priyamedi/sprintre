<h2 class="test-h2 mt-3">What does the Nicotine Metabolite Quantitative urine test involve?</h2>
<p> A urine spot sample is needed for the test. The test measures the level of cotinine, a metabolite of nicotine, in the urine. High levels of cotinine typically indicate recent tobacco use or exposure to secondhand smoke.</p>

<h2 class="test-h2 mt-3">How long does it take to get the results? </h2>
<p>The turnaround time for the Nicotine Metabolite Quantitative test is typically 2-4 days, although this can vary depending on the laboratory.</p>

