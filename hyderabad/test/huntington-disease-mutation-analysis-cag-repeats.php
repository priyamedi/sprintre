<h1 class="test-h1">Huntington Disease - Mutation Analysis CAG Repeats </h1>
<div class="post-text">
<p><strong>Huntington Disease (HD)</strong> is a neurodegenerative disorder that affects muscle coordination, leading to
cognitive decline and psychiatric problems. It is a genetic disorder, inherited in an autosomal dominant
pattern. This means that an affected person has a 50% chance of passing the gene mutation that causes
the disease to each of their offspring. </p>
<p> The HD gene, located on chromosome 4, codes for a protein called Huntingtin. Within this gene is a
segment of DNA known as a CAG trinucleotide repeat. This repeat, which consists of a series of the DNA
building blocks (cytosine, adenine, and guanine), appears multiple times in a row. In people without HD,
the CAG segment is repeated 10 to 35 times. However, in people with HD, the CAG segment is repeated
36 to 120 times. The number of CAG repeats is typically related to the age at onset of signs and
<a href="https://www.sprintdiagnostics.in/symptoms"><b style="color:#2C53A5">symptoms</b></a>. The more repeats, the earlier the onset.</p>
