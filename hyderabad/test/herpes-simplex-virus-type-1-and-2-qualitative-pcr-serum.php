<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is done to confirm the presence of HSV-1 or HSV-2 in the blood serum, particularly if you have signs and
    symptoms suggestive of herpes, like painful sores or blisters in the oral or genital areas.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test requires a blood sample which is obtained through a needle inserted into a vein in your arm.</p>
