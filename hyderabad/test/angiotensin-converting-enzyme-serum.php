<h2 class="test-h2 mt-3"> What is an Angiotensin Converting Enzyme (ACE) test?</h2>
	<p>
An ACE test is a blood test that measures the level of ACE in your blood. ACE is an enzyme involved in
regulating blood pressure, but its level can increase in certain medical conditions, particularly
sarcoidosis. </p>
<h2 class="test-h2 mt-3"> Why is an ACE test ordered?</h2>
<p>
An ACE test is primarily ordered to help diagnose sarcoidosis, especially in cases where the symptoms
are unclear or when the results of other tests are inconclusive. It can also be used to monitor the
progress of the disease and the effectiveness of treatment. </p>
