<h1 class="test-h1">AFB/TB Susceptibility: Streptomycin (Rapid- MGIT) </h1>
<div class="post-text">
<p>Streptomycin is an aminoglycoside antibiotic used in the treatment of Tuberculosis (TB), a severe infectious disease caused by Mycobacterium tuberculosis. This bacterium can develop resistance to streptomycin and other anti-TB drugs, leading to drug-resistant forms of TB. The AFB/TB Susceptibility: Streptomycin (Rapid- MGIT) test is a drug susceptibility test that determines the sensitivity or resistance of the TB bacteria to streptomycin.
 </p>
<p>The test employs the Mycobacteria Growth Indicator Tube (MGIT) method, a rapid, automated, non-radiometric detection system. It is used to detect the growth of mycobacteria in a liquid medium, providing rapid results to guide treatment decisions.
 </p>
