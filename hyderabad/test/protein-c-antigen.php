<h2 class="test-h2 mt-3">Why do I need a Protein C Antigen test?</h2>
<p>Your doctor may recommend a Protein C Antigen test if you have a history of unexplained blood clotting events,
    repeated miscarriages, or if there is a known history of Protein C deficiency in your family. The test can help
    identify a deficiency of Protein C, which can make you more prone to developing blood clots.</p>
<h2 class="test-h2 mt-3">Is fasting required for a Protein C Antigen test?</h2>
<p>No, fasting is not required for a Protein C Antigen test. You can continue with your regular eating and drinking
    habits unless instructed otherwise by your healthcare provider.</p>
