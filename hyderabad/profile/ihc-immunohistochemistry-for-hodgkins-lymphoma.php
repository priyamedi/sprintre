<h2 class="test-h2 mt-3">How is the sample collected?</h2>
<p>A biopsy of the suspected lymph node or other affected tissue is taken by your healthcare provider. This is usually
    done under local or general anesthesia.</p>
<h2 class="test-h2 mt-3">What does each marker indicate?</h2>
<p>Each marker has a specific meaning. For instance, CD15 and CD30 are usually positive in classic Hodgkin's lymphoma.
    Other markers like EBV, CD20, PAX5, and LCA can help differentiate it from non-Hodgkin's lymphoma. Markers like CD3,
    CD5, CD4, CD57, Oct2, BOB1, and ALK-1 can also assist in identifying different subtypes or ruling out other
    conditions.</p>
<h2 class="test-h2 mt-3">How long will it take to get the results?</h2>
<p>The turnaround time is typically 5-7 days but may vary depending on the laboratory.</p>
<h2 class="test-h2 mt-3">What preparation is needed for this test?</h2>
<p>There is no specific preparation needed for this test. However, because a biopsy is involved, your healthcare
    provider will give you instructions based on your general health, the location of the biopsy, and the type of
    anesthesia used.</p>
<h2 class="test-h2 mt-3">When should one get this test done?</h2>
<p>This test is done when Hodgkin's lymphoma is suspected based on clinical symptoms, physical examination, and other
    preliminary tests.</p>
<h2 class="test-h2 mt-3">What information does this test provide?</h2>
<p>The IHC panel for Hodgkin's lymphoma can provide critical information about the type and stage of the lymphoma, which
    is essential for determining the best treatment strategy.</p>
<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>This test is typically performed once to establish a diagnosis. However, if treatment is given and then symptoms
    reappear, the test may be repeated.</p>
<h2 class="test-h2 mt-3">What are the normal values?</h2>
<p>In a normal lymph node, there is a specific pattern of marker expression. Any significant deviation from this
    pattern, like the increased presence of CD15 or CD30, can indicate Hodgkin's lymphoma.</p>
<h2 class="test-h2 mt-3">What precautions must be taken?</h2>
<p>The primary precaution is related to the biopsy procedure. Your healthcare provider will give you specific
    instructions based on your overall health and the location of the biopsy.</p>
<h2 class="test-h2 mt-3">What are the modifiable and non-modifiable factors that affect these markers?</h2>
<p>Non-modifiable factors include genetic predisposition to Hodgkin's lymphoma. Modifiable factors are limited as these
    markers are specific to the cells and not influenced by lifestyle or environmental factors.</p>
<h2 class="test-h2 mt-3">Which doctor should be consulted in case of abnormal results?</h2>
<p>If results are abnormal, you should consult with your hematologist or oncologist. They will interpret the results in
    the context of your overall health and symptoms and recommend the next steps.</p>
<h2 class="test-h2 mt-3">What other tests might be done along with this?</h2>
<p>Other tests might include complete blood count (CBC), blood chemistry tests, imaging tests (like CT, PET, or MRI
    scans), bone marrow biopsy, and genetic testing.</p>
<h2 class="test-h2 mt-3">Are the results accurate?</h2>
<p>The results are usually reliable, but like any test, there may be a chance of false positives or negatives. The
    results should always be interpreted in conjunction with your symptoms, history, and other test results.</p>
<p>In conclusion, the IHC panel for Hodgkin's lymphoma is a powerful diagnostic tool. It provides essential insights
    into the nature and type of lymphoma, guiding the path for precise treatment. As with any test, ensure that you
    discuss the results with your healthcare provider to understand what they mean for your health and treatment plan.
</p>
 
