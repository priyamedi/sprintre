<h2 class="test-h2 mt-3">What happens during a Microfilaria Smear Examination?</h2>
<p>During a Microfilaria Smear Examination, a sample of your blood is collected. This sample is then stained and
    examined under a microscope for the presence of microfilariae.</p>
<h2 class="test-h2 mt-3">Why do I need this test?</h2>
<p>If you live in or have recently traveled to a region where filariasis is common, and you have symptoms such as fever,
    lymphedema, or elephantiasis, your doctor might recommend this test.</p>
