<h2 class="test-h2 mt-3">Why would my doctor recommend an MPS Type-VI blood test?</h2>
<p>Your healthcare provider might suggest an MPS Type-VI blood test if your child is displaying symptoms that suggest
    MPS VI. These symptoms could include growth delay, changes in facial features, joint stiffness, heart and lung
    problems, and skeletal irregularities. The test can help confirm or rule out a diagnosis of MPS VI.</p>
<h2 class="test-h2 mt-3">How is the MPS Type-VI blood test performed?</h2>
<p>The test requires a sample of the patient's blood, usually drawn from a vein in the arm. The sample is then sent to a
    lab for analysis.</p>
