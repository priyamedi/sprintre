<p><strong>Non-Ketotic Hyperglycinemia (NKH)</strong>, also known as Glycine Encephalopathy, is a rare genetic disorder
characterized by an excess of glycine in the body, particularly in the brain and spinal fluid. This condition
is caused by a deficiency of an enzyme required to break down glycine, an amino acid that acts as a
neurotransmitter in the brain. Elevated levels of glycine can disrupt the normal activity of other
neurotransmitters, leading to a range of neurological symptoms and complications. The Amino Acid
Analysis for Non-Ketotic Hyperglycinemia Panel in CSF and Plasma is a diagnostic test that measures the
levels of glycine and other amino acids in the cerebrospinal fluid and blood plasma. </p>

