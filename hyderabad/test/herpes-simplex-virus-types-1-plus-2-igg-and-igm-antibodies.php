<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is primarily done to diagnose a current or past infection with HSV-1 or HSV-2. The presence of IgM
    antibodies indicates a recent or active infection, while the presence of IgG antibodies suggests a past infection
    and immunity.</p>
<h2 class="test-h2 mt-3">What is the importance of this test?</h2>
<p>The HSV Types 1 and 2 IgG and IgM antibodies test can help determine if you have been infected with HSV and if so,
    which type. It can also aid in the diagnosis of atypical cases of HSV infection, such as HSV encephalitis or
    disseminated HSV infection in immunocompromised patients.</p>
