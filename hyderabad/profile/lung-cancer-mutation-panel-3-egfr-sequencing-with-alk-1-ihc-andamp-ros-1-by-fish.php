	<h2 class="test-h2 mt-3">What is ROS-1 FISH? </h2>
<p>ROS-1 FISH is a laboratory method used to identify rearrangements in the ROS-1 gene in cancer cells. This technique uses fluorescent probes that bind to the ROS-1 gene if it is rearranged.</p>

	<h2 class="test-h2 mt-3">What does it mean if I have a mutation in one of these genes? </h2>
<p>A mutation in one of these genes means that your cancer may be driven by this mutation. This could impact your treatment options, as certain targeted therapies are designed to target these specific genetic changes.</p>

	<h2 class="test-h2 mt-3">Can these mutations be inherited? </h2>
<p>Most mutations found in lung cancer are acquired during a person's lifetime and are not inherited. This means that these mutations are not typically passed down from parents to their children.</p>

	<h2 class="test-h2 mt-3">Can the results of this test affect my treatment options? </h2>
<p>Yes, the results of this test can significantly impact your treatment options. If a specific mutation is identified, targeted therapies can be used to specifically treat your type of cancer.</p>

	<h2 class="test-h2 mt-3">Can the results of this test change over time? </h2>
<p>Yes, the genetic makeup of a tumor can change over time, particularly in response to treatment. Your doctor may recommend repeat testing if your cancer progresses or if you become resistant to treatment.</p>

	<h2 class="test-h2 mt-3">How is the test performed? </h2>
<p>This test is performed on a sample of your blood or tissue. Your doctor will provide instructions on how and when the sample will be collected.</p>

