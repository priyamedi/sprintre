<h2 class="test-h2 mt-3">What does a positive result mean? </h2>
<p>A positive result indicates the presence of IgG antibodies to Borrelia burgdorferi, suggesting an ongoing or past infection with Lyme disease. However, a positive result should be interpreted alongside clinical findings and other diagnostic tests as false positives can occur.</p>
<h2 class="test-h2 mt-3">Can the test differentiate between a past and current infection?</h2>
<p> The ELISA test for Borrelia burgdorferi IgG cannot distinguish between a past and current infection. The IgG antibodies remain in the body long after the infection has resolved.</p>
