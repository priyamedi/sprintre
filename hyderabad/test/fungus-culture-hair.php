<h2 class="test-h2 mt-3"> Why is a fungus culture from hair necessary? </h2>
<p>A fungus culture from hair is necessary to diagnose fungal infections of the hair and scalp, such as tinea capitis. This is important for determining the proper course of treatment to effectively target the specific type of fungus causing the infection.</p>
<h2 class="test-h2 mt-3"> What symptoms might lead a doctor to order this test?</h2>
<p> If you are experiencing itching, scaling, red patches, hair loss or brittle hair on the scalp, a doctor might suspect a fungal infection and order this test.</p>
