<h1 class="test-h1">Thyroid Profile - VI (FT4 & TSH) </h1>
<div class="post-text">
<p>The Thyroid Profile - VI is a focused diagnostic panel that primarily measures the levels of Free Thyroxine (FT4) and Thyroid-Stimulating Hormone (TSH). FT4 and TSH are critical markers in the assessment of thyroid function, and their levels can help diagnose a range of thyroid disorders.</p>

<p>The thyroid, a small gland situated in the neck, produces the hormone thyroxine (T4). A portion of T4 circulates in the blood unbound to proteins, known as Free T4 (FT4), and it is readily available for your cells to use. The production of T4 in the thyroid is controlled by the Thyroid-Stimulating Hormone (TSH), which is released by the pituitary gland in your brain.</p>

