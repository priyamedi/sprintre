<h2 class="test-h2 mt-3">What is the Allergen, Individual - Food Coriander test?</h2>
<p>The test is a type of blood test that identifies specific antibodies, known as Immunoglobulin E (IgE), produced in
    response to coriander, a common food allergen.</p>
<h2 class="test-h2 mt-3">Who should consider taking the Allergen, Individual - Food Coriander test?</h2>
<p>Anyone who has experienced symptoms of an allergic reaction after consuming coriander should consider this test. The
    symptoms could include hives, skin rashes, respiratory issues, or gastrointestinal discomfort.</p>
