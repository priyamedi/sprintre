<h1 class="test-h1">FISH for Angelman Prader Willi Syndrome (Microdeletion 15q11-13) </h1>
<div class="post-text">
<p>Angelman Syndrome and Prader-Willi Syndrome are distinct genetic disorders that result from the
absence of certain genes in the region 15q11-13 of chromosome 15. Fluorescence In Situ Hybridization,
commonly known as FISH, for Angelman Prader Willi Syndrome is a specialized genetic test aimed at
detecting microdeletions in this particular region of chromosome 15. The absence or malfunction of
genes in this region leads to a variety of developmental and neurological issues that are characteristic of
Angelman Syndrome and Prader-Willi Syndrome. </p>
<p> Angelman Syndrome is characterized by intellectual disability, problems with movement and balance,
and a happy demeanor with frequent laughter. Prader-Willi Syndrome also involves intellectual disability
and developmental problems, but is additionally associated with chronic feelings of hunger that often
lead to obesity, and low muscle tone. Both syndromes are the result of the loss of function of genes in
the same chromosomal region, but the manifestation differs due to the parent from whom the affected
chromosome is inherited.</p>
