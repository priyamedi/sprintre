<h2 class="test-h2 mt-3">Are there any risks associated with this test?</h2>
<p>The risks are minimal and are associated with the sample collection procedure. There might be slight pain, bruising,
    or infection at the biopsy site, but these are rare.</p>
<h2 class="test-h2 mt-3">What do the results mean?</h2>
<p>A positive result indicates the presence of Mycobacterium leprae DNA in the sample, confirming a diagnosis of
    leprosy. A negative result suggests that the bacterium is not present in the sample, but it does not completely rule
    out the disease.</p>
<h2 class="test-h2 mt-3">How accurate is this test?</h2>
<p>The Real Time PCR for Mycobacterium leprae is highly accurate due to its ability to amplify and detect the
    bacterium's DNA. However, a negative result does not exclude the possibility of the disease.</p>
