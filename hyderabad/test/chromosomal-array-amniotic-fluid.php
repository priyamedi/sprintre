<h2 class="test-h2 mt-3"> What is a Chromosomal Array?</h2>
<p>A chromosomal array is a test that examines the fetus's chromosomes in detail. It can identify small amounts of extra or missing chromosomal material that standard chromosome tests might miss.</p>
<h2 class="test-h2 mt-3"> Why would I need a Chromosomal Array test on Amniotic Fluid?</h2>
<p>A chromosomal array test on amniotic fluid is performed to determine if your baby has a genetic or chromosomal abnormality. This might be recommended if you have a high-risk pregnancy, abnormal ultrasound findings, or a family history of genetic disorders.</p>
