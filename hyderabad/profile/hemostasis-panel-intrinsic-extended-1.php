<h2 class="test-h2 mt-3">Why is the Hemostasis Panel - Intrinsic - Extended 1 performed?</h2>
<p>This panel is performed to evaluate the intrinsic blood clotting pathway, particularly when a patient has prolonged
    APTT or presents symptoms of a bleeding disorder, such as unexplained bruising, prolonged bleeding from wounds,
    frequent nosebleeds, or blood in the urine or stool.</p>
<h2 class="test-h2 mt-3">What does APTT indicate?</h2>
<p>APTT is used to measure how long it takes for blood to clot. If the APTT is too long, it may indicate a deficiency in
    one of the blood clotting factors, or the presence of an inhibitor that is preventing normal clotting.</p>
