<p> This test is primarily used to detect neuroblastomas in children, which are types of tumors that often start in the adrenal glands.</p>
<h2 class="test-h2 mt-3">What kind of sample is needed for this test?</h2>
<p> A urine sample is required for this test. It's often collected over a 24-hour period to give a more accurate representation of HVA levels.</p>
