<div class="post-text">
<p>The Alkaline Phosphatase Isoenzymes test is a laboratory method used to measure the different types,
or isoenzymes, of alkaline phosphatase in your blood. Alkaline phosphatase (ALP) is an enzyme found in
many tissues throughout the body, with the highest concentrations found in the liver, bile ducts, and
bone. Smaller amounts can be found in the intestines, kidneys, and placenta.
 </p>
<p> This test is usually performed when an ALP test shows higher-than-normal levels of the enzyme. Since
ALP can be produced in several different tissues, the isoenzymes test can help your doctor determine
exactly which tissues are producing too much ALP. This can aid in diagnosing specific conditions and
monitoring the effectiveness of treatment.</p>
