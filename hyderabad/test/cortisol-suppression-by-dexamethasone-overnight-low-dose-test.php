<h2 class="test-h2 mt-3">What is the Cortisol Suppression by Dexamethasone, Overnight Low Dose Test?</h2>
<p>This test involves taking a low dose of dexamethasone to suppress cortisol production and measuring cortisol
    levels in the blood the next morning. It helps diagnose disorders related to excessive cortisol, such as Cushing's
    syndrome.</p>
<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>You will take a low dose of dexamethasone, usually at 11 p.m., and then have blood drawn the next morning at 8
    a.m. to measure cortisol levels.</p>
