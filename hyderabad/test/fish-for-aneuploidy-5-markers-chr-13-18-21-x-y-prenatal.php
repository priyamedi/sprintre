<div class="post-text">
<p>FISH for Aneuploidy - 5 markers Chr 13, 18, 21, X, Y is an essential prenatal diagnostic test that examines
the number of specific chromosomes in a fetus. This test is of immense value as it provides insight into
chromosomal disorders that may affect the developing baby. FISH, or Fluorescence In Situ Hybridization,
uses fluorescent probes that attach to the chromosomes in a sample. This enables the chromosomes to
be visualized and counted under a microscope. Through this, the presence of an abnormal number of
chromosomes, which can be indicative of certain genetic disorders, can be detected.
</p>
<p>Specifically, this test evaluates chromosomes 13, 18, 21, X, and Y. Abnormalities in chromosome 13 can
be associated with Patau syndrome, while abnormalities in chromosome 18 are associated with Edwards
syndrome. Chromosome 21 is linked with Down syndrome. The X and Y chromosomes are crucial for
determining the sex of the child, and anomalies in these chromosomes can lead to a range of sex-linked
disorders such as Turner syndrome and Klinefelter syndrome.  </p>
