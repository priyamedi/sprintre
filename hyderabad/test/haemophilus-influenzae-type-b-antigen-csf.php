	<h2 class="test-h2 mt-3">How is the sample collected for this test? </h2>
<p>The test requires a sample of CSF, which is collected through a procedure known as a lumbar puncture or spinal tap. The doctor inserts a needle into the lower back to draw a small amount of CSF for testing.</p>

	<h2 class="test-h2 mt-3">How is the Haemophilus Influenza B Antigen - CSF test performed?</h2>
<p>The CSF sample is sent to a laboratory where it's analyzed for the presence of Hib antigen. If the antigen is detected, it indicates an active Hib infection.</p>

	<h2 class="test-h2 mt-3">Is fasting required for this test? </h2>
<p>No, fasting is not required for this test.</p>

