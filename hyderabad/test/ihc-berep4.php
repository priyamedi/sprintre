<h2 class="test-h2 mt-3">Why is the IHC - BerEP4 test done?</h2>
<p>The <strong>IHC - BerEP4 test</strong> is usually performed to confirm whether a suspected tumor originated from epithelial cells. BerEP4 binds to most epithelial cells, making it a useful tool in diagnosing cancers that originate from these cells, like many types of carcinomas.</p>
<h2 class="test-h2 mt-3">Is there any specific preparation required for this test?</h2>
<p>No specific preparation is needed for the IHC - BerEP4 test. It is performed on a tissue sample, typically obtained through a biopsy or surgery.</p>
