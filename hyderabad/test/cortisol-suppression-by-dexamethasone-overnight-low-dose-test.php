<p>The Cortisol Suppression by Dexamethasone, Overnight Low Dose Test, often known as the Low Dose Dexamethasone Suppression Test, is used to evaluate the adrenal glands' function by measuring how well they respond to the drug dexamethasone. This test is particularly beneficial in diagnosing disorders associated with excessive cortisol levels, such as Cushing's syndrome.</p>

