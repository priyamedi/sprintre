<h2 class="test-h2 mt-3">Why is the Malaria Screening Profile done?</h2>
<p>The Malaria Screening Profile is carried out when symptoms such as high fever, chills, sweats,
headaches, nausea, and body aches are suggestive of malaria. The test helps in the early detection and
appropriate treatment of the disease.</p>

<h2 class="test-h2 mt-3">What does the test involve?</h2>
<p>The test involves taking a small sample of blood from your arm. The blood is then analyzed using various
methods including a full blood count, microscopic examination, and immunoassay techniques.</p>
