<p>Yes, the PML/RARA t(15:17) Qualitative - PCR test can be used to detect minimal residual disease (MRD) in patients
    with acute promyelocytic leukemia (APL). MRD refers to the presence of small amounts of leukemic cells that may
    remain after treatment. Monitoring MRD levels helps assess treatment response and guide further therapy decisions,
    such as the duration of treatment or the need for consolidation therapy.</p>
<h2 class="test-h2 mt-3">How often should the PML/RARA t(15:17) Qualitative - PCR test be done?</h2>
