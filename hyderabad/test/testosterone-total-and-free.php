<h2 class="test-h2 mt-3">Why is the Testosterone Total and Free test done?</h2>
<p>The Testosterone Total and Free test is done to assess testosterone levels in the body. It can help diagnose
	conditions such as hypogonadism (low testosterone levels) in men, testosterone producing tumors in men and women,
	and polycystic ovary syndrome (PCOS) in women. This test can also monitor the effectiveness of testosterone
	replacement therapy.</p>
	<h2 class="test-h2 mt-3">Is fasting required for the Testosterone Total and Free test?</h2>
<p>No, fasting is not required for this test. You can eat and drink normally before the test.</p>
