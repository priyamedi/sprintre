<h2 class="test-h2 mt-3">Are there any risks associated with this test?</h2>
<p>There are minimal risks associated with blood draws and tissue biopsies. There may be discomfort, bruising, and in
    rare cases, infection.</p>
<h2 class="test-h2 mt-3">How long does it take to get the results?</h2>
<p>It generally takes 7-10 days to receive the results from a FISH for PDGFR-A test.</p>
<h2 class="test-h2 mt-3">Can PDGFR-A rearrangements be inherited?</h2>
<p>PDGFR-A rearrangements are typically not inherited but are acquired changes that occur in the cells during an
    individual’s lifetime.</p>
