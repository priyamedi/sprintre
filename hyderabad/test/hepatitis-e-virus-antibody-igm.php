<h2 class="test-h2 mt-3">What is the Hepatitis E Virus (HEV) Antibody - IgM test?</h2>
<p>The Hepatitis E Virus (HEV) Antibody - IgM test is a blood test that checks for the presence of IgM antibodies
    against the Hepatitis E virus in your blood. The presence of these antibodies typically indicates a recent or
    ongoing infection with HEV.</p>
<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>This test is done if you present symptoms of liver disease such as jaundice, dark urine, fatigue, or abdominal pain,
    especially if you live in or have recently traveled to an area where Hepatitis E is common.</p>
