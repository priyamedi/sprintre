<h2 class="test-h2 mt-3">Why is the Amylase - Pleural Fluid test done?</h2>
<p>The Amylase - Pleural Fluid test is done to help diagnose the cause of pleural effusion, which is an accumulation of excess fluid in the pleural space around the lungs. Elevated levels of amylase in pleural fluid can indicate conditions such as acute pancreatitis, lung cancer, or an esophageal rupture.</p>

<h2 class="test-h2 mt-3">How is the pleural fluid collected for the test?</h2>
<p>Pleural fluid is collected through a procedure known as thoracentesis. A healthcare professional will insert a needle through the skin into the pleural space to draw fluid for testing. This procedure is generally performed under local anesthesia to minimize discomfort.</p>

