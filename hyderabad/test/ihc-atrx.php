<h2 class="test-h2 mt-3">What is the purpose of the IHC - ATRX test?</h2>
<p>The <strong>IHC - ATRX test</strong> helps in the diagnosis and characterization of several genetic disorders and cancers. It can provide important insights for healthcare professionals regarding the prognosis of the disease and guide them in making therapeutic decisions.</p>
<h2 class="test-h2 mt-3">Do I need to fast before the IHC - ATRX test?</h2>
<p>No, fasting is not required for this test since it is performed on a tissue sample obtained from a biopsy or surgical procedure.</p>
