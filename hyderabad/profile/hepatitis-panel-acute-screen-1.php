<h2 class="test-h2 mt-3">What does the Hepatitis Panel - Acute Screen-I test measure?</h2>
<p>The panel measures the presence of specific antibodies and antigens related to Hepatitis A and B viruses. Antibodies
    are proteins your immune system produces in response to harmful substances. Antigens are substances that trigger an
    immune response. In this case, the test detects the HAV-IgM, Anti-HBc IgM, and HBsAg, indicative of current or past
    infections.</p>
<h2 class="test-h2 mt-3">Why is the Hepatitis Panel - Acute Screen-I test important?</h2>
<p>This test is crucial for diagnosing acute infections with Hepatitis A and B. Early diagnosis can help start
    appropriate treatment, prevent complications, and avoid transmission to others.</p>
