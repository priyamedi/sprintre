<h2 class="test-h2 mt-3">What is the Allergen, Individual - Food Cardamom test?</h2>
<p>This test is used to determine whether an individual has an allergy to cardamom. It measures the presence of specific
    IgE antibodies in the blood, which are produced by the immune system in response to cardamom.</p>
<h2 class="test-h2 mt-3">Why might I need this test?</h2>
<p>If you experience symptoms such as itching, swelling, hives, difficulty breathing, stomach pain, or anaphylaxis after
    eating cardamom, you may need this test to confirm if you have an allergy.</p>
