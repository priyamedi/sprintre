	<h2 class="test-h2 mt-3">Why is the Bacterial Meningitis Multiplex PCR test ordered?
 </h2>
	<p>The test is typically ordered when a patient presents symptoms suggestive of meningitis, such as high fever, severe headache, neck stiffness, nausea, vomiting, sensitivity to light, or changes in mental status.
 </p>
	<h2 class="test-h2 mt-3">What does a positive result mean?
 </h2>
	<p>A positive result indicates the presence of one or more of the three types of bacteria (H. influenzae, N. meningitidis, or S. pneumoniae) in the CSF, which is indicative of bacterial meningitis.
 </p>
