<h2 class="test-h2 mt-3">Who should consider having PGS testing?</h2>
<p>PGS testing may be considered by anyone undergoing IVF treatment, especially those with a history of recurrent
    miscarriages, failed IVF cycles, genetic disorders, or advanced maternal age. However, the decision to undertake PGS
    testing should be discussed thoroughly with a fertility specialist.</p>
<h2 class="test-h2 mt-3">What does a chromosomally normal embryo mean?</h2>
<p>A chromosomally normal (or euploid) embryo is one that has the correct number of chromosomes - 46 in total, 23 from
    the egg and 23 from the sperm. Embryos with the correct number of chromosomes are more likely to develop into a
    successful pregnancy and a healthy baby.</p>
<h2 class="test-h2 mt-3">What happens if an abnormality is found?</h2>
<p>If an abnormality is detected, that particular embryo is not chosen for implantation. The decision to proceed with
    the implantation of any remaining normal embryos would be made in discussion with your fertility specialist.</p>
<h2 class="test-h2 mt-3">Is it possible to use PGS testing to choose the gender of the baby?</h2>
<p>Yes, as PGS testing involves the examination of chromosomes, it can identify the gender of the embryos. However, the
    main aim of PGS testing is to identify chromosomally normal embryos, not to select the sex of the baby.</p>
<h2 class="test-h2 mt-3">How long does it take to get the results?</h2>
<p>The results of PGS testing are generally available within 7-10 days. During this waiting period, the embryos are
    safely cryopreserved (frozen) until the results are ready.</p>
<h2 class="test-h2 mt-3">How reliable is PGS testing?</h2>
<p>PGS testing is a highly reliable method for detecting chromosomal abnormalities. However, like all tests, it's not
    100% accurate and there is a small chance of a false positive or false negative result.</p>
<h2 class="test-h2 mt-3">Can the PGS test harm the embryos?</h2>
<p>The process of biopsy involves the removal of a few cells from each embryo, which usually does not harm the embryos.
    However, the procedure should be performed by a skilled embryologist to minimize any potential risks.</p>
<h2 class="test-h2 mt-3">What are the risks associated with PGS testing?</h2>
<p>The main risk associated with PGS testing is the potential for damage to the embryos during the biopsy process.
    However, this risk is minimal when the procedure is performed by a skilled embryologist.</p>
<h2 class="test-h2 mt-3">Does PGS testing guarantee a successful pregnancy?</h2>
<p>While PGS testing can significantly increase the chances of a successful pregnancy by ensuring only chromosomally
    normal embryos are transferred, it does not guarantee a pregnancy. Other factors, such as the health and age of the
    woman, also play important roles.</p>
<h2 class="test-h2 mt-3">Can PGS testing prevent all genetic disorders?</h2>
<p>PGS testing can identify a wide range of chromosomal abnormalities, but it cannot detect all genetic disorders. Some
    genetic disorders are caused by single gene mutations that are not detectable by PGS testing.</p>
<h2 class="test-h2 mt-3">Can PGS testing be performed on frozen embryos?</h2>
<p>Yes, PGS testing can be performed on frozen embryos. The embryos would be thawed, biopsied, and then refrozen until
    the results are available.</p>
<h2 class="test-h2 mt-3">Does PGS testing increase the chance of having twins or multiples?</h2>
<p>No, PGS testing itself does not increase the chance of having twins or multiples. However, the transfer of multiple
    embryos can increase the chance of a multiple pregnancy.</p>
<h2 class="test-h2 mt-3">How is PGS testing different from Preimplantation Genetic Diagnosis (PGD)?</h2>
<p>While both PGS and PGD involve genetic testing of embryos, they are used for different purposes. PGS is used to
    screen for chromosomal abnormalities in embryos, while PGD is used to identify specific genetic disorders in embryos
    when one or both parents are known to be carriers of the disorder.</p>










