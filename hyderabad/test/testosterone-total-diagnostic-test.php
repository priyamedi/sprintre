<h2 class="test-h2 mt-3">Why is the Total Testosterone test performed?</h2>
<p>The Total Testosterone test is typically performed when a person shows symptoms of too much or too little
	testosterone. Your healthcare provider may also order this test to monitor treatment if you're receiving
	testosterone therapy.</p>
<h2 class="test-h2 mt-3">How is the Total Testosterone test done?</h2>
<p>This test is carried out using a simple blood draw. A healthcare professional will take a small blood sample from
	your arm, which is then sent to a laboratory for analysis.</p>
