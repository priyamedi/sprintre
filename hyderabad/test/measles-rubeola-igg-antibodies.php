<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>The measles IgG antibodies test is typically used to determine whether an individual has immunity to
measles. It can be used to verify that a vaccination has provided immunity, or it can be used to check if a
person has ever been infected with the virus. This is often done before traveling to regions where
measles is prevalent or in cases of potential exposure to the virus.</p>

<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>This test requires a blood sample, which is usually drawn from a vein in your arm. The blood sample is
then sent to a laboratory where it will be tested for the presence of measles IgG antibodies.</p>
