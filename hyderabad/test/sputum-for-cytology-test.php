<h2 class="test-h2 mt-3">What is the importance of getting this test done?</h2>
<p>The Sputum for Cytology Test is essential for early detection of lung diseases, including lung cancer. It can also
	help diagnose infections or other respiratory conditions. Early detection is crucial for effective treatment and
	management.</p>
<h2 class="test-h2 mt-3">Is fasting required for this test?</h2>
<p>Fasting is not required for this test, but it is advisable to avoid eating or drinking for at least one hour before
	collecting the sputum sample.</p>
