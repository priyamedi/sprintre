    discomfort. Your doctor will explain the procedure to you beforehand and guide you through the necessary
    preparations.</p>
<h2 class="test-h2 mt-3">Can I eat and drink before a tissue sample is taken for the IHC - CDX2 test?</h2>
<p>You should follow your doctor's instructions regarding eating and drinking before the procedure. In some cases, you
    may need to fast for a certain period. The specific instructions will depend on the method used to collect the
    tissue sample.</p>
<h2 class="test-h2 mt-3">What type of tissue is usually sampled for the IHC - CDX2 test?</h2>
<p>The IHC - CDX2 test is typically performed on a tissue sample from the gastrointestinal tract, often obtained through
    a biopsy. However, it may also be performed on tissue from other areas if gastrointestinal cancer spread is
    suspected.</p>
<p>In conclusion, the IHC - CDX2 test is an important tool in diagnosing and understanding
    gastrointestinal tumors. As with all tests, it is most effective when used in combination with other diagnostic
