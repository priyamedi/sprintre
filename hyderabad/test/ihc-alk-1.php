<h2 class="test-h2 mt-3">What is the importance of getting the IHC - ALK-1 test?</h2>
<p>This test can help identify the presence of ALK protein, indicative of specific cancer types. The detection of ALK-1 can guide the clinician to select a more targeted therapy for the patient, such as ALK inhibitors, enhancing the treatment's efficacy.</p>
<h2 class="test-h2 mt-3">Is fasting required for this test?</h2>
<p>No. The <strong>IHC - ALK-1 test</strong> is performed on a tissue sample obtained via biopsy. It does not require any fasting or dietary restrictions.</p>
