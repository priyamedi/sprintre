<h1 class="test-h1">FISH for Miller-Dieker Syndrome (Microdeletion 17p13.3) </h1>
<div class="post-text">
<p>Miller-Dieker Syndrome (MDS) is a rare genetic disorder characterized by lissencephaly (smooth brain), microcephaly (small head size), facial abnormalities, and severe intellectual disability. MDS is associated with a deletion in a region of chromosome 17 known as 17p13.3. The Fluorescence In Situ Hybridization (FISH) test is used to detect this microdeletion and confirm a diagnosis of Miller-Dieker Syndrome.</p>

<p>The 17p13.3 region contains several genes, including LIS1 and YWHAE, which are critical for normal brain development. A microdeletion in this area results in the absence of these genes and leads to the symptoms characteristic of MDS.</p>

<p>FISH is a molecular cytogenetic technique that uses fluorescent probes to bind to specific DNA sequences. In the case of MDS, probes are designed to attach to the 17p13.3 region. If the fluorescent signal is absent or reduced, it indicates a deletion in the 17p13.3 region.</p>

