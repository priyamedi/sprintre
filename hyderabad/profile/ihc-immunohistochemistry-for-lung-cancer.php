<h2 class="test-h2 mt-3">What is the purpose of this test?</h2>
<p>The IHC panel for lung cancer is designed to confirm the diagnosis of lung cancer and differentiate its subtypes. The
    information obtained from this test helps guide the most appropriate treatment options.</p>
<h2 class="test-h2 mt-3">Is fasting required for this test?</h2>
<p>No, fasting is not required for this test. The test is performed on a tissue sample obtained via a biopsy procedure.
