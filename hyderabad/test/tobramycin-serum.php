<h2 class="test-h2 mt-3">Why is the Tobramycin Serum Test done?</h2>
<p>This test is done to monitor the levels of tobramycin in your blood. This helps your healthcare provider adjust your
    dose if needed to ensure the drug is effective in treating your infection and to prevent potential side effects.</p>
<h2 class="test-h2 mt-3">How is the Tobramycin Serum Test performed?</h2>
<p>The Tobramycin Serum Test is a blood test. A healthcare provider will use a needle to draw blood from a vein in your
    arm. The blood sample is then sent to a laboratory for analysis.</p>
