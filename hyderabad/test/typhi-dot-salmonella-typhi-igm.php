<h2 class="test-h2 mt-3">What is a TYPHI DOT (Salmonella typhi IgM) test?</h2>
<p>A TYPHI DOT (Salmonella typhi IgM) test is a blood test that detects the presence of IgM antibodies against
    Salmonella typhi, the bacteria that causes typhoid fever. The presence of these antibodies usually signifies a
    recent or ongoing infection.</p>
<h2 class="test-h2 mt-3">Who should get a TYPHI DOT test?</h2>
<p>Individuals exhibiting symptoms of typhoid fever or those who have been in close contact with a typhoid patient or
    have travelled to an area where typhoid is prevalent should get a TYPHI DOT test.</p>
