<h2 class="test-h2 mt-3">What is the purpose of the IHC - CD20 test?</h2>
<p> The <strong>IHC - CD20</strong> test is used primarily to detect and classify B-cell lymphomas, a type of cancer that originates in the <a href="https://www.sprintdiagnostics.in/hyderabad/test/total-leukocyte-count"><b style="color:#2C53A5">white blood cells</b></a> (lymphocytes).</p>
<h2 class="test-h2 mt-3">What does a positive result mean?</h2>
<p> A positive result indicates the presence of CD20 proteins on cells in the tested tissue. This is often indicative of a B-cell lymphoma, but your doctor will interpret your results in the context of your <a  href="https://www.sprintdiagnostics.in/symptoms/"><b style="color:#2C53A5">symptoms</b></a> and other test results.</p>
