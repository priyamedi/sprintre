<h2 class="test-h2 mt-3">Why is PGS testing done on the 3rd day of embryo development?</h2>
<p>On the 3rd day of embryo development, the embryos have about 6-8 cells. This is an optimal stage to perform PGS
    testing because the cells are dividing rapidly, and chromosomal abnormalities can be more accurately detected.</p>
<h2 class="test-h2 mt-3">What kind of abnormalities can be detected with PGS testing?</h2>
<p>PGS testing can detect any abnormalities in the number of chromosomes in an embryo. It can identify conditions like
    Down syndrome (trisomy 21), Edwards syndrome (trisomy 18), and Patau syndrome (trisomy 13), among others.</p>
