<h2 class="test-h2 mt-3">Why is the Fungus Culture [THROAT SWAB] test performed?</h2>
<p>The test is performed when a healthcare provider suspects a fungal infection in a patient's throat. Symptoms of a throat fungal infection may include a sore throat, difficulty swallowing, or a sensation of something stuck in the throat.</p>

<h2 class="test-h2 mt-3">How is the throat swab sample collected for the test?</h2>
<p>A healthcare provider will take a sample from the back of your throat by rubbing a sterile cotton swab over it. The procedure is quick and causes minimal discomfort.</p>

