<h1 class="test-h1">Acute Leukemia Philadelphia Positive Panel (Flow Cytometry Acute Leukemia Characterization Panel + Karyotyping + FISH for BCR-ABL and Reflex RT-PCR for BCR-ABL - Qualitative if FISH is Positive)</h1>
<div class="post-text">
<p>Acute Leukemia is a type of cancer affecting the blood and bone marrow. It is characterized by an abnormal proliferation of immature blood cells known as blasts. The Philadelphia chromosome is a specific genetic abnormality in chromosome 22 of leukemia cancer cells. The Acute Leukemia Philadelphia Positive Panel is an essential group of tests used to characterize acute leukemia and detect the presence of the Philadelphia chromosome, which is important in diagnosing and treating Chronic Myeloid Leukemia (CML) and sometimes Acute Lymphoblastic Leukemia (ALL).</p>
<p>Flow cytometry is a technique that allows for the analysis of multiple characteristics of cells in a sample, including the presence or absence of certain markers on the cell surface. Karyotyping is a test to examine chromosomes in a sample of cells. FISH (Fluorescence In Situ Hybridization) involves using fluorescent probes that bind to the DNA sequences, including the BCR-ABL gene fusion characteristic of the Philadelphia chromosome. If FISH is positive, Reflex RT-PCR (Reverse Transcription Polymerase Chain Reaction) for BCR-ABL is performed, which is a more sensitive and specific test for the gene fusion.</p>
