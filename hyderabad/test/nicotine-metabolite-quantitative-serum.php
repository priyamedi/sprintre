<h2 class="test-h2 mt-3">What does the Nicotine Metabolite Quantitative test involve? </h2>
<p>The test involves a simple blood draw, usually from a vein in the arm. The blood sample is then sent to a laboratory, where it is tested for the presence and amount of cotinine.</p>

<h2 class="test-h2 mt-3">How long will it take to get the results? </h2>
<p>The turnaround time for the Nicotine Metabolite Quantitative test is usually 2-4 days, although this can vary depending on the specific laboratory.</p>

