<h2 class="test-h2 mt-3">Why is the Lupus Anticoagulant Panel performed?</h2>
<p> This panel is performed when you have unexplained bleeding or clotting problems, recurrent miscarriages, or if you're preparing for surgery and have a history of clotting disorders. It's also used to monitor conditions associated with lupus anticoagulants.</p>
<h2 class="test-h2 mt-3">What does a positive result mean? </h2>
<p>A positive result means lupus anticoagulants were detected in your blood. This could be a sign of an autoimmune disorder, infection, certain cancers, or it could be due to medications.</p>
