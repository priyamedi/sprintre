<div class="post-text">
<p><strong>Spinocerebellar ataxia type 3 (SCA3)</strong>, also known as Machado-Joseph disease, is a genetic disorder characterized by the progressive degeneration of specific areas of the brain, including the cerebellum and brainstem. This leads to a variety of symptoms, including poor coordination and balance, difficulty speaking and swallowing, and abnormal eye movements.</p>
<p>SCA3 is caused by a mutation in the ATXN3 gene, which instructs the production of the ataxin-3 protein. An abnormal expansion in this gene leads to an altered ataxin-3 protein that accumulates in nerve cells, causing them to function improperly and ultimately leading to their death. A genetic test, usually requiring a blood sample, is the definitive way to diagnose SCA3, particularly for individuals with a family history of the condition.</p>

