	<h2 class="test-h2 mt-3">What is the purpose of the APCA test? 
 </h2>
	<p>The APCA test is used to detect the presence of anti-parietal cell antibodies in the blood. It is often ordered for individuals showing symptoms of anemia or other signs of vitamin B12 deficiency, such as fatigue, weakness, or numbness in extremities.
 </p>
	<h2 class="test-h2 mt-3">How is the APCA test conducted? </h2>
	<p>The APCA test is conducted using a blood sample, which is typically drawn from a vein in the arm. The blood sample is then sent to a laboratory where it is analyzed using Indirect Immunofluorescence Assay (IFA) to detect anti-parietal cell antibodies.
 </p>
