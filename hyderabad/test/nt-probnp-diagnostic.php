<p>The NT-ProBNP test is done to help diagnose heart failure, to determine the severity of heart failure, and to monitor
    the effectiveness of treatment for heart failure.</p>
<h2 class="test-h2 mt-3">Is fasting required for the NT-ProBNP test?</h2>
<p>No, fasting is not required for the NT-ProBNP test.</p>
