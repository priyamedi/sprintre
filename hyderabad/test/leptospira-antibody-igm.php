<h2 class="test-h2 mt-3">What are the symptoms of Leptospirosis?</h2>
<p>Common symptoms include fever, chills, muscle aches, headaches, nausea, vomiting, jaundice (yellowing of the skin and
    eyes), and sometimes a rash.</p>
<h2 class="test-h2 mt-3">How is the Leptospira Antibody IgM test performed?</h2>
<p>The test requires a blood sample, which is drawn from a vein in the arm. The sample is then sent to a laboratory
    where it is tested for the presence of IgM antibodies to Leptospira bacteria.</p>
