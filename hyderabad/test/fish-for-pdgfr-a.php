<p>In particular, the FIP1L1-PDGFRα fusion gene, resulting from a chromosomal rearrangement, is associated with hypereosinophilic syndrome/chronic eosinophilic leukemia (HES/CEL). Moreover, other rearrangements involving the PDGFR-A gene have been observed in gastrointestinal stromal tumors (GISTs), as well as other malignancies.</p>

<p>Fluorescence in situ hybridization (FISH) is a technique used to detect and visualize specific DNA sequences in cells by using fluorescent probes that bind to those sequences. For PDGFR-A, FISH is employed to detect rearrangements involving the PDGFR-A gene.</p>

