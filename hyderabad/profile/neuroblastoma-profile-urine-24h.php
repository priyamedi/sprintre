<h1 class="test-h1">Neuroblastoma Profile (VMA, 5-HIAA and HVA) - Urine 24H </h1>
<div class="post-text">
<p>The Neuroblastoma Profile test measures three specific biochemical markers in the urine -
Vanillylmandelic Acid (VMA), 5-Hydroxyindoleacetic Acid (5-HIAA), and Homovanillic Acid (HVA). These
substances are metabolites of certain neurotransmitters which are often found in increased levels in
individuals with neuroblastoma and other tumors of the nervous system. </p>
<p>Neuroblastoma is a type of cancer that most often affects children under 5 years old. It starts in the
adrenal glands but can spread to other parts of the body. The early symptoms of neuroblastoma are
often vague and can be easily mistaken for other, less serious childhood conditions, making it
challenging to diagnose. </p>
<p>The VMA, 5-HIAA, and HVA metabolites are produced when the body breaks down certain chemicals
known as catecholamines, which include adrenaline and noradrenaline. These chemicals are produced in
larger amounts by neuroblastoma tumors, leading to increased levels of these metabolites in the urine.</p>
