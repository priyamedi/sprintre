<h2 class="test-h2 mt-3">What is the Androstenedione test?</h2>
<p>The Androstenedione test is a blood test that measures the level of Androstenedione in the body. Androstenedione is a type of androgen, a group of hormones responsible for male characteristics. It is present in both men and women, but in different amounts.
Androstenedione serves as a precursor in the production of testosterone, an essential hormone for the development of male characteristics and sexual function, and estrogens, the primary female sex hormones.</p>

<h2 class="test-h2 mt-3">Why is the Androstenedione test done?</h2>
<p>This test is often used to evaluate adrenal function and to distinguish between androgen-secreting conditions that are caused by the adrenal glands from those that are due to the ovaries in women. It can be particularly helpful for diagnosing conditions such as polycystic ovary syndrome (PCOS), adrenal tumors, congenital adrenal hyperplasia (CAH), and other disorders that can cause excess production of androgen hormones.</p>

