<h2 class="test-h2 mt-3">Who should consider taking this test?</h2>
<p>Patients who have been diagnosed with AML and have morphologic features suggestive of APL should be tested for the PML/RARα t(15;17) translocation.</p>

<h2 class="test-h2 mt-3">What does a positive result mean?</h2>
<p>A positive result indicates the presence of the t(15;17) translocation and confirms the diagnosis of APL. It also means that the patient is likely to benefit from therapy that includes ATRA and arsenic trioxide.</p>

