<h2 class="test-h2 mt-3">Why is the Leishmania Species Culture performed on liver or splenic aspirate?</h2>
<p>This culture is performed on liver or splenic aspirate because visceral leishmaniasis, a severe form of the disease,
    often affects internal organs such as the liver and spleen. Culturing tissue from these organs can help in the
    accurate diagnosis of the infection.</p>
<h2 class="test-h2 mt-3">How is the liver or splenic aspirate sample obtained?</h2>
<p>The liver or splenic aspirate is obtained by inserting a needle into the liver or spleen and extracting a small
    sample of tissue. This is done under local anesthesia to minimize discomfort.</p>
