<h2 class="test-h2 mt-3"> What is the IHC-SOX2 test?</h2>
	<p>
The IHC-SOX2 test is an immunohistochemistry test that detects the presence of the SOX2 protein in
tissue samples. Elevated levels of this protein can suggest the presence of certain types of cancers,
including squamous cell carcinoma and small cell lung cancer. </p>
<h2 class="test-h2 mt-3"> How is the IHC-SOX2 test performed?</h2>
<p>
The test is performed on a tissue sample, often collected via biopsy. The sample is treated with
antibodies designed to bind to the SOX2 protein. If present, the SOX2 protein can be identified under a
microscope. </p>
