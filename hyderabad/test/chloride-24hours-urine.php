	<h2 class="test-h2 mt-3">What is the Chloride - 24 hours Urine test?
 </h2>
	<p>This test measures the amount of chloride, a type of electrolyte, excreted in your urine over a 24-hour period. Chloride helps regulate the amount of fluid in your body and maintain the acid-base balance. </p>
	<h2 class="test-h2 mt-3">Why is this test done?
 </h2>
	<p>The test is typically performed to evaluate kidney function or to help diagnose conditions such as adrenal gland disorders, some types of acidosis or alkalosis, and certain metabolic disorders.
 </p>
