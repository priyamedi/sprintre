<h2 class="test-h2 mt-3">What is the purpose of the Troponin-T Quantitative test?</h2>
<p>The primary purpose of the Troponin-T Quantitative test is to diagnose a heart attack. The test can also be used to
    monitor individuals with heart disease and to assess the severity of heart damage.</p>
<h2 class="test-h2 mt-3">How is the Troponin-T Quantitative test performed?</h2>
<p>The test is performed by drawing a small sample of blood, usually from a vein in your arm. The blood is then sent to
    a laboratory, where it's analyzed for Troponin-T levels.</p>
