<h2 class="test-h2 mt-3">What does the CD59 test measure?</h2>
<p>The CD59 test measures the presence and quantity of the CD59 protein on cells. This protein plays a vital role in
    protecting cells from potential damage by inhibiting the complement system.</p>
<h2 class="test-h2 mt-3">How often should I have the CD59 test?</h2>
<p>The frequency of the CD59 test depends on your unique health circumstances and your doctor's advice. Regular
