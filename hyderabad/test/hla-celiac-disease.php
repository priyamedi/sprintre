<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p> The test is performed on a blood sample taken from a vein in your arm.</p>
<h2 class="test-h2 mt-3">What does a positive result mean?</h2>
<p> A positive result means that you carry the gene variants associated with an increased risk of celiac disease. However, it doesn't confirm that you have the disease.</p>
<h2 class="test-h2 mt-3">What does a negative result mean?</h2>
<p> A negative result means you don't carry the <strong>HLA-DQ2 or HLA-DQ8</strong> gene variants, and it's unlikely that you have celiac disease.</p>
<h2 class="test-h2 mt-3">How accurate is this test?</h2>
<p> The test is highly accurate at identifying the HLA-DQ2 and HLA-DQ8 gene variants, but it's not a definitive test for celiac disease.</p>
<h2 class="test-h2 mt-3">Can children undergo this test?</h2>
<p> Yes, children can undergo this test, especially if they have <a href="https://www.sprintdiagnostics.in/symptoms"><b style="color:#2C53A5">symptoms</b></a> of celiac disease or a family history of the condition.</p>
<h2 class="test-h2 mt-3">Are there any risks or side effects associated with the test?</h2>
<p> The test involves a routine blood draw, which may cause slight pain, bruising, or bleeding at the site of needle insertion.</p>
<h2 class="test-h2 mt-3">Can I eat before the test?</h2>
<p> Yes, you can eat and drink normally before the test. There are no special preparations needed.</p>
<h2 class="test-h2 mt-3"> Does a positive result mean I have celiac disease?</h2>
<p>Not necessarily. A positive result means you have the gene variants associated with celiac disease, but it doesn't confirm that you have the disease.</p>
<h2 class="test-h2 mt-3"> If I have a negative result, does that mean I can't develop celiac disease?</h2>
<p>A negative result means it's highly unlikely you'll develop celiac disease.</p>
<h2 class="test-h2 mt-3"> If I have a positive result, should my family members also be tested?</h2>
<p>Yes, if you have a positive result, your first-degree relatives (parents, siblings, children) may also want to consider testing as they have a higher risk of also having the disease.</p>
<h2 class="test-h2 mt-3"> How long will it take to get the results?</h2>
<p>The results are usually available within 7-10 days.</p>
<h2 class="test-h2 mt-3"> Do I need to stop taking my medications before the test?</h2>
<p>No, you don't need to stop taking your medications before the test. However, it's always a good idea to inform your doctor about all the medications you're taking.</p>
<h2 class="test-h2 mt-3"> Can this test diagnose other gluten-related disorders?</h2>
<p>No, this test is specific for celiac disease. Other gluten-related disorders require different tests for diagnosis.</p>
