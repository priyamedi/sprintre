<h2 class="test-h2 mt-3"> What is the Mercury - Blood test used for?</h2>
	<p>
The Mercury - Blood test is primarily used to help identify potential excessive exposure to mercury. If
you are exhibiting signs and symptoms of mercury poisoning such as weakness, fatigue, and numbness
in hands and feet, your healthcare provider may recommend this test.</p>
<h2 class="test-h2 mt-3">Do I need to fast before this test?</h2>
<p>
No, fasting is not necessary for this test. You can continue with your regular diet and daily activities
unless otherwise directed by your healthcare provider.</p>
