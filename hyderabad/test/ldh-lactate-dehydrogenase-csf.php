<h2 class="test-h2 mt-3"> What is LDH and why is it important?</h2>

<p>Lactate dehydrogenase (LDH) is an enzyme involved in the conversion of lactate to
pyruvate, a critical step in producing energy for cells. Measuring LDH levels in CSF
can help diagnose or monitor diseases affecting the brain and spinal cord.</p>

<h2 class="test-h2 mt-3"> How is the test performed?</h2>
<p>The test is performed on a sample of CSF obtained through a lumbar puncture,
where a needle is inserted into the lower part of the spine to collect fluid. This
procedure is performed by a healthcare professional.</p>
