<h2 class="test-h2 mt-3">What is the Immunoglobulin - IgG4 Sub Class Test?</h2>
<p>This test measures the level of IgG4, a subtype of the IgG antibody, in the blood. It's used to help diagnose and monitor IgG4-related disease.</p>
<h2 class="test-h2 mt-3">Why is this test important?</h2>
<p>This test is crucial for diagnosing and monitoring IgG4-related disease, a condition characterized by inflammation and fibrosis in various parts of the body. It helps guide treatment and monitor the disease's progression or response to therapy.</p>
