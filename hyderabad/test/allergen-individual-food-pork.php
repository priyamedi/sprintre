<h2 class="test-h2 mt-3">What are the symptoms of pork allergy?</h2>
<p>Symptoms of pork allergy can range from mild to severe and include itching, hives, swelling of the lips, face,
    tongue, or throat, stomach cramps, nausea, vomiting, runny nose, sneezing, coughing, wheezing, and, in severe cases,
    anaphylaxis.</p>
<h2 class="test-h2 mt-3">What causes pork allergy?</h2>
<p>Pork allergy is caused by an overreaction of the immune system to proteins found in pork. The immune system
    mistakenly identifies these proteins as harmful and produces IgE antibodies, leading to the release of chemicals
    that cause allergic symptoms.</p>
