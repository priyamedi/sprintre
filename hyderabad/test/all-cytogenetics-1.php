	<p>A sample of blood is drawn from a vein, typically in the arm. In some cases, a bone marrow sample may be taken, which involves inserting a needle into the hip bone to collect a small amount of bone marrow tissue. </p>
	<h2 class="test-h2 mt-3">How does this test affect treatment decisions? </h2>
	<p>The results of the ALL Cytogenetics 1 panel can guide doctors in making treatment decisions, including the selection of specific therapies that target the genetic abnormalities identified. </p>
