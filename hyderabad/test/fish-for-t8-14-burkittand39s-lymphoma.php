<h1 class="test-h1">FISH for t(8;14) - Burkitt's Lymphoma </h1>
<div class="post-text">
<p>Burkitt's Lymphoma is an aggressive type of non-Hodgkin lymphoma that is characterized by the rapid growth of tumors. It is more common in children and young adults. This lymphoma is associated with a particular genetic abnormality called t(8;14) translocation. This translocation involves the rearrangement of genetic material between chromosome 8 and chromosome 14. The t(8;14) translocation leads to the overexpression of the c-MYC gene, which is involved in cell growth and proliferation. The FISH (Fluorescence in situ hybridization) test for t(8;14) is an essential tool in the diagnosis and classification of Burkitt's lymphoma.</p>

<p>FISH is a molecular cytogenetic technique that uses fluorescent probes that bind to only those parts of the chromosome with a high degree of sequence complementarity. This test is highly sensitive and specific for detecting genetic abnormalities, such as the t(8;14) translocation.</p>
