<h2 class="test-h2 mt-3">What is the FISH for del(20q) test used for?</h2>
	<p>This test is used to detect deletions in the long arm of chromosome 20. It is commonly used to diagnose
or monitor hematological disorders like myelodysplastic syndromes, acute myeloid leukemia, and
chronic myelomonocytic leukemia.</p>

<h2 class="test-h2 mt-3">How is the sample for this test collected?</h2>
<p>The sample is usually collected through a blood draw or bone marrow biopsy. The choice between blood
or bone marrow depends on the physician's discretion and the clinical context.</p>
