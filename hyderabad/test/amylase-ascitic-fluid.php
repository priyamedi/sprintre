<h2 class="test-h2 mt-3">What is Ascitic Fluid?</h2>
<p>Ascitic fluid is the excess fluid that accumulates in the peritoneal cavity, the space between the two membranes lining the abdomen. This condition, known as ascites, may occur due to liver disease, cancer, heart failure, or other conditions.</p>

<h2 class="test-h2 mt-3">Why is the Amylase - Ascitic Fluid test done?</h2>
<p>This test is done to determine the levels of amylase in the ascitic fluid. High levels of amylase in ascitic fluid may indicate pancreatic diseases such as acute pancreatitis, or gastrointestinal perforation, like a perforated peptic ulcer.</p>

