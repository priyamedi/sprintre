<h2 class="test-h2 mt-3">What is FISH for TOP2A?</h2>
<p>FISH for TOP2A is a laboratory test that uses fluorescent probes to detect alterations in the TOP2A gene. The TOP2A
    gene is important in the regulation of DNA structure and is often altered in cancer.</p>
<h2 class="test-h2 mt-3">Why is the FISH for TOP2A test done?</h2>
<p>This test is primarily done to evaluate the status of the TOP2A gene in cancer patients, particularly those with
    breast cancer. The amplification or deletion of TOP2A can influence how the tumor responds to certain chemotherapy
    drugs and can provide information on prognosis.</p>
