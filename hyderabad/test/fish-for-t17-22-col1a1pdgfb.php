<div class="post-text">
<p>FISH for t(17;22) COL1A1/PDGFB is a specialized test used in the diagnosis of dermatofibrosarcoma protuberans (DFSP), a rare type of skin tumor that arises in the dermis and can infiltrate deeper layers of the skin and underlying tissue. DFSP is a low-to-intermediate grade malignancy and is characterized by the specific chromosomal translocation t(17;22). This translocation results in the fusion of the COL1A1 gene on chromosome 17 with the PDGFB gene on chromosome 22, leading to the overexpression of platelet-derived growth factor (PDGF) which contributes to tumor growth. <br><br>
Fluorescence in situ hybridization (FISH) is a laboratory technique that uses fluorescent probes to detect specific DNA sequences in the chromosomes. In the context of DFSP, FISH is used to identify the t(17;22) translocation, which is a hallmark of this condition, and plays a vital role in confirming the diagnosis and guiding treatment decisions.</p>

