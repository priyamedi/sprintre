	<h2 class="test-h2 mt-3">What does a negative test result mean? </h2>
<p>A negative result means that the specific EGFR mutations and ALK rearrangements tested were not detected. However, it does not rule out the presence of other mutations or rearrangements in these or other genes.</p>

	<h2 class="test-h2 mt-3">Can this test predict how my cancer will progress? </h2>
<p>While this test can identify specific mutations and rearrangements associated with certain types of lung cancer, it cannot predict how the disease will progress. Your doctor will use this information, along with other factors, to help estimate the likely course of your disease.</p>

	<h2 class="test-h2 mt-3">What types of treatments are available if a mutation or rearrangement is detected? </h2>
<p>If an EGFR mutation or ALK rearrangement is detected, targeted therapies such as tyrosine kinase inhibitors can be used. These drugs are designed to specifically interfere with the function of the mutated or rearranged proteins, blocking their ability to promote cancer growth.</p>

	<h2 class="test-h2 mt-3">What happens if no mutations or rearrangements are detected? </h2>
<p>If no mutations or rearrangements are detected, traditional treatment options such as surgery, radiation, and chemotherapy may be recommended. Your doctor may also suggest other tests to look for different mutations.</p>

	<h2 class="test-h2 mt-3">How accurate is this test?</h2> 
<p>The accuracy of this test is high, but it depends on the quality and type of the sample used. Inadequate sample or poor preservation of the tissue can affect the results.</p>

	<h2 class="test-h2 mt-3">Can this test be used to monitor my response to treatment? </h2>
<p>Yes, in some cases, this test can be used to monitor your response to targeted therapies. Your doctor may recommend repeated testing during your treatment.</p>

	<h2 class="test-h2 mt-3">Are there any risks or side effects associated with this test?</h2> 
<p>Since this test is usually performed on a tissue sample taken during a biopsy or surgery, the risks are associated with those procedures and not the test itself.</p>

	<h2 class="test-h2 mt-3">Can this test be done on a blood sample? </h2>
<p>While some genetic tests can be performed on a blood sample, this test is usually done on a tissue sample from the tumor. However, liquid biopsy, a newer technique that looks for tumor DNA in the blood, may be used in certain situations.</p>

	<h2 class="test-h2 mt-3">What is the advantage of doing both EGFR PCR and ALK IHC tests?</h2> 
<p>Testing for both EGFR mutations and ALK rearrangements allows for a more comprehensive understanding of the genetic changes driving a patient's cancer. This information can help guide treatment decisions.</p>

	<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p> The frequency of testing will depend on your individual circumstances. Your healthcare provider will provide specific guidance based on your condition and response to treatment.</p>

	<h2 class="test-h2 mt-3">How quickly can I expect the results? </h2>
<p>The turn-around time for this test is typically about a week, but it may vary depending on the lab.</p>

	<h2 class="test-h2 mt-3">Are the results of this test definitive? </h2>
<p>While the test results are generally reliable, they do not provide a complete picture of all the genetic changes that may be present in a tumor. Other tests may be necessary to fully understand the genetics of your cancer.</p>

	<h2 class="test-h2 mt-3">How can I prepare for this test? </h2>
<p>No specific preparation is required for this test. It is important to discuss any concerns or questions you may have with your healthcare provider before the test.</p>

