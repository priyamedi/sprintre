	<h2 class="test-h2 mt-3">What is the Cholesterol - HDL (Direct) test?
 </h2>
	<p>The Cholesterol - HDL (Direct) test measures the level of high-density lipoprotein cholesterol, also known as "good" cholesterol, directly in your blood.
 </p>
	<h2 class="test-h2 mt-3">Why is this test done?
 </h2>
	<p>This test is often performed as part of a lipid profile to assess your risk of heart disease. High levels of HDL cholesterol are generally associated with a lower risk of developing heart disease or stroke.
 </p>
