<h2 class="test-h2 mt-3">Why is this test ordered?</h2>
<p>The MYD88 - ASO PCR test may be ordered if you have symptoms suggestive of a non-Hodgkin lymphoma, such as
    unexplained weight loss, fever, and swelling of lymph nodes. The test is used to identify mutations in the MYD88
    gene that may indicate certain types of these cancers.</p>
<h2 class="test-h2 mt-3">How is this test performed?</h2>
<p>This test is performed on a blood sample. The sample is sent to a laboratory, where technicians will isolate the DNA
    and analyze it for the presence of MYD88 mutations using PCR technology.</p>
