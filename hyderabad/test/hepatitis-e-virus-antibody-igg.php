<h2 class="test-h2 mt-3">Why is the Hepatitis E Virus (HEV) Antibody - IgG test done?</h2>
<p>This test is done to determine whether you have been infected with the Hepatitis E virus. It is also used to
    ascertain the cause of acute or chronic liver disease when other more common causes have been ruled out.</p>
<h2 class="test-h2 mt-3">Is fasting required for the Hepatitis E Virus (HEV) Antibody - IgG test?</h2>
<p>No, fasting is not required for this test. You can continue your usual diet and water consumption.</p>
