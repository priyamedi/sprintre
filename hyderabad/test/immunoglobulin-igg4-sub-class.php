<h1 class="test-h1">Immunoglobulin - IgG4 Sub Class Test</h1>
<div class="post-text">
<p>Immunoglobulin G4 (IgG4) is one of the four subclasses of Immunoglobulin G (IgG), which is the most common type of antibody found in blood circulation. IgG antibodies are vital for fighting bacterial and viral infections, and IgG4 represents the smallest fraction of IgG.</p>
<p>This test measures the amount of IgG4 in your blood. Elevated levels of IgG4 can be indicative of a rare immune system disorder known as IgG4-related disease (IgG4-RD). This disease is characterized by tissue inflammation and fibrosis (scarring) in various parts of the body. It can affect various organs, including the pancreas, salivary glands, lungs, kidneys, and lymph nodes.</p>
