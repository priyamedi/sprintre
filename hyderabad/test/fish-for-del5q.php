<h2 class="test-h2 mt-3">Why is the FISH for del(5q) test performed?</h2>
	<p>FISH for del(5q) is performed to detect the deletion of a segment of chromosome 5, which can be
associated with various hematological disorders, particularly Myelodysplastic Syndromes (MDS). This
information is essential for diagnosis, treatment planning, and prognostic assessment.</p>

<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test is performed on a blood or bone marrow sample. The sample is treated with fluorescent
probes that bind to specific regions of chromosome 5. It is then examined under a special microscope to
detect the presence or absence of the deletion.</p>
