<p><strong>IDH 1&2 Mutation Analysis</strong> is a genetic test used primarily in the diagnosis and management of certain
types of cancers, notably brain tumors and acute myeloid leukemia (AML). The test detects mutations in
the IDH1 and IDH2 genes, which are often associated with these malignancies.
 </p>
<p>IDH1 and IDH2 are genes that code for isocitrate dehydrogenase enzymes, which are involved in cellular
metabolism. When functioning normally, these enzymes help to break down nutrients and generate
energy for cells. However, mutations in these genes can cause the enzymes to function abnormally and
lead to the development of cancer.</p>
