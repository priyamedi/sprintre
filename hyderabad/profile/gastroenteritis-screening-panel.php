<h2 class="test-h2 mt-3">What is the Gastroenteritis Screening Panel?</h2>
<p>This panel includes several tests that can identify infectious agents causing gastroenteritis. It typically includes
    a stool routine and culture and a test to detect Rotavirus, one of the most common causes of severe diarrhea in
    children.</p>
<h2 class="test-h2 mt-3">Why is this test important?</h2>
<p>The Gastroenteritis Screening Panel can help diagnose the cause of diarrhea, stomach pain, vomiting, and other
    symptoms of gastroenteritis. Identifying the cause is important for appropriate treatment and preventing the spread
    of infection.</p>
