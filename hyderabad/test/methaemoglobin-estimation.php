<h1 class="test-h1">Methaemoglobin Estimation </h1>
<div class="post-text">
<p>Methemoglobin estimation is a laboratory test done to measure the levels of methemoglobin in your blood. Methemoglobin is a form of hemoglobin - the protein in your red blood cells that carries oxygen from your lungs to the rest of your body. Unlike regular hemoglobin, methemoglobin is not able to release oxygen to tissues, leading to a condition known as methemoglobinemia, where tissues throughout the body are starved of oxygen. <br><br>
This test is often ordered when methemoglobinemia is suspected. Symptoms can vary widely, from mild (bluish skin and mucous membranes, fatigue, dizziness) to severe (shortness of breath, headache, altered mental status, seizures, and even death). Certain medications and chemicals can cause methemoglobinemia, as can certain inherited conditions. </p>

