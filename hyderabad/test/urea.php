<h2 class="test-h2 mt-3">What is a urea test?</h2>
<p>A urea test measures the level of urea in the blood. Urea is a waste product produced in the liver and is eliminated
    by the kidneys. The test helps assess kidney function.</p>
<h2 class="test-h2 mt-3">Why is a urea test done?</h2>
<p>A urea test is done to check how well your kidneys are working. High levels of urea in the blood can indicate kidney
    disease or other conditions that can affect kidney function.</p>
