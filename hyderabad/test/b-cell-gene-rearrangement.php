	<h2 class="test-h2 mt-3">When is the B-Cell Gene Rearrangement test ordered?
 </h2>
	<p>The test is typically ordered when a patient presents with symptoms suggestive of a B-cell malignancy (like unexplained weight loss, fever, night sweats, swollen lymph nodes) and when initial diagnostic tests, such as blood tests and imaging studies, suggest the possibility of a B-cell malignancy.
 </p>
	<h2 class="test-h2 mt-3">What does a positive result mean?
 </h2>
	<p>A positive result indicates the presence of a clonal IGH gene rearrangement, suggesting a B-cell malignancy like lymphoma or leukemia. However, the result should always be interpreted in conjunction with clinical findings and other diagnostic tests.
 </p>
