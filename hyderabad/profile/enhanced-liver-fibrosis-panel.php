<h2 class="test-h2 mt-3">Why is the Enhanced Liver Fibrosis (ELF) Panel done?</h2>
<p>The ELF panel is done to assess the degree of liver fibrosis or scarring. It is especially useful in chronic
liver diseases such as hepatitis B, hepatitis C, and non-alcoholic fatty liver disease to monitor the
progression and help make treatment decisions.</p>
<h2 class="test-h2 mt-3"> How is the ELF panel performed?</h2>
<p>A blood sample is taken from a vein in your arm using a needle. The blood is then sent to a laboratory
for analysis.</p>
