<h2 class="test-h2 mt-3">Why is the Hirsutism Evaluation Panel II performed?</h2>
<p>The Hirsutism Evaluation Panel II is used to identify the potential causes of excessive hair growth in women. The
    test may be recommended if you are experiencing symptoms of hirsutism such as thick, dark, and coarse hair growth on
    areas of the body where men typically grow hair but women do not, including the face, chest, and back.</p>
<h2 class="test-h2 mt-3">How is the Hirsutism Evaluation Panel II test performed?</h2>
<p>The test requires a blood sample which is drawn from a vein in your arm. This is typically a quick and easy procedure
    with minimal discomfort.</p>
