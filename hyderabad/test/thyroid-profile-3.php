<h1 class="test-h1">Thyroid Profile - III (Anti-TG, Anti-TPO, T3, T4, & TSH) </h1>
<div class="post-text">
<p>The Thyroid Profile - III is a comprehensive diagnostic panel that measures the levels of various hormones and antibodies related to thyroid function. This profile includes tests for Anti-Thyroglobulin (Anti-TG), Anti-Thyroid Peroxidase (Anti-TPO), Triiodothyronine (T3), Thyroxine (T4), and Thyroid-Stimulating Hormone (TSH). This panel provides a complete assessment of your thyroid function and can help diagnose a wide range of thyroid disorders.</p>

<p>The thyroid, a small gland in the neck, produces hormones T3 and T4, which play a vital role in body metabolism. TSH, produced by the pituitary gland, regulates the production of T3 and T4. Anti-TG and Anti-TPO are antibodies that attack thyroid proteins and can indicate autoimmune thyroid diseases such as Hashimoto's thyroiditis and Graves' disease.</p>

