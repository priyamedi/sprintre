<h2 class="test-h2 mt-3">What is the CMV-PCR Qualitative test on CSF?</h2>
<p>The CMV-PCR Qualitative test on CSF is a diagnostic procedure that identifies the presence of CMV DNA
in cerebrospinal fluid. It is primarily used to diagnose CMV infections in the brain or spinal cord.</p>

<h2 class="test-h2 mt-3">Why is this test done?</h2>
<p>The test is done when a person with a weakened immune system shows symptoms that suggest a CNS
infection, such as headache, fever, changes in mental status, or neurological deficits. It can also be
performed in individuals with unexplained encephalitis or meningitis.</p>
