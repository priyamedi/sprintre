<h2 class="test-h2 mt-3">When should I get this test?</h2>
<p>This test is usually ordered when a person shows symptoms suggestive of TB, such as a prolonged cough, fatigue,
    weight loss, fever, night sweats, and chest pain.</p>
<h2 class="test-h2 mt-3">What information does this test provide?</h2>
<p>This test provides preliminary evidence of the presence of Mycobacterium tuberculosis or other acid-fast bacilli in
    the sample.</p>
<h2 class="test-h2 mt-3">How often should this test be performed?</h2>
<p>The frequency of the test depends on the clinical condition of the patient and the healthcare provider's judgment.
</p>
<h2 class="test-h2 mt-3">What are normal values for this test?</h2>
<p>A normal result is no acid-fast bacilli detected in the sample.</p>
<h2 class="test-h2 mt-3">Are there any precautions to be taken?</h2>
<p>It is crucial to collect the sample properly to avoid contamination and ensure the most accurate results.</p>
<h2 class="test-h2 mt-3">What factors can affect the test levels?</h2>
<p>Technical issues with the staining or microscopic examination process, contamination of the sample, or improper
    sample collection can affect test results.</p>
<h2 class="test-h2 mt-3">Which doctor should I consult if the test result is abnormal?</h2>
<p>If the test results are abnormal, you should consult with an infectious disease specialist or a pulmonologist.</p>
