<p>No, fasting is not required for this test.</p>
<h2 class="test-h2 mt-3">Is there any specific preparation needed for this test?</h2>
<p>You should maintain your usual diet, stay well hydrated, and avoid alcohol before this test.</p>
<h2 class="test-h2 mt-3">When should I get this test?</h2>
<p>This test may be ordered if you exhibit symptoms of adrenal gland disorders or androgen excess, such as rapid weight
    gain, skin changes, hirsutism, or menstrual irregularities.</p>
<h2 class="test-h2 mt-3">What does this test measure?</h2>
<p>This test measures the amount of <strong>17-Ketogenic Steroids</strong> in the urine, which provides information about the production
    of androgenic and corticosteroid hormones by the adrenal glands.</p>
<h2 class="test-h2 mt-3">How often should I get tested?</h2>
<p>The frequency of testing depends on your symptoms and your doctor's advice.</p>
<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
<p>Normal values may vary between laboratories, but generally, they range from 5 to 15 mg/24h for adults.</p>
<h2 class="test-h2 mt-3">What precautions should I take before the test?</h2>
<p>Inform your healthcare provider about any medications you're taking, as some may influence the test results.</p>
 

<h2 class="test-h2 mt-3">What factors can affect the test results?</h2>
<p><b>Modifiable Factors:</b> Certain medications, including corticosteroids and hormonal contraceptives, can affect the results.</p>
