<h1 class="test-h1">Auto Immune Encephalitis Panel-CSF (NMDA, AMPA-GluR1 & R2, GABA Beptor, VGKC (LGI1), CASPR2 by IFA) </h1>
<div class="post-text">
<p>Autoimmune encephalitis, a condition where the body's immune system mistakenly attacks healthy
brain cells, causing inflammation, can be a challenging disorder to diagnose. The Auto Immune
Encephalitis Panel-CSF is a diagnostic tool used to detect specific antibodies associated with different
types of autoimmune encephalitis, guiding the path to accurate diagnosis and appropriate treatment.
The test examines cerebrospinal fluid (CSF) as it often provides more definitive evidence of the
condition. In the following sections, we will delve deeper into the test's specifics and how it assists in
managing autoimmune encephalitis. </p>
<p> The Auto Immune Encephalitis Panel-CSF employs a laboratory technique known as
immunofluorescence assay (IFA) to detect the presence of specific autoantibodies in the cerebrospinal
fluid. These autoantibodies include those against N-methyl-D-aspartate (NMDA) receptor, α-amino-3-
hydroxy-5-methyl-4-isoxazolepropionic acid (AMPA) receptor GluR1 &amp; R2, γ-aminobutyric acid B (GABA
B) receptor, voltage-gated potassium channel (VGKC) complex including leucine-rich glioma-inactivated
1 (LGI1), and contactin-associated protein 2 (CASPR2).</p>
<p>The presence of these specific antibodies in the cerebrospinal fluid signals different types of
autoimmune encephalitis. For instance, NMDA receptor encephalitis is often indicated by anti-NMDA
receptor antibodies, while limbic encephalitis and Morvan's syndrome can be linked with anti-LGI1 and
anti-CASPR2 antibodies. Therefore, by identifying these antibodies, the Auto Immune Encephalitis Panel-
CSF contributes to accurate diagnosis, which consequently facilitates the implementation of
appropriate, targeted treatments.</p>
