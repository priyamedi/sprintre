							<h2 class="test-h2 mt-3">What is MR Urography and why is it used?</h2>
							<p>MR Urography is an imaging test that uses magnetic resonance imaging (MRI) to obtain
								detailed images of the urinary tract, including the kidneys, ureters, and bladder. It is
								used to detect and diagnose various conditions affecting the urinary tract such as
								tumors, kidney stones, infections, and structural abnormalities.</p>

							<h2 class="test-h2 mt-3">How is MR Urography performed?</h2>
							<p>During MR Urography, you will lie on a movable examination table that slides into an MRI
								machine. A coil may be placed around the area being imaged. The MRI machine uses
								magnetic fields and radio waves to capture images. A contrast material may be injected
								into your veins to enhance the images. The procedure usually takes about 30 minutes to
								an hour.</p>

							<h2 class="test-h2 mt-3">Does MR Urography use radiation?</h2>
							<p>No, MR Urography does not use ionizing radiation. It uses magnetic fields and radio waves
								to create images, which is generally considered safer than radiation-based imaging
								techniques.</p>

							<h2 class="test-h2 mt-3">Is contrast material always used in MR Urography?</h2>
							<p>Contrast material is often used in MR Urography to enhance the clarity of the images, but
								it’s not always necessary. Your doctor will determine whether contrast material is
								needed based on the specifics of your case.</p>

							<h2 class="test-h2 mt-3">Is MR Urography painful?</h2>
							<p>MR Urography is a non-invasive procedure and is not painful. However, you may feel
								discomfort from lying still for an extended period. If contrast material is used, you
								might feel a cool sensation at the injection site.</p>

							<h2 class="test-h2 mt-3">What are the benefits of MR Urography over other imaging
								techniques?</h2>
							<p>MR Urography provides highly detailed images of the urinary tract, which can be
								especially helpful in cases where other imaging methods, such as X-rays or ultrasound,
								are not sufficient. It does not use ionizing radiation, making it a safer option for
								certain patients.</p>

							<h2 class="test-h2 mt-3">What conditions can be diagnosed with MR Urography?</h2>
							<p>MR Urography can diagnose a wide range of conditions affecting the urinary tract,
								including kidney stones, tumors, infections, congenital abnormalities, and blockages in
								the urinary tract.</p>

							<h2 class="test-h2 mt-3">Are there any risks associated with MR Urography?</h2>
							<p>The risks associated with MR Urography are minimal. However, the use of contrast material
								can cause allergic reactions or problems in patients with kidney issues. The strong
								magnetic field can also affect metallic implants.</p>

							<h2 class="test-h2 mt-3">What should I do after the procedure?</h2>
							<p>After the procedure, you can usually resume your normal activities immediately. If
								contrast material was used, you might be advised to drink fluids to help flush it out of
								your system.</p>

