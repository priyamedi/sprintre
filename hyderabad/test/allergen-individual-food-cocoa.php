<h2 class="test-h2 mt-3">How is the Allergen, Individual - Food Cocoa test performed?</h2>
<p>This test is performed using a simple blood draw. The blood sample is then analyzed in a lab to detect specific IgE
    antibodies against cocoa proteins.</p>
<h2 class="test-h2 mt-3">Who should get this test?</h2>
<p>This test is recommended for anyone who experiences allergic symptoms after consuming cocoa or cocoa-based products.
    Symptoms could range from mild reactions like hives or stomach discomfort to more severe reactions like difficulty
    breathing or anaphylaxis.</p>
