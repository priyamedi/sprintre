<h2 class="test-h2 mt-3"> What is the IHC-S100 test?</h2>
	<p>
The IHC-S100 test is a pathology test that uses specific antibodies to detect the presence of the S100
protein in tissue samples. This protein is typically present in certain types of cells, and its detection can
aid in diagnosing a variety of conditions.</p>
<h2 class="test-h2 mt-3"> How is the IHC-S100 test performed?</h2>
<p>
The test is performed on a tissue sample, typically obtained through a biopsy. The tissue is treated with
antibodies specific to the S100 protein. If this protein is present, it will bind with the antibodies and can
be detected under a microscope.</p>
