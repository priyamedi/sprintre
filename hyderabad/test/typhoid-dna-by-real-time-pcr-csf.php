<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>The Typhoid DNA (By Real Time PCR) - CSF test is performed when a patient has symptoms of typhoid fever, especially
    when symptoms of neurological involvement like confusion, altered consciousness, or seizures are present. It helps
    in confirming the diagnosis of Salmonella typhi infection in the central nervous system.</p>
<h2 class="test-h2 mt-3">How is the sample collected for this test?</h2>
<p>The sample for this test is collected through a procedure called lumbar puncture or spinal tap. In this procedure, a
    needle is inserted into the lower part of the spinal column to collect a sample of cerebrospinal fluid. The
    procedure is performed under local anesthesia, and the sample is then sent to the lab for analysis.</p>
