<h2 class="test-h2 mt-3">Why is a Sepsis Panel done?</h2>
<p>The Sepsis Panel is done to diagnose sepsis, identify the causative pathogens, and determine their antibiotic
    susceptibility. This information is vital for guiding treatment decisions.</p>
<h2 class="test-h2 mt-3">Do I need to fast before this test?</h2>
<p>No, fasting is not required before a Sepsis Panel test.</p>
