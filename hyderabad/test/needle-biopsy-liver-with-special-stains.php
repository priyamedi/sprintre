<h2 class="test-h2 mt-3">Why is a needle biopsy of the liver performed?</h2>
<p>A needle biopsy of the liver can be performed to diagnose liver diseases, determine the severity of liver disease,
    monitor the progression of liver disease, or to evaluate the effectiveness of a treatment.</p>
<h2 class="test-h2 mt-3">How is a liver needle biopsy performed?</h2>
<p>The procedure involves the insertion of a thin needle through the skin and into the liver to collect a small tissue
    sample. This is typically done under local anesthesia and with the guidance of imaging techniques like ultrasound or
    CT scans.</p>
