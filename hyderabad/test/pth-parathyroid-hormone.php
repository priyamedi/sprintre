<h2 class="test-h2 mt-3"> Why is a PTH test ordered?</h2>
	<p>
The PTH test is often ordered to determine the cause of abnormal calcium levels. It can help diagnose
parathyroid diseases, kidney diseases, or vitamin D deficiency.</p>
<h2 class="test-h2 mt-3"> What does a high PTH level mean?</h2>
<p>
High PTH levels could indicate a condition called hyperparathyroidism, where the parathyroid glands
produce too much PTH, leading to high levels of calcium in the blood.</p>
