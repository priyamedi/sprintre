<h1 class="test-h1">FISH for Synovial Sarcoma </h1>
<div class="post-text">
<p>Synovial sarcoma is a rare type of cancer that typically arises near the joints of the arms, legs, or trunk, but can occur in other areas. Despite its name, it does not arise from synovial cells but rather from pluripotent stem cells. FISH, which stands for Fluorescence In Situ Hybridization, for synovial sarcoma is a diagnostic test used to detect specific chromosomal translocations that are characteristic of this type of cancer. The most common translocation in synovial sarcoma involves the fusion of the SYT gene on chromosome 18 and the SSX gene on chromosome X.</p>
<P>FISH uses fluorescent probes that specifically attach to the SYT and SSX genes. Under a fluorescent microscope, if these probes are in close proximity or appear fused, this suggests the presence of the characteristic chromosomal translocation of synovial sarcoma.</P>
