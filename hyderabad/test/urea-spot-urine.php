<h2 class="test-h2 mt-3">What is a Urea - Spot Urine test?</h2>
<p>A Urea - Spot Urine test measures the concentration of urea, a waste product from protein metabolism, in a single
	urine sample collected at any time of the day.</p>
<h2 class="test-h2 mt-3">Why would I need a Urea - Spot Urine test?</h2>
<p>This test may be needed if your healthcare provider suspects problems with your kidneys or liver. It can also be used
	to monitor the effectiveness of treatments for kidney or liver disease.</p>
