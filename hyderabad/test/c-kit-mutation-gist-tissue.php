<h2 class="test-h2 mt-3">What is the c-Kit Mutation (GIST) - Tissue test?</h2>
<p>The c-Kit Mutation (GIST) - Tissue test is a genetic test that identifies mutations in the c-Kit gene in tissue samples. These mutations are often linked to gastrointestinal stromal tumors (GISTs).</p>
<h2 class="test-h2 mt-3">Why might I need this test?</h2>
<p>This test may be recommended if you've been diagnosed with a gastrointestinal stromal tumor (GIST) or if you exhibit symptoms of this condition. The results can guide your treatment plan.</p>
