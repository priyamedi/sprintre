<h2 class="test-h2 mt-3">What is the purpose of Cytology - FNAC with Procedure?</h2>
<p>Cytology - FNAC with Procedure is used to investigate and diagnose lumps or masses. It’s effective in identifying
    cancers, infections, inflammatory diseases, and other abnormalities.</p>
<h2 class="test-h2 mt-3">How is the FNAC procedure performed?</h2>
<p>During FNAC, a healthcare professional inserts a thin, hollow needle into the lump or mass to extract cells. The
    cells are then placed on slides, stained, and examined under a microscope by a pathologist.</p>
