<h2 class="test-h2 mt-3">What is the Amino Levulinic Acid 5 - ALA - 24 hrs urine test?</h2>
<p>This is a diagnostic test that measures the levels of delta-aminolevulinic acid (ALA) in the urine collected over a 24-hour period. It is used to evaluate the synthesis of heme, an essential component of hemoglobin.</p>

<h2 class="test-h2 mt-3">Why is this test important?</h2>
<p>The test is crucial for diagnosing disorders related to heme synthesis, such as porphyria. These disorders can cause various symptoms, including abdominal pain, muscle weakness, and neurological issues. Detecting these disorders early is essential for proper management and treatment.</p>

