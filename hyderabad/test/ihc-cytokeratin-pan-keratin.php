<h2 class="test-h2 mt-3">What is the IHC - Cytokeratin - Pan Keratin test?</h2>
<p>This test helps in the identification of various cytokeratins in a tissue sample. It's particularly useful in
    diagnosing carcinomas or other tumors of epithelial origin.</p>
<h2 class="test-h2 mt-3">Why is this test necessary?</h2>
<p>This test is crucial when a tumor's origin is hard to determine. It assists in identifying whether the tumor is of
    epithelial origin, influencing the treatment approach.</p>
