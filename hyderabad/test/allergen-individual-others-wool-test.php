	<h2 class="test-h2 mt-3"> What symptoms might indicate a wool allergy?</h2>
	<p>
Common symptoms can include redness, itching, swelling, and hives on the skin that comes into contact
with wool. Some people may also experience respiratory symptoms, such as sneezing, runny nose, and
<a href="https://www.sprintdiagnostics.in/symptoms/dry-or-itchy-eyes"><b style="color:#2C53A5">itchy eyes</b></a>, especially when wool fibers are airborne.</p>
