<h2 class="test-h2 mt-3"> Why is the Beta-HCG test done?</h2>
	<p>
The Beta-HCG test is done primarily to confirm pregnancy, estimate the age of the fetus, and diagnose
any potential complications in pregnancy. Additionally, it is used as a tumor marker to help diagnose and
monitor certain types of cancer.</p>
<h2 class="test-h2 mt-3"> What cancers are associated with elevated levels of Beta-HCG?</h2>
<p>
Elevated levels of Beta-HCG can be associated with germ cell tumors, choriocarcinoma, and, in rare
cases, other cancers such as breast, lung, and gastrointestinal cancers.</p>
