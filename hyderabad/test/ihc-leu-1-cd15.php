<h2 class="test-h2 mt-3"> What is the IHC - Leu 1 (CD15) test?</h2>

<p>	The IHC - Leu 1 (CD15) test is a laboratory procedure that identifies the presence of the CD15 molecule
in tissue samples. It is a vital tool for diagnosing and monitoring certain cancers, notably Hodgkin's
lymphoma.</p>
<h2 class="test-h2 mt-3"> Why is the IHC - Leu 1 (CD15) test important?</h2>

<p>The IHC - Leu 1 (CD15) test helps determine the presence of Hodgkin's lymphoma and certain other
cancers. The CD15 marker is expressed on Reed-Sternberg cells, characteristic of Hodgkin's lymphoma,
and its identification can significantly aid in diagnosing and monitoring the disease.</p>
