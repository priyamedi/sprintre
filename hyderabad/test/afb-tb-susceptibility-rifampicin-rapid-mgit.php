	<h2 class="test-h2 mt-3">When should I get this test done?</h2>
<p>Your doctor may recommend this test if you have been diagnosed with TB and your condition does not improve despite treatment, or if you have been exposed to someone with drug-resistant TB.</p>

	<h2 class="test-h2 mt-3">What does this test measure?</h2>
<p>This test measures the susceptibility of Mycobacterium tuberculosis to rifampicin. It helps in identifying whether the TB bacteria are resistant or sensitive to rifampicin.</p>

	<h2 class="test-h2 mt-3">How often should this test be done?</h2>
<p>The frequency of the test depends on your doctor's recommendation, usually done when there's a suspicion of drug-resistant TB.</p>

	<h2 class="test-h2 mt-3">What are the normal values for this test?</h2>
<p>A sensitive result indicates that the TB bacteria are likely to respond to rifampicin-based treatment. A resistant result indicates that the TB bacteria are not likely to respond to rifampicin.</p>

	<h2 class="test-h2 mt-3">What precautions should I take?</h2>
<p>The primary precaution is to provide a proper sample for accurate results. Follow the instructions provided by the healthcare facility or your doctor.</p>

	<h2 class="test-h2 mt-3">What factors can affect the levels?</h2>
<p>Factors that can affect the test results include improper collection of the sample, contamination of the sample, and technical issues with the testing process.</p>

	<h2 class="test-h2 mt-3">What are the modifiable and non-modifiable factors?</h2>
<p>Modifiable factors include the method of sample collection and handling. Non-modifiable factors are related to the biological properties of the TB bacteria itself, such as natural genetic mutation leading to drug resistance.</p>

	<h2 class="test-h2 mt-3">Which doctor should I consult in case of abnormal values?</h2>
<p>If your test results indicate rifampicin resistance, you should consult a specialist in infectious diseases or a pulmonologist for further management.</p>

	<h2 class="test-h2 mt-3">Is there any specific preparation required for this test?</h2>
<p>No specific preparation is required. Just ensure to follow the instructions given by your healthcare professional for the sample collection.</p>

	<h2 class="test-h2 mt-3">Can the test results vary in different labs?</h2>
<p>The test should be highly standardized, but slight differences might occur due to technical variances.</p>

	<h2 class="test-h2 mt-3">Does this test pose any risk?</h2>
<p>The test itself poses no risk, but sample collection may cause minor discomfort.</p>

	<h2 class="test-h2 mt-3">Can I eat and drink normally before the test?</h2>
<p>Yes, you can eat and drink normally before the test.</p>

	<h2 class="test-h2 mt-3">Can drug-resistant TB be treated?</h2>
<p>Yes, drug-resistant TB can be treated, but it requires a longer duration and different combinations of anti-TB drugs compared to drug-sensitive TB.</p>

<p>This test plays an essential role in the management of tuberculosis, particularly in regions where drug-resistant TB is prevalent. By identifying the resistance profile of the TB bacteria, healthcare professionals can customize the treatment regimen, which is crucial for patient recovery and to prevent the spread of drug-resistant TB. Timely and accurate results from this test can significantly impact the outcome and prognosis of the disease.</p>

