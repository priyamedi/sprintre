<h2 class="test-h2 mt-3">What are the symptoms of an allergy to moths?</h2>
<p> Common symptoms of moth allergies include sneezing, runny nose, itchy eyes, coughing, wheezing, skin rashes, or worsening of asthma symptoms. In severe cases, exposure to moth allergens can lead to anaphylaxis, a life-threatening allergic reaction.</p>
<h2 class="test-h2 mt-3">How is an allergy to moths diagnosed?</h2>
<p> A blood test is performed to detect the presence of antibodies specific to moth allergens. In some cases, a skin prick test, where a small amount of the allergen is applied to the skin using a tiny needle, may be performed.</p>
