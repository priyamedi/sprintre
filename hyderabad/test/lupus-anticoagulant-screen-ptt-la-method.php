<h1 class="test-h1">Lupus Anticoagulant Screen (PTT-LA Method) </h1>
<div class="post-text">
<p>The Lupus Anticoagulant Screen, performed via the PTT-LA (Partial Thromboplastin Time - Lupus Anticoagulant) Method, is a blood test that helps identify the presence of lupus anticoagulants, a type of antibody, in your blood. These antibodies are commonly associated with antiphospholipid syndrome (APS), a disorder that can lead to increased blood clot formation. </p>
<p>Lupus anticoagulants are so named because they were first discovered in individuals with the autoimmune disease known as lupus. However, the presence of these antibodies does not necessarily mean that a person has lupus. They can be found in those with other autoimmune diseases, infections, malignancies, and even in people on certain medications. </p>
