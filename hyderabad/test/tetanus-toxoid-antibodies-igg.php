<p>Tetanus is a severe bacterial infection that affects the nervous system and can lead to muscle stiffness and spasms. The bacteria that cause tetanus are present in soil, dust, and animal feces, and can enter the body through a wound or cut. The Tetanus Toxoid Antibodies (IgG) test is used to measure the level of these specific antibodies in your blood. </p>
