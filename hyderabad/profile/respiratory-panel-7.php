<h2 class="test-h2 mt-3">What is the Respiratory Panel 7 diagnostic test?</h2>
<p>The Respiratory Panel 7 test is a set of tests designed to detect four common pathogens: Klebsiella pneumoniae,
    Legionella, Pneumocystis jiroveci, and various species of Salmonella. These microorganisms can cause a variety of
    respiratory infections, especially in individuals with compromised immune systems.</p>
<h2 class="test-h2 mt-3">Who needs to undergo this test?</h2>
<p>Individuals presenting with symptoms of a severe respiratory infection that is not responding to initial treatments,
    or those suspected of having an infection caused by one of these pathogens may be advised to undergo this test.</p>
