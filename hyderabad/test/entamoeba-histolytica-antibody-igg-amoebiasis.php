<h2 class="test-h2 mt-3">What is amoebiasis?</h2>
<p>Amoebiasis is an infection caused by the parasite Entamoeba histolytica. It mostly affects the intestines
and can cause symptoms like diarrhea, stomach pain, and bloody stools. In some cases, it can also affect
the liver.</p>
<h2 class="test-h2 mt-3"> How is the Entamoeba Histolytica Antibody (IgG) test performed?</h2>
<p>A healthcare professional will collect a blood sample from a vein in your arm using a syringe or vial. This
sample will then be sent to a lab to detect the presence of IgG antibodies against Entamoeba histolytica.</p>
