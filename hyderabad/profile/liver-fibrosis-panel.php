	<h2 class="test-h2 mt-3">Why is the Liver Fibrosis Panel ordered? </h2>
	<p>The panel is ordered when a patient shows symptoms of liver disease or has a history of conditions known to affect the liver. These include fatigue, jaundice, abdominal pain, swelling, nausea, and vomiting.
 </p>
	<h2 class="test-h2 mt-3">What does a Liver Function Test entail?  </h2>
	<p>A Liver Function Test measures the levels of various enzymes and proteins in your blood that are either produced by liver cells or released into the blood when your liver is damaged.
 </p>
