					<h2 class="test-h2 mt-3">What is the Coxsackie Antibody IgM Test?</h2>
<p>This test is used to detect IgM antibodies against the Coxsackie virus in the blood, indicating a recent or
    ongoing infection.</p><div class="mt-3"></div>
<h2 class="test-h2 mt-3">Why is this test performed?</h2>
<p>The test is performed to help diagnose a current or recent infection with the Coxsackie virus, especially in cases
    presenting symptoms such as fever, rash, or blisters in the mouth and on the hands and feet.</p>

	



					
