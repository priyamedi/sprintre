	<h2 class="test-h2 mt-3"> What other tests might be done in conjunction with this test? </h2>
	<p>Your doctor might recommend other tests such as an echocardiogram, electrocardiogram, or other genetic tests to fully understand your condition. </p>
	<h2 class="test-h2 mt-3">  Can I do anything to influence the results of this test? </h2>
	<p> No, the results of a genetic test are not influenced by your actions or lifestyle. They are a reflection of your inherited DNA.</p>
	<h2 class="test-h2 mt-3">Is it possible for an individual to have a normal result and still have symptoms of MICM?  </h2>
	<p> Yes, it is possible for an individual to have a normal test result and still experience symptoms. This could be due to mutations that the test did not analyze or to other forms of cardiomyopathy not related to mitochondrial mutations.</p>
