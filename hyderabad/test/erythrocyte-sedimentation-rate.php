<h2 class="test-h2 mt-3">What does the ESR - Westergren's Method measure?</h2>
<p>This test measures the distance (in millimeters) that red blood cells fall in a test tube in one hour. When
inflammation is present, certain proteins cause red blood cells to stick together and fall more quickly
than normal.</p>

<h2 class="test-h2 mt-3">Why is the ESR test performed?</h2>
<p>The ESR test is often used to help diagnose conditions associated with inflammation, such as arthritis or
vasculitis. It can also be used to monitor the progress of inflammatory diseases.</p>
