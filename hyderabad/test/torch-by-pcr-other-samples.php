<h2 class="test-h2 mt-3">What types of samples can be used for the TORCH by PCR test apart from blood?</h2>
<p>Besides blood, the TORCH by PCR test can use amniotic fluid, urine, throat swabs, or other tissue samples depending
    on the suspected infection and the patient.</p>
<h2 class="test-h2 mt-3">Why would a healthcare provider use a different type of sample?</h2>
<p>Other samples such as amniotic fluid or urine may be used to diagnose infections in fetuses, neonates, or when blood
    testing is not feasible or may not yield sufficient information.</p>
