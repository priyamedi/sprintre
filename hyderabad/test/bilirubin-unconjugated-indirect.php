<h1 class="test-h1">Bilirubin Unconjugated (Indirect)</h1>
<div class="post-text">
<p>Bilirubin is a yellow pigment produced as a by-product of the breakdown of hemoglobin, which is a component of red blood cells. The liver plays a key role in processing bilirubin so that it can be eliminated from the body through the feces. There are two main types of bilirubin: unconjugated (indirect) and conjugated (direct). The Bilirubin Unconjugated, or Indirect, test specifically measures the amount of unconjugated bilirubin in the blood.</p>
<p>Unconjugated bilirubin is not water-soluble and needs to be transported to the liver, where it gets converted into the conjugated form. Conjugated bilirubin is water-soluble and can be excreted through the bile into the intestine.</p>
<p>
<strong>The Bilirubin Unconjugated test is done to:</strong>
<ul>
	<li>Evaluate the liver’s function and its ability to process bilirubin.</li>
	<li>Diagnose conditions that cause increased destruction of red blood cells.</li>
	<li>Monitor the progression and treatment of liver diseases.</li>
	<li>Help in the diagnosis of jaundice.</li>
	<li>Evaluate the risk of kernicterus (a type of brain damage) in newborns with jaundice.</li>
</ul>
</p>
<p>The test is conducted by drawing blood from a vein, usually in your arm. The blood sample is then sent to the laboratory for analysis. Unconjugated bilirubin is measured in milligrams per deciliter (mg/dL) or micromoles per liter (µmol/L).<br>
<strong>Normal Range:</strong> The normal range for unconjugated bilirubin is typically 0.2 to 1.0 mg/dL, but this can vary slightly depending on the laboratory.<br>
<strong>High Levels:</strong> Elevated levels of unconjugated bilirubin could indicate hemolytic anemia, Gilbert's syndrome, or other conditions that increase the rate of red blood cell breakdown or impair the liver's ability to conjugate bilirubin.<br>
<strong>Low Levels:</strong> Unconjugated bilirubin levels are not usually clinically significant if they are low.
</p>
