<h1 class="test-h1">Autoimmune Hepatitis Panel I (ANA-IFA with titers, AMA- IFA, ANCA-IFA withtiters, ASMA -IFA, Anti LKM) </h1>
<div class="post-text">
<p>Autoimmune Hepatitis Panel I plays an instrumental role in the diagnosis and management of
autoimmune hepatitis, a condition where the body's immune system attacks liver cells, causing
inflammation and liver damage. This comprehensive panel detects a range of antibodies using an
immunofluorescence assay (IFA) to provide key insights into the disease and guide treatment strategies.
Understanding the intricacies of this diagnostic test can help patients and healthcare professionals
navigate the path to optimal liver health. </p>
<p>The Autoimmune Hepatitis Panel I utilizes an IFA to identify several types of antibodies present in the
blood, which can signify different aspects of autoimmune hepatitis. The panel includes antinuclear
antibodies (ANA), anti-mitochondrial antibodies (AMA), antineutrophil cytoplasmic antibodies (ANCA),
anti-smooth muscle antibodies (ASMA), and anti-liver kidney microsomal type 1 antibodies (Anti-LKM1). </p>
<p>Autoimmune hepatitis can be classified into different types based on the presence of these antibodies.
Type 1 autoimmune hepatitis, the most common form, is typically associated with ANA, ASMA, and at
times, ANCA. Type 2 autoimmune hepatitis, on the other hand, is identified by the presence of Anti-
LKM1 antibodies. Although AMA antibodies are usually linked with primary biliary cirrhosis, they can
also be seen in autoimmune hepatitis. The detection and quantification of these antibodies through the
Autoimmune Hepatitis Panel I, therefore, contribute to a more precise diagnosis, providing the basis for
targeted treatment.</p>
