	<h2 class="test-h2 mt-3">Why is the serum electrolytes test ordered? </h2>
	<p>The serum electrolytes test is ordered as part of a routine health check-up or to help diagnose and
monitor conditions that affect electrolyte levels. It is also ordered for patients with hypertension, kidney
disease, or disorders of the heart, liver, or endocrine system. </p>
	<h2 class="test-h2 mt-3">What does it mean if my electrolyte levels are high or low? </h2>
	<p>High or low levels of electrolytes can indicate an imbalance which may be due to dehydration,
medications, kidney dysfunction, or underlying health conditions. For example, high sodium levels might
indicate dehydration, while low sodium levels might suggest a condition called hyponatremia. </p>
