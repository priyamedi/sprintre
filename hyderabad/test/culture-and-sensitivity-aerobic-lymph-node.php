<h2 class="test-h2 mt-3"> What is the purpose of the Culture and Sensitivity - Aerobic [Lymph Node] test?</h2>
<p> The test is used to identify bacteria that may be causing an infection in a lymph node and to
determine which antibiotics are most effective against the particular strain of bacteria involved.</p>
<h2 class="test-h2 mt-3"> How is the sample for the test collected?</h2>
<p> The sample is typically collected through aspiration, where a needle is used to draw fluid from the
lymph node, or through a biopsy, where a small piece of tissue is removed. These procedures are usually
performed by a healthcare professional.</p>
