<p>After a period of fasting, the patient is given a glucose-rich drink. Blood samples are then taken at various intervals to measure glucose and growth hormone levels.</p>

	<h2 class="test-h2 mt-3">What should I do to prepare for this test? </h2>
<p>You should fast for 8-12 hours prior to the test. Avoid exercise and caffeine for 8-12 hours before the test.</p>

	<h2 class="test-h2 mt-3">What does the result of this test indicate? </h2>
<p>High glucose levels may indicate diabetes or prediabetes. Abnormal levels of the growth hormone could indicate a condition related to growth hormone imbalance.</p>

	<h2 class="test-h2 mt-3">What kind of sample is needed for this test?</h2>
<p>A blood sample is needed for this test.</p>

	<h2 class="test-h2 mt-3">Are there any risks associated with this test?</h2>
<p>The risks associated with this test are minimal and similar to those of a regular blood draw.</p>

