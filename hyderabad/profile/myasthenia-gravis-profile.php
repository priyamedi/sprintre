<h2 class="test-h2 mt-3">What is Myasthenia Gravis?</h2>
<p>Myasthenia Gravis is an autoimmune disorder that impairs communication between nerves and muscles, resulting in
    muscle weakness and fatigue. This condition may affect any voluntary muscle but is often noticed first in the
    muscles controlling eye and eyelid movement, facial expression, chewing, talking, and swallowing.</p>
<h2 class="test-h2 mt-3">What is the purpose of the AChR, ASKA, MUSK tests?</h2>
<p>These tests are designed to detect the presence of specific antibodies in the blood, which are often present in
    individuals with Myasthenia Gravis. Detecting these antibodies can help confirm a diagnosis of Myasthenia Gravis.
</p>
