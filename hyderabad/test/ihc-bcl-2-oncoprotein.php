<div class="post-text">
<p><strong>Bcl-2</strong> is an oncoprotein, a type of protein that is involved in the regulation of cell death or apoptosis. It gets its name from B-cell lymphoma 2, as it is the second member of a range of proteins initially described in chromosomal translocations involving chromosomes 14 and 18 in follicular lymphomas. An increase in Bcl-2 expression has been associated with a variety of different cancers, including breast, lung, and prostate cancer, as well as lymphomas and leukemias. </p>
<p>The <strong>IHC - Bcl-2 oncoprotein test</strong> uses immunohistochemistry (IHC), a laboratory method that utilizes antibodies to detect specific proteins in cells within a tissue section. This test specifically identifies the presence of Bcl-2 oncoprotein in a sample of your tissue. </p>
