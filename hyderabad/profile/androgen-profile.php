<h2 class="test-h2 mt-3">What is the Androgen Profile test?</h2>
<p>The Androgen Profile test is a blood test that measures the level of various hormones, specifically androgens, in the body. Androgens are a group of hormones that play a role in male traits and reproductive activity. They are present in both men and women, but in different amounts. The hormones measured in this profile are:
<ul>
	<li><strong>Testosterone (Total and Free):</strong> An essential hormone in both men and women, influencing mood, energy, and sexual function.</li>
	<li><strong>SHBG (Sex Hormone Binding Globulin):</strong> A protein that binds to sex hormones and carries them in the bloodstream.</li>
	<li><strong>DHT (Dihydrotestosterone):</strong> A powerful male sex hormone that is responsible for the development of male characteristics.</li>
	<li><strong>FAI (Free Androgen Index):</strong> A calculation based on the ratio of testosterone and SHBG and used to estimate the amount of bioavailable testosterone.</li>
	<li><strong>Androstenedione:</strong> A hormone that serves as a precursor in the production of testosterone and estrogen.</li>
</ul>
</p>

<h2 class="test-h2 mt-3">Why is the Androgen Profile test done?</h2>
<p>This test is often used to diagnose hormonal imbalances in both men and women. It can be particularly helpful for diagnosing conditions such as hypogonadism, polycystic ovary syndrome (PCOS), adrenal gland disorders, and tumors that might produce excess amounts of certain hormones. The androgen profile may also be used to evaluate physical and sexual development in boys and girls.</p>

