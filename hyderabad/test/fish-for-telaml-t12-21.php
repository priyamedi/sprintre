<h2 class="test-h2 mt-3">What is the FISH for TEL/AML t(12;21) test?</h2>
<p>The FISH for TEL/AML t(12;21) test is a diagnostic procedure that uses fluorescent probes to detect the presence of
    the TEL-AML1 fusion gene, which is created when parts of chromosomes 12 and 21 exchange places. This genetic
    alteration is commonly seen in cases of acute lymphoblastic leukemia (ALL).</p>
<h2 class="test-h2 mt-3">Why is the FISH for TEL/AML t(12;21) test done?</h2>
<p>The FISH test for TEL/AML t(12;21) is done to confirm the diagnosis of ALL, particularly in children, and to guide
    the choice of treatment. It helps in identifying patients who may respond to specific therapies targeting the
    abnormal protein produced by the TEL-AML1 fusion gene.</p>
