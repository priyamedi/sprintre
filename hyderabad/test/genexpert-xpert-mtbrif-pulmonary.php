<h1 class="test-h1">GeneXpert (Xpert MTB/RIF) - Pulmonary</h1>
<div class="post-text">
<p>GeneXpert MTB/RIF, also known as Xpert MTB/RIF, is a rapid molecular test that detects Mycobacterium tuberculosis complex (MTBC) DNA and rifampicin resistance. MTBC is the group of bacteria that causes tuberculosis (TB). The detection of rifampicin resistance serves as a surrogate marker for multidrug-resistant TB (MDR-TB). This test is specifically intended for samples from pulmonary sites, meaning it's used for diagnosing TB in the lungs.</p>
<p>The GeneXpert MTB/RIF test has revolutionized the diagnosis of TB by providing a high degree of accuracy and much quicker results than traditional culture-based tests. Additionally, its ability to simultaneously test for drug resistance is a significant advantage, allowing for the immediate initiation of appropriate therapy, which is critical in managing TB effectively, particularly in cases of MDR-TB.</p>
