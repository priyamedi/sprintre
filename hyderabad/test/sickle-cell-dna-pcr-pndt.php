<div class="post-text">
<p>The Sickle Cell DNA PCR PNDT (Pre-Natal Diagnostic Testing) is a highly specialized diagnostic test used to detect sickle cell disease in a fetus. Sickle cell disease is a genetic disorder that affects the hemoglobin in red blood cells, causing the cells to become rigid and sickle-shaped, leading to a variety of health complications.</p>

<p>In this test, DNA is extracted from a sample of amniotic fluid or tissue from the chorionic villus, obtained during an amniocentesis or chorionic villus sampling procedure. The DNA is then amplified using Polymerase Chain Reaction (PCR) and tested for the presence of the sickle cell mutation.</p>
 
