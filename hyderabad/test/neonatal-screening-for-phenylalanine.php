<h2 class="test-h2 mt-3">Why is the phenylalanine screening test necessary?</h2>
<p>The phenylalanine screening test is crucial for early detection of PKU. Early detection and treatment can prevent the
    severe intellectual disability and other health problems associated with PKU. The test is a preventative health
    measure that is part of a standard screening panel for newborns in many countries.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>The test is performed by collecting a few drops of blood from the baby's heel. This blood is placed on a special
    filter paper and sent to a laboratory for analysis.</p>
