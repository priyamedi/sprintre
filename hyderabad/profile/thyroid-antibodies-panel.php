<h2 class="test-h2 mt-3">Why do I need to undergo a Thyroid Antibodies Panel?</h2>
<p>A Thyroid Antibodies Panel is performed when a healthcare provider suspects that you have an autoimmune thyroid
    disorder, based on symptoms or abnormal results from other thyroid tests.</p>
<h2 class="test-h2 mt-3">How is the test performed?</h2>
<p>A small blood sample is drawn from a vein in your arm using a needle. The sample is then sent to a laboratory, where
    the levels of Anti-TG and Anti-TPO are measured.</p>
