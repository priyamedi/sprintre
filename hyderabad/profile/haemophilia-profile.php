<h1 class="test-h1">Haemophilia Profile (APTT with mixing studies, Factor VIII, Factor IX) </h1>
<div class="post-text">
<p>Haemophilia is an inherited genetic disorder that impairs the body's ability to make blood clots, a process needed to stop bleeding. There are two main types of haemophilia: Haemophilia A, which is caused by a deficiency of clotting Factor VIII, and Haemophilia B, which is caused by a deficiency of clotting Factor IX. A Haemophilia Profile is used to diagnose these two types of haemophilia and determine their severity.
 </p>
<p>The profile includes tests for Activated Partial Thromboplastin Time (APTT) with mixing studies, Factor VIII, and Factor IX. APTT is a screening test that measures the time it takes for blood to clot, while mixing studies are used to determine whether a prolonged APTT is due to a deficiency of certain clotting factors or the presence of an inhibitor. The tests for Factor VIII and Factor IX measure the levels of these specific clotting factors in the blood.
 </p>
