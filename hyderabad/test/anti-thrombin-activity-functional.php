<h2 class="test-h2 mt-3">What is the purpose of the Anti-Thrombin (AT III) Activity test? </h2>
<p>The Anti-Thrombin (AT III) Activity test is used to assess the activity of the anti-thrombin protein in the blood. This is important for diagnosing disorders associated with excessive clotting or bleeding and monitoring the effectiveness of anticoagulant therapy.</p>
<h2 class="test-h2 mt-3">How is the Anti-Thrombin (AT III) Activity test performed? </h2>
<p>A healthcare professional will take a blood sample from a vein in your arm. The sample is then sent to a laboratory where the activity of the anti-thrombin protein is measured.</p>
