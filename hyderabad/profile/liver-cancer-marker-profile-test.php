<h1 class="test-h1">Liver Cancer Marker Profile Test (AFP, CEA, HCG)
 </h1>
<div class="post-text">
<p>The Liver Cancer Marker Profile test is a blood test that measures the levels of certain proteins, namely Alpha-Fetoprotein (AFP), Carcinoembryonic Antigen (CEA), and Human Chorionic Gonadotropin (HCG). These are known as tumor markers, and they can be produced by both normal cells and cancer cells. However, in certain types of cancers, including liver cancer, the levels of these markers can significantly increase.
 </p>
<p>AFP is a protein produced by the liver and yolk sac of a fetus during development. In adults, AFP levels are typically very low, but liver disease or liver cancer can cause these levels to rise. CEA is a protein that can be overproduced in certain types of cancers, including liver cancer. HCG is a hormone produced during pregnancy but can be elevated in certain cancers. </p>
<p>This test can be used for several purposes in individuals diagnosed with or suspected of having liver cancer, including diagnosis, determining the stage of the cancer, assessing response to treatment, and monitoring for recurrence.
</p>
