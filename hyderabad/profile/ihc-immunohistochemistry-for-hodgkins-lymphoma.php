<h1 class="test-h1">IHC - diagnostic panel for Hodgkin's Lymphoma  </h1>
<div class="post-text">
<p>Hodgkin's lymphoma, also known as Hodgkin's disease, is a type of lymphoma, which is a blood cancer that starts in the lymphatic system. The lymphatic system helps the body fight infections and diseases. In Hodgkin's lymphoma, cells in the lymphatic system grow abnormally and may spread beyond it. There are several types of Hodgkin's lymphoma, and their diagnosis and treatment can differ.</p>

<p>To accurately diagnose Hodgkin's lymphoma, a biopsy of the affected lymph node is needed. After that, an Immunohistochemistry (IHC) diagnostic panel is often performed. IHC uses antibodies to detect the presence of specific proteins (antigens) in tissue samples, helping to identify the type of cells involved in a disease process, like cancer. In this case, the panel is reflexively selecting among markers including LCA, CD30, CD15, EBV, CD20, PAX5, CD3, CD5, CD4, CD57, Oct2, BOB1, ALK-1. Each of these markers provides essential information that helps in the diagnosis and management of Hodgkin's lymphoma.</p>

