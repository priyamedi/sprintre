<h2 class="test-h2 mt-3">How is the FISH for Trisomy 13 test performed?</h2>
<p>For prenatal testing, an amniocentesis is performed where a small sample of amniotic fluid is collected. For
    postnatal testing, a blood sample is collected. The sample is then analyzed using fluorescent probes that bind to
    chromosome 13. The cells are examined under a fluorescence microscope to check for the presence of an extra
    chromosome.</p>
<h2 class="test-h2 mt-3">Why is it important to test for Trisomy 13?</h2>
<p>Testing for Trisomy 13 is important for early detection and management of the condition. Knowing if a fetus has
    Trisomy 13 can help in making informed decisions regarding the pregnancy and preparing for the specialized care that
    will be needed if the child is born with the condition.</p>
<h2 class="test-h2 mt-3">Who should get tested for Trisomy 13?</h2>
<p>Pregnant women who have a family history of genetic disorders, are of advanced maternal age, or have had abnormal
    ultrasound findings or other screening tests should consider getting tested for Trisomy 13.</p>
<h2 class="test-h2 mt-3">What are the symptoms of Trisomy 13?</h2>
<p>Trisomy 13 can cause a range of symptoms including heart defects, brain abnormalities, small or underdeveloped eyes,
