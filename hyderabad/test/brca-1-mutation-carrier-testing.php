<h2 class="test-h2 mt-3"> What is BRCA 1 Mutation Carrier Testing?</h2>
	<p>
It is a genetic test that detects mutations in the BRCA1 gene. Individuals who carry a mutation in this
gene have an increased risk of developing breast, ovarian, and other types of cancer.</p>
<h2 class="test-h2 mt-3">Who should consider getting tested?</h2>
<p>
Individuals with a personal or family history of breast or ovarian cancer, especially if diagnosed at a
young age, or with multiple family members affected by these cancers, should consider this test. Also,
people from populations with higher incidences of BRCA1 mutations, like Ashkenazi Jewish descent, may
benefit from testing.</p>
