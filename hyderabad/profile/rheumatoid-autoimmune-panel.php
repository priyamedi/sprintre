<h2 class="test-h2 mt-3">What does this panel test for?</h2>
	<p>
This panel tests for a range of antibodies associated with various autoimmune disorders, including but
not limited to lupus and rheumatoid arthritis. It also measures levels of the complement proteins C3 and
C4.</p>
<h2 class="test-h2 mt-3">What preparation is required for the test?</h2>
<p>
No special preparation or fasting is needed for this test. Always follow the instructions provided by your
healthcare provider.</p>
