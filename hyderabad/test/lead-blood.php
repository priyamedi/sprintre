<h2 class="test-h2 mt-3">Why is the lead blood test done?</h2>
<p>The lead blood test is used to screen children and pregnant women for lead poisoning. It may also be used to monitor
	the effectiveness of treatment in people who have been exposed to lead.</p>
<h2 class="test-h2 mt-3">What do the results mean?</h2>
<p>If lead levels in the blood are elevated, it indicates lead exposure and possible lead poisoning. The severity of
	symptoms and the treatment approach depend on how high the lead levels are.</p>
