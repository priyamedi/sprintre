<h1 class="test-h1">Heterophile Antibodies (HA) (Infectious Mononucleosis) - Monotest/ Paul Bunnel Test</h1>
<div class="post-text">
<p>Infectious mononucleosis, also known as "mono" or the "kissing disease," is a common condition caused by the Epstein-Barr virus (EBV). It's most often seen in teenagers and young adults, but it can occur at any age. Symptoms can include fatigue, sore throat, fever, and swollen lymph nodes, especially in the neck.</p>
<p>The Heterophile Antibodies (HA) test, also known as the Monotest or the Paul-Bunnell test, is one of the diagnostic tests used to confirm a suspected infectious mononucleosis infection. This test detects the presence of heterophile antibodies, which are a type of antibody produced by the immune system in response to the EBV infection. These antibodies react with antigens (substances that stimulate an immune response) from other species, hence the name "heterophile."</p>

