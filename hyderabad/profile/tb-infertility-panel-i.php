<h2 class="test-h2 mt-3">What is TB Infertility Panel I?</h2>
<p>TB Infertility Panel I is a set of tests that are used to detect the presence of Mycobacterium tuberculosis (MTB) in
    the endometrial tissue and evaluate its role in infertility. The tests included are Endometrial Biopsy, MTB Culture,
    PCR for MTB/MOTT, Endometrium for NK cell stromal index, and TNF alpha activity in the endometrium.</p>
<h2 class="test-h2 mt-3">Why is this panel important?</h2>
<p>This panel is vital in diagnosing tuberculosis infection of the uterus, which can lead to infertility. Early
    detection and appropriate treatment can improve the chances of conception in affected women.</p>
