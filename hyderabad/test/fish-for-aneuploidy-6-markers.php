<div class="post-text">
<p>Fluorescence In Situ Hybridization, commonly known as FISH, is a molecular cytogenetic technique that
is used to detect and visualize specific DNA sequences on chromosomes. The FISH for Aneuploidy test
utilizing 6 markers - 13, 16, 18, 21, X, and Y - is a prenatal diagnostic test that assesses the presence of
an abnormal number of these specific chromosomes in a fetus. This test is paramount in prenatal
diagnostics as it provides insight into potential chromosomal disorders which may affect the fetus.
</p>
<p>The FISH technique employs fluorescent probes which attach to the chromosomes in a sample, allowing
them to be observed and enumerated under a microscope. It particularly evaluates chromosomes 13,
16, 18, 21, X, and Y. Abnormalities in chromosome 13 are associated with Patau syndrome, chromosome
18 with Edwards syndrome, and chromosome 21 with Down syndrome. Chromosome 16 abnormalities
may also be related to developmental issues. The X and Y chromosomes are important in determining
the sex of the baby and any irregularities in these chromosomes can lead to sex-linked disorders such as
Turner syndrome and Klinefelter syndrome. </p>
