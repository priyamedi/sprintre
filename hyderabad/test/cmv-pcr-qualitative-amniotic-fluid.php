	<h2 class="test-h2 mt-3">What is the CMV-PCR Qualitative test? </h2>
<p>The CMV-PCR Qualitative test is a diagnostic test that uses Polymerase Chain Reaction (PCR) technology to identify the presence of CMV DNA in the amniotic fluid. It helps to detect intrauterine CMV infection in the fetus.</p>
<h2 class="test-h2 mt-3">Why is the CMV-PCR Qualitative test done? </h2>
<p>This test is done when a pregnant woman shows signs of a primary CMV infection or when an ultrasound reveals abnormalities that could indicate a fetal CMV infection. This can include microcephaly, ventriculomegaly, intrauterine growth restriction, and other signs.</p>
