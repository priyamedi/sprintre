<h2 class="test-h2 mt-3">What is the purpose of the MM-Mutation Detection for Mitochondrial Myopathy test?</h2>
<p>This test is used to detect specific mutations in the mitochondrial DNA that can cause mitochondrial myopathies. If
    you have symptoms or a family history of these disorders, this test can help in your diagnosis and guide your
    treatment plan.</p>
<h2 class="test-h2 mt-3">What happens during the test?</h2>
<p>A healthcare provider will take a blood sample, usually from a vein in your arm. The procedure is quick and involves
    minimal discomfort.</p>
