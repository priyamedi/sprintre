<h1 class="test-h1">Thyroxine Binding Globulin (TBG) </h1>
<div class="post-text">
<p>Thyroxine Binding Globulin (TBG) test measures the amount of TBG in the blood. TBG is a protein produced by the liver that carries thyroid hormones in the blood. The majority of the thyroid hormones T3 (triiodothyronine) and T4 (thyroxine) circulating in the blood are bound to TBG.</p>

<p>The TBG test is often used to evaluate thyroid diseases and conditions affecting TBG levels, such as liver disease and pregnancy. It is especially useful in cases where standard thyroid function tests provide results that are not consistent with clinical observations.</p>
