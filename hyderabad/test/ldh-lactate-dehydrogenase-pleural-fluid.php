<h2 class="test-h2 mt-3"> What is LDH and what role does it play in the lungs?</h2>

<p>Lactate dehydrogenase (LDH) is an enzyme found in many body tissues, including the
lungs. It is involved in energy production. In the context of pleural fluid, LDH levels
can indicate the presence of disease or injury to the lung tissues.</p>

<h2 class="test-h2 mt-3"> How is the test for LDH in pleural fluid performed?</h2>

<p>The test is performed on a sample of pleural fluid. This fluid is usually obtained
through a procedure called thoracentesis, where a needle is inserted through the
chest wall into the pleural space to withdraw fluid.</p>

