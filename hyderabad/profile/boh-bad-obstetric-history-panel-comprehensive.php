	<h2 class="test-h2 mt-3">What is Anti Cardiolipin and why is it tested?</h2>
<p>Anti Cardiolipin antibodies are antibodies that increase the risk of blood clotting. These antibodies are tested because they can lead to pregnancy complications such as miscarriage or stillbirth.</p>

	<h2 class="test-h2 mt-3">Why is TSH tested in the BOH Panel?</h2>
<p>TSH, or Thyroid Stimulating Hormone, is tested because thyroid function is crucial during pregnancy. Abnormal levels of thyroid hormones can affect fetal development and increase the risk of miscarriage.</p>

	<h2 class="test-h2 mt-3">What does TORCH-10 testing involve?</h2>
<p>TORCH-10 testing involves checking for ten different infections that are known to affect pregnancy. These infections include toxoplasmosis, rubella, cytomegalovirus, and herpes simplex virus among others.</p>

	<h2 class="test-h2 mt-3">What is Lupus Anticoagulant and how is it tested?</h2>
<p>Lupus Anticoagulant is an antibody that can cause blood to clot more easily. It is tested using the Dilute Russell’s Viper Venom Test (DRVVT), which measures the time it takes for blood to clot.</p>

	<h2 class="test-h2 mt-3">What are the risks associated with the BOH Panel?</h2>
<p>The risks are minimal and similar to those of any blood draw, such as minor bruising or infection at the site where the needle was inserted.</p>

	<h2 class="test-h2 mt-3">What should I do if my results are abnormal?</h2>
<p>If your results are abnormal, it is important to discuss them with your doctor who can help interpret the results and suggest a course of action or treatment.</p>

	<h2 class="test-h2 mt-3">How can I improve my chances of a successful pregnancy if I have Bad Obstetric History?</h2>
<p>Improving the chances of a successful pregnancy involves managing the underlying conditions identified in the BOH Panel, adhering to your doctor's recommendations, and maintaining a healthy lifestyle.</p>

	<h2 class="test-h2 mt-3">What are modifiable factors affecting pregnancy outcomes?</h2>
<p>Modifiable factors include lifestyle choices such as diet, exercise, smoking, alcohol use, and proper medication management.</p>

	<h2 class="test-h2 mt-3">What are non-modifiable factors affecting pregnancy outcomes?</h2>
<p>Non-modifiable factors include genetics, age, and chronic medical conditions.</p>

	<h2 class="test-h2 mt-3">Is fasting required for the BOH Panel?</h2>
<p>No, fasting is not required for the BOH Panel.</p>

	<h2 class="test-h2 mt-3">How is the BOH Panel performed?</h2>
<p>The BOH Panel is performed using a blood sample taken from a vein in the arm.</p>

	<h2 class="test-h2 mt-3">Which doctor should I consult if the results are abnormal?</h2>
<p>You should consult an obstetrician or a specialist in reproductive medicine if the results are abnormal.</p>

	<h2 class="test-h2 mt-3">Can the BOH Panel prevent future pregnancy losses?</h2>
<p>While the BOH Panel itself cannot prevent pregnancy losses, the information it provides can be vital for your doctor to create a tailored plan to manage underlying conditions and improve the chances of a successful pregnancy.
</p>
