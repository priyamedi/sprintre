<h2 class="test-h2 mt-3">Why would my doctor order an IHC - CD31 test?</h2>
<p> The <strong>IHC - CD31</strong> test may be ordered by your doctor if a tumor's origin is uncertain. It helps differentiate between vascular and non-vascular tumors, as CD31 is a marker typically found on the surface of cells that line blood vessels.</p>
<h2 class="test-h2 mt-3">How is the IHC - CD31 test performed?</h2>
<p> The test involves staining a tissue sample with specific antibodies that bind to the CD31 antigen. If CD31 is present in the sample, it will bind to the antibody and create a visual marker that can be seen under a microscope.</p>
