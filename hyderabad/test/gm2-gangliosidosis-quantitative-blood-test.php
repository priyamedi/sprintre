<h1 class="test-h1">GM2 Gangliosidosis - Quantitative Blood Test (Tay Sach's & Sandhoff Disease) </h1>
<div class="post-text">

<p>The GM2 Gangliosidosis - Quantitative Blood test is an essential diagnostic tool for detecting GM2 Gangliosidoses, a group of rare and genetic neurological disorders. This includes conditions such as Tay-Sachs disease and Sandhoff disease, which are characterized by the buildup of fatty substances called GM2 gangliosides in the neurons. These disorders progressively destroy nerve cells (neurons) in the brain and spinal cord.</p>

<p>This test measures the levels of specific enzymes—Hexosaminidase A (Hex A) and Hexosaminidase B (Hex B)—in the blood. The deficiency of these enzymes, specifically Hex A, leads to the accumulation of GM2 gangliosides, causing GM2 Gangliosidoses. The outcomes of this test are vital for diagnosing and managing these conditions.</p>
