	<h2 class="test-h2 mt-3">When should I get this test done? </h2>
	<p>This test is usually advised when a doctor suspects a diagnosis of acute leukemia based on clinical symptoms and initial blood tests. It may also be recommended for monitoring during treatment. </p>
	<h2 class="test-h2 mt-3">What information does this test provide? </h2>
	<p>The test provides detailed information about the type of acute leukemia and helps in identifying genetic abnormalities and mutations. This information is critical for staging the disease and planning treatment. </p>
	<h2 class="test-h2 mt-3">How often should this test be done? </h2>
	<p>The frequency of this test depends on the clinical status of the patient and the doctor's recommendations. It is generally performed at the time of diagnosis and may be repeated at various stages of treatment to monitor response. </p>
	<h2 class="test-h2 mt-3">What are the normal values for this test? </h2>
	<p>Normal values would indicate no presence of abnormal or immature cells and no chromosomal abnormalities. However, in patients with acute leukemia, the presence of such cells and abnormalities is expected. </p>
	<h2 class="test-h2 mt-3">Are there any precautions to be taken for the test? </h2>
	<p>There are no specific precautions. However, it is advisable to inform the doctor of any medications or supplements you are taking, as some medications may interfere with the test results. </p>
	<h2 class="test-h2 mt-3">What are the factors affecting the levels of this test? </h2>
	<p>Factors affecting the levels include the type of leukemia, stage of the disease, the patient's age, health, and response to any ongoing treatment. </p>
	<h2 class="test-h2 mt-3">Which doctor should I consult if I have abnormal values in this test? </h2>
	<p>If the test results are abnormal, it is important to consult a hematologist or an oncologist, who specialize in blood disorders and cancer, respectively. </p>
	<h2 class="test-h2 mt-3">How is the sample collected for this test? </h2>
	<p>A bone marrow aspirate (BMA) or peripheral blood (PB) sample is usually collected for this test. The procedure for collecting a bone marrow sample involves inserting a needle into the bone to collect a small amount of bone marrow. </p>
	<h2 class="test-h2 mt-3">Is the test painful? </h2>
	<p>You may experience some discomfort or pain during the collection of the bone marrow sample. Local anesthesia is used to minimize pain. </p>
	<h2 class="test-h2 mt-3">What is FISH and why is it important? </h2>
	<p>Fluorescence In Situ Hybridization (FISH) is a test that uses fluorescent probes to detect specific DNA sequences on chromosomes. It is essential for identifying chromosomal changes that are too small to be detected under a microscope with conventional cytogenetic techniques. </p>
	<h2 class="test-h2 mt-3">What is karyotyping? </h2>
	<p>Karyotyping is a test that evaluates the number, size, and shape of a person's chromosomes. It helps to detect structural abnormalities in chromosomes, such as deletions, translocations, and duplications. <br><br>
