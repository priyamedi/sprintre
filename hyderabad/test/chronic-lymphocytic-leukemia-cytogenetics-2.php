	<h2 class="test-h2 mt-3">What are the chromosomal abnormalities that CLL Cytogenetics 2 test identifies? </h2>
	<p>This test identifies three chromosomal abnormalities - deletion in chromosome 17p (del17p), deletion in chromosome 11q (del 11q, also known as ATM deletion), and trisomy 12 (extra copy of chromosome 12). </p>
	<h2 class="test-h2 mt-3">What is the significance of these chromosomal abnormalities in CLL? </h2>
	<p>Del17p and del 11q are associated with a more aggressive form of CLL and are known to be resistant to standard chemotherapy. Trisomy 12 is generally associated with an intermediate prognosis. Identifying these abnormalities can help in risk stratification and in formulating an effective treatment plan. </p>
