<h1 class="test-h1">Bilirubin Conjugated</h1>
<div class="post-text">
<p>Bilirubin is a yellow pigment that is produced when the red blood cells are broken down in the body. Conjugated Bilirubin, also known as direct Bilirubin, is the form of Bilirubin that has been processed by the liver and made water-soluble. It is then excreted into the bile and eventually eliminated from the body through feces. The test for Conjugated Bilirubin is used to evaluate the liver’s ability to take up, process, and secrete Bilirubin into the bile.</p>
<p>The measurement of Conjugated Bilirubin levels is crucial for diagnosing and monitoring liver diseases, such as hepatitis or cirrhosis, and for assessing bile duct blockages. High levels of Conjugated Bilirubin can cause jaundice, a yellowing of the skin and eyes.</p>
