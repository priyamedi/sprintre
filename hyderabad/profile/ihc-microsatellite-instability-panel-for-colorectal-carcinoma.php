<h1 class="test-h1">IHC - Microsatellite Instability Panel for Colorectal Carcinoma (MLH-1, MSH-2, MSH- 6, PMS-2)</h1>
<div class="post-text">
<p>The <strong>Immunohistochemistry (IHC) - Microsatellite Instability Panel</strong> for Colorectal Carcinoma is a diagnostic test that uses tissue samples to identify changes in proteins produced by MLH1, MSH2, MSH6, and PMS2 genes. These genes are key components of the DNA mismatch repair (MMR) system, which works to fix mistakes that can occur when DNA is copied during cell division.</p>
<p>Mutations in any of these genes can disrupt the normal functioning of the MMR system, causing a phenomenon known as microsatellite instability (MSI). This instability, characterized by changes in the length of DNA sequences called microsatellites, is a common feature of several types of cancer, including colorectal cancer.</p>
<p>The <strong>IHC - Microsatellite Instability Panel</strong> is a crucial test for the evaluation and management of colorectal cancer. It can aid in determining the likelihood of the disease, particularly in individuals with a family history of colorectal cancer or other risk factors. The results can also guide treatment options, as cancers with high microsatellite instability (MSI-H) often respond differently to certain therapies compared to cancers with low MSI (MSI-L) or those that are microsatellite stable (MSS).</p>

