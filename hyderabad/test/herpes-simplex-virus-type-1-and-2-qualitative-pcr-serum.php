<p>Herpes Simplex Virus can be categorized into two types: HSV-1, primarily causing oral herpes, leading to cold sores or fever blisters around the mouth; and HSV-2, leading to genital herpes. However, both types can cause herpes sores in either the oral or genital area.</p>

