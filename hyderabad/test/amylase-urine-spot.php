<h2 class="test-h2 mt-3">What is Amylase?</h2>
<p>Amylase is an enzyme produced primarily by the pancreas and the salivary glands to help digest carbohydrates. It converts carbohydrates into simple sugars, which are then absorbed by the body for energy.</p>

<h2 class="test-h2 mt-3">Why is an Amylase - Urine Spot test performed?</h2>
<p>The Amylase - Urine Spot test is typically performed to help diagnose and monitor diseases of the pancreas, such as pancreatitis, pancreatic cancer, or cystic fibrosis. It's also used to determine the cause of abdominal pain or evaluate the severity of a gastrointestinal disease. The test can also help to monitor patients who have undergone surgery on the pancreas.</p>

