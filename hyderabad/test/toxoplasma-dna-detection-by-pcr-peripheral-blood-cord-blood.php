<h2 class="test-h2 mt-3">Why is the Toxoplasma DNA Detection by PCR - Peripheral Blood/Cord Blood test performed?
</h2>
<p>This test is performed to confirm a suspected case of toxoplasmosis, especially in cases where the person is
    immunocompromised, pregnant, or when congenital toxoplasmosis is suspected in a newborn.</p>
<h2 class="test-h2 mt-3">How is the blood sample collected?</h2>
<p>A healthcare provider collects a blood sample from a vein in your arm using a small needle. For newborns, cord blood
    may be collected at the time of birth.</p>
