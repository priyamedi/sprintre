	<p>Chronic Myeloid Leukemia (CML) is a cancer of the white blood cells. It is caused by a genetic mutation, specifically a translocation between chromosomes 9 and 22, which forms the Philadelphia chromosome and produces an abnormal enzyme, BCR-ABL, that promotes the proliferation of leukemia cells. </p>
	<h2 class="test-h2 mt-3">What is the significance of Karyotyping in diagnosing CML? </h2>
	<p>Karyotyping involves the examination of chromosomes in a cell. In CML, Karyotyping is used to detect the presence of the Philadelphia chromosome, which is a diagnostic hallmark of the disease. </p>
